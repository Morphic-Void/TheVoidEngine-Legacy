char usage[] = \
    "\n"\
    "%s [args]\n"\
    "\n"\
    "returns this string if the first arg is '?' or 'help' or the args are invalid\n"\
    "\n"\
    "valid args are:\n"\
    "\n"\
    "    -s file.json file.bin\n"\
    "    processes the .json shader definitions to a binary shaders library file\n"\
    "\n"\
    "    -r file.json file.bin\n"\
    "    processes the .json state definitions to a binary resources library file\n";

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////	file name seperator
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

_TCHAR* getNameFromPath( _TCHAR* path )
{
    int length = 0;
    while( path[ length ] ) ++length;
    while( length )
    {
        --length;
        _TCHAR value = path[ length ];
        if( ( value == '\\' ) || ( value == '/' ) )
        {
            return( &path[ length + 1 ] );
        }
    }
    return( path );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////	application entry point
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int _tmain(int argc, _TCHAR* argv[])
{
    if( ( argc > 1 ) && ( _stricmp( argv[ 1 ], "?" ) != 0 ) && ( _stricmp( argv[ 1 ], "help" ) != 0 ) )
    {
        if( ( argc == 4 ) && ( _stricmp( argv[ 1 ], "-s" ) == 0 ) )
        {
            pipeline::ShaderLibraryParser shaderLibrary;
            if( shaderLibrary.parse( argv[ 2 ] ) )
            {
                if( shaderLibrary.build( argv[ 3 ] ) )
                {
                    printf( "Successfully built \"%s\" from \"%s\"\n", getNameFromPath( argv[ 3 ] ), getNameFromPath( argv[ 2 ] ) );
                    return( 0 );
                }
            }
            printf( "\n*** ERROR *** Build shaders library failed - check logs\\log.txt *** ERROR ***\n" );
            return( -1 );
        }
        if( ( argc == 4 ) && ( _stricmp( argv[ 1 ], "-r" ) == 0 ) )
        {
            pipeline::ResourceLibraryParser resourceLibrary;
            if( resourceLibrary.parse( argv[ 2 ] ) )
            {
                if( resourceLibrary.build( argv[ 3 ] ) )
                {
                    printf( "Successfully built \"%s\" from \"%s\"\n", getNameFromPath( argv[ 3 ] ), getNameFromPath( argv[ 2 ] ) );
                    return( 0 );
                }
            }
            printf( "\n*** ERROR *** Build resource library failed - check logs\\log.txt *** ERROR ***\n" );
            return( -1 );
        }
    }
    printf( usage, argv[ 0 ] );
	return( 0 );
}

