bool RoomGeometry::create( const CoreSystem& system, const constructs::LayeringConstruct& construct )
{
    kill();
    if( init( system, construct ) )
    {
        return( true );
    }
    kill();
    return( false );
}

void RoomGeometry::destroy()
{
    if( valid() )
    {
        kill();
    }
}

void RoomGeometry::relocate( const maths::vec3& scale, const maths::vec3& offset )
{
    if( valid() )
    {
        m_scaling = scale;
        m_offset = offset;
    }
}

bool RoomGeometry::init( const CoreSystem& system, const constructs::LayeringConstruct& construct )
{
    const constructs::GeometryConstruct* const renderable = construct.getConstruct( ERenderableLayer1 );
    const constructs::GeometryConstruct* const collidable = construct.getConstruct( ECollidableLayer1 );
    if( ( renderable != NULL ) && ( collidable != NULL ) )
    {
        if( m_renderer.create( system, *renderable ) )
        {
            if( m_collider.create( *collidable ) )
            {
                m_valid = true;
                return( true );
            }
        }
    }
    return( false );
}

void RoomGeometry::kill()
{
    BaseGeometry::kill();
    m_renderer.destroy();
    m_collider.destroy();
}

