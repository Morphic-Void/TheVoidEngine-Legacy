bool ItemGeometry::create( const CoreSystem& system, const constructs::LayeringConstruct& construct )
{
    kill();
    if( init( system, construct ) )
    {
        return( true );
    }
    kill();
    return( false );
}

void ItemGeometry::destroy()
{
    if( valid() )
    {
        kill();
    }
}

void ItemGeometry::relocate( const maths::vec3& scale, const maths::vec3& offset )
{
    if( valid() )
    {
        m_scaling = scale;
        m_offset = offset;
    }
}

bool ItemGeometry::init( const CoreSystem& system, const constructs::LayeringConstruct& construct )
{
    const constructs::GeometryConstruct* const renderable = construct.getConstruct( ERenderableLayer1 );
    if( renderable != NULL )
    {
        if( m_renderer.create( system, *renderable ) )
        {
            m_valid = true;
            return( true );
        }
    }
    return( false );
}

void ItemGeometry::kill()
{
    BaseGeometry::kill();
    m_renderer.destroy();
}

