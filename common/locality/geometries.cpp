void BaseGeometry::relocate( const maths::vec3& scale, const maths::vec3& offset )
{
    if( valid() )
    {
        m_scaling = scale;
        m_offset = offset;
    }
}

void BaseGeometry::kill()
{
    m_valid = false;
    m_scaling = maths::consts::VEC3::ONE;
    m_offset = maths::consts::VEC3::ZERO;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    DoorGeometry door geometry description class function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool DoorGeometry::create( const CoreSystem& system, const maths::vec2* const verts, const uint count )
{
    kill();
    if( init( system, verts, count ) )
    {
        return( true );
    }
    kill();
    return( false );
}

void DoorGeometry::destroy()
{
    if( valid() )
    {
        kill();
    }
}

void DoorGeometry::relocate( const maths::vec3& scale, const maths::vec3& offset )
{
    if( valid() )
    {
        m_scaling = scale;
        m_offset = offset;
        m_collider.relocate( scale.t_vec2(), offset.t_vec2() );
    }
}

bool DoorGeometry::init( const CoreSystem& system, const maths::vec2* const verts, const uint count )
{
    if( !m_renderer.create( system, verts, count ) )
    {
        return( false );
    }
    if( !m_collider.create( verts, count ) )
    {
        return( false );
    }
    m_valid = true;
    return( true );
}

void DoorGeometry::kill()
{
    BaseGeometry::kill();
    m_renderer.destroy();
    m_collider.destroy();
}

