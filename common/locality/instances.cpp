void ItemInstance::zero()
{
    locality = NULL;
    nextItem = -1;
    roomIndex = -1;
    itemClone = -1;
    stencilRef = 0;
    localToLight = maths::consts::JOINT::IDENTITY;
    localToView = maths::consts::JOINT::IDENTITY;
    nearScale = doorScale = 1.0f;
    doorClip.x = doorClip.y = doorClip.w = 0.0f;
    doorClip.z = 1.0f;
    object.release();
    viewer.release();
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    DoorInstance struct function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void DoorInstance::zero()
{
    locality = NULL;
    nextDoor = -1;
    roomIndex = -1;
    stencilRef = 0;
    stencilNew = 0;
    localToLight = maths::consts::JOINT::IDENTITY;
    localToView = maths::consts::JOINT::IDENTITY;
    passes[ 0 ] = passes[ 1 ] = passes[ 2 ] = passes[ 3 ] = passes[ 4 ] = EDoorNullPass;
    faceClip.x = faceClip.y = faceClip.w = 0.0f;
    faceClip.z = 1.0f;
    object.release();
    viewer.release();
    lintel.release();
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    RoomInstance struct function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void RoomInstance::zero()
{
    locality = NULL;
    entrance = NULL;
    observer = NULL;
    topLevel = false;
    doorCount = 0;
    firstDoor = -1;
    itemCount[ 0 ] = itemCount[ 1 ] = 0;
    firstItem[ 0 ] = firstItem[ 1 ] = -1;
    stencilRef = 0;
    localToLight = maths::consts::JOINT::IDENTITY;
    localToView = maths::consts::JOINT::IDENTITY;
    viewToWorld = maths::consts::JOINT::IDENTITY;
    projection = maths::consts::MAT44::IDENTITY;
    scissoring.left = scissoring.top = scissoring.right = scissoring.bottom = 0;
    viewport.min.x = viewport.min.y = viewport.min.z = viewport.max.x = viewport.max.y = viewport.max.z = 0.0f;
    nearClip.x = nearClip.y = nearClip.w = 0.0f;
    nearClip.z = 1.0f;
    object.release();
    viewer.release();
}

