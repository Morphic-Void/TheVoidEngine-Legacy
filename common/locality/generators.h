////
////    StairStyle stair style enumeration
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

enum StairStyle
{
    EStairsSolidStep = 0,                   //  single sided steps
    EStairsSolidRamp,                       //  single sided ramp
    EStairsPlainRamp,                       //  double sided ramp
    EStairsFlatUnder,                       //  double sided with a flat under-side
    EStairsAdjoining,                       //  double sided with adjoining block steps
    EStairsInterlock,                       //  double sided with interlocked steps
    EStairsAlternate,                       //  double sided with offset steps
    EStairStyleForce32 = 0x7fffffff
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    StairFlags stair building control flag enumeration
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

enum StairFlags
{
    EStairFlagCloseLower    =   0x00000001, //  close the joint at the lower end of the stairs (if applicable)
    EStairFlagCloseUpper    =   0x00000002, //  close the joint at the upper end of the stairs (if applicable)
    EStairFlagsForce32      =   0x7fffffff
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    StairGen stair geometry generation class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class StairGen
{
public:
    inline          StairGen() { defaults(); };
    inline          ~StairGen() { defaults(); };
    void            build( constructs::LayeringConstruct& construct );
protected:
    void            defaults();
    void            buildSolidStep( constructs::LayeringConstruct& construct );
    void            buildSolidRamp( constructs::LayeringConstruct& construct );
    void            buildPlainRamp( constructs::LayeringConstruct& construct );
    void            buildFlatUnder( constructs::LayeringConstruct& construct );
    void            buildAdjoining( constructs::LayeringConstruct& construct );
    void            buildInterlock( constructs::LayeringConstruct& construct );
    void            buildAlternate( constructs::LayeringConstruct& construct );
    maths::aabb     m_extents;          //  extents of the stairs
    uint32_t        m_style;            //  style of the steps
    uint32_t        m_flags;            //  flags for joint caps and joint styles
    uint32_t        m_count;            //  number of steps
    float           m_ratio;            //  ratio to use for double sided spacing
    float           m_shift;            //  shift to use for double sided spacing
    uint32_t        m_step_colour;      //  colour to use for the faces of the steps
    uint32_t        m_side_colour;      //  colour to use for the sides of the steps
    uint32_t        m_join_colour;      //  colour to use for the joint caps
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    SwitchGen switch geometry generation class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class SwitchGen
{
public:
    inline          SwitchGen() { defaults(); };
    inline          ~SwitchGen() { defaults(); };
    void            buildMono( constructs::LayeringConstruct& construct );
    void            buildBody( constructs::LayeringConstruct& construct );
    void            buildHead( constructs::LayeringConstruct& construct );
protected:
    void            defaults();
    void            setup();
    void            buildBase( constructs::LayeringConstruct& construct );
    struct          HPOS { float x, z; };
    enum            EVPos { EVLowB = 0, EVLowH, EVTopB, EVTopH, EVCut0, EVCut1, EVCut2, EVRow0, EVRow1, EVRow2, EVRow3, EVRow4, EVRow5, EVRow6, EVRow7, EVRow8, EVRow9, EVFace, EVHalf, EVGemB, EVGemT, EVBoxB, EVBoxT, EVTriB, EVTriH, EVCount, EVForce32 = 0x7fffffff };
    enum            EHPos { EHPosL = 0, EHPosR, EHOutL, EHOutR, EHCutL, EHCutR, EHGemL, EHGemR, EHBoxL, EHBoxR, EHHalf, EHCount, EHforce32 = 0x7fffffff };
    float           m_vpos[ EVCount ];
    HPOS            m_hpos[ EHCount ][ 16 ];
    uint32_t        m_face_count;
    float           m_face_width;
    float           m_face_height;
    uint32_t        m_face_colour;
    uint32_t        m_band_count;
    float           m_band_depth;
    uint32_t        m_band_colours[ 3 ];
    uint32_t        m_icon_enable;
    uint32_t        m_icon_select;
    uint32_t        m_icon_colours[ 16 ];
    float           m_icon_ratio;
    float           m_shim_depth;
    uint32_t        m_shim_colour;
    bool            m_shim_top;
};

