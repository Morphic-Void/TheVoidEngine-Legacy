////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    ItemInstance struct
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct ItemInstance
{
    ItemLocality*       locality;           //  description of the item geometry and transform
    int                 nextItem;           //  index of the next visible item instance in the same room instance
    int                 roomIndex;          //  index of the room instance containing this item
    int                 itemClone;          //  index of the item instance containing the viewer resource to copy
    uint                stencilRef;         //  stencil reference value
    maths::joint        localToLight;       //  transform local-space to world-space for lighting
    maths::joint        localToView;        //  transform local-space to view-space for rendering
    float               nearScale;          //  near clip plane scale factor
    float               doorScale;          //  door clip plane scale factor
    maths::plane        doorClip;           //  view space clip plane of the door (if any)
    ResourceConsts      object;             //  object constant buffer resource
    ResourceConsts      viewer;             //  viewer constant buffer resource
    void                zero();
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    DoorInstance struct
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct DoorInstance
{
    DoorLocality*       locality;           //  description of the door geometry and transform
    int                 nextDoor;           //  index of the next visible door instance in the same room instance
    int                 roomIndex;          //  index of the room instance containing this door
    uint                stencilRef;         //  stencil reference value
    uint                stencilNew;         //  stencil replacement value
    maths::joint        localToLight;       //  transform local-space to world-space for lighting
    maths::joint        localToView;        //  transform local-space to view-space for rendering
    EDoorRenderPass     passes[ 5 ];        //  render passes
    maths::plane        faceClip;           //  view space clip plane of this door
    ResourceConsts      object;             //  object constant buffer resource
    ResourceConsts      viewer;             //  viewer constant buffer resource
    ResourceConsts      lintel;             //  lintel constant buffer resource
    void                zero();
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    RoomInstance struct
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct RoomInstance
{
    RoomLocality*       locality;           //  description of the room geometry and transform
    DoorLocality*       entrance;           //  description of the door this room was entered via
    ViewLocality*       observer;           //  description of the view being used for rendering
    bool                topLevel;           //  top level rooms write their stencil values without testing
    int                 doorCount;          //  count of visible door instances in this room
    int                 firstDoor;          //  index of the first visible door instance in this room
    int                 itemCount[ 2 ];     //  counts of visible item instances in this room [ unstenciled, stenciled ]
    int                 firstItem[ 2 ];     //  indices of the first visible item instance in this room [ unstenciled, stenciled ]
    uint                stencilRef;         //  stencil reference value
    maths::joint        localToLight;       //  transform local-space to world-space for lighting
    maths::joint        localToView;        //  transform local-space to view-space for rendering
    maths::joint        viewToWorld;        //  transform view-space to world-space
    maths::mat44        projection;         //  projection matrix
    SRECT               scissoring;         //  render target scissoring rect
    FBOX                viewport;           //  render target viewport
    maths::plane        nearClip;           //  view space clip plane of the entry door
    ResourceConsts      object;             //  object constant buffer resource
    ResourceConsts      viewer;             //  viewer constant buffer resource
    void                zero();
};

