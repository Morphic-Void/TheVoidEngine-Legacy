class GameWorld
{
public:
    inline                  GameWorld() : m_valid( false ) {};
    inline                  ~GameWorld() { kill(); };
    inline bool             valid() const { return( this && m_valid ); };
    bool                    create( const CoreSystem& system, const PrimaryViews& primary );
    void                    destroy();
    void                    update( const float time, const input::CGamePadState& gamePadState, const input::CKeyMouseState& keyMouseState );
    void                    render();
    void                    resize( const PrimaryViews& primary );
    const maths::joint&     getViewer() const;
protected:
    bool                    init( const CoreSystem& system, const PrimaryViews& primary );
    void                    kill();
    bool                    buildGeometry();
    void                    buildScene();
    void                    updateViewer( const float time, const input::CGamePadState& gamePadState, const input::CKeyMouseState& keyMouseState );
    void                    preloadRooms();
    void                    resetRenderPools();
    void                    updateRoomConsts( RoomInstance& roomInstance );
    void                    updateDoorConsts( DoorInstance& doorInstance, const RoomInstance& roomInstance );
    void                    updateItemConsts( ItemInstance& itemInstance, const RoomInstance& roomInstance );
    void                    relocateViews();
    DoorLocality*           relocatePoint( RoomLocality* room, const maths::vec3& start, const maths::vec3& end, maths::joint& world );
    static void             getLocalToView( const maths::joint& localToWorld, const maths::joint& viewToWorld, maths::joint& localToView );
    static void             getClipPlane( const maths::joint& localToView, maths::plane& clipPlane, const bool reverse = false );
    bool                    m_valid;
    CoreSystem              m_system;
    PrimaryViews            m_primary;
    Renderables             m_renderables;
    RoomRenderables         m_roomRenderables;
    ItemRenderables         m_itemRenderables;
    DoorRenderables         m_doorRenderables;
    ManagedCBuffers         m_objectBuffers;
    ManagedCBuffers         m_viewerBuffers;
    RoomInstances           m_roomInstances;
    ItemInstances           m_itemInstances;
    DoorInstances           m_doorInstances;
    NodeLocalities          m_nodeLocalities;
    RoomLocalities          m_roomLocalities;
    ItemLocalities          m_itemLocalities;
    DoorLocalities          m_doorLocalities;
    ViewLocalities          m_viewLocalities;
    RoomGeometries          m_roomGeometries;
    ItemGeometries          m_itemGeometries;
    DoorGeometries          m_doorGeometries;
    ResourceConsts          m_lintelResource;
    CConstants              m_lintelBuffer;
};

