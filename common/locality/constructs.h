class LayeringConstruct
{
public:
    inline                          LayeringConstruct() { zero(); };
    inline                          ~LayeringConstruct() { kill(); };
    inline bool                     valid() const { return( this != NULL ); };
    void                            setConstruct( const int layer, GeometryConstruct* const construct );
    inline void                     setConstruct( const int layer, GeometryConstruct& construct ) { setConstruct( layer, &construct ); };
    GeometryConstruct*              getConstruct( const int layer ) const;
    inline void                     setMask( const uint32_t mask ) { m_mask = ( mask & m_active ); };
    inline uint32_t                 getMask() const { return( m_mask ); };
    inline void                     enableAll() { m_mask = m_active; };
    inline void                     enableLayer( const int layer ) { m_mask |= ( uint32_t( 1 ) << layer ); m_mask &= m_active; };
    inline void                     disableLayer( const int layer ) { m_mask &= ~( uint32_t( 1 ) << layer ); };
    void                            init();
    void                            kill();
    void                            reset();
    void                            resetAll();
    void                            setTolerance( const float tolerance );
    void                            setOffset( const maths::vec3& offset );
    void                            setScale( const maths::vec3& scale );
    void                            setScale( const float scale );
    void                            setUsage( const uint32_t usage );
    void                            setParams( const uint32_t params );
    void                            beginFan();
    void                            beginStrip();
    void                            addVert( const maths::vec3& position );
    void                            addVert( const maths::vec3& position, const uint32_t params );
    void                            addPair( const maths::vec3& position1, const maths::vec3& position2 );
    void                            addPair( const maths::vec3& position1, const maths::vec3& position2, const uint32_t params );
    void                            addPair( const maths::vec3& position1, const uint32_t params1, const maths::vec3& position2, const uint32_t params2 );
    void                            addTri( const maths::vec3& position1, const maths::vec3& position2, const maths::vec3& position3 );
    void                            addTri( const maths::vec3& position1, const maths::vec3& position2, const maths::vec3& position3, const uint32_t params );
    void                            addTri( const maths::vec3& position1, const uint32_t params1, const maths::vec3& position2, const uint32_t params2, const maths::vec3& position3, const uint32_t params3 );
    void                            addQuad( const maths::vec3& position1, const maths::vec3& position2, const maths::vec3& position3, const maths::vec3& position4 );
    void                            addQuad( const maths::vec3& position1, const maths::vec3& position2, const maths::vec3& position3, const maths::vec3& position4, const uint32_t params );
    void                            addQuad( const maths::vec3& position1, const uint32_t params1, const maths::vec3& position2, const uint32_t params2, const maths::vec3& position3, const uint32_t params3, const maths::vec3& position4, const uint32_t params4 );
    void                            clearTopology();
    void                            setupTopology();
    void                            setupBounds();
    void                            finalise();
protected:
    void                            zero();
    uint32_t                        m_mask;
    uint32_t                        m_active;
    GeometryConstruct*              m_constructs[ 32 ];
private:
    inline                          LayeringConstruct( const LayeringConstruct& ) {};
    inline void                     operator=( const LayeringConstruct& ) {};
};

