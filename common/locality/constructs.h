////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    GeometryConstruct geometry construction class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class GeometryConstruct
{
public:
    inline                          GeometryConstruct() : m_valid( false ), m_planes(), m_positions(), m_descs(), m_verts(), m_edges(), m_triangles(), m_surfaces() { zero(); };
    inline                          ~GeometryConstruct() { kill(); };
    inline bool                     valid() const { return( this && m_valid ); };
    void                            init();
    void                            kill();
    void                            reset();
    inline void                     setTolerance( const float tolerance ) { if( valid() ) { m_tolerance = tolerance; m_sqrTolerance = ( tolerance * tolerance ); } };
    inline void                     setOffset( const maths::vec3& offset ) { if( valid() ) m_offset = offset; };
    inline void                     setScale( const maths::vec3& scale ) { if( valid() ) m_scale = scale; };
    inline void                     setScale( const float scale ) { if( valid() ) { m_scale.x = m_scale.y = m_scale.z = scale; } };
    inline void                     setUsage( const uint32_t usage ) { m_usage = usage; };
    inline void                     setParams( const uint32_t params ) { m_params = params; };
    inline void                     beginFan() { m_chainStrip = false; m_chainIndex = 0; };
    inline void                     beginStrip() { m_chainStrip = true; m_chainIndex = 0; };
    void                            addVert( const maths::vec3& position );
    void                            addVert( const maths::vec3& position, const uint32_t params );
    void                            addPair( const maths::vec3& position1, const maths::vec3& position2 );
    void                            addPair( const maths::vec3& position1, const maths::vec3& position2, const uint32_t params );
    void                            addPair( const maths::vec3& position1, const uint32_t params1, const maths::vec3& position2, const uint32_t params2 );
    void                            addTri( const maths::vec3& position1, const maths::vec3& position2, const maths::vec3& position3 );
    void                            addTri( const maths::vec3& position1, const maths::vec3& position2, const maths::vec3& position3, const uint32_t params );
    void                            addTri( const maths::vec3& position1, const uint32_t params1, const maths::vec3& position2, const uint32_t params2, const maths::vec3& position3, const uint32_t params3 );
    void                            addQuad( const maths::vec3& position1, const maths::vec3& position2, const maths::vec3& position3, const maths::vec3& position4 );
    void                            addQuad( const maths::vec3& position1, const maths::vec3& position2, const maths::vec3& position3, const maths::vec3& position4, const uint32_t params );
    void                            addQuad( const maths::vec3& position1, const uint32_t params1, const maths::vec3& position2, const uint32_t params2, const maths::vec3& position3, const uint32_t params3, const maths::vec3& position4, const uint32_t params4 );
    void                            clearTopology();
    void                            setupTopology();
    void                            setupBounds();
    inline void                     finalise() { setupTopology(); setupBounds(); };
    inline const maths::sphere&     getBoundingSphere() const { return( m_sphereBounds ); };
    inline const maths::aabb&       getBoundingAABB() const { return( m_aabbBounds ); };
    inline const GeometryPlanes&    getPlanes() const { return( m_planes ); };
    inline const GeometryPositions& getPositions() const { return( m_positions ); };
    inline const GeometryDescs&     getDescs() const { return( m_descs.getBlob() ); };
    inline const GeometryVerts&     getVerts() const { return( m_verts.getBlob() ); };
    inline const GeometryEdges&     getEdges() const { return( m_edges.getBlob() ); };
    inline const GeometryTriangles& getTriangles() const { return( m_triangles ); };
    inline const GeometrySurfaces&  getSurfaces() const { return( m_surfaces ); };
	void                            buildOutlining( GeometryOutlining& outlining, const uint32_t itriangle, const OutliningConfig& config ) const;
protected:
    void                            zero();
    void                            resetTopology();
    void                            chainVert( const maths::vec3& position, const uint32_t params );
    uint32_t                        insertPos( const maths::vec3& position );
    uint32_t                        insertVert( const maths::vec3& position );
    uint32_t                        insertDesc( const maths::vec3& position, const uint32_t params );
    uint32_t                        insertEdge( const uint32_t ivert1, const uint32_t ivert2, const uint32_t itriangle );
    void                            appendTri( const uint32_t idesc1, const uint32_t idesc2, const uint32_t idesc3 );
    bool                            m_valid;
    bool                            m_chainStrip;
    uint32_t                        m_chainIndex;
    maths::vec3                     m_chainVerts[ 2 ];
    uint32_t                        m_chainParams[ 2 ];
    uint                            m_chainIndices[ 2 ];
    uint32_t                        m_usage;
    uint32_t                        m_params;
    float                           m_tolerance;
    float                           m_sqrTolerance;
    maths::sphere                   m_sphereBounds;
    maths::aabb                     m_aabbBounds;
    maths::vec3                     m_offset;
    maths::vec3                     m_scale;
    GeometryPlanes                  m_planes;
    GeometryPositions               m_positions;
    GeometryDescArray               m_descs;
    GeometryVertArray               m_verts;
    GeometryEdgeArray               m_edges;
    GeometryTriangles               m_triangles;
    GeometrySurfaces                m_surfaces;
private:
    inline const maths::vec3&       getVertPosition( const uint32_t ivert ) const { return( m_positions.getItem( static_cast< uint >( m_verts.getBlob().getItem( static_cast< uint >( ivert ) ).iposition ) ) ); };
    uint32_t                        findOutlineEdge( const uint32_t isurface, const uint32_t itriangle, const uint32_t iedge, const uint32_t ivert, const uint32_t mask ) const;
    EOutlineType                    getOutlineType( const uint32_t flags ) const;
    inline                          GeometryConstruct( const GeometryConstruct& ) {};
    inline void                     operator=( const GeometryConstruct& ) {};
};

