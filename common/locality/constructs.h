////
////    EConvexity vert, edge, triangle and surface convexity code enumeration
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

enum EConvexity
{
    ENoConvexity = 0,
    EConvexEdge = ( 1 << 0 ),               //  the edge is convex
    EConvexVert = ( 1 << 1 ),               //  the vert is on a convex edge (at least 1 convex edge references this vert)
    EConvexApex = ( 1 << 2 ),               //  the vert is a convex apex (at least 3 convex edges reference this vert)
    EConvexMask = ( EConvexEdge | EConvexVert | EConvexApex ),
    EConcaveEdge = ( 1 << 3 ),              //  the edge is concave
    EConcaveVert = ( 1 << 4 ),              //  the vert is on a concave edge (at least 1 concave edge references this vert)
    EConcaveBase = ( 1 << 5 ),              //  the vert is a concave base (at least 3 concave edges reference this vert)
    EConcaveMask = ( EConcaveEdge | EConcaveVert | EConcaveBase ),
    EOutsideEdge = ( 1 << 6 ),              //  the edge is associated with only 1 triangle
    EOutsideVert = ( 1 << 7 ),              //  the vert is on an outside edge (2 outside edges reference this vert)
    EOutsideMask = ( EOutsideEdge | EOutsideVert ),
    ESectionEdge = ( 1 << 8 ),              //  the edge is a boundary between 2 different materials
    ESectionVert = ( 1 << 9 ),              //  the vert is on a material edge (at least 1 section edge references this vert)
    ESectionMask = ( ESectionEdge | ESectionVert ),
    EConvexityForce32 = 0x7fffffff
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    EOutlineType outlining type indices enumeration
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

enum EOutlineType
{
    EOutlineNone = -1,
    EOutlineConvex = 0,
    EOutlineConcave,
    EOutlineOutside,
    EOutlineSectionFlat,
    EOutlineSectionConvex,
    EOutlineSectionConcave,
    EOutlineTypeCount,
    EOutlineTypeForce32 = 0x7fffffff
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    EOutlineEnable outlining type enabling bit enumeration
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

enum EOutlineEnable
{
    EOutlineEnableConvex         = ( 1 << EOutlineConvex ),
    EOutlineEnableConcave        = ( 1 << EOutlineConcave ),
    EOutlineEnableOutside        = ( 1 << EOutlineOutside ),
    EOutlineEnableSectionFlat    = ( 1 << EOutlineSectionFlat ),
    EOutlineEnableSectionConvex  = ( 1 << EOutlineSectionConvex ),
    EOutlineEnableSectionConcave = ( 1 << EOutlineSectionConcave ),
    EOutlineEnableForce32 = 0x7fffffff
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    OutliningConfig outlining configuration data structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct OutliningConfig
{
    float           scale;
    float           thickness[ EOutlineTypeCount ];
    uint            outlineEnable;	//  combination of EOutlineEnable flags
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    OutlineEdge triangle edge outlining vertex offset data structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct OutlineEdge
{
    float           offset[ 3 ];
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    TriangleOutlining triangle edge outlining triangle vertex interpolation data structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct TriangleOutlining
{
	OutlineEdge     edgesT[ 3 ];    //  triangle edges opposite the vertex
    void            setDefault();
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    GeometryOutlining general geometry edge outlining triangle vertex interpolation data structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct GeometryOutlining
{
	OutlineEdge     edgesT[ 3 ];    //  triangle edges opposite the vertex
	OutlineEdge     edgesC[ 3 ];    //  outline edges clockwise from the vertex
	OutlineEdge     edgesA[ 3 ];    //  outline edges anti-clockwise from the vertex
    void            setDefault();
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    GeometryDesc vertex parameter binding structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct GeometryDesc
{
    uint32_t        params;             //  user vertex params
    uint32_t        ivert;              //  vertex index
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    GeometryEdge vertex structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct GeometryVert
{
    uint32_t        flags;              //  convexity flags
    uint32_t        iposition;          //  position index
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    GeometryEdge edge structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct GeometryEdge
{
    uint32_t        flags;              //  convexity flags
    uint32_t        iverts[ 2 ];        //  vertex indices
    uint32_t        itriangles[ 2 ];    //  edge triangle indices
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    GeometryTriangle triangle structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct GeometryTriangle
{
    uint32_t        flags;              //  convexity flags
    uint32_t        usage;              //  usage code (various interpretations)
    uint32_t        isurface;           //  owning surface index
    uint32_t        inext;              //  next triangle index
    uint32_t        idescs[ 3 ];        //  vertex parameter binding indices
    uint32_t        iverts[ 3 ];        //  vertex indices
    uint32_t        iedges[ 3 ];        //  edge indices
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    GeometrySurface surface structure 
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct GeometrySurface
{
    uint32_t        flags;              //  convexity flags
    uint32_t        iplane;             //  plane index
    uint32_t        itriangle;          //  first triangle index
    uint32_t        triangles;          //  triangle count
};

