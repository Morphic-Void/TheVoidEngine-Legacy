    inline                      ItemGeometry() : BaseGeometry(), m_renderer() {};
    inline                      ~ItemGeometry() { kill(); };
    bool                        create( const CoreSystem& system, const constructs::LayeringConstruct& construct );
    void                        destroy();
    void                        relocate( const maths::vec3& scale, const maths::vec3& offset );
    inline ItemRenderer&        getRenderer() { return( m_renderer ); };
    inline const ItemRenderer&  getRenderer() const { return( m_renderer ); };
protected:
    bool                        init( const CoreSystem& system, const constructs::LayeringConstruct& construct );
