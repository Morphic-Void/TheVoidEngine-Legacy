////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    BaseGeometry geometry description base class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class BaseGeometry
{
public:
    inline                      BaseGeometry() : m_valid( false ), m_scaling( maths::consts::VEC3::ONE ), m_offset( maths::consts::VEC3::ZERO ) {};
    inline                      ~BaseGeometry() { kill(); };
    inline bool                 valid() const { return( this && m_valid ); };
    void                        relocate( const maths::vec3& scale, const maths::vec3& offset );
    inline const maths::vec3&   getScaling() const { return( m_scaling ); };
    inline const maths::vec3&   getOffset() const { return( m_offset ); };
protected:
    void                        kill();
    bool                        m_valid;
    maths::vec3                 m_scaling;
    maths::vec3                 m_offset;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    DoorGeometry door geometry description class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class DoorGeometry : public BaseGeometry
{
public:
    inline                      DoorGeometry() : BaseGeometry(), m_renderer() {};
    inline                      ~DoorGeometry() { kill(); };
    bool                        create( const CoreSystem& system, const maths::vec2* const verts, const uint count );
    void                        destroy();
    void                        relocate( const maths::vec3& scale, const maths::vec3& offset );
    inline DoorRenderer&        getRenderer() { return( m_renderer ); };
    inline const DoorRenderer&  getRenderer() const { return( m_renderer ); };
    inline DoorCollider&        getCollider() { return( m_collider ); };
    inline const DoorCollider&  getCollider() const { return( m_collider ); };
protected:
    bool                        init( const CoreSystem& system, const maths::vec2* const verts, const uint count );
    void                        kill();
    DoorRenderer                m_renderer;
    DoorCollider                m_collider;
};

