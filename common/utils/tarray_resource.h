template< typename T >
class TArrayResource : public TArraySort< T >
{
public:
    inline                          TArrayResource() : TArraySort< T >(), m_strings( NULL ) {};
    inline                          TArrayResource( StringTableBlob& strings, const uint count = 32 ) : TArraySort< T >(), m_strings( NULL ) { init( strings, count ); };
    virtual                         ~TArrayResource() { m_strings = NULL; };
    inline bool                     valid() const { return( ( m_strings != NULL ) ? TArraySort< T >::valid() : false ); };
    bool                            init( StringTableBlob& strings, const uint count = 32 );
    inline void                     kill() { m_strings = NULL; TArraySort< T >::kill(); };
    int                             find( const uint8_t* const name, const uint resourceType ) const;
    int                             find( const uint8_t* const name, const uint nameLength, const uint resourceType ) const;
    inline int                      find( const char* const name, const uint resourceType ) const { return( find( reinterpret_cast< const uint8_t* const >( name ), resourceType ) ); };
    inline int                      find( const char* const name, const uint nameLength, const uint resourceType ) const { return( find( reinterpret_cast< const uint8_t* const >( name ), nameLength, resourceType ) ); };
	int                             insert( const T& resource );
	bool                            update( const int index, const T& resource );
protected:
    struct                          KeyDesc { const uint8_t* name; uint nameLength; uint resourceType; };
    virtual const void*             getKey( const int index ) const;
    virtual int                     cmpKey( const void* const key, const int index ) const;
    StringTableBlob*                m_strings;
    mutable KeyDesc                 m_key;
private:
    inline                          TArrayResource( const TArrayResource& ) {};
    inline void                     operator=( const TArrayResource& ) const {};
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    TArrayResource sortable typed array template public function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
bool TArrayResource< T >::init( StringTableBlob& strings, const uint count )
{
    kill();
    uint size = ( ( count < 4 ) ? 4 : count );
    if( TArraySort< T >::init( size ) )
    {
        m_strings = &strings;
        return( true );
    }
    kill();
    return( false );
}

template< typename T >
int TArrayResource< T >::find( const uint8_t* const name, uint resourceType ) const
{
    if( name != NULL )
    {
        uint nameLength = 0;
        while( name[ nameLength ] ) ++nameLength;
        return( find( name, nameLength, resourceType ) );
    }
    return( -1 );
}

template< typename T >
int TArrayResource< T >::find( const uint8_t* const name, const uint nameLength, uint resourceType ) const
{
    if( m_strings != NULL )
    {
        if( name != NULL )
        {
            m_key = { name, nameLength, resourceType };
            return( ArraySort::find( &m_key ) );
        }
        return( -1 );
    }
    ASSERT_FAIL( "TArrayResource< T > uninitialised\n" );
    return( -1 );
}

template< typename T >
int TArrayResource< T >::insert( const T& resource )
{
    if( m_strings != NULL )
    {
        m_key.name = m_strings->getString( resource.nameHandle, m_key.nameLength );
        m_key.resourceType = resource.resourceType;
        int index = ArraySort::insert( &m_key );
        if( index >= 0 )
        {
            m_items.setItem( static_cast< uint >( index ), resource );
        }
        return( index );
    }
    ASSERT_FAIL( "TArrayResource< T > uninitialised\n" );
    return( -1 );
}

template< typename T >
bool TArrayResource< T >::update( const int index, const T& resource )
{
    if( m_strings != NULL )
    {
        if( contains( index ) )
        {
            m_items.setItem( static_cast< uint >( index ), resource );
            ArraySort::update( index );
            return( true );
        }
        ASSERT_FAIL( "TArrayResource< T >::update() failed\n" );
        return( false );
    }
    ASSERT_FAIL( "TArrayResource< T > uninitialised\n" );
    return( false );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    TArrayResource sortable typed array template protected virtual function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
const void* TArrayResource< T >::getKey( const int index ) const
{
    if( m_strings != NULL )
    {
        const T& item = m_items.getItem( static_cast< uint >( index ) );
        m_key.name = m_strings->getString( item.nameHandle, m_key.nameLength );
        m_key.resourceType = item.resourceType;
        return( &m_key );
    }
    return( NULL );
}

template< typename T >
int TArrayResource< T >::cmpKey( const void* const key, const int index ) const
{
    int delta = 0;
    if( m_strings != NULL )
    {
        const T& item = m_items.getItem( static_cast< uint >( index ) );
        const KeyDesc& key1 = *reinterpret_cast< const KeyDesc* const >( key );
        KeyDesc key2;
        key2.name = m_strings->getString( item.nameHandle, key2.nameLength );
        key2.resourceType = item.resourceType;
        delta = static_cast< int >( key1.resourceType - key2.resourceType );
        if( delta == 0 )
        {
            delta = static_cast< int >( key1.nameLength - key2.nameLength );
            if( delta == 0 )
            {
                delta = memcmp( key1.name, key2.name, key2.nameLength );
            }
        }
    }
    return( delta );
}

