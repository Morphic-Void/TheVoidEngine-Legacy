template< typename T >
class TArrayNamed : public TArraySort< T >
{
public:
    inline                          TArrayNamed() : TArraySort< T >(), m_strings( NULL ) {};
    inline                          TArrayNamed( StringTableBlob& strings, const uint count = 32 ) : TArraySort< T >(), m_strings( NULL ) { init( strings, count ); };
    virtual                         ~TArrayNamed() { m_strings = NULL; };
    inline bool                     valid() const { return( ( m_strings != NULL ) ? TArraySort< T >::valid() : false ); };
    bool                            init( StringTableBlob& strings, const uint count = 32 );
    inline void                     kill() { m_strings = NULL; TArraySort< T >::kill(); };
    int                             find( const uint8_t* const name ) const;
    int                             find( const uint8_t* const name, const uint nameLength ) const;
    inline int                      find( const char* const name ) const { return( find( reinterpret_cast< const uint8_t* const >( name ) ) ); };
    inline int                      find( const char* const name, const uint length ) const { return( find( reinterpret_cast< const uint8_t* const >( name ), length ) ); };
	int                             insert( const T& item );
	bool                            update( const int index, const T& item );
protected:
    struct                          KeyDesc { const uint8_t* name; uintptr_t nameLength; };
    virtual const void*             getKey( const int index ) const;
    virtual int                     cmpKey( const void* const key, const int index ) const;
    StringTableBlob*                m_strings;
    mutable KeyDesc                 m_key;
private:
    inline                          TArrayNamed( const TArrayNamed& ) {};
    inline void                     operator=( const TArrayNamed& ) const {};
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    TArrayNamed sortable typed array template public function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
bool TArrayNamed< T >::init( StringTableBlob& strings, const uint count )
{
    kill();
    uint size = ( ( count < 4 ) ? 4 : count );
    if( TArraySort< T >::init( size ) )
    {
        m_strings = &strings;
        return( true );
    }
    kill();
    return( false );
}

template< typename T >
int TArrayNamed< T >::find( const uint8_t* const name ) const
{
    if( name != NULL )
    {
        uint nameLength = 0;
        while( name[ nameLength ] ) ++nameLength;
        return( find( name, nameLength ) );
    }
    return( -1 );
}

template< typename T >
int TArrayNamed< T >::find( const uint8_t* const name, const uint nameLength ) const
{
    if( m_strings != NULL )
    {
        if( name != NULL )
        {
            m_key = { name, static_cast< uintptr_t >( nameLength ) };
            return( ArraySort::find( &m_key ) );
        }
        return( -1 );
    }
    ASSERT_FAIL( "TArrayNamed< T > uninitialised\n" );
    return( -1 );
}

template< typename T >
int TArrayNamed< T >::insert( const T& item )
{
    if( m_strings != NULL )
    {
        uint nameLength = 0;
        m_key.name = m_strings->getString( item.nameHandle, nameLength );
        m_key.nameLength = nameLength;
        int index = ArraySort::insert( &m_key );
        if( index >= 0 )
        {
            m_items.setItem( static_cast< uint >( index ), item );
        }
        return( index );
    }
    ASSERT_FAIL( "TArrayNamed< T > uninitialised\n" );
    return( -1 );
}

template< typename T >
bool TArrayNamed< T >::update( const int index, const T& item )
{
    if( m_strings != NULL )
    {
        if( contains( index ) )
        {
            m_items.setItem( static_cast< uint >( index ), item );
            ArraySort::update( index );
            return( true );
        }
        ASSERT_FAIL( "TArrayNamed< T >::update() failed\n" );
        return( false );
    }
    ASSERT_FAIL( "TArrayNamed< T > uninitialised\n" );
    return( false );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    TArrayNamed sortable typed array template protected virtual function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
const void* TArrayNamed< T >::getKey( const int index ) const
{
    if( m_strings != NULL )
    {
        uint nameLength = 0;
        const T& item = m_items.getItem( static_cast< uint >( index ) );
        m_key.name = m_strings->getString( item.nameHandle, nameLength );
        m_key.nameLength = nameLength;
        return( &m_key );
    }
    return( NULL );
}

template< typename T >
int TArrayNamed< T >::cmpKey( const void* const key, const int index ) const
{
    int delta = 0;
    if( m_strings != NULL )
    {
        uint nameLength = 0;
        const T& item = m_items.getItem( static_cast< uint >( index ) );
        const KeyDesc& key1 = *reinterpret_cast< const KeyDesc* const >( key );
        KeyDesc key2;
        key2.name = m_strings->getString( item.nameHandle, nameLength );
        key2.nameLength = nameLength;
        delta = static_cast< int >( key1.nameLength - key2.nameLength );
        if( delta == 0 )
        {
            delta = memcmp( key1.name, key2.name, static_cast< size_t >( key2.nameLength ) );
        }
    }
    return( delta );
}

