////    NamedShares named shared pointer registry class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class NamedShares : public IArrayQuery
{
public:
    inline                  NamedShares() : m_sorting(), m_strings( NULL ), m_items( NULL ), m_null(), m_size( 0 ) {};
    inline                  NamedShares( StringTableBlob& strings, const int count = 32 ) : m_sorting(), m_strings( NULL ), m_items( NULL ), m_null(), m_size( 0 ) { init( strings, count ); };
    inline                  ~NamedShares() { kill(); };
    inline bool             valid() const { return( ( m_strings && m_items ) ? true : false ); };
    inline bool             valid( const int index ) const { return( ( static_cast< uint >( index ) < static_cast< uint >( getCount() ) ) ? true : false ); };
    bool                    init( StringTableBlob& strings, const int count = 32 );
    void                    kill();
    int                     findIndex( const StringItem& name ) const;
    inline int              getCount() const { return( valid() ? m_sorting.used() : 0 ); };
    bool                    getName( const int index, StringItem& name ) const;
    const SharedPtr&        getItem( const int index ) const;
    bool                    addItem( const StringItem& name, const SharedPtr& sharedPtr, const bool allowReplace = false );
    bool                    replace( const int index, const SharedPtr& sharedPtr );
protected:
    struct                  Item { SharedPtr sharedPtr; uintptr_t nameHandle; };
    virtual const void*     getKey( const int index ) const;
    virtual int             cmpKey( const void* const key, const int index ) const;
    virtual void            moveItem( const int source, const int target );
    ArraySort               m_sorting;
    StringTableBlob*        m_strings;
    Item*                   m_items;
    mutable Item            m_temp;
    SharedPtr               m_null;
    int                     m_size;
private:
    inline                  NamedShares( const NamedShares& ) {};
    inline void             operator=( const NamedShares& ) {};
};

