////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    CArrayAllocBase class
////
////    Base class for all array allocators
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class CArrayAllocBase
{
public:
    inline                  CArrayAllocBase() { zero(); };
    inline                  ~CArrayAllocBase() { zero(); };
    inline uint32_t         getSlotSize() const { return( m_slotSize ); };
    inline uint32_t         getSlotCount() const { return( m_slotCount ); };
    inline uint32_t         getFreeCount() const { return( m_freeCount ); };
    inline uint32_t         getUsedCount() const { return( m_usedCount ); };
    inline uint32_t         getPeakCount() const { return( m_peakCount ); };
protected:
    inline void             zero();
    uint8_t*                m_memory;
    uint32_t                m_slotSize;
    uint32_t                m_slotCount;
    uint32_t                m_freeCount;
    uint32_t                m_usedCount;
    uint32_t                m_peakCount;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    CIndexAlloc1k class
////
////    Notes:
////
////        Index allocator for upto 1024 allocations.
////
////        Memory usage of 148 bytes or approx 1 bit per allocation.
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class CIndexAlloc1k : public CArrayAllocBase
{
public:
    inline                  CIndexAlloc1k() { freeAll(); };
    inline                  ~CIndexAlloc1k() { freeAll(); };
    inline bool             isAllocated( const int index );
    int                     allocIndex();
    bool                    allocIndex( const int index );
    bool                    freeIndex( const int index );
    void                    freeAll();
protected:
    uint32_t                m_slots0;
    uint32_t                m_slots1[ 32 ];
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    CIndexAlloc32k class
////
////    Notes:
////
////        Index allocator for upto 32,768 allocations.
////
////        Memory usage of 4244 bytes (about 4k) or approx. 1 bit per allocation.
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class CIndexAlloc32k : public CArrayAllocBase
{
public:
    inline                  CIndexAlloc32k() { freeAll(); };
    inline                  ~CIndexAlloc32k() { freeAll(); };
    inline bool             isAllocated( const int index );
    int                     allocIndex();
    bool                    allocIndex( const int index );
    bool                    freeIndex( const int index );
    void                    freeAll();
protected:
    uint32_t                m_slots0;
    uint32_t                m_slots1[ 32 ];
    uint32_t                m_slots2[ 32 * 32 ];
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    CIndexAlloc1024k class
////
////    Notes:
////
////        Index allocator for upto 1,048,576 allocations.
////
////        Memory usage of 135316 bytes (about 132k) or approx. 1 bit per allocation.
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class CIndexAlloc1024k : public CArrayAllocBase
{
public:
    inline                  CIndexAlloc1024k() { freeAll(); };
    inline                  ~CIndexAlloc1024k() { freeAll(); };
    inline bool             isAllocated( const int index );
    int                     allocIndex();
    bool                    allocIndex( const int index );
    bool                    freeIndex( const int index );
    void                    freeAll();
protected:
    uint32_t                m_slots0;
    uint32_t                m_slots1[ 32 ];
    uint32_t                m_slots2[ 32 * 32 ];
    uint32_t                m_slots3[ 32 * 32 * 32 ];
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    inline function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void CArrayAllocBase::zero()
{
    m_memory = 0;
    m_slotSize = 0;
    m_slotCount = m_freeCount = m_usedCount = m_peakCount = 0;
}

bool CIndexAlloc1k::isAllocated( const int index )
{
    return( ( ( static_cast< uint32_t >( index ) < m_slotCount ) && ( ( m_slots1[ index >> 5 ] >> ( index & 31 ) ) & 1 ) ) ? true : false );
}

bool CIndexAlloc32k::isAllocated( const int index )
{
    return( ( ( static_cast< uint32_t >( index ) < m_slotCount ) && ( ( m_slots2[ index >> 5 ] >> ( index & 31 ) ) & 1 ) ) ? true : false );
}

bool CIndexAlloc1024k::isAllocated( const int index )
{
    return( ( ( static_cast< uint32_t >( index ) < m_slotCount ) && ( ( m_slots3[ index >> 5 ] >> ( index & 31 ) ) & 1 ) ) ? true : false );
}

