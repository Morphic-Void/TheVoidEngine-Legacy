class Node;
class Tree;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Node class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Node
{
public:
	inline				    Node() : m_id( 0 ) { m_parent = m_children[ 0 ] = m_children[ 1 ] = 0; m_balance = 0; };
	inline				    Node( const unsigned int id ) : m_id( id ) { m_parent = m_children[ 0 ] = m_children[ 1 ] = 0; m_balance = 0; };
	inline				    ~Node() {};
    inline bool             initID( const unsigned int id ) { if( loose() ) { m_id = id; return( true ); } return( false ); };
	inline unsigned int     getID( void ) const { return( m_id ); };
	Node*		            find( const unsigned int id ) const;
	Node*		            first( void ) const;
	Node*		            last( void ) const;
	Node*		            prev( void ) const;
	Node*		            next( void ) const;
	Node*		            top( void ) const;
	bool				    loose( void ) const;
protected:
	Node*		            setID( const unsigned int id );
	Node*		            insert( Node* const root );
	Node*		            remove( void );
	Node*		            resort( void );
	void			        rotate( void );
	int					    height( void ) const;
	int					    weight( void ) const;
	bool				    validate( void ) const;
	Node*		            m_parent;
	Node*		            m_children[ 2 ];
	int				        m_balance;
	unsigned int	        m_id;
    friend class            Tree;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Tree class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Tree
{
public:
	inline		            Tree() : m_root( 0 ) {};
	inline		            ~Tree() { kill(); };
    inline void             kill() { m_root = 0; };
	bool		            contains( const Node* const node ) const;
	bool		            setID( Node* const node, const unsigned int id );
	bool		            insert( Node* const node );
	bool		            remove( Node* const node );
	void		            resort( void );
	Node*		            find( const unsigned int id ) const;
	Node*		            root( void ) const;
	Node*		            first( void ) const;
	Node*		            last( void ) const;
	int			            height( void ) const;
	int			            weight( void ) const;
	bool		            validate( void ) const;
protected:
	Node*		            m_root;
};

