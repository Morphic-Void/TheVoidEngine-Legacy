////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Blob structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct Blob
{
	const uint8_t*		    data;
    uintptr_t               size;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Node class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Node
{
public:
	inline				    Node() { m_blob.data = 0; m_blob.size = 0; m_parent = m_children[ 0 ] = m_children[ 1 ] = 0; m_balance = 0; };
	inline				    Node( const Blob& blob ) { m_blob.data = blob.data; m_blob.size = blob.size; m_parent = m_children[ 0 ] = m_children[ 1 ] = 0; m_balance = 0; };
	inline				    Node( const uint8_t* const blob, const uint length ) { m_blob.data = blob; m_blob.size = length; m_parent = m_children[ 0 ] = m_children[ 1 ] = 0; m_balance = 0; };
	inline				    ~Node() {};
    inline bool             init( const Blob& blob ) { if( loose() ) { m_blob.data = blob.data; m_blob.size = blob.size; return( true ); } return( false ); };
    inline bool             init( const uint8_t* const blob, const uint size ) { if( loose() ) { m_blob.data = blob; m_blob.size = size; return( true ); } return( false ); };
	inline const Blob&      blob( void ) const { return( m_blob ); };
	Node*		            find( const Blob& blob ) const;
	inline Node*		    find( const uint8_t* const data, const uint size ) { Blob blob; blob.data = data; blob.size = size; return( find( blob ) ); };
	Node*		            first( void ) const;
	Node*		            last( void ) const;
	Node*		            prev( void ) const;
	Node*		            next( void ) const;
	Node*		            top( void ) const;
	bool				    loose( void ) const;
protected:
	Node*		            rename( const Blob& blob );
	inline Node*		    rename( const uint8_t* const data, const uint size ) { Blob blob; blob.data = data; blob.size = size; return( rename( blob ) ); };
	Node*		            insert( Node* const root );
	Node*		            remove( void );
	Node*		            resort( void );
	void				    rotate( void );
	int					    height( void ) const;
	int					    weight( void ) const;
	bool				    validate( void ) const;
    static intptr_t         cmp( const Blob& blob1, const Blob& blob2 );
	Blob		            m_blob;
	Node*		            m_parent;
	Node*		            m_children[ 2 ];
	int					    m_balance;
    friend class            Tree;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Tree class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Tree
{
public:
	inline				    Tree() : m_root( 0 ) {};
	inline		            ~Tree() { kill(); };
    inline void             kill() { m_root = 0; };
	bool				    contains( const Node* const node ) const;
	bool				    rename( Node* const node, const Blob& blob );
	inline bool				rename( Node* const node, const uint8_t* const data, const uint size ) { Blob blob; blob.data = data; blob.size = size; return( rename( node, blob ) ); };
	bool				    insert( Node* const node );
	bool				    remove( Node* const node );
	void				    resort( void );
	Node*		            find( const Blob& blob ) const;
	inline Node*		    find( const uint8_t* const data, const uint size ) const { Blob blob; blob.data = data; blob.size = size; return( find( blob ) ); };
	Node*		            root( void ) const;
	Node*		            first( void ) const;
	Node*		            last( void ) const;
	int					    height( void ) const;
	int					    weight( void ) const;
	bool				    validate( void ) const;
protected:
	Node*		            m_root;
};

