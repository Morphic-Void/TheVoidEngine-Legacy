inline uint32_t		IntToSortable32(const int32_t& i) {return(i^0x80000000);};
inline int32_t		SortableToInt32(const uint32_t& u) {return(u^0x80000000);};
inline uint32_t		FloatToSortable32(const float32_t& f) {alias32_t a=reinterpret_cast<const alias32_t&>(f);a.u^=((a.i>>31)|0x80000000);return(a.u);};
inline float32_t	SortableToFloat32(const uint32_t& u) {alias32_t a;a.u=(u^(((u>>31)-1)|0x80000000));return(a.f);};

