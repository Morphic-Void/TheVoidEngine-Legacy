////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Node class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Node
{
public:
	inline				    Node() : m_name( 0 ) { m_parent = m_children[ 0 ] = m_children[ 1 ] = 0; m_balance = 0; };
	inline				    Node( const uint8_t* const name ) : m_name( name ) { m_parent = m_children[ 0 ] = m_children[ 1 ] = 0; m_balance = 0; };
	inline				    ~Node() {};
    inline bool             init( const uint8_t* const name ) { if( loose() ) { m_name = name; return( true ); } return( false ); };
	inline const uint8_t*   name( void ) const { return( m_name ); };
	Node*		            find( const uint8_t* const name ) const;
	Node*		            first( void ) const;
	Node*		            last( void ) const;
	Node*		            prev( void ) const;
	Node*		            next( void ) const;
	Node*		            top( void ) const;
	bool				    loose( void ) const;
protected:
	Node*		            rename( const uint8_t* const name );
	Node*		            insert( Node* const root );
	Node*		            remove( void );
	Node*		            resort( void );
	void				    rotate( void );
	int					    height( void ) const;
	int					    weight( void ) const;
	bool				    validate( void ) const;
    static int              cmp( const uint8_t* const name1, const uint8_t* const name2 );
	const uint8_t*		    m_name;
	Node*		            m_parent;
	Node*		            m_children[ 2 ];
	int					    m_balance;
    friend class            Tree;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Tree class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Tree
{
public:
	inline				    Tree() : m_root( 0 ) {};
	inline		            ~Tree() { kill(); };
    inline void             kill() { m_root = 0; };
	bool				    contains( const Node* const node ) const;
	bool				    rename( Node* const node, const uint8_t* const name );
	bool				    insert( Node* const node );
	bool				    remove( Node* const node );
	void				    resort( void );
	Node*		            find( const uint8_t* const name ) const;
	Node*		            root( void ) const;
	Node*		            first( void ) const;
	Node*		            last( void ) const;
	int					    height( void ) const;
	int					    weight( void ) const;
	bool				    validate( void ) const;
protected:
	Node*		            m_root;
};

