void KVMap::kill()
{
    while( avl_kv::Node* node = m_tree.root() )
    {
        m_tree.remove( node );
        m_pool.recycle( *node );
    }
}

void KVMap::optimise()
{
    m_tree.resort();
}

bool KVMap::addKey( const uint8_t* const key, void* const value )
{
    avl_kv::Node& node = m_pool.fetch();
    new( &node ) avl_kv::Node( key, value );
    if( m_tree.insert( &node ) )
    {
        return( true );
    }
    m_pool.recycle( node );
    return( false );
}

bool KVMap::changeKey( const uint8_t* const key, void* const value )
{
    avl_kv::Node* node = m_tree.find( key );
    if( node )
    {
        node->set( value );
        return( true );
    }
    return( false );
}

bool KVMap::deleteKey( const uint8_t* const key )
{
    avl_kv::Node* node = m_tree.find( key );
    if( node )
    {
        return( m_tree.remove( node ) );
    }
    return( false );
}

void* KVMap::findValue( const uint8_t* const key ) const
{
    avl_kv::Node* node = m_tree.find( key );
    if( node )
    {
        return( node->get() );
    }
    return( NULL );
}

