class KVMap
{
public:
    inline                  KVMap( KVPool& pool ) : m_pool( pool ) {};
    inline                  ~KVMap() { kill(); };
    void                    kill();
    void                    optimise();
    bool                    addKey( const uint8_t* const key, void* const value );
    bool                    changeKey( const uint8_t* const key, void* const value );
    bool                    deleteKey( const uint8_t* const key );
    void*                   findValue( const uint8_t* const key ) const;
protected:
    KVPool&                 m_pool;
    avl_kv::Tree            m_tree;
private:
    inline void             operator=( const KVMap& ) {};
};

