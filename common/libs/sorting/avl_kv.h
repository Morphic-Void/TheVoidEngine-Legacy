////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Node class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Node
{
public:
	inline				    Node() : m_key( 0 ), m_value( 0 ) { m_parent = m_children[ 0 ] = m_children[ 1 ] = 0; m_balance = 0; };
	inline				    Node( const uint8_t* const key ) : m_key( key ), m_value( 0 ) { m_parent = m_children[ 0 ] = m_children[ 1 ] = 0; m_balance = 0; };
	inline				    Node( const uint8_t* const key, void* const value ) : m_key( key ), m_value( value ) { m_parent = m_children[ 0 ] = m_children[ 1 ] = 0; m_balance = 0; };
	inline				    ~Node() {};
    inline bool             init( const uint8_t* const key ) { if( loose() ) { m_key = key; return( true ); } return( false ); };
    inline bool             init( const uint8_t* const key, void* const value ) { if( loose() ) { m_key = key; m_value = value; return( true ); } return( false ); };
	inline const uint8_t*   key( void ) const { return( m_key ); };
    inline void*            get( void ) { return( m_value ); };
    inline void             set( void* const value ) { m_value = value; };
	Node*		            find( const uint8_t* const key ) const;
	Node*		            first( void ) const;
	Node*		            last( void ) const;
	Node*		            prev( void ) const;
	Node*		            next( void ) const;
	Node*		            top( void ) const;
	bool				    loose( void ) const;
protected:
	Node*		            rename( const uint8_t* const key );
	Node*		            insert( Node* const root );
	Node*		            remove( void );
	Node*		            resort( void );
	void				    rotate( void );
	int					    height( void ) const;
	int					    weight( void ) const;
	bool				    validate( void ) const;
    static int              cmp( const uint8_t* const key1, const uint8_t* const key2 );
	const uint8_t*		    m_key;
    void*                   m_value;
	Node*		            m_parent;
	Node*		            m_children[ 2 ];
	int					    m_balance;
    friend class            Tree;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Tree class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Tree
{
public:
	inline				    Tree() : m_root( 0 ) {};
	inline		            ~Tree() { kill(); };
    inline void             kill() { m_root = 0; };
	bool				    contains( const Node* const node ) const;
	bool				    rename( Node* const node, const uint8_t* const key );
	bool				    insert( Node* const node );
	bool				    remove( Node* const node );
	void				    resort( void );
	Node*		            find( const uint8_t* const key ) const;
	Node*		            root( void ) const;
	Node*		            first( void ) const;
	Node*		            last( void ) const;
	int					    height( void ) const;
	int					    weight( void ) const;
	bool				    validate( void ) const;
protected:
	Node*		            m_root;
};

