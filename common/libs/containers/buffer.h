#include "memory.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    byte data append buffer object class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Buffer : public Memory
{
public:
    inline                  Buffer() : Memory() {};
    inline                  Buffer( Memory&& take_me ) { take_obj( take_me ); };
    inline                  Buffer( const Memory& clone_me ) { clone_obj( clone_me ); };
    inline                  Buffer( const size_t size, const bool clear = false ) : Memory( size, clear ) {};
    inline                  ~Buffer() { kill(); };
    bool                    align( const size_t alignment );
    void                    shrink( const size_t bytes );
    uint8_t*                append( const size_t bytes, const bool clear = true );
    uint8_t*                append( const void* const data, const size_t bytes );
    uint8_t*                insert( const size_t offset, const size_t bytes );
    uint8_t*                insert( const size_t offset, const void* const data, const size_t bytes );
    void                    remove( const size_t offset, const size_t bytes );
    template< typename T >
    inline uint8_t*         append( const T& data ) { return( append( reinterpret_cast< const void* const >( &data ), static_cast< size_t >( sizeof( T ) ) ) ); };
    template< typename T >
    inline uint8_t*         insert( const size_t offset, const T& data ) { return( insert( offset, reinterpret_cast< const void* const >( &data ), static_cast< size_t >( sizeof( T ) ) ) ); };
    template< typename T >
    inline uint8_t*         append( const T* const data, const size_t count ) { return( append( reinterpret_cast< const void* const >( data ), static_cast< size_t >( sizeof( T ) * count ) ) ); };
    template< typename T >
    inline uint8_t*         insert( const size_t offset, const T* const data, const size_t count ) { return( insert( offset, reinterpret_cast< const void* const >( data ), static_cast< size_t >( sizeof( T ) * count ) ) ); };
    uint8_t*                appendString( const uint8_t* const string );
    uint8_t*                appendString( const uint8_t* const string, const size_t length );
    inline uint8_t*         appendString( const char* const string ) { return( appendString( reinterpret_cast< const uint8_t* const >( string ) ) ); };
    inline uint8_t*         appendString( const char* const string, const size_t length ) { return( appendString( reinterpret_cast< const uint8_t* const >( string ), length ) ); };
    uint8_t*                insertString( const size_t offset, const uint8_t* const string );
    uint8_t*                insertString( const size_t offset, const uint8_t* const string, const size_t length );
    inline uint8_t*         insertString( const size_t offset, const char* const string ) { return( insertString( offset, reinterpret_cast< const uint8_t* const >( string ) ) ); };
    inline uint8_t*         insertString( const size_t offset, const char* const string, const size_t length ) { return( insertString( offset, reinterpret_cast< const uint8_t* const >( string ), length ) ); };
    inline size_t           getFree() const { return( m_size - m_used ); };
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////   smart buffer type
