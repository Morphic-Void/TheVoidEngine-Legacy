#include "memory.h"
#include "libs/system/debug/asserts.h"
#include <string.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    simple static byte data buffer object class functions
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Memory::init( const size_t size, const bool clear )
{
    kill();
    m_used = 0;
    m_size = size;
    if( m_size ) 
    {
        m_buffer = new uint8_t[ m_size ];
        if( clear )
        {
            memset( m_buffer, 0, m_size );
        }
    }
}

void Memory::clone_obj( const Memory& clone_me )
{
    if( clone_me.valid() )
    {
        m_used = clone_me.m_used;
        m_size = clone_me.m_size;
        m_buffer = new uint8_t[ m_size ];
        memcpy_s( m_buffer, m_size, clone_me.m_buffer, m_size );
    }
    else
    {
        m_used = m_size = 0;
        m_buffer = NULL;
    }
}

