#include "tsmart.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    simple static byte data buffer object class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Memory
{
public:
    inline                  Memory() : m_used( 0 ), m_size( 0 ), m_buffer( NULL ) {};
    inline                  Memory( Memory&& take_me ) { take_obj( take_me ); };
    inline                  Memory( const Memory& clone_me ) { clone_obj( clone_me ); };
    inline                  Memory( const size_t size, const bool clear = false ) : m_used( 0 ), m_size( 0 ), m_buffer( NULL ) { init( size, clear ); };
    inline                  Memory( uint8_t* const buffer, const size_t size ) : m_used( size ), m_size( size ), m_buffer( buffer ) {};
    inline                  ~Memory() { kill(); };
    inline Memory&          operator =( Memory&& take_me ) { take( take_me ); return( *this ); };
    inline Memory&          operator =( const Memory& clone_me ) { clone( clone_me ); return( *this ); };
    inline bool             valid() const { return( ( ( m_buffer != NULL ) && ( m_size > 0 ) && ( m_used <= m_size ) ) ? true : false ); };
    void                    init( const size_t size = 4096, const bool clear = false );
    inline void             init( uint8_t* const buffer, const size_t size ) { kill(); m_used = m_size = size; m_buffer = buffer; };
    inline void             kill() { m_used = m_size = 0; if( m_buffer != NULL ) { delete[] m_buffer; m_buffer = NULL; } };
    inline void             take( Memory& take_me ) { kill(); take_obj( take_me ); };
    inline void             clone( const Memory& clone_me ) { kill(); clone_obj( clone_me ); };
    inline void             reset() { m_used = 0; };
    inline bool             setUsed( const size_t used ) { if( used <= m_size ) { m_used = used; return( true ); } return( false ); };
    inline size_t           getUsed() const { return( m_used ); };
    inline size_t           getSize() const { return( m_size ); };
    inline uint8_t*         getBuffer() { return( m_buffer ); };
    inline const uint8_t*   getBuffer() const { return( m_buffer ); };
protected:
    inline void             take_obj( Memory& take_me ) { m_used = take_me.m_used; m_size = take_me.m_size; m_buffer = take_me.m_buffer; take_me.m_used = take_me.m_size = 0; take_me.m_buffer = NULL; };
    void                    clone_obj( const Memory& clone_me );
    size_t                  m_used;
    size_t                  m_size;
    uint8_t*                m_buffer;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////   smart memory type
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

typedef TSmart< Memory >    SmartMemory;

