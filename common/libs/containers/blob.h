#include "memory.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    byte data blob object class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Blob : public Memory
{
public:
    inline                  Blob() : Memory() {};
    inline                  Blob( Memory&& take_me ) { take_obj( take_me ); };
    inline                  Blob( const Memory& clone_me ) { clone_obj( clone_me ); };
    inline                  Blob( const size_t size, const bool clear = false ) : Memory( size, clear ) {};
    inline                  ~Blob() { kill(); };
    void                    align( const size_t alignment );
    void                    shrink( const size_t bytes );
    size_t                  append( const size_t bytes, const bool clear = true );
    size_t                  append( const void* const data, const size_t bytes );
    void                    insert( const size_t offset, const size_t bytes );
    void                    insert( const size_t offset, const void* const data, const size_t bytes );
    void                    remove( const size_t offset, const size_t bytes );
    template< typename T >
    inline size_t           append( const T& data ) { return( append( reinterpret_cast< const void* const >( &data ), static_cast< size_t >( sizeof( T ) ) ) ); };
    template< typename T >
    inline void             insert( const size_t offset, const T& data ) { insert( offset, reinterpret_cast< const void* const >( &data ), static_cast< size_t >( sizeof( T ) ) ); };
    template< typename T >
    inline size_t           append( const T* const data, const size_t count ) { return( append( reinterpret_cast< const void* const >( data ), static_cast< size_t >( sizeof( T ) * count ) ) ); };
    template< typename T >
    inline void             insert( const size_t offset, const T* const data, const size_t count ) { insert( offset, reinterpret_cast< const void* const >( data ), static_cast< size_t >( sizeof( T ) * count ) ); };
    size_t                  appendString( const uint8_t* const string );
    size_t                  appendString( const uint8_t* const string, const size_t length );
    inline size_t           appendString( const char* const string ) { return( appendString( reinterpret_cast< const uint8_t* const >( string ) ) ); };
    inline size_t           appendString( const char* const string, const size_t length ) { return( appendString( reinterpret_cast< const uint8_t* const >( string ), length ) ); };
    void                    insertString( const size_t offset, const uint8_t* const string );
    void                    insertString( const size_t offset, const uint8_t* const string, const size_t length );
    inline void             insertString( const size_t offset, const char* const string ) { insertString( offset, reinterpret_cast< const uint8_t* const >( string ) ); };
    inline void             insertString( const size_t offset, const char* const string, const size_t length ) { insertString( offset, reinterpret_cast< const uint8_t* const >( string ), length ); };
protected:
    void                    grow( const size_t size );
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
