////
////    Simple templated smart pointer object
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
class TSmart
{
public:
    static TSmart           create() { return( *reinterpret_cast< TSmart* >( &( new TObj ) ) ); };
    inline                  TSmart() : m_ptr( NULL ) {};
    inline                  TSmart( TSmart&& smart ) { move_ptr( smart ); };
    inline                  TSmart( const TSmart& smart ) { copy_ptr( smart ); };
    inline                  ~TSmart() { release(); };
    inline bool             valid() const { return( m_ptr ? true : false ); };
    inline uint             count() const { return( m_ptr ? m_ptr->count() : 0 ); };
    inline void             release() const { TSmart* ptr = m_ptr; m_ptr = NULL; if( ptr ) ptr->release(); };
    inline TSmart&          operator =( TSmart&& smart ) { TSmart* ptr = m_ptr; move_ptr( smart ); if( ptr ) ptr->release(); return( *this ); };
    inline TSmart&          operator =( const TSmart& smart ) { TSmart* ptr = m_ptr; copy_ptr( smart ); if( ptr ) ptr->release(); return( *this ); };
    inline bool             operator ==( const TSmart& smart ) const { return( ( m_ptr == smart.m_ptr ) ? true : false ); };
    inline bool             operator !=( const TSmart& smart ) const { return( ( m_ptr != smart.m_ptr ) ? true : false ); };
    inline T&               operator *();
    inline const T&         operator *() const;
    inline T*               operator ->();
    inline const T*         operator ->() const;
protected:
    class TObj
    {
    public:
        inline              TObj() : m_refs( 1 ) {};
        inline uint         count() { return( m_refs ); };
        inline T&           get() { return( m_obj ); };
        inline void         addRef() { ++m_refs; };
        inline void         release() { if( m_refs > 0 ) { --m_refs; if( m_refs == 0 ) delete this; } };
    protected:
        inline              ~TObj() { m_refs = 0; };
        uint                m_refs;
        T                   m_obj;
    };
    inline void             move_ptr( TSmart& smart ) { m_ptr = smart.m_ptr; smart.m_ptr = nullptr; };
    inline void             copy_ptr( const TSmart& smart ) { m_ptr = smart.m_ptr; if( m_ptr ) m_ptr->addRef(); };
    mutable TObj*           m_ptr;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Simple templated smart pointer object functions
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
T& TSmart< T >::operator*()
{
    ASSERT( m_ptr );
    return( m_ptr ? m_ptr->get() : *static_cast< T* >( 0 ) );
}

template< typename T >
const T& TSmart< T >::operator*() const
{
    ASSERT( m_ptr );
    return( m_ptr ? m_ptr->get() : *static_cast< T* >( 0 ) );
}

template< typename T >
T* TSmart< T >::operator->()
{
    ASSERT( m_ptr );
    return( m_ptr ? &m_ptr->get() : static_cast< T* >( 0 ) );
}

template< typename T >
const T* TSmart< T >::operator->() const
{
    ASSERT( m_ptr );
    return( m_ptr ? &m_ptr->get() : static_cast< T* >( 0 ) );
}

