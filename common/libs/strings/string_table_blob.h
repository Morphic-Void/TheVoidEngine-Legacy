#include "libs/containers/blob.h"
#include "libs/memory/tarray_sort.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    forward declarations
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class FrozenStringTable;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Internal types
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct OffsetAndLength { uint32_t offset; uint32_t length; };
using StringTableBlobBase = TArraySort< OffsetAndLength >;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    StringTableBlob class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class StringTableBlob : protected StringTableBlobBase
{
public:
    inline                      StringTableBlob( const uint bytes = 1024 ) : StringTableBlobBase(), m_bytes(), m_string( NULL ) { init( bytes ); };
    inline                      StringTableBlob( const uint bytes, const uint count ) : StringTableBlobBase(), m_bytes(), m_string( NULL ) { init( bytes, count ); };
    inline                      ~StringTableBlob() { m_bytes.kill(); m_string = NULL; };
    inline void                 init( const uint bytes = 1024 ) { init( bytes, ( bytes >> 4 ) ); };
    void                        init( const uint bytes, const uint count );
    void                        kill();
    void                        reset();
    inline uint                 getCount() const { return( m_items.getUsed() ); };
    inline uint                 getBytes() const { return( m_bytes.getUsed() ); };
    uint                        addString( const uint8_t* const string );
    uint                        addString( const uint8_t* const string, const uint length );
    inline uint                 addString( const char* const string ) { return( addString( reinterpret_cast< const uint8_t* >( string ) ) ); };
    inline uint                 addString( const char* const string, const uint length ) { return( addString( reinterpret_cast< const uint8_t* >( string ), length ) ); };
    const uint8_t*              getString( const uint handle ) const;
    const uint8_t*              getString( const uint handle, uint& length ) const;
    uint                        getStringLength( const uint handle ) const;
    uint                        findString( const uint8_t* const string ) const;
    uint                        findString( const uint8_t* const string, const uint length ) const;
    inline uint                 findString( const char* const string ) const { return( findString( reinterpret_cast< const uint8_t* >( string ) ) ); };
    inline uint                 findString( const char* const string, const uint length ) const { return( findString( reinterpret_cast< const uint8_t* >( string ), length ) ); };
    inline const int            getRank( const uint handle ) const { return( getIndexRank( getIndex( handle ) ) ); };
    inline int                  getIndex( const uint handle ) const { return( static_cast< int >( ~handle ) ); };
    inline const int            getIndexRank( const int index ) const { return( rank( index ) ); };
    inline const int*           getIndexRanks() const { return( rank() ); };
    inline const int            getFrozenIndex( const uint handle ) const { return( getRank( handle ) ); };
    uint                        getFrozenSize() const;
    inline FrozenStringTable*   freeze() const { uint size = 0; return( freeze( size ) ); };
    FrozenStringTable*          freeze( uint& size ) const;
    uint                        freeze( void* const buffer, const uint buffer_size ) const;
protected:
    virtual int                 cmpKey( const void* const key, const int index ) const;
    Blob                        m_bytes;
    mutable const uint8_t*      m_string;
private:
    inline                      StringTableBlob( const StringTableBlob& ) {};
    inline void                 operator=( const StringTableBlob& ) {};
};

