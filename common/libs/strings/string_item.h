class StringItem
{
public:
    inline                      StringItem() : m_string( NULL ), m_length( 0 ) {};
    inline                      StringItem( const uint8_t* const string ) { setString( string ); };
    inline                      StringItem( const uint8_t* const string, const uint length ) { setString( string, length ); };
    inline                      StringItem( const char* const string ) { setString( string ); };
    inline                      StringItem( const char* const string, const uint length ) { setString( string, length ); };
    inline                      ~StringItem() { m_string = NULL; m_length = 0; };
    inline bool                 isEmpty() const { return( ( m_string && m_length ) ? false : true ); };
    inline bool                 notEmpty() const { return( ( m_string && m_length ) ? true : false ); };
    inline const uint8_t*       getString( uint& length ) const { length = static_cast< uint >( m_length ); return( m_string ); };
    inline const uint8_t*       getString() const { return( m_string ); };
    inline const uint           getLength() const { return( static_cast< uint >( m_length ) ); };
    inline void                 setString( const uint8_t* const string ) { uintptr_t length = 0; if( string ) while( string[ length ] ) ++length; m_string = string; m_length = length; };
    inline void                 setString( const uint8_t* const string, const uint length ) { m_string = string; m_length = static_cast< uintptr_t >( length ); };
    inline void                 setString( const char* const string ) { setString( reinterpret_cast< const uint8_t* >( string ) ); };
    inline void                 setString( const char* const string, const uint length ) { setString( reinterpret_cast< const uint8_t* >( string ), length ); };
protected:
    const uint8_t*              m_string;
    uintptr_t                   m_length;
};

