class FrozenStringTable
{
public:
    inline              FrozenStringTable() { m_data.count = 0; };
    inline              ~FrozenStringTable() {};
    const uint8_t*      getString( const int index ) const;
    const uint8_t*      getString( const int index, uint& length ) const;
    uint                getStringLength( const int index ) const;
    int                 findString( const uint8_t* const string ) const;
    int                 findString( const uint8_t* const string, const uint length ) const;
    inline int          findString( const char* const string ) const { return( findString( reinterpret_cast< const uint8_t* const >( string ) ) ); };
    inline int          findString( const char* const string, const uint length ) const { return( findString( reinterpret_cast< const uint8_t* const >( string ), length ) ); };
    int                 cmpString( const uint8_t* const string, const uint index ) const;
    int                 cmpString( const uint8_t* const string, const uint length, const uint index ) const;
    inline int          cmpString( const char* const string, const uint index ) const { return( cmpString( reinterpret_cast< const uint8_t* const >( string ), index ) ); };
    inline int          cmpString( const char* const string, const uint length, const uint index ) const { return( cmpString( reinterpret_cast< const uint8_t* const >( string ), length, index ) ); };
    uint                getSize() const;
protected:
    int                 cmp( const uint8_t* const string, const uint length, const uint index ) const;
    struct String
    {
        uint32_t        offset;
        uint32_t        length;
    };
    struct Data
    {
        uint32_t        count;
        String          strings[ 1 ];
    };
    Data                m_data;
private:
    inline              FrozenStringTable( const FrozenStringTable& ) {};
    inline void         operator=( const FrozenStringTable& ) {};
};

