void StringsBuffer::init( const uint bytes, const uint count )
{
    kill();
    m_bytes.init( ( bytes < 1024 ) ? 1024 : bytes );
    m_items.init( ( count < 32 ) ? 32 : count );
    appendString( "", 0 );
}

void StringsBuffer::kill()
{
    m_bytes.kill();
    m_items.kill();
}

void StringsBuffer::reset()
{
    m_bytes.reset();
    m_items.reset();
    appendString( "", 0 );
}

uint StringsBuffer::appendString( const uint8_t* const string )
{
    uint length = 0;
    if( string ) while( string[ length ] ) ++length;
    return( appendString( string, length ) );
}

uint StringsBuffer::appendString( const uint8_t* const string, const uint length )
{
    Item item = { m_bytes.appendString( string, length ), static_cast< uintptr_t >( length ) };
    return( m_items.append( item ) );
}

bool StringsBuffer::insertString( const uint index, const uint8_t* const string )
{
    uint length = 0;
    if( string ) while( string[ length ] ) ++length;
    return( insertString( index, string, length ) );
}

bool StringsBuffer::insertString( const uint index, const uint8_t* const string, const uint length )
{
    if( index <= m_items.getUsed() )
    {
        Item item = { m_bytes.appendString( string, length ), static_cast< uintptr_t >( length ) };
        m_items.insert( index, item );
        return( true );
    }
    return( false );
}

const uint8_t* StringsBuffer::getString( const uint index ) const
{
    if( index < m_items.getUsed() )
    {
        return( m_items.getItem( index ).string );
    }
    return( NULL );
}

const uint8_t* StringsBuffer::getString( const uint index, uint& length ) const
{
    if( index < m_items.getUsed() )
    {
        const Item& item = m_items.getItem( index );
        length = static_cast< uint >( item.length );
        return( item.string );
    }
    length = 0;
    return( NULL );
}

uint StringsBuffer::getStringLength( const uint index ) const
{
    if( index < m_items.getUsed() )
    {
        return( static_cast< uint >( m_items.getItem( index ).length ) );
    }
    return( 0 );
}

