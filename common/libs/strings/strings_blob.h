#include "libs/containers/blob.h"
#include "libs/memory/tblob.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    StringsBlob class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class StringsBlob
{
public:
    inline              StringsBlob( const uint size = 1024 ) { init( size ); };
    inline              StringsBlob( const uint size, const uint count ) { init( size, count ); };
    inline              ~StringsBlob() { kill(); };
    inline void         init( const uint size = 1024 ) { init( size, ( size >> 4 ) ); };
    void                init( const uint size, const uint count );
    void                kill();
    void                reset();
    inline uint         getCount() const { return( m_items.getUsed() ); };
    inline uint         getBytes() const { return( m_bytes.getUsed() ); };
    uint                appendString( const uint8_t* const string );
    uint                appendString( const uint8_t* const string, const uint length );
    inline uint         appendString( const char* const string ) { return( appendString( reinterpret_cast< const uint8_t* const >( string ) ) ); };
    inline uint         appendString( const char* const string, const uint length ) { return( appendString( reinterpret_cast< const uint8_t* const >( string ), length ) ); };
    bool                insertString( const uint index, const uint8_t* const string );
    bool                insertString( const uint index, const uint8_t* const string, const uint length );
    inline bool         insertString( const uint index, const char* const string ) { return( insertString( index, reinterpret_cast< const uint8_t* const >( string ) ) ); };
    inline bool         insertString( const uint index, const char* const string, const uint length ) { return( insertString( index, reinterpret_cast< const uint8_t* const >( string ), length ) ); };
    const uint8_t*      getString( const uint index ) const;
    const uint8_t*      getString( const uint index, uint& length ) const;
    uint                getStringLength( const uint index ) const;
protected:
    struct Item
    {
        uint32_t        offset;
        uint32_t        length;
    };
    TBlob< Item >       m_items;
    Blob                m_bytes;
private:
    inline              StringsBlob( const StringsBlob& ) {};
    inline void         operator=( const StringsBlob& ) {};
};

