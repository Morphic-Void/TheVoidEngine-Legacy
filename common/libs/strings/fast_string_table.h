class FrozenStringTable;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    StringTableBuffer class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class FastStringTable : protected NBuffer
{
public:
    class Handle
    {
    public:
        inline                          Handle() : m_handle( 0 ) {};
        inline                          Handle( const Handle& handle ) : m_handle( handle.m_handle ) {};
        inline                          ~Handle() { m_handle = 0; };
    protected:
        const Handle*                   m_handle;
    };
    inline                              FastStringTable( const uint size = 4096 ) : NBuffer( size ), m_handles( ( size >> 4 ) + 1 ), m_tree() {};
    inline                              ~FastStringTable() { kill(); };
    void                                init( const uint size = 4096 );
    void                                kill();
    void                                reset();
    Handle                              addString( const uint8_t* const string );
    Handle                              addString( const uint8_t* const string, const uint length );
    inline Handle                       addString( const char* const string ) { return( addString( reinterpret_cast< const uint8_t* >( string ) ) ); };
    inline Handle                       addString( const char* const string, const uint length ) { return( addString( reinterpret_cast< const uint8_t* >( string ), length ) ); };
    Handle                              getStringHandle( const int index ) const;
    const uint8_t*                      getString( const Handle handle ) const;
    const uint8_t*                      getString( const Handle handle, uint& length ) const;
    uint                                getStringLength( const Handle handle ) const;
    int                                 getStringIndex( const Handle handle ) const;
    const uint8_t*                      getString( const int index ) const;
    const uint8_t*                      getString( const int index, uint& length ) const;
    uint                                getStringLength( const int index ) const;
    Handle                              findString( const uint8_t* const string ) const;
    Handle                              findString( const uint8_t* const string, const uint length ) const;
    inline Handle                       findString( const char* const string ) { return( findString( reinterpret_cast< const uint8_t* >( string ) ) ); };
    inline Handle                       findString( const char* const string, const uint length ) { return( findString( reinterpret_cast< const uint8_t* >( string ), length ) ); };
    uint                                getFrozenSize() const;
    FrozenStringTable*                  freeze() const;
    void*                               freeze( uint& size ) const;
    uint                                freeze( void* const buffer, const uint buffer_size ) const;
protected:
    inline Handle                       encodeHandle( const sorting::avl_blob::Node* node ) const { return( *reinterpret_cast< Handle* >( &node ) ); };
    inline sorting::avl_blob::Node*     decodeHandle( const Handle handle ) const { return( *reinterpret_cast< sorting::avl_blob::Node* const* >( &handle ) ); };
    TSlot< sorting::avl_blob::Node >    m_handles;
    sorting::avl_blob::Tree             m_tree;
private:
    inline                              FastStringTable( const FastStringTable& ) {};
    inline void                         operator=( const FastStringTable& ) {};
};

