////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    vector text buffer structures
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct VTEXT_LINE
{	//	a single text line
	inline			VTEXT_LINE( void ) {};
	inline			VTEXT_LINE( const unsigned int _width, char* const _text, char* const _codes = 0 ) : width( _width ), text( _text ), codes( _codes ) {};
	unsigned int	width;		//	characters used
	char*			text;		//	character buffer
	char*			codes;		//	character effect codes (i.e. style, colour, flashing, pulsing etc.)
};

struct VTEXT_LINES
{	//	text as a series of lines
	inline			VTEXT_LINES( void ) {};
	inline			VTEXT_LINES( const unsigned int _height, VTEXT_LINE** const _lines ) : start( 0 ), height( _height ), lines( _lines ) {};
	unsigned int	start;		//	buffer start line (buffer wraps vertically)
	unsigned int	height;		//	lines used
	VTEXT_LINE**	lines;		//	line buffers
};

struct VTEXT_CMAP
{	//	a fixed width and height text buffer
	inline			VTEXT_CMAP( void ) {};
	inline			VTEXT_CMAP( const unsigned int _width, const unsigned int _height, char* const _text, char* const _codes = 0 ) : start( 0 ), width( _width ), height( _height ), text( _text ), codes( _codes ) {};
	unsigned int	start;		//	buffer start line (buffer wraps vertically)
	unsigned int	width;		//	buffer width in characters
	unsigned int	height;		//	buffer height in characters
	char*			text;		//	character buffer
	char*			codes;		//	character effect codes (i.e. style, colour, flashing, pulsing etc.)
};

struct VTEXT_RAW
{	//	a raw text file
	inline			VTEXT_RAW( void ) {};
	inline			VTEXT_RAW( const unsigned int _size, char* const _text, char* const _codes = 0 ) : start( 0 ), size( _size ), text( _text ), codes( _codes ) {};
	unsigned int	start;		//	starting line
	unsigned int	size;		//	characters in buffer
	char*			text;		//	character buffer
	char*			codes;		//	character effect codes (i.e. style, colour, flashing, pulsing etc.)
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    vector text handling
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Text
{
public:
	inline			Text( const VFONT font = VFONT_DEFAULT, const unsigned int scale = 1, const bool descenders = true ) : m_font( font ), m_tabs( 4 ), m_scale( scale ), m_descenders( descenders ), m_clip( false ) { m_rect.left = m_rect.top = m_rect.right = m_rect.bottom = 0; m_origin.x = m_origin.y = 0; m_spacing.x = m_spacing.y = -1; };
	inline			~Text() {};
	inline void	    Reset() { m_font = VFONT_DEFAULT; m_tabs = 4; m_scale = 1; m_descenders = true; m_clip = false; m_rect.left = m_rect.top = m_rect.right = m_rect.bottom = 0; m_origin.x = m_origin.y = 0; m_spacing.x = m_spacing.y = -1; };
	inline void		SetFont( const VFONT font = VFONT_DEFAULT ) { m_font = font; };
	inline void		SetFont( const VFONT font, const unsigned int scale ) { m_font = font; m_scale = scale; };
	inline void		SetFont( const VFONT font, const unsigned int scale, const bool descenders ) { m_font = font; m_scale = scale; m_descenders = descenders; };
	inline void		SetScale( const unsigned int scale ) { m_scale = scale; };
	inline void		SetDescenders( const bool descenders = true ) { m_descenders = descenders; };
	inline void		SetTabs( const unsigned int tabs = 1 ) { m_tabs = tabs; };
	inline void		SetOrigin( const int x, const int y ) { m_origin.x = x; m_origin.y = y; };
	inline void		SetOriginX( const int x ) { m_origin.x = x; };
	inline void		SetOriginY( const int y ) { m_origin.y = y; };
	inline void		SetSpacing( const unsigned int h, const unsigned int v ) { m_spacing.x = static_cast< int >( h ); m_spacing.y = static_cast< int >( v ); };
	inline void		SetClipping( const SRECT& clip ) { m_clip = true; m_rect.left = clip.left; m_rect.top = clip.top; m_rect.right = clip.right; m_rect.bottom = clip.bottom; };
	inline void		EnableClipping( const bool enable = true ) { m_clip = enable; };
	inline void		DisableClipping( const bool disable = true ) { m_clip = !disable; };
	inline void		SetDefaultSpacing( void ) { m_spacing.x = m_spacing.y = -1; };
    inline int      GetOriginX( void ) const { return( m_origin.x ); };
    inline int      GetOriginY( void ) const { return( m_origin.y ); };
	void			GetSpacing( unsigned int& h, unsigned int& v ) const;
	void			GetStepping( unsigned int& x, unsigned int& y ) const;
	void			GetFontCharDimensions( unsigned int& w, unsigned int& h ) const;
	void			ConvertCharsToClip( const unsigned int chars_wide, const unsigned int chars_high, unsigned int& clip_width, unsigned int& clip_height, const bool spaced = false ) const;
	void			ConvertClipToChars( const unsigned int clip_width, const unsigned int clip_height, unsigned int& chars_wide, unsigned int& chars_high, const bool spaced = false ) const;
	void			Draw( ICharacterStream& stream, const char* const text, const char* const codes = 0 );
	void			Draw( ICharacterStream& stream, const VTEXT_LINES& lines );
	void			Draw( ICharacterStream& stream, const VTEXT_CMAP& cmap );
	void			Draw( ICharacterStream& stream, const VTEXT_RAW& raw );
protected:
	struct SETUP
	{
		SRECT		rect;		//	character window (inclusive)
		SPOINT		origin;		//	pixel origin of the first character
		SPOINT		stepping;	//	pixel stepping between characters
	};
	const Font&		Setup( SETUP& setup ) const;
	VFONT			m_font;
	unsigned int	m_tabs;
	unsigned int	m_scale;
	bool			m_descenders;
	bool			m_clip;
	SRECT			m_rect;
	SPOINT			m_origin;
	SPOINT			m_spacing;
};

