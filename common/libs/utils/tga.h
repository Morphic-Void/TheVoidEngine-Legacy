////
////    Notes:
////
////    	The pitch and stride used by CreateTGA_Grey are in multiples of uint8_t.
////    	The pitch used by CreateTGA_ARGB is in multiples of uint32_t.
////        The stride is the required texel to texel stepping.
////        The pitch is the required row to row stepping.
