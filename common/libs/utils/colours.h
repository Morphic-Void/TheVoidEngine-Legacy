typedef uint32_t	ARGB8;
typedef uint32_t	RGBA8;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    colour helper macros
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#define MAKE_ARGB8( a, r, g, b ) ( ( ( ( ( ( \
	( static_cast< ARGB8 >( a ) & 255 ) << 8 ) + \
	( static_cast< ARGB8 >( r ) & 255 ) ) << 8 ) + \
	( static_cast< ARGB8 >( g ) & 255 ) ) << 8 ) + \
	( static_cast< ARGB8 >( b ) & 255 ) )

#define UNMAKE_ARGB8( argb, a, r, g, b ) \
	b = ( ( argb ) & 255 ); \
	g = ( ( ( argb ) >> 8 ) & 255 ); \
	r = ( ( ( argb ) >> 16 ) & 255 ); \
	a = ( ( ( argb ) >> 24 ) & 255 );

#define MAKE_XRGB8( r, g, b ) MAKE_ARGB8( 255, r, g, b )

#define UNMAKE_XRGB8( xrgb, r, g, b ) \
	b = ( ( xrgb ) & 255 ); \
	g = ( ( ( xrgb ) >> 8 ) & 255 ); \
	r = ( ( ( xrgb ) >> 16 ) & 255 );

#define MAKE_RGBA8( r, g, b, a ) ( ( ( ( ( ( \
	( static_cast< RGBA8 >( a ) & 255 ) << 8 ) + \
	( static_cast< RGBA8 >( b ) & 255 ) ) << 8 ) + \
	( static_cast< RGBA8 >( g ) & 255 ) ) << 8 ) + \
	( static_cast< RGBA8 >( r ) & 255 ) )

#define UNMAKE_RGBA8( rgba, r, g, b, a ) \
	r = ( ( rgba ) & 255 ); \
	g = ( ( ( rgba ) >> 8 ) & 255 ); \
	b = ( ( ( rgba ) >> 16 ) & 255 ); \
	a = ( ( ( rgba ) >> 24 ) & 255 );

#define MAKE_RGBX8( r, g, b ) MAKE_RGBA8( r, g, b, 255 )

#define UNMAKE_RGBX8( rgbx, r, g, b ) \
	r = ( ( rgbx ) & 255 ); \
	g = ( ( ( rgbx ) >> 8 ) & 255 ); \
	b = ( ( ( rgbx ) >> 16 ) & 255 );

#define	ARGBtoRGBA( argb ) ( ( argb & 0xff00ff00 ) | ( ( argb & 255 ) << 16 ) | ( ( argb >> 16 ) & 255 ) )

#define	RGBAtoARGB( rgba ) ARGBtoRGBA( rgba )

