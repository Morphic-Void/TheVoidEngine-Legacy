class CBitmapARGB
{
public:
	inline		CBitmapARGB() { zero(); };
	inline		CBitmapARGB( ARGB8* const bits, const int width, const int height, const int pitch = 0, const int stride = 0 ) { set( bits, width, height, pitch, stride ); };
	inline		~CBitmapARGB() { zero(); };
	void		set( ARGB8* const bits, const int width, const int height, const int pitch = 0, const int stride = 0 );
	inline int	getWidth( void ) const { return( m_width ); };
	inline int	getHeight( void ) const { return( m_height ); };
	void		clear( const ARGB8 argb );
	void		plot( const ARGB8 argb, const int x, const int y );
	void		draw( const ARGB8 argb, const int x1, const int y1, const int x2, const int y2 );
	void		rect( const ARGB8 argb, const int x, const int y, const int width, const int height );
	void		fill( const ARGB8 argb, const int x, const int y, const int width, const int height );
	bool		sample( const int x, const int y, ARGB8& colour ) const;
protected:
	void		zero( void );
	ARGB8*		m_bits;
	int			m_width;
	int			m_height;
	int			m_stride;
	int			m_pitch;
};

