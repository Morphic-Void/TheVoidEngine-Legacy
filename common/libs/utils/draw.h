////
////    Greyscale bitmap class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class CBitmapGrey
{
public:
	inline		CBitmapGrey() { zero(); };
	inline		CBitmapGrey( uint8_t* const bits, const int width, const int height, const int pitch = 0, const int stride = 0 ) { set( bits, width, height, pitch, stride ); };
	inline		~CBitmapGrey() { zero(); };
	void		set( uint8_t* const bits, const int width, const int height, const int pitch = 0, const int stride = 0 );
	inline int	getWidth( void ) const { return( m_width ); };
	inline int	getHeight( void ) const { return( m_height ); };
	void		clear( const uint8_t grey );
	void		plot( const uint8_t grey, const int x, const int y );
	void		draw( const uint8_t grey, const int x1, const int y1, const int x2, const int y2 );
	void		rect( const uint8_t grey, const int x, const int y, const int width, const int height );
	void		fill( const uint8_t grey, const int x, const int y, const int width, const int height );
	bool		sample( const int x, const int y, uint8_t& grey ) const;
protected:
	void		zero( void );
	uint8_t*	m_bits;
	int			m_width;
	int			m_height;
	int			m_stride;
	int			m_pitch;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Colour bitmap class
