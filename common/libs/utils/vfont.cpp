WriteVectorFontImageFiles writeVectorFontImageFiles;

void WriteVectorFontImageFiles::Run( void )
{
	for( int image = 0; image < 2; ++image )
	{
		static const char* files[ 2 ] = { "output/vfont_x1.tga", "output/vfont_x2.tga" };
		static const int scales[ 2 ] = { 1, 2 };
		static const int widths[ 2 ] = { 276, 552 };
		static const int heights[ 2 ] = { 276, 552 };
		int scale = scales[ image ];
		int width = widths[ image ];
		int height = heights[ image ];
		ARGB8* memory = new ARGB8[ width * height ];
		draw::CBitmapARGB bitmap( memory, width, height );
		bitmap.Clear( MAKE_XRGB8( 0, 31, 0 ) );
		int s = vtext::Font::GetSpacing( scale );
		for( int pass = 0; pass < 9; ++pass )
		{
			static const int x_offsets[] = {  1,  1,  1,  0, -1, -1, -1,  0,  0 };
			static const int y_offsets[] = {  1,  0, -1, -1, -1,  0,  1,  1,  0 };
			int x_offset = x_offsets[ pass ];
			int y_offset = y_offsets[ pass ];
			ARGB8 plot_colour = MAKE_XRGB8( 0, 0, 0 );
			ARGB8 draw_colour = MAKE_XRGB8( 0, 0, 0 );
			if( pass > 7 )
			{
				plot_colour = MAKE_XRGB8( 0, 255, 0 );
				draw_colour = MAKE_XRGB8( 0, 191, 0 );
			}
			int x = s;
			int y = s;
			int w = 0;
			int h = 0;
			for( int font = ( vtext::VFONT_COUNT - 1 ); font >= 0; --font )
			{
				if( x > s )
				{
					x = s;
					y += h;
				}
				unsigned int sw, sh;
				vtext::Font::GetFont( static_cast< vtext::VFONT >( font ) ).GetStepping( sw, sh, scale );
				w = static_cast< int >( sw );
				h = static_cast< int >( sh );
				int dx = 0;
				int dy = 0;
				vtext::Font::GetFont( static_cast< vtext::VFONT >( font ) ).GetOrigin( dx, dy, scale );
				dx -= ( 8 * scale );
				dy -= ( 10 * scale );
				dx += x_offset;
				dy += y_offset;
				for( int character = 0; character < 256; ++character )
				{
					if( vtext::glyphs[ font ][ character ].data )
					{
						const unsigned char* const data = vtext::glyphs[ font ][ character ].data;
						unsigned int vert_count = data[ 0 ];
						if( vert_count )
						{
							unsigned int line_count = data[ 1 ];
							const unsigned char* verts = ( data + 2 );
							const unsigned char* lines = ( verts + vert_count );
							while( line_count )
							{
								int line = *lines;
								int a = ( line >> 4 );
								int b = ( ( line & 15 ) + a );
								int vert1 = verts[ a ];
								int vert2 = verts[ b ];
								int u1 = ( x + dx + ( static_cast< int >( vert1 & 15 ) * scale ) );
								int v1 = ( y + dy + ( static_cast< int >( vert1 >> 4 ) * scale ) );
								int u2 = ( x + dx + ( static_cast< int >( vert2 & 15 ) * scale ) );
								int v2 = ( y + dy + ( static_cast< int >( vert2 >> 4 ) * scale ) );
								bitmap.Draw( draw_colour, u1, v1, u2, v2 );
								++lines;
								--line_count;
							}
							while( vert_count )
							{
								int vert = *verts;
								int u = ( x + dx + ( static_cast< int >( vert & 15 ) * scale ) );
								int v = ( y + dy + ( static_cast< int >( vert >> 4 ) * scale ) );
								bitmap.Plot( plot_colour, u, v );
								++verts;
								--vert_count;
							}
							x += w;
							if( ( x + w ) > width )
							{
								x = s;
								y += h;
							}
						}
					}
				}
			}
		}
		file::SaveTGA_ARGB( files[ image ], memory, static_cast< uint16_t >( width ), static_cast< uint16_t >( height ) );
		delete[] memory;
	}
}

