class WriteReformattedVectorFontDataFile
{
public:
	inline		WriteReformattedVectorFontDataFile() { Run(); };
	inline		~WriteReformattedVectorFontDataFile() {};
private:
	void		Run( void );
};

WriteReformattedVectorFontDataFile writeReformattedVectorFontDataFile;

void WriteReformattedVectorFontDataFile::Run( void )
{
	file::Log log( "output/reformatted_font_data.cpp" );
	log.write(
		"#define GDEF( name, verts, lines ) const unsigned char glyph_##name[verts+lines+2]={verts,lines\n"\
		"#define GEOG };\n"\
		"#define GDUP( name, copy ) const unsigned char* glyph_##name(&glyph_##copy[0]);\n"\
		"struct  GDAT {const unsigned char* data;const char* name;};\n"\
		"#define GREF( name ) {&glyph_##name[0],#name}\n"\
		"#define GNUL {0,0}\n"\
		"#define V(x,y) (((10-(y))<<4)|(8+(x)))\n"\
		"#define L(a,b) (((a)<<4)|((b)-(a)))\n" );
	for( int g = 0; g < ( sizeof( vtext::glyphs ) / sizeof( vtext::GDAT ) ); ++g )
	{
		const unsigned char* const data = vtext::glyphs[ g >> 8 ][ g & 255 ].data;
		if( data )
		{
			bool dup = false;
			unsigned int vert_count = data[ 0 ];
			unsigned int line_count = data[ 1 ];
			unsigned int data_count = ( vert_count + line_count + 2 );
			for( int h = 0; h < g; ++h )
			{
				const unsigned char* const copy = vtext::glyphs[ h >> 8 ][ h & 255 ].data;
				if( copy )
				{
					if( ( vert_count == copy[ 0 ] ) && ( line_count == copy[ 1 ] ) )
					{
						if( memcmp( data, copy, data_count ) == 0 )
						{
							dup = true;
							log.write( "GDUP(%s,%s)\n", vtext::glyphs[ g >> 8 ][ g & 255 ].name, vtext::glyphs[ h >> 8 ][ h & 255 ].name );
							break;
						}
					}
				}
			}
			if( !dup )
			{
				const unsigned char* verts = ( data + 2 );
				const unsigned char* lines = ( verts + vert_count );
				log.write( "GDEF(%s,%d,%d)", vtext::glyphs[ g >> 8 ][ g & 255 ].name, vert_count, line_count );
				if( vert_count )
				{
					while( vert_count )
					{
						--vert_count;
						int v = *verts;
						int x = ( ( v & 15 ) - 8 );
						int y = ( 10 - ( v >> 4 ) );
						log.write( ",V(%d,%d)", x, y );
						++verts;
					}
				}
				if( line_count )
				{
					while( line_count )
					{
						--line_count;
						int l = *lines;
						int a = ( l >> 4 );
						int b = ( ( l & 15 ) + a );
						log.write( ",L(%d,%d)", a, b );
						++lines;
					}
				}
				log.write( " GEOG\n" );
			}
		}
	}
}

