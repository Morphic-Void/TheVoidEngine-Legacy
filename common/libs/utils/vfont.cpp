const Font& Font::GetFont( const VFONT font )
{	//	get a font
	int font_index = ( ( static_cast< unsigned int >( font ) < VFONT_COUNT ) ? font : ( VFONT_COUNT - 1 ) );
	return( *reinterpret_cast< const Font* >( &fonts[ font_index ] ) );
}

const Glyph& Font::GetGlyph( const char c, const bool descenders ) const
{	//	get a glyph
	char g = ( ( !descenders ) && ( ( c >= 'a' ) && ( c <= 'z' ) ) ? ( c - 'a' + 1 ) : c );
	const FDAT& font = *reinterpret_cast< const FDAT* const >( this );
	const unsigned char* glyph = glyphs[ font.font ][ static_cast< unsigned char >( g ) ].data;
	return( *reinterpret_cast< const Glyph* >( ( glyph != NULL ) ? glyph : glyphs[ font.font ][ 0 ].data ) );
}

unsigned int Font::GetWidth( const unsigned int scale ) const
{	//	get inclusive character width
	const FDAT& font = *reinterpret_cast< const FDAT* const >( this );
	return( ( ( font.center << 1 ) * scale ) + 1 );
}

unsigned int Font::GetHeight( const unsigned int scale, const bool descenders ) const
{	//	get inclusive character height
	const FDAT& font = *reinterpret_cast< const FDAT* const >( this );
	return( ( ( font.ascent + ( descenders ? font.descent : font.below ) ) * scale ) + 1 );
}

unsigned int Font::GetSpacing( const unsigned int scale )
{	//	get spacing between characters
	return( ( scale * 3 ) - 1 );
}

void Font::GetOrigin( int& x, int& y, const unsigned int scale ) const
{	//	get character origin coordinate offset
	const FDAT& font = *reinterpret_cast< const FDAT* const >( this );
	x = ( font.center * scale );
	y = ( font.ascent * scale );
}

void Font::GetStepping( unsigned int& x, unsigned int& y, const unsigned int scale, const bool descenders ) const
{	//	get coordinate stepping to the next character
	const FDAT& font = *reinterpret_cast< const FDAT* const >( this );
	x = ( ( ( font.center << 1 ) + 3 ) * scale );
	y = ( ( font.ascent + ( descenders ? font.descent : font.below ) + 3 ) * scale );
}

void Font::GetDimensions( unsigned int& w, unsigned int& h, const unsigned int scale, const bool descenders ) const
{
	const FDAT& font = *reinterpret_cast< const FDAT* const >( this );
	w = ( ( ( font.center << 1 ) * scale ) + 1 );
	h = ( ( ( font.ascent + ( descenders ? font.descent : font.below ) ) * scale ) + 1 );
}

