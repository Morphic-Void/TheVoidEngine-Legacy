const Glyph& Glyph::GetGlyph( const VFONT font, const char c, const bool descenders )
{	//	get a glyph
	char g = ( ( !descenders ) && ( ( c >= 'a' ) && ( c <= 'z' ) ) ? ( c - 'a' + 1 ) : c );
	int font_index = ( ( static_cast< unsigned int >( font ) < VFONT_COUNT ) ? font : ( VFONT_COUNT - 1 ) );
	const unsigned char* glyph = glyphs[ font_index ][ static_cast< unsigned char >( g ) ].data;
	return( *reinterpret_cast< const Glyph* >( ( glyph != NULL ) ? glyph : glyphs[ font_index ][ 0 ].data ) );
}

bool Glyph::GetRect( const int x, const int y, const unsigned int scale, int& left, int& right, int& top, int& bottom ) const
{	//	get the rect extents of this glyph (the glyph width is (right-left+1 )and it's height is (bottom-top+1))
	const unsigned char* const data = reinterpret_cast< const unsigned char* const >( this );
	unsigned int vert_count = data[ 0 ];
	if( vert_count )
	{
		const unsigned char* verts = ( data + 2 );
		int l, r, t, b;
		int c = *verts;
		l = r = ( static_cast< int >( c & 15 ) - 8 );
		t = b = ( 10 - static_cast< int >( c >> 4 ) );
		while( --vert_count )
		{
			++verts;
			c = *verts;
			int u = ( static_cast< int >( c & 15 ) - 8 );
			int v = ( 10 - static_cast< int >( c >> 4 ) );
			if( left > u ) left = u;
			if( right < u ) right = u;
			if( top > v ) top = v;
			if( bottom < v ) bottom = v;
		}
		left = ( ( l * scale ) + x );
		right = ( ( r * scale ) + x );
		top = ( ( t * scale ) + y );
		bottom = ( ( b * scale ) + y );
		return( true );
	}
	return( false );
}

unsigned int Glyph::GetVertCount( void ) const
{	//	get the number of vertices that form this glyph
	return( reinterpret_cast< const unsigned char* const >( this )[ 0 ] );
}

unsigned int Glyph::GetLineCount( void ) const
{	//	get the number of lines that form this glyph
	return( reinterpret_cast< const unsigned char* const >( this )[ 1 ] );
}

void Glyph::Draw( IDirectPointStream& stream, const int x, const int y, const unsigned int scale ) const
{	//	draw this glyph's vertices as a point list
	const unsigned char* const data = reinterpret_cast< const unsigned char* const >( this );
	unsigned int vert_count = data[ 0 ];
	if( vert_count )
	{
		const unsigned char* verts = ( data + 2 );
		while( vert_count )
		{
			int vert = *verts;
			int u = ( x + ( ( static_cast< int >( vert & 15 ) - 8 ) * scale ) );
			int v = ( y + ( ( static_cast< int >( vert >> 4 ) - 10 ) * scale ) );
			stream.AddPoint( u, v );
			++verts;
			--vert_count;
		}
	}
}

void Glyph::Draw( IDirectLineStream& stream, const int x, const int y, const unsigned int scale ) const
{	//	draw this glyph as a line list
	const unsigned char* const data = reinterpret_cast< const unsigned char* const >( this );
	unsigned int vert_count = data[ 0 ];
	if( vert_count )
	{
		unsigned int line_count = data[ 1 ];
		const unsigned char* verts = ( data + 2 );
		const unsigned char* lines = ( verts + vert_count );
		while( line_count )
		{
			int line = *lines;
			int a = ( line >> 4 );
			int b = ( ( line & 15 ) + a );
			int vert1 = verts[ a ];
			int vert2 = verts[ b ];
			int u1 = ( x + ( ( static_cast< int >( vert1 & 15 ) - 8 ) * scale ) );
			int v1 = ( y + ( ( static_cast< int >( vert1 >> 4 ) - 10 ) * scale ) );
			int u2 = ( x + ( ( static_cast< int >( vert2 & 15 ) - 8 ) * scale ) );
			int v2 = ( y + ( ( static_cast< int >( vert2 >> 4 ) - 10 ) * scale ) );
			stream.AddLine( u1, v1, u2, v2 );
			++lines;
			--line_count;
		}
	}
}

void Glyph::Draw( IIndexedLineStream& stream, const int x, const int y, const unsigned int scale ) const
{	//	draw this glyph as an indexed line list
	const unsigned char* const data = reinterpret_cast< const unsigned char* const >( this );
	unsigned int vert_count = data[ 0 ];
	if( vert_count )
	{
		unsigned int line_count = data[ 1 ];
		const unsigned char* verts = ( data + 2 );
		const unsigned char* lines = ( verts + vert_count );
		while( vert_count )
		{
			int vert = *verts;
			int u = ( x + ( ( static_cast< int >( vert & 15 ) - 8 ) * scale ) );
			int v = ( y + ( ( static_cast< int >( vert >> 4 ) - 10 ) * scale ) );
			stream.AddVert( u, v );
			++verts;
			--vert_count;
		}
		while( line_count )
		{
			int line = *lines;
			int a = ( line >> 4 );
			int b = ( ( line & 15 ) + a );
			stream.AddLine( a, b );
			++lines;
			--line_count;
		}
	}
}

