//  calculate simple curvature from the second derivative vector
//      second  :   second derivative vector of the spline
float calculateSimpleCurvature( const vec3& second )
{
	return( second.Length() );
}

//  calculate curvature from the first and second derivative vectors
//      first   :   first derivative vector of the spline
//      second  :   second derivative vector of the spline
float calculateCurvature( const vec3& first, const vec3& second )
{
	float d = first.SqrLen();
	if( d < consts::FLOAT_MIN_RCP )
	{
		return( 0 );
	}
	else
	{
		vec3 a, b;
		a.SetMul( first, ( first.Dot( second ) / d ) );
		b.SetSub ( second, a );
		return( b.Length() );
	}
}

//  calculate a pseudo-sine value between 0 and 1 representative of the curvature
//      first   :   first derivative vector of the spline
//      second  :   second derivative vector of the spline
float calculateCurvaturePseudoSine( const vec3& first, const vec3& second )
{
	float d = ( first.SqrLen() * second.SqrLen() );
    float c = first.Dot( second );
	return( ( d < consts::FLOAT_MIN_RCP ) ? 0 : sqrtf( fabsf( 1.0f - ( ( c * c ) / d ) ) ) );
}

//  calculate a pseudo-sine value between -1 and 1 representative of the curvature
//      first   :   first derivative vector of the spline
//      second  :   second derivative vector of the spline
//      up      :   up vector or normal of the desired plane
float calculatePlanarCurvaturePseudoSine( const vec3& first, const vec3& second, const vec3& up )
{
	vec3 next, bitangent, curvature;
	next.SetAdd( first, second );
	bitangent.SetCross( up, first );
    flattenVectorRelativeToNormal( up, next, curvature );
	float d = ( bitangent.SqrLen() * curvature.SqrLen() );
	return( ( d < consts::FLOAT_MIN_RCP ) ? 0 : ( bitangent.Dot( curvature ) / sqrtf( d ) ) );
}

//  calculate curvature vector (acceleration away from the tangent) from the first and second derivative vectors
//      first   :   first derivative vector of the spline
//      second  :   second derivative vector of the spline
void calculateCurvatureVector( const vec3& first, const vec3& second, vec3& curvature )
{
    return( flattenVectorRelativeToNormal( first, second, curvature ) );
}

