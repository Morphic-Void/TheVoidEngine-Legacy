//  find the un-clamped linear t value of a point projected onto a line segment (a, b)
float getLinearT( const vec3& a, const vec3& b, const vec3& point )
{
	vec3 d;
	d.SetSub( b, a );
	float dd = d.SqrLen();
	if( dd < consts::FLOAT_MIN_RCP )
	{
		return( 0 );
	}
	else
	{
		vec3 p;
		p.SetSub( point, a );
		return( d.Dot( p ) / dd );
	}
}

//  flatten a vector relative to the given normal
void flattenVectorRelativeToNormal( const vec3& normal, const vec3& vector, vec3& flattened )
{
	float d = normal.SqrLen();
	if( d < consts::FLOAT_MIN_RCP )
	{
		flattened.Set( 0, 0, 0 );
	}
	else
	{
		vec3 project;
		project.SetMul( normal, ( normal.Dot( vector ) / d ) );
		flattened.SetSub( vector, project );
	}
}

