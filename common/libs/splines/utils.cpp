////    	general splines:
////
////    		The degree of a curve is the highest power that the interpolation
////    		factor must be raised to.
////
////    		The order of a curve is the degree plus one.
////
////    		The minimum number of control points is equal to the order of the curve.
////
////    	bsplineInsertBlends functions:
////
////    		These calculate the control point blending factors required to insert
////    		a new knot vector at the specified t-value.
////
////    		For quadratic knot insertion, the center control point will
////    		be discarded and a new set of 2 control points inserted:
////
////    			new_cp[ 1 ] = lerp( old_cp[ 0 ], old_cp[ 1 ], w[ 0 ] )
////    			new_cp[ 2 ] = lerp( old_cp[ 1 ], old_cp[ 2 ], w[ 1 ] )
////
////    		For cubic knot insertion, the center 2 control points will
////    		be discarded and a new set of 3 control points inserted:
////
////    			new_cp[ 1 ] = lerp( old_cp[ 0 ], old_cp[ 1 ], w[ 0 ] )
////    			new_cp[ 2 ] = lerp( old_cp[ 1 ], old_cp[ 2 ], w[ 1 ] )
////    			new_cp[ 3 ] = lerp( old_cp[ 2 ], old_cp[ 3 ], w[ 2 ] )
