////
////    span finder for knot based splines
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//!	scan a knot vector for the span containing t
int findSpan( const float t, const int elements, const float* const pknots )
{	//	elements is the number of spans plus one
	int l = 0;
	int	h = ( elements - 2 );
	while( l < h )
	{
		int i = ( ( l + h + 1 ) >> 1 );
		if( t < pknots[ i ] )
		{
			h = ( i - 1 );
		}
		else
		{
			l = i;
		}
	}
	return( l );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    knot validation for knot based splines
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//!	validate a quadratic knot
bool validateKnot4( const float* const pknots )
{
	const float k0 = pknots[ 0 ];
	const float k1 = pknots[ 1 ];
	const float k2 = pknots[ 2 ];
	const float k3 = pknots[ 3 ];
	return( ( ( ( k2 - k1 ) * ( k2 - k0 ) * ( k3 - k1 ) ) > consts::FLOAT_MAX_DENORM ) ? true : false );
}

//!	validate a cubic knot
bool validateKnot6( const float* const pknots )
{
	const float k0 = pknots[ 0 ];
	const float k1 = pknots[ 1 ];
	const float k2 = pknots[ 2 ];
	const float k3 = pknots[ 3 ];
	const float k4 = pknots[ 4 ];
	const float k5 = pknots[ 5 ];
	return( ( ( ( k3 - k2 ) * ( k3 - k1 ) * ( k4 - k2 ) * ( k3 - k0 ) * ( k4 - k1 ) * ( k5 - k2 ) ) > consts::FLOAT_MAX_DENORM ) ? true : false );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    b-spline knot insertion control point re-weighting calculation
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//!	calculate control point blend weights for knot insertion into a quadratic b-spline
void bsplineInsertBlends( const float t, const float* const pknots, vec2& wb )
{
	const float k0 = pknots[ 0 ];
	const float k1 = pknots[ 1 ];
	const float k2 = pknots[ 2 ];
	const float k3 = pknots[ 3 ];
	wb.x = ( ( t - k0 ) / ( k2 - k0 ) );
	wb.y = ( ( t - k1 ) / ( k3 - k1 ) );
}

//!	calculate control point blend weights for knot insertion into a cubic b-spline
void bsplineInsertBlends( const float t, const float* const pknots, vec3& wb )
{
	const float k0 = pknots[ 0 ];
	const float k1 = pknots[ 1 ];
	const float k2 = pknots[ 2 ];
	const float k3 = pknots[ 3 ];
	const float k4 = pknots[ 4 ];
	const float k5 = pknots[ 5 ];
	wb.x = ( ( t - k0 ) / ( k3 - k0 ) );
	wb.y = ( ( t - k1 ) / ( k4 - k1 ) );
	wb.z = ( ( t - k2 ) / ( k5 - k2 ) );
}

