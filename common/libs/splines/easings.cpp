//!	sine function ease in/out
float sineEase( const float t )
{	//	(sin(-PI/2)+1)/2 to (sin(PI/2)+1)/2
	return( ( sinf( ( t - 0.5f ) * consts::PI ) + 1.0f ) * 0.5f );
}

//!	sine function ease in
float sineEaseIn( const float t )
{	//	sin(-PI/2)+1 to sin(0)+1
	return( sinf( ( t - 1 ) * 0.5f * consts::PI ) + 1.0f );
}

//!	sine function ease out
float sineEaseOut( const float t )
{	//	sin(0) to sin(PI/2)
	return( sinf( t * 0.5f * consts::PI ) );
}

//!	cubic hermite ease in/out
float cubicEase( const float t )
{	//	xx(3-2x)
	return( ( 3 - t - t ) * t * t );
}

//!	cubic hermite ease in
float cubicEaseIn( const float t )
{	//	x(3-xx)/2
	return( ( 3 - ( t * t ) ) * t * 0.5f );
}

//!	cubic hermite ease out
float cubicEaseOut( const float t )
{	//	xx(3-x)/2
	return( ( 3 - t ) * t * t * 0.5f );
}

//!	quintic hermite ease in/out
float quinticEase( const float t )
{	//	xxx(3x(2x-5)+10)
	return( ( ( ( ( t * 6 ) - 15 ) * t ) + 10 ) * t * t * t );
}
 
//!	quintic hermite ease in
float quinticEaseIn( const float t )
{	//	x(xx(3xx-10)+15)/8
	return( ( ( ( ( t * t * 3 ) - 10 ) * t * t ) + 15 ) * t * 0.125f );
}

//!	quintic hermite ease out
float quinticEaseOut( const float t )
{	//	xxx(3x(x-5)+20)/8
	return( ( ( ( t - 5 ) * t * 3 ) + 20 ) * t * t * t * 0.125f );
}

//!	linear ping-pong
float linearPingPong( const float t )
{
	float l = ( t - floorf( t ) );
	return( 1 - fabsf( l + l - 1 ) );
}

//!	cubic ping-pong
float cubicPingPong( const float t )
{
	float c = fabsf( t - floorf( t ) - 0.5f );
	return( ( ( ( 16 * c ) - 12 ) * c * c ) + 1 );
}

//!	quintic ping-pong
float quinticPingPong( const float t )
{
	float q = fabsf( t - floorf( t ) - 0.5f );
	return( 1 - ( ( ( ( ( 192 * q ) - 240 ) * q ) + 80 ) * q * q * q ) );
}

//! sine approximating ping-pong
float pingPongSinLike( const float t )
{
	return( pingPongCosLike( t + 0.75f ) );
}

//! cosine approximating ping-pong
float pingPongCosLike( const float t )
{
	float c = fabsf( t - floorf( t ) - 0.5f );
	return( ( ( 24 - ( c * 32 ) ) * c * c ) - 1 );
}

