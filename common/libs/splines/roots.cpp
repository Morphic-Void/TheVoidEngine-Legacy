/// Calculate the roots of a linear polynomial
EROOTS linearRoot( const float a, const float b, float& r0 )
{
    if( fabsf( a ) < consts::FLOAT_EPSILON )
    {
        r0 = 0;
        return( EINVALID_POLYNOMIAL );
    }
    r0 = ( -b / a );
    return( ELINEAR_SINGLE );
}

/// Calculate the roots of a quadratic polynomial
EROOTS quadraticRoots( const float a, const float b, const float c, float& r0, float& r1 )
{
    if( fabsf( a ) < consts::FLOAT_EPSILON )
    {   //  not quadratic, call the linear solver
        r1 = 0;
        return( linearRoot( b, c, r0 ) );
    }
    r0 = r1 = 0;
    float nb = -( b / a );    //  NEGATIVE normalized b
    float nc = ( c / a );     //  normalized c
    if( fabsf( nc ) < consts::FLOAT_EPSILON )
    {   //  at least one of the roots is zero, solve as linear
        if( fabsf( nb ) < consts::FLOAT_EPSILON )
        {   //  both roots are zero
            return( EQUADRATIC_DOUBLE );
        }
        r0 = nb;
        return( EQUADRATIC_DISCRETE );
    }
    nb *= 0.5f;
    nc -= ( nb * nb );
    if( nc < -consts::FLOAT_EPSILON )
    {   //  2 real roots
        nc = sqrtf( -nc );
        r0 = ( nb - nc );
        r1 = ( nb + nc );
        return( EQUADRATIC_DISCRETE );
    }
    r0 = r1 = nb;
    if( nc > consts::FLOAT_EPSILON )
    {   //  complex conjugate roots
        r1 = sqrtf( nc );
        return( EQUADRATIC_COMPLEX );
    }
    return( EQUADRATIC_DOUBLE );
}

/// Calculate the roots of a cubic polynomial
EROOTS cubicRoots( const float a, const float b, const float c, const float d, float& r0, float& r1, float& r2 )
{
	if( fabsf( a ) < consts::FLOAT_EPSILON )
    {   //  not cubic, call the quadratic solver
        r2 = 0;
        return( quadraticRoots( b,c,d, r0, r1 ) );
    }
    r0 = r1 = r2 = 0;
    float nb = -( b / a );    //  NEGATIVE normalized b
    float nc = ( c / a );     //  normalized c
    float nd = ( d / a );     //  normalized d
    if( fabsf( nd ) < consts::FLOAT_EPSILON )
    {   //  at least one of the roots is zero, solve as quadratic
        if( fabsf( nc ) < consts::FLOAT_EPSILON )
        {   //  at least two of the roots are zero, solve as linear
            if( fabsf( nb ) < consts::FLOAT_EPSILON )
            {   //  all the roots are zero
                return( ECUBIC_TRIPLE );
            }
            r0 = nb;
            return( ECUBIC_DOUBLE );
        }
        nb *= 0.5f;
        nc -= ( nb * nb );
        if( nc < -consts::FLOAT_EPSILON )
        {   //  2 real roots
            nc = sqrtf( -nc );
            r1 = ( nb - nc );
            r2 = ( nb + nc );
            return( ECUBIC_DISCRETE );
        }
        r1 = r2 = nb;
        if( nc > consts::FLOAT_EPSILON )
        {   //  complex conjugate roots
            r2 = sqrtf( nc );
            return( ECUBIC_COMPLEX );
        }
        return( ECUBIC_DOUBLE );
    }
    float R = ( ( nb * nb * nb * 8 ) - ( nb * nc * 36 ) - ( nd * 108 ) );	//  depressed cubic 'd' * -4
    float Q = ( ( nc * 12 ) - ( nb * nb * 4 ) );							//  depressed cubic 'c' * 12
    if( fabsf( R ) < consts::FLOAT_EPSILON )
    {   //  real triple root
        r0 = r1 = r2 = ( nb / 3 );
		if( fabsf( Q ) < consts::FLOAT_EPSILON )
		{
			return( ECUBIC_TRIPLE );
		}
		Q = sqrtf( fabsf( Q * 0.75f ) );
		r1 = ( ( nb + Q ) / 3 );
		r2 = ( ( nb - Q ) / 3 );
        return( ECUBIC_DOUBLE );
    }
    if( fabsf( Q ) < consts::FLOAT_EPSILON )
    {   //  sqrtf( D ) == R
        r0 = ( ( nb + cbrtf( R + R ) ) / 3 );
    }
    else
    {
        float D = ( ( Q * Q * Q ) + ( R * R ) );
        if( fabsf( D ) < consts::FLOAT_EPSILON )
        {
			R /= Q;
			r0 = ( ( nb - R ) / 3 );
			r1 = r2 = ( ( nb + nb + R ) / 6 );
            return( ECUBIC_DOUBLE );
        }
        r0 = ( ( nb + ( ( D < 0 ) ? ( cbrtf( R ) * cosf( atan2f( sqrtf( -D ), R ) / 3 ) ) : cbrtf( R + sqrtf( D ) ) ) ) / 3 );
    }
    nb = ( ( nb - r0 ) * 0.5f );
    nc = -( ( nb * nb ) + ( nd / r0 ) );
    if( nc < -consts::FLOAT_EPSILON )
    {   //  2 real roots
        nc = sqrtf( -nc );
        r1 = ( nb - nc );
        r2 = ( nb + nc );
        return( ECUBIC_DISCRETE );
    }
    r1 = r2 = nb;
    if( nc > consts::FLOAT_EPSILON )
    {   //  complex conjugate roots
        r2 = sqrtf( nc );
        return( ECUBIC_COMPLEX );
    }
    return( ECUBIC_DOUBLE );
}

