////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    root solver return codes
////
////    	cubic:
////
////    		if the root solver determines that the polynomial is not cubic it
////    		sets r2 to 0 and returns the result of the quadratic solver.
////
////    		ECUBIC_DISCRETE		: r0, r1 and r2 are discrete real roots
////    		ECUBIC_DOUBLE		: r0 is a real root, r1 and r2 are the same real double root
////    		ECUBIC_TRIPLE		: r0, r1 and r2 are the same real triple root
////    		ECUBIC_COMPLEX		: r0 is a real root, r1 and r2 are the real and imaginary parts respectively of the complex conjugate root
////
////    	quadratic:
////
////    		if the root solver determines that the polynomial is not quadratic it
////    		sets r1 to 0 and returns the result of the linear solver.
////
////    		EQUADRATIC_DISCRETE	: r0 and r1 are discrete real roots
////    		EQUADRATIC_DOUBLE	: r0 and r1 are the same real double root
////    		EQUADRATIC_COMPLEX	: r0 and r1 are the real and imaginary parts respectively of the complex conjugate root
////
////    	linear:
////
////    		if the root solver determines that the polynomial is not linear it
////    		sets r0 to 0 and returns EINVALID_POLYNOMIAL.
////
////    		ELINEAR_SINGLE		: r0 is the real root
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

enum EROOTS
{
//	bit-field codes:
	EROOT_1_DISTINCT			= 1,
	EROOT_2_DISTINCT			= 2,
	EROOT_3_DISTINCT			= 3,
	EROOT_DISTINCT_COUNT_MASK	= 3,
	EROOT_HAS_SINGLE			= 4,
	EROOT_HAS_DOUBLE			= 8,
	EROOT_HAS_TRIPLE			= 16,
	EROOT_HAS_COMPLEX			= 32,

//	return codes:
	EINVALID_POLYNOMIAL			= 0,
	ELINEAR_SINGLE				= ( EROOT_HAS_SINGLE + EROOT_1_DISTINCT ),
	EQUADRATIC_DISCRETE			= EROOT_2_DISTINCT,
	EQUADRATIC_DOUBLE			= ( EROOT_HAS_DOUBLE + EROOT_1_DISTINCT ),
	EQUADRATIC_COMPLEX			= EROOT_HAS_COMPLEX,
	ECUBIC_DISCRETE				= EROOT_3_DISTINCT,
	ECUBIC_DOUBLE				= ( EROOT_HAS_DOUBLE + EROOT_HAS_SINGLE + EROOT_2_DISTINCT ),
	ECUBIC_TRIPLE				= ( EROOT_HAS_TRIPLE + EROOT_1_DISTINCT ),
	ECUBIC_COMPLEX				= ( EROOT_HAS_COMPLEX + EROOT_HAS_SINGLE + EROOT_1_DISTINCT ),

//	force the enumerated type to be at least 32 bit:
	EROOTS_FORCE32				= 0x7fffffff
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    root solvers
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

EROOTS linearRoot( const float a, const float b, float& r0 );
EROOTS quadraticRoots( const float a, const float b, const float c, float& r0, float& r1 );
EROOTS cubicRoots( const float a, const float b, const float c, const float d, float& r0, float& r1, float& r2 );

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    inline root solving helper functions
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

inline EROOTS linearRoot( const float a, const float b, const float value, float& r0 )
{
    return( linearRoot( a, ( b - value ), r0 ) );
}

inline EROOTS quadraticRoots( const float a, const float b, const float c, const float value, float& r0, float& r1 )
{
    return( quadraticRoots( a, b, ( c - value ), r0, r1 ) );
}

inline EROOTS cubicRoots( const float a, const float b, const float c, const float d, const float value, float& r0, float& r1, float& r2 )
{
    return( cubicRoots( a, b, c, ( d - value ), r0, r1, r2 ) );
}

inline EROOTS linearRoot( const vec2& polynomial, float& r0 )
{
	return( linearRoot( polynomial.x, polynomial.y, r0 ) );
}

inline EROOTS quadraticRoots( const vec3& polynomial, float& r0, float& r1 )
{
	return( quadraticRoots( polynomial.x, polynomial.y, polynomial.z, r0, r1 ) );
}

inline EROOTS cubicRoots( const vec4& polynomial, float& r0, float& r1, float& r2 )
{
	return( cubicRoots( polynomial.x, polynomial.y, polynomial.z, polynomial.w, r0, r1, r2 ) );
}

inline EROOTS linearRoot( const vec2& polynomial, const float value, float& r0 )
{
	return( linearRoot( polynomial.x, ( polynomial.y - value ), r0 ) );
}

inline EROOTS quadraticRoots( const vec3& polynomial, const float value, float& r0, float& r1 )
{
	return( quadraticRoots( polynomial.x, polynomial.y, ( polynomial.z - value ), r0, r1 ) );
}

inline EROOTS cubicRoots( const vec4& polynomial, const float value, float& r0, float& r1, float& r2 )
{
	return( cubicRoots( polynomial.x, polynomial.y, polynomial.z, ( polynomial.w - value ), r0, r1, r2 ) );
}

