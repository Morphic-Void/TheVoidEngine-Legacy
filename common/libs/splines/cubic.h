////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    spline form enumeration
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

enum ECubicSplineForm
{
    ECubicBezier = 0,
    ECubicBSpline = 1,
    ECubicCatmull = 2,
    ECubicHermite = 3,
    ECubicHBezier = 4,
    ECubicNonUniformBSpline = 5,
    ECubicNonUniformCatmull = 6,
    ECubicSplineTypeCount = 7,
    ECubicSplineFormForce32 = 0x7fffffff
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    cubic vector spline structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct CubicVectorSpline
{
	struct Span
	{
		mat44				control;		//  control vectors
		mat44				polynomial;		//  polynomial matrix
		float				knots[ 6 ];		//	knot vector section
		float				lower;			//  lower limit of 't'
		float				upper;			//  upper limit of 't'
		float				range;			//  range of 't': ( upper - lower )
		float				scale;			//  normalizing factor to get span local 't': ( 1 / range )
		float				length;			//  integrated length of the span
		float				summed;			//  cumulative integrated length of the splines' spans to the end of this span
	};
    ECubicSplineForm		form;
	bool					looping;
    bool					rational;
    uint					span_count;
    Span**					span_array;
    int						findSpanIndex( const float t_in, float& t_out ) const;
	void					updatePolynomial( Span& span ) const;
	void					draw( ISplineDrawLineStreamOut& stream_out, const uint divisions = 1 ) const;
	void					drawTangents( ISplineDrawLineStreamOut& stream_out, const float scale = 1 ) const;
	void					drawControlHull( ISplineDrawLineStreamOut& stream_out ) const;
	void					drawControlPoints( ISplineDrawPointStreamOut& stream_out ) const;
	void					drawControlTangents( ISplineDrawLineStreamOut& stream_out ) const;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    cubic scalar spline structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct CubicScalarSpline
{
	struct Span
	{
		vec4				control;		//  control scalars
		vec4				polynomial;		//  polynomial vector
		float				knots[ 6 ];		//	knot vector section
		float				lower;			//  lower limit of 't'
		float				upper;			//  upper limit of 't'
		float				range;			//  range of 't': ( upper - lower )
		float				scale;			//  normalizing factor to get span local 't': ( 1 / range )
		float				length;			//  integrated length of the span
		float				summed;			//  cumulative integrated length of the splines' spans to the end of this span
	};
    ECubicSplineForm		form;
    uint					span_count;
    Span**					span_array;
    int						findSpanIndex( const float t_in, float& t_out ) const;
	void					updatePolynomial( Span& span ) const;
	void					draw( ISplineDrawLineStreamOut& stream_out, const uint divisions = 1 ) const;
};

