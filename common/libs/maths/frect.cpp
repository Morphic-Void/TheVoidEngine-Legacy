bool frect::IsNan( void ) const
{
	return( ::IsNan( l ) || ::IsNan( t ) || ::IsNan( r ) || ::IsNan( b ) );
}

bool frect::IsReal( void ) const
{
	return( ::IsReal( l ) && ::IsReal( t ) && ::IsReal( r ) && ::IsReal( b ) );
}

bool frect::Union( const frect& other )
{
	if( l < other.l ) l = other.l;
	if( t > other.t ) t = other.t;
	if( r > other.r ) r = other.r;
	if( b < other.b ) b = other.b;
	if( ( l > r ) || ( b > t ) )
	{
		l = t = r = b = 0.0f;
		return( false );
	}
	return( true );
}

void frect::Merge( const frect& other )
{
	if( l > other.l ) l = other.l;
	if( t < other.t ) t = other.t;
	if( r < other.r ) r = other.r;
	if( b > other.b ) b = other.b;
}

bool frect::SetUnion( const frect& rect1, const frect& rect2 )
{
	l = ( ( rect1.l > rect2.l ) ? rect1.l : rect2.l );
	t = ( ( rect1.t < rect2.t ) ? rect1.t : rect2.t );
	r = ( ( rect1.r < rect2.r ) ? rect1.r : rect2.r );
	b = ( ( rect1.b > rect2.b ) ? rect1.b : rect2.b );
	if( ( l > r ) || ( b > t ) )
	{
		l = t = r = b = 0.0f;
		return( false );
	}
	return( true );
}

void frect::SetMerge( const frect& rect1, const frect& rect2 )
{
	l = ( ( rect1.l < rect2.l ) ? rect1.l : rect2.l );
	t = ( ( rect1.t > rect2.t ) ? rect1.t : rect2.t );
	r = ( ( rect1.r > rect2.r ) ? rect1.r : rect2.r );
	b = ( ( rect1.b < rect2.b ) ? rect1.b : rect2.b );
}

bool frect::Contains( const vec2& point ) const
{
	return( (
		( l <= point.x ) && ( r >= point.x ) &&
		( b <= point.y ) && ( t >= point.y ) ) ? true : false );
}

bool frect::Contains( const frect& other ) const
{
	return( (
		( l <= other.l ) && ( r >= other.r ) &&
		( b <= other.b ) && ( t >= other.t ) ) ? true : false );
}

bool frect::Intersects( const frect& other ) const
{
	return( (
		( l <= other.r ) && ( r >= other.l ) &&
		( b <= other.t ) && ( t >= other.b ) ) ? true : false );
}

