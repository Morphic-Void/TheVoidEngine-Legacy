////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    dquat
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct dquat
{
	quat					r, e;
	bool					IsNan( void ) const;
	bool					IsReal( void ) const;
	void					Transform( const vec3& src, vec3& trg ) const;
	void					Transform( const vec4& src, vec4& trg ) const;
	void					UnscaledTransform( const vec3& src, vec3& trg ) const;
	void					UnscaledTransform( const vec4& src, vec4& trg ) const;
	void					PointAt( const vec3& from, const vec3& at, const vec3& up );
	void					PointTo( const vec3& from, const vec3& to, const vec3& up );
	void					PointAt( const vec3& at, const vec3& up );
	void					PointTo( const vec3& to, const vec3& up );
	void					LookAt( const vec3& from, const vec3& at, const vec3& up );
	void					LookTo( const vec3& from, const vec3& to, const vec3& up );
	void					LookAt( const vec3& at, const vec3& up );
	void					LookTo( const vec3& to, const vec3& up );
	void					Scale( const float scale );
	void					Twist( const float radians );
	void					Tilt( const float radians );
	void					Turn( const float radians );
	void					Roll( const float radians );
	void					Pitch( const float radians );
	void					Yaw( const float radians );
	void					EndScale( const float scale );
	void					EndTwist( const float radians );
	void					EndTilt( const float radians );
	void					EndTurn( const float radians );
	void					EndRoll( const float radians );
	void					EndPitch( const float radians );
	void					EndYaw( const float radians );
	void					Move( const float move_x, const float move_y, const float move_z );
	void					Move( const vec3& move );
	void					Pan( const float pan_x, const float pan_y, const float pan_z );
	void					Pan( const vec3& pan );
	void					Add( const dquat& add );
	void					Sub( const dquat& sub );
	void					Mul( const dquat& mul );
	void					Mul( const quat& mul );
	void					Mul( const float mul );
	void					Div( const dquat& div );
	void					Div( const quat& div );
	void					Div( const float div );
	void					Pow( const dquat& pow );
	void					Pow( const float pow );
	void					Log( void );
	void					Exp( void );
	void					Negate( void );
	void					Invert( void );
	void					Conjugate( void );
	void					Normalize( void );
	void					Lerp( const dquat& trg, const float t );
	void					Sclerp( const dquat& trg, const float t );
	void					Blend( const dquat& trg, const float t );
	void					SetAdd( const dquat& src, const dquat& add );
	void					SetSub( const dquat& src, const dquat& sub );
	void					SetMul( const dquat& src, const dquat& mul );
	void					SetMul( const dquat& src, const quat& mul );
	void					SetMul( const quat& src, const dquat& mul );
	void					SetMul( const dquat& src, const float mul );
	void					SetDiv( const dquat& src, const dquat& div );
	void					SetDiv( const dquat& src, const quat& div );
	void					SetDiv( const dquat& src, const float div );
	void					SetPow( const dquat& src, const dquat& pow );
	void					SetPow( const dquat& src, const float pow );
	void					SetLog( const dquat& src );
	void					SetExp( const dquat& src );
	void					SetNegate( const dquat& src );
	void					SetInverse( const dquat& src );
	void					SetConjugate( const dquat& src );
	void					SetNormalized( const dquat& src );
    inline void             SetUnit( const dquat& src ) { SetNormalized( src ); };
	void					SetLerp( const dquat& src, const dquat& trg, const float t );
	void					SetSclerp( const dquat& src, const dquat& trg, const float t );
	void					SetBlend( const dquat& src, const dquat& trg, const float t );
	void					SetDelta( const dquat& src, const dquat& trg );
	inline void				SetIdentity( void ) { Set( consts::DQUAT::IDENTITY ); };
	void					Set( const dquat& d );
	void					Set( const mat43& m );
	void					Set( const quat& rotate, const vec3& translate );
	void					Get( dquat& d ) const;
	void					Get( mat43& m ) const;
	void					Get( quat& rotate, vec3& translate ) const;
	void					GetTranslation( vec3& translate ) const;
	void					GetInverse( dquat& d ) const;
	void					GetInverse( mat43& m ) const;
	void					GetInverse( quat& rotate, vec3& translate ) const;
	void					GetInverseTranslation( vec3& translate ) const;
	inline dquat&			operator=( const dquat& d ) { Set( d ); return( *this ); };
	inline dquat&			operator=( const mat43& m ) { Set( m ); return( *this ); };
	inline dquat&			operator*=( const dquat& mul ) { Mul( mul ); return( *this ); };
	inline dquat&			operator/=( const dquat& div ) { Div( div ); return( *this ); };
};

inline dquat operator+( const dquat& src, const dquat& add ) { dquat d; d.SetAdd( src, add ); return( d ); };
inline dquat operator-( const dquat& src, const dquat& sub ) { dquat d; d.SetSub( src, sub ); return( d ); };
inline dquat operator*( const dquat& src, const dquat& mul ) { dquat d; d.SetMul( src, mul ); return( d ); };
inline dquat operator*( const dquat& src, const quat& mul ) { dquat d; d.SetMul( src, mul ); return( d ); };
inline dquat operator*( const quat& src, const dquat& mul ) { dquat d; d.SetMul( src, mul ); return( d ); };
inline dquat operator*( const dquat& src, const float mul ) { dquat d; d.SetMul( src, mul ); return( d ); };
inline dquat operator/( const dquat& src, const dquat& div ) { dquat d; d.SetDiv( src, div ); return( d ); };
inline dquat operator/( const dquat& src, const quat& div ) { dquat d; d.SetDiv( src, div ); return( d ); };
inline dquat operator/( const dquat& src, const float div ) { dquat d; d.SetDiv( src, div ); return( d ); };

inline dquat normalize( const dquat& src ) { dquat d; d.SetNormalized( src ); return( d ); };
inline dquat invert( const dquat& src ) { dquat d; d.SetInverse( src ); return( d ); };
inline dquat conjugate( const dquat& src ) { dquat d; d.SetConjugate( src ); return( d ); };
inline dquat pow( const dquat& src, const dquat& pow ) { dquat d; d.SetPow( src, pow ); return( d ); };
inline dquat pow( const dquat& src, const float pow ) { dquat d; d.SetPow( src, pow ); return( d ); };
inline dquat log( const dquat& src ) { dquat d; d.SetLog( src ); return( d ); };
inline dquat exp( const dquat& src ) { dquat d; d.SetExp( src ); return( d ); };
inline dquat lerp( const dquat& a, const dquat& b, const float t ) { dquat d; d.SetLerp( a, b, t ); return( d ); };
inline dquat sclerp( const dquat& a, const dquat& b, const float t ) { dquat d; d.SetSclerp( a, b, t ); return( d ); };
inline dquat blend( const dquat& a, const dquat& b, const float t ) { dquat d; d.SetBlend( a, b, t ); return( d ); };

