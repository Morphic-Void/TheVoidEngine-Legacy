struct irect
{
	int				l, t, r, b;
	inline void		Set( const irect& other ) { l = other.l; t = other.t; r = other.r; b = other.b; };
	inline void		Set( const int _l, const int _t, const int _r, const int _b ) { l = _l; t = _t; r = _r; b = _b; };
	bool			Union( const irect& other );
	void			Merge( const irect& other );
	bool			SetUnion( const irect& rect1, const irect& rect2 );
	void			SetMerge( const irect& rect1, const irect& rect2 );
	bool			Contains( const irect& other ) const;
	bool			Intersects( const irect& other ) const;
	inline irect&	operator=( const irect& other ) { Set( other ); return( *this ); };
};

class cirect : public irect
{
public:
	inline			cirect() {};
	inline			cirect( const irect& other ) { Set( other ); };
	inline			cirect( const int _l, const int _t, const int _r, const int _b ) { Set( _l, _t, _r, _b ); };
	inline			~cirect() {};
	inline			operator irect() { return( *reinterpret_cast< irect* >( this ) ); };
	inline			operator irect() const { return( *reinterpret_cast< const irect* >( this ) ); };
	inline irect&	operator=( const irect& other ) { Set( other ); return( *this ); };
};

