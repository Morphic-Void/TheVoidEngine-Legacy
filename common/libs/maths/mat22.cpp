bool mat22::IsNan( void ) const
{
	return( x.IsNan() || y.IsNan() );
}

bool mat22::IsReal( void ) const
{
	return( x.IsReal() && y.IsReal() );
}

float mat22::Det( void ) const
{
	return( ( x.x * y.y ) - ( x.y * y.x ) );
}

void mat22::Scale( const float scale )
{
	x.x *= scale;
	y.x *= scale;
	x.y *= scale;
	y.y *= scale;
}

void mat22::Scale( const float scale_x, const float scale_y )
{
	x.x *= scale_x;
	y.x *= scale_x;
	x.y *= scale_y;
	y.y *= scale_y;
}

void mat22::Rotate( const float radians )
{
	float s = sinf( radians );
	float c = cosf( radians );
	vec2 t = { x.x, x.y };
	x.x = ( ( x.x * c ) - ( y.x * s ) );
	x.y = ( ( x.y * c ) - ( y.y * s ) );
	y.x = ( ( y.x * c ) + ( t.x * s ) );
	y.y = ( ( y.y * c ) + ( t.y * s ) );
}

void mat22::Mul( const mat22& mul )
{
	mat22 tmp;
	tmp.SetMul( *this, mul );
	Set( tmp );
}

bool mat22::Invert( void )
{
	float d = ( ( x.x * y.y ) - ( x.y * y.x ) );
	ASSERT( fabsf( d ) >= consts::FLOAT_MIN_RCP );
	if( fabsf( d ) >= consts::FLOAT_MIN_RCP )
	{
		d = ( 1.0f / d );
		float t;
		t = x.x;
		x.x = ( y.y * d );
		y.y = ( t * d );
		d = -d;
		t = x.y;
		x.y = ( y.x * d );
		y.x = ( t * d );
		return( true );
	}
	else
	{
		x.x = y.y = 1.0f;
		x.y = y.x = 0.0f;
		return( false );
	}
}

void mat22::Transpose( void )
{
	float t;
	t = x.y;
	x.y = y.x;
	y.x = t;
}

bool mat22::InvertTranspose( void )
{
	float d = ( ( x.x * y.y ) - ( x.y * y.x ) );
	ASSERT( fabsf( d ) >= consts::FLOAT_MIN_RCP );
	if( fabsf( d ) >= consts::FLOAT_MIN_RCP )
	{
		d = ( 1.0f / d );
		float t;
		t = x.x;
		x.x = ( y.y * d );
		y.y = ( t * d );
		d = -d;
		x.y *= d;
		y.x *= d;
		return( true );
	}
	else
	{
		x.x = y.y = 1.0f;
		x.y = y.x = 0.0f;
		return( false );
	}
}

void mat22::SetMul( const mat22& src, const mat22& mul )
{
	ASSERT( ( this != &src ) && ( this != &mul ) );
	x.x = ( ( src.x.x * mul.x.x ) + ( src.y.x * mul.x.y ) );
	x.y = ( ( src.x.y * mul.x.x ) + ( src.y.y * mul.x.y ) );
	y.x = ( ( src.x.x * mul.y.x ) + ( src.y.x * mul.y.y ) );
	y.y = ( ( src.x.y * mul.y.x ) + ( src.y.y * mul.y.y ) );
}

void mat22::SetDelta( const mat22& src, const mat22& trg )
{
	SetInverse( src );
	Mul( trg );
}

bool mat22::SetInverse( const mat22& src )
{
	ASSERT( this != &src );
	float d = ( ( src.x.x * src.y.y ) - ( src.x.y * src.y.x ) );
	ASSERT( fabsf( d ) >= consts::FLOAT_MIN_RCP );
	if( fabsf( d ) >= consts::FLOAT_MIN_RCP )
	{
		d = ( 1.0f / d );
		x.x = ( src.y.y * d );
		y.y = ( src.x.x * d );
		d = -d;
		x.y = ( src.y.x * d );
		y.x = ( src.x.y * d );
		return( true );
	}
	else
	{
		x.x = y.y = 1.0f;
		x.y = y.x = 0.0f;
		return( false );
	}
}

void mat22::SetTranspose( const mat22& src )
{
	float t;
	t = src.x.y;
	x.y = src.y.x;
	y.x = t;
	x.x = src.x.x;
	y.y = src.y.y;
}

bool mat22::SetInverseTranspose( const mat22& src )
{
	ASSERT( this != &src );
	float d = ( ( src.x.x * src.y.y ) - ( src.x.y * src.y.x ) );
	ASSERT( fabsf( d ) >= consts::FLOAT_MIN_RCP );
	if( fabsf( d ) >= consts::FLOAT_MIN_RCP )
	{
		d = ( 1.0f / d );
		x.x = ( src.y.y * d );
		y.y = ( src.x.x * d );
		d = -d;
		x.y = ( src.x.y * d );
		y.x = ( src.y.x * d );
		return( true );
	}
	else
	{
		x.x = y.y = 1.0f;
		x.y = y.x = 0.0f;
		return( false );
	}
}

void mat22::SetRotate( const float radians )
{
	float s = sinf( radians );
	float c = cosf( radians );
	x.x = c;
	x.y = -s;
	y.x = s;
	y.y = c;
}

void mat22::SetScaling( const float scale )
{
	x.x = y.y = scale;
	x.y = y.x = 0.0f;
}

void mat22::SetScaling( const float scale_x, const float scale_y )
{
	x.x = scale_x;
	y.y = scale_y;
	x.y = y.x = 0.0f;
}

void mat22::SetTensor( const vec2& u, const vec2& v )
{
	x.x = ( u.x * v.x );
	x.y = ( u.y * v.x );
	y.x = ( u.x * v.y );
	y.y = ( u.y * v.y );
}

void mat22::Set( const mat22& m )
{
	x.x = m.x.x;
	x.y = m.x.y;
	y.x = m.y.x;
	y.y = m.y.y;
}

float mat22::GetRotation( void ) const
{
	return( atan2f( y.x, y.y ) );
}

void mat22::Get( mat22& m ) const
{
	m.Set( *this );
}

void mat22::GetSplineWeights( const float t, vec2& weights ) const
{
	weights.x = ( ( x.x * t ) + y.x );
	weights.y = ( ( x.y * t ) + y.y );
}

void mat22::GetSplineWeightsD( vec2& weights ) const
{
	weights.x = x.x;
	weights.y = x.y;
}

