////
////    vec2
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct vec2
{
	float					x, y;
	bool					IsNan( void ) const;
	bool					IsReal( void ) const;
	float					Manhattan( void ) const;
	float					Chebyshev( void ) const;
	float					Length( void ) const;
	float					SqrLen( void ) const;
	void					Abs( void );
	void					Sat( void );
	void					Frac( void );
	void					Floor( void );
	void					Ceil( void );
	float					Dot( const vec2& dot ) const;
	void					Add( const vec2& add );
	void					Sub( const vec2& sub );
	void					Mul( const vec2& mul );
	void					Mul( const float mul );
	void					Mul( const mat22& mul );
	void					Div( const vec2& div );
	void					Div( const float div );
	void					Lerp( const vec2& trg, const float t );
	float					Cross( const vec2& cross ) const;
	void					Negate( void );
	void					Normalize( void );
	void					SetAbs( const vec2& src );
	void					SetSat( const vec2& src );
	void					SetFrac( const vec2& src );
	void					SetFloor( const vec2& src );
	void					SetCeil( const vec2& src );
	void					SetAdd( const vec2& src, const vec2& add );
	void					SetSub( const vec2& src, const vec2& sub );
	void					SetMul( const vec2& src, const vec2& mul );
	void					SetMul( const vec2& src, const float mul );
	void					SetMul( const vec2& src, const mat22& mul );
	void					SetDiv( const vec2& src, const vec2& div );
	void					SetDiv( const vec2& src, const float div );
	void					SetLerp( const vec2& src, const vec2& trg, const float t );
	void					SetNegate( const vec2& src );
	void					SetNormalized( const vec2& src );
    inline void             SetUnit( const vec2& src ) { SetNormalized( src ); };
	void					Set( const float vx, const float vy );
	void					Set( const vec2& v );
	void					Get( float& vx, float& vy ) const;
	void					Get( vec2& v ) const;
	inline vec2&			operator=( const vec2& v ) { Set( v ); return( *this ); };
	inline vec2&			operator+=( const vec2& v ) { Add( v ); return( *this ); };
	inline vec2&			operator-=( const vec2& v ) { Sub( v ); return( *this ); };
	inline vec2&			operator*=( const vec2& v ) { Mul( v ); return( *this ); };
	inline vec2&			operator*=( const float f ) { Mul( f ); return( *this ); };
	inline vec2&			operator/=( const vec2& v ) { Div( v ); return( *this ); };
	inline vec2&			operator/=( const float f ) { Div( f ); return( *this ); };
	inline vec2&			operator*=( const mat22& m ) { Mul( m ); return( *this ); };
};

inline vec2 operator+( const vec2& src, const vec2& add ) { vec2 v; v.SetAdd( src, add ); return( v ); };
inline vec2 operator-( const vec2& src, const vec2& sub ) { vec2 v; v.SetSub( src, sub ); return( v ); };
inline vec2 operator*( const vec2& src, const vec2& mul ) { vec2 v; v.SetMul( src, mul ); return( v ); };
inline vec2 operator*( const vec2& src, const float mul ) { vec2 v; v.SetMul( src, mul ); return( v ); };
inline vec2 operator*( const vec2& src, const mat22& mul ) { vec2 v; v.SetMul( src, mul ); return( v ); };
inline vec2 operator/( const vec2& src, const vec2& div ) { vec2 v; v.SetDiv( src, div ); return( v ); };
inline vec2 operator/( const vec2& src, const float div ) { vec2 v; v.SetDiv( src, div ); return( v ); };
inline vec2 operator-( const vec2& src ) { vec2 v; v.SetNegate( src ); return( v ); };

inline vec2 normalize( const vec2& src ) { vec2 v; v.SetNormalized( src ); return( v ); };
inline vec2 abs( const vec2& src ) { vec2 v; v.SetAbs( src ); return( v ); };
inline vec2 sat( const vec2& src ) { vec2 v; v.SetSat( src ); return( v ); };
inline vec2 frac( const vec2& src ) { vec2 v; v.SetFrac( src ); return( v ); };
inline vec2 floor( const vec2& src ) { vec2 v; v.SetFloor( src ); return( v ); };
inline vec2 ceil( const vec2& src ) { vec2 v; v.SetCeil( src ); return( v ); };
inline vec2 lerp( const vec2& a, const vec2& b, const float t ) { vec2 v; v.SetLerp( a, b, t ); return( v ); };
inline float dot( const vec2& a, const vec2& b ) { return( a.Dot( b ) ); };
inline float cross( const vec2& a, const vec2& b ) { return( a.Cross( b ) ); };

class cvec2 : public vec2
{
public:
	inline					cvec2() {};
	inline					cvec2( const float vx, const float vy ) { Set( vx, vy ); };
	inline					~cvec2() {};
	inline					operator vec2() { return( *reinterpret_cast< vec2* >( this ) ); };
	inline					operator vec2() const { return( *reinterpret_cast< const vec2* >( this ) ); };
};

