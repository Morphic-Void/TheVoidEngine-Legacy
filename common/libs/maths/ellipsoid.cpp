bool ellipsoid::Contains( const vec3& apoint ) const
{
	vec3 v;
	v.x = ( apoint.x - transform.x.w );
	v.y = ( apoint.y - transform.y.w );
	v.z = ( apoint.z - transform.z.w );
	mat33 m;
	m.x.SetCross( transform.y.t_vec3(), transform.z.t_vec3() );
	m.y.SetCross( transform.z.t_vec3(), transform.x.t_vec3() );
	m.z.SetCross( transform.x.t_vec3(), transform.y.t_vec3() );
	vec3 d;
	d.x = ( ( v.x * m.x.x ) + ( v.y * m.y.x ) + ( v.z * m.z.x ) );
	d.y = ( ( v.x * m.x.y ) + ( v.y * m.y.y ) + ( v.z * m.z.y ) );
	d.z = ( ( v.x * m.x.z ) + ( v.y * m.y.z ) + ( v.z * m.z.z ) );
	float r = m.x.Dot( transform.x.t_vec3() );
	return( ( d.SqrLen() <= ( r * r ) ) ? true : false );
}

