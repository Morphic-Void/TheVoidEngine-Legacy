bool vec2::IsNan( void ) const
{
	return( ::IsNan( x ) || ::IsNan( y ) );
}

bool vec2::IsReal( void ) const
{
	return( ::IsReal( x ) && ::IsReal( y ) );
}

float vec2::Manhattan( void ) const
{
	return( fabsf( x ) + fabsf( y ) );
}

float vec2::Chebyshev( void ) const
{
	float c = fabsf( x );
	float v = fabsf( y );
	if( c < v ) c = v;
	return( c );
}

float vec2::Length( void ) const
{
	return( sqrtf( ( x * x ) + ( y * y ) ) );
}

float vec2::SqrLen( void ) const
{
	return( ( x * x ) + ( y * y ) );
}

void vec2::Abs( void )
{
	x = fabsf( x );
	y = fabsf( y );
}

void vec2::Sat( void )
{
	x = fsatf( x );
	y = fsatf( y );
}

void vec2::Frac( void )
{
	x -= floorf( x );
	y -= floorf( y );
}

void vec2::Floor( void )
{
	x = floorf( x );
	y = floorf( y );
}

void vec2::Ceil( void )
{
	x = ceilf( x );
	y = ceilf( y );
}

float vec2::Dot( const vec2& dot ) const
{
	return( ( x * dot.x ) + ( y * dot.y ) );
}

void vec2::Add( const vec2& add )
{
	x += add.x;
	y += add.y;
}

void vec2::Sub( const vec2& sub )
{
	x -= sub.x;
	y -= sub.y;
}

void vec2::Mul( const vec2& mul )
{
	x *= mul.x;
	y *= mul.y;
}

void vec2::Mul( const float mul )
{
	x *= mul;
	y *= mul;
}

void vec2::Mul( const mat22& mul )
{
	SetMul( *this, mul );
}

void vec2::Div( const vec2& div )
{
	ASSERT( ( fabsf( div.x ) >= consts::FLOAT_MIN_RCP ) && ( fabsf( div.y ) >= consts::FLOAT_MIN_RCP ) );
	x /= div.x;
	y /= div.y;
}

void vec2::Div( const float div )
{
	ASSERT( fabsf( div ) >= consts::FLOAT_MIN_RCP );
	Mul( 1.0f / div );
}

void vec2::Lerp( const vec2& trg, const float t )
{
	SetLerp( *this, trg, t );
}

float vec2::Cross( const vec2& cross ) const
{
	return( ( x * cross.y ) - ( y * cross.x ) );
}

void vec2::Negate( void )
{
	x = -x;
	y = -y;
}

void vec2::Normalize( void )
{
	float n = ( 1.0f / ( sqrtf( ( x * x ) + ( y * y ) ) + consts::FLOAT_MIN_NORM ) );
	x *= n;
	y *= n;
}

void vec2::SetAbs( const vec2& src )
{
	x = fabsf( src.x );
	y = fabsf( src.y );
}

void vec2::SetSat( const vec2& src )
{
	x = fsatf( src.x );
	y = fsatf( src.y );
}

void vec2::SetFrac( const vec2& src )
{
	x -= floorf( src.x );
	y -= floorf( src.y );
}

void vec2::SetFloor( const vec2& src )
{
	x = floorf( src.x );
	y = floorf( src.y );
}

void vec2::SetCeil( const vec2& src )
{
	x = ceilf( src.x );
	y = ceilf( src.y );
}

void vec2::SetAdd( const vec2& src, const vec2& add )
{
	x = ( src.x + add.x );
	y = ( src.y + add.y );
}

void vec2::SetSub( const vec2& src, const vec2& sub )
{
	x = ( src.x - sub.x );
	y = ( src.y - sub.y );
}

void vec2::SetMul( const vec2& src, const vec2& mul )
{
	x = ( src.x * mul.x );
	y = ( src.y * mul.y );
}

void vec2::SetMul( const vec2& src, const float mul )
{
	x = ( src.x * mul );
	y = ( src.y * mul );
}

void vec2::SetMul( const vec2& src, const mat22& mul )
{
	float _x = ( ( src.x * mul.x.x ) + ( src.y * mul.x.y ) );
	float _y = ( ( src.x * mul.y.x ) + ( src.y * mul.y.y ) );
	x = _x;
	y = _y;
}

void vec2::SetDiv( const vec2& src, const vec2& div )
{
	ASSERT( ( fabsf( div.x ) >= consts::FLOAT_MIN_RCP ) && ( fabsf( div.y ) >= consts::FLOAT_MIN_RCP ) );
	x = ( src.x / div.x );
	y = ( src.y / div.y );
}

void vec2::SetDiv( const vec2& src, const float div )
{
	ASSERT( fabsf( div ) >= consts::FLOAT_MIN_RCP );
	SetMul( src, ( 1.0f / div ) );
}

void vec2::SetLerp( const vec2& src, const vec2& trg, const float t )
{
	float s = ( 1.0f - t );
	x = ( ( src.x * s ) + ( trg.x * t ) );
	y = ( ( src.y * s ) + ( trg.y * t ) );
}

void vec2::SetNegate( const vec2& src )
{
	x = -src.x;
	y = -src.y;
}

void vec2::SetNormalized( const vec2& src )
{
	Set( src );
	Normalize();
}

void vec2::Set( const float vx, const float vy )
{
	x = vx;
	y = vy;
}

void vec2::Set( const vec2& v )
{
	x = v.x;
	y = v.y;
}

void vec2::Get( float& vx, float& vy ) const
{
	vx = x;
	vy = y;
}

void vec2::Get( vec2& v ) const
{
	v.x = x;
	v.y = y;
}

