	namespace VEC2
	{
		const vec2		ORIGIN = { 0, 0 };
		const vec2		UNIT_X = { 1, 0 };
		const vec2		UNIT_Y = { 0, 1 };
		const vec2		ZERO   = { 0, 0 };
		const vec2		ONE    = { 1, 1 };
	};
