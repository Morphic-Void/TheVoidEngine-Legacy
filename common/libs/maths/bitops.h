inline unsigned int ValueToGreyCode( const unsigned int value )
{
	return( value ^ ( value >> 1 ) );
}

inline unsigned int GreyCodeToValue( const unsigned int code )
{
	unsigned int value = code;
	unsigned int check = value;
	while( check >>= 1 ) value ^= check;
	return( value );
}

