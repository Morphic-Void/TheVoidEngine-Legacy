inline unsigned int NonZero( unsigned int value )
{
	return( ( value | ( 0 - value ) ) >> ( ( sizeof( int ) << 3 ) - 1 ) );
}

inline unsigned int SignBit( unsigned int value )
{
	return( value >> ( ( sizeof( int ) << 3 ) - 1 ) );
}

inline int SignFlag( unsigned int value )
{
	return( ( static_cast< int >( value ) >> ( ( sizeof( int ) << 3 ) - 1 ) ) | 1 );
}

inline int SignValue( unsigned int value )
{
	return( ( ( static_cast< int >( value ) >> ( ( sizeof( int ) << 3 ) - 1 ) ) | 1 ) & ( static_cast< int >( value | ( 0 - value ) ) >> ( ( sizeof( int ) << 3 ) - 1 ) ) );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    greatest common denominator
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

inline unsigned int GCD( unsigned int a, unsigned int b )
{
    if( a | b )
    {
        while( a %= b ) { a ^= b; b ^= a; a ^= b; };
        return( b );
    }
    return( 0 );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    round up to power of 2
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

inline unsigned int RoundPow2( unsigned int value )
{
	--value;
	value |= ( value >> 1 );
	value |= ( value >> 2 );
	value |= ( value >> 4 );
	value |= ( value >> 8 );
	value |= ( value >> 16 );
	++value;
	return( value );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    bit parity
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

inline unsigned int Parity( unsigned int value )
{
	value ^= ( value >> 16 );
	value ^= ( value >> 8 );
	value ^= ( value >> 4 );
	value &= 15;
	return( ( 0x00006996 >> value ) & 1 );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    bit counting
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

inline unsigned int CountSetBits( unsigned int value )
{
	value -= ( ( value >> 1 ) & 0x55555555 );
	value = ( ( value & 0x33333333 ) + ( ( value >> 2 ) & 0x33333333 ) );
	value += ( ( value >> 4 ) & 0x0f0f0f0f );
	return( ( value * 0x01010101 ) >> 24 );
}

