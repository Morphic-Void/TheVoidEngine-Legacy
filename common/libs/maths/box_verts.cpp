bool box_verts::IsNan( void ) const
{
	for( int vert = 0; vert < BOX_VERT_COUNT; ++vert )
	{
		if( verts[ vert ].IsNan() )
		{
			return( true );
		}
	}
	return( false );
}

bool box_verts::IsReal( void ) const
{
	for( int vert = 0; vert < BOX_VERT_COUNT; ++vert )
	{
		if( !verts[ vert ].IsReal() )
		{
			return( false );
		}
	}
	return( true );
}

void box_verts::SetAABB( const aabb& box )
{
    verts[ maths::BOX_VERT_NBL ].x = verts[ maths::BOX_VERT_NTL ].x = verts[ maths::BOX_VERT_FBL ].x = verts[ maths::BOX_VERT_FTL ].x = box.min.x;
    verts[ maths::BOX_VERT_NBR ].x = verts[ maths::BOX_VERT_NTR ].x = verts[ maths::BOX_VERT_FBR ].x = verts[ maths::BOX_VERT_FTR ].x = box.max.x;
    verts[ maths::BOX_VERT_NBL ].y = verts[ maths::BOX_VERT_NBR ].y = verts[ maths::BOX_VERT_FBL ].y = verts[ maths::BOX_VERT_FBR ].y = box.min.y;
    verts[ maths::BOX_VERT_NTL ].y = verts[ maths::BOX_VERT_NTR ].y = verts[ maths::BOX_VERT_FTL ].y = verts[ maths::BOX_VERT_FTR ].y = box.max.y;
    verts[ maths::BOX_VERT_NBL ].z = verts[ maths::BOX_VERT_NBR ].z = verts[ maths::BOX_VERT_NTL ].z = verts[ maths::BOX_VERT_NTR ].z = box.min.z;
    verts[ maths::BOX_VERT_FBL ].z = verts[ maths::BOX_VERT_FBR ].z = verts[ maths::BOX_VERT_FTL ].z = verts[ maths::BOX_VERT_FTR ].z = box.max.z;
}

const vec3& box_verts::GetVert( const BOX_VERT vert ) const
{
	ASSERT( vert < BOX_VERT_COUNT );
#ifdef _DEBUG
	return( verts[ vert < BOX_VERT_COUNT ? vert : BOX_VERT_NBL ] );
#else
	return( verts[ vert ] );
#endif
}
 
void box_verts::GetStab( const float u, const float v, vec3& near, vec3& far ) const
{
	float tl = ( ( 1.0f - u ) * ( 1.0f - v ) );
	float tr = ( u * ( 1.0f - v ) );
	float bl = ( ( 1.0f - u ) * v );
	float br = ( u * v );
	near.x = (
		( verts[ BOX_VERT_NTL ].x * tl ) +
		( verts[ BOX_VERT_NTR ].x * tr ) +
		( verts[ BOX_VERT_NBL ].x * bl ) +
		( verts[ BOX_VERT_NBR ].x * br ) );
	near.y = (
		( verts[ BOX_VERT_NTL ].y * tl ) +
		( verts[ BOX_VERT_NTR ].y * tr ) +
		( verts[ BOX_VERT_NBL ].y * bl ) +
		( verts[ BOX_VERT_NBR ].y * br ) );
	near.z = (
		( verts[ BOX_VERT_NTL ].z * tl ) +
		( verts[ BOX_VERT_NTR ].z * tr ) +
		( verts[ BOX_VERT_NBL ].z * bl ) +
		( verts[ BOX_VERT_NBR ].z * br ) );
	far.x = (
		( verts[ BOX_VERT_FTL ].x * tl ) +
		( verts[ BOX_VERT_FTR ].x * tr ) +
		( verts[ BOX_VERT_FBL ].x * bl ) +
		( verts[ BOX_VERT_FBR ].x * br ) );
	far.y = (
		( verts[ BOX_VERT_FTL ].y * tl ) +
		( verts[ BOX_VERT_FTR ].y * tr ) +
		( verts[ BOX_VERT_FBL ].y * bl ) +
		( verts[ BOX_VERT_FBR ].y * br ) );
	far.z = (
		( verts[ BOX_VERT_FTL ].z * tl ) +
		( verts[ BOX_VERT_FTR ].z * tr ) +
		( verts[ BOX_VERT_FBL ].z * bl ) +
		( verts[ BOX_VERT_FBR ].z * br ) );
}

