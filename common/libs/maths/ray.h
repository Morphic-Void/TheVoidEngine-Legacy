struct ray
{
	vec3	pos, dir;
	bool	IsNan( void ) const;
	bool	IsReal( void ) const;
	bool	GetIntersect( const plane& aplane, float& t ) const;
	bool	GetIntersect( const sphere& asphere, float& t ) const;
	bool	GetIntersect( const vec3& apoint, const float radius, float& t ) const;
	bool	GetIntersect( const plane& aplane, const float radius, float& t ) const;
	bool	GetNormalizedPlaneIntersect( const plane& aplane, const float radius, float& t ) const;
	bool	GetIntersect( const aabb& abox, vec3& intersection, vec3& normal, float& t ) const;
	bool	GetIntersect( const vec3& vtx1, const vec3& vtx2, const vec3& vtx3, vec3& intersection, vec3& normal, float& t ) const;
	bool	GetIntersect( const vec3& apoint, const float radius, vec3& intersection, vec3& normal, float& t ) const;
	bool	GetIntersect( const vec3& start, const vec3& end, const float radius, vec3& intersection, vec3& normal, float& t ) const;
	bool	GetIntersect( const vec3& vtx1, const vec3& vtx2, const vec3& vtx3, const float radius, vec3& intersection, vec3& normal, float& t ) const;
	void	Get( plane& aplane ) const;
};

