struct aabb;
struct plane;
struct sphere;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    ray
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
