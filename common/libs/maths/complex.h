struct complex
{
	float					r, i;
	inline vec2&			t_vec2( void ) { return( *reinterpret_cast< vec2* >( this ) ); };
	inline const vec2&		t_vec2( void ) const { return( *reinterpret_cast< const vec2* >( this ) ); };
	bool					IsNan( void ) const;
	bool					IsReal( void ) const;
	float					Length( void ) const;
	inline float			SqrLen( void ) const { return( ( r * r ) + ( i * i ) ); };
	inline float			Dot( const complex& dot ) const { return( ( r * dot.r ) - ( i * dot.i ) ); };
	inline void				Add( const complex& add ) { r += add.r; i += add.i; };
	inline void				Sub( const complex& sub ) { r -= sub.r; i -= sub.i; };
	inline void				Mul( const complex& mul ) { SetMul( *this, mul ); };
	inline void				Mul( const float mul ) { r *= mul; i *= mul; };
	inline void				Div( const complex& div ) { SetDiv( *this, div ); };
	inline void				Div( const float div ) { SetDiv( *this, div ); };
	inline void				Pow( const complex& pow ) { Log(); Mul( pow ); Exp(); };
	void					Pow( const float pow );
	void					Log( void );
	void					Exp( void );
	void					Sqrt( void );
	inline void				Negate( void ) { r = -r; i = -i; };
	void					Invert( void );
	inline void				Conjugate( void ) { i = -i; };
	void					Normalize( void );
	inline void				Lerp( const complex& trg, const float t ) { SetLerp( *this, trg, t ); };
	inline void				Slerp( const complex& trg, const float t ) { SetSlerp( *this, trg, t ); };
	inline void				Blend( const complex& trg, const float t ) { SetBlend( *this, trg, t ); };
	inline void				SetAdd( const complex& src, const complex& add ) { r = ( src.r + add.r ); i = ( src.i + add.i ); };
	inline void				SetSub( const complex& src, const complex& sub ) { r = ( src.r - sub.r ); i = ( src.i - sub.i ); };
	inline void				SetMul( const complex& src, const complex& mul ) { complex c; c.r = ( ( src.r * mul.r ) - ( src.i * mul.i ) ); c.i = ( ( src.r * mul.i ) + ( src.i * mul.r ) ); r = c.r; i = c.i; };
	inline void				SetMul( const complex& src, const float mul )  { r = ( src.r * mul ); i = ( src.i * mul ); };
	void					SetDiv( const complex& src, const complex& div );
	void					SetDiv( const complex& src, const float div );
	inline void				SetPow( const complex& src, const complex& pow ) { SetLog( src ); Mul( pow ); Exp(); };
	void					SetPow( const complex& src, const float pow );
	void					SetLog( const complex& src );
	void					SetExp( const complex& src );
	void					SetSqrt( const complex& src );
	inline void				SetNegate( const complex& src ) { r = -src.r; i = -src.i; };
	void					SetInverse( const complex& src );
	inline void				SetConjugate( const complex& src ) { r = src.r; i = -src.i; };
	void					SetNormalized( const complex& src );
	void					SetLerp( const complex& src, const complex& trg, const float t );
	void					SetSlerp( const complex& src, const complex& trg, const float t );
	void					SetBlend( const complex& src, const complex& trg, const float t );
	void					SetDelta( const complex& src, const complex& trg );
	inline void				SetIdentity( void ) { Set( consts::COMPLEX::IDENTITY ); };
	void					SetNormalized( const float nr, const float ni );
	inline void				Set( const complex& c ) { r = c.r; i = c.i; };
	void					Set( const vec2& v );
	inline void				Set( const float _r, const float _i ) { r = _r; i = _i; };
	void					GetNormalized( float& nr, float& ni ) const;
	inline void				Get( complex& c ) const { c.r = r; c.i = i; };
	void					Get( vec2& v ) const;
	inline void				Get( float& _r, float& _i ) const { _r = r; _i = i; };
	inline complex&			operator=( const complex& c ) { Set( c ); return( *this ); };
	inline complex&			operator=( const vec2& v ) { Set( v ); return( *this ); };
	inline complex&			operator+=( const complex& add ) { Add( add ); return( *this ); };
	inline complex&			operator-=( const complex& sub ) { Sub( sub ); return( *this ); };
	inline complex&			operator*=( const complex& mul ) { Mul( mul ); return( *this ); };
	inline complex&			operator*=( const float mul ) { Mul( mul ); return( *this ); };
	inline complex&			operator/=( const complex& div ) { Div( div ); return( *this ); };
	inline complex&			operator/=( const float div ) { Div( div ); return( *this ); };
};

inline complex operator+( const complex& src, const complex& add ) { complex c; c.SetAdd( src, add ); return( c ); };
inline complex operator-( const complex& src, const complex& sub ) { complex c; c.SetSub( src, sub ); return( c ); };
inline complex operator*( const complex& src, const complex& mul ) { complex c; c.SetMul( src, mul ); return( c ); };
inline complex operator*( const complex& src, const float mul ) { complex c; c.SetMul( src, mul ); return( c ); };
inline complex operator/( const complex& src, const complex& div ) { complex c; c.SetDiv( src, div ); return( c ); };
inline complex operator/( const complex& src, const float div ) { complex c; c.SetDiv( src, div ); return( c ); };
inline complex operator-( const complex& src ) { complex c; c.SetNegate( src ); return( c ); };

inline complex normalize( const complex& src ) { complex c; c.SetNormalized( src ); return( c ); };
inline complex invert( const complex& src ) { complex c; c.SetInverse( src ); return( c ); };
inline complex conjugate( const complex& src ) { complex c; c.SetConjugate( src ); return( c ); };
inline complex pow( const complex& src, const complex& pow ) { complex c; c.SetPow( src, pow ); return( c ); };
inline complex pow( const complex& src, const float pow ) { complex c; c.SetPow( src, pow ); return( c ); };
inline complex log( const complex& src ) { complex c; c.SetLog( src ); return( c ); };
inline complex exp( const complex& src ) { complex c; c.SetExp( src ); return( c ); };
inline complex lerp( const complex& a, const complex& b, const float t ) { complex c; c.SetLerp( a, b, t ); return( c ); };
inline complex slerp( const complex& a, const complex& b, const float t ) { complex c; c.SetSlerp( a, b, t ); return( c ); };

