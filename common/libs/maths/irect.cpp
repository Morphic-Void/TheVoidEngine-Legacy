bool irect::Union( const irect& other )
{
	if( l < other.l ) l = other.l;
	if( t < other.t ) t = other.t;
	if( r > other.r ) r = other.r;
	if( b > other.b ) b = other.b;
	if( ( l > r ) || ( t > b ) )
	{
		l = t = r = b = 0;
		return( false );
	}
	return( true );
}

void irect::Merge( const irect& other )
{
	if( l > other.l ) l = other.l;
	if( t > other.t ) t = other.t;
	if( r < other.r ) r = other.r;
	if( b < other.b ) b = other.b;
}

bool irect::SetUnion( const irect& rect1, const irect& rect2 )
{
	l = ( ( rect1.l > rect2.l ) ? rect1.l : rect2.l );
	t = ( ( rect1.t > rect2.t ) ? rect1.t : rect2.t );
	r = ( ( rect1.r < rect2.r ) ? rect1.r : rect2.r );
	b = ( ( rect1.b < rect2.b ) ? rect1.b : rect2.b );
	if( ( l > r ) || ( t > b ) )
	{
		l = t = r = b = 0;
		return( false );
	}
	return( true );
}

void irect::SetMerge( const irect& rect1, const irect& rect2 )
{
	l = ( ( rect1.l < rect2.l ) ? rect1.l : rect2.l );
	t = ( ( rect1.t < rect2.t ) ? rect1.t : rect2.t );
	r = ( ( rect1.r > rect2.r ) ? rect1.r : rect2.r );
	b = ( ( rect1.b > rect2.b ) ? rect1.b : rect2.b );
}

bool irect::Contains( const irect& other ) const
{
	return( (
		( l <= other.l ) && ( r >= other.r ) &&
		( t <= other.t ) && ( b >= other.b ) ) ? true : false );
}

bool irect::Intersects( const irect& other ) const
{
	return( (
		( l <= other.r ) && ( r >= other.l ) &&
		( t <= other.b ) && ( b >= other.t ) ) ? true : false );
}

