bool complex::IsNan( void ) const
{
	return( ::IsNan( r ) || ::IsNan( i ) );
}

bool complex::IsReal( void ) const
{
	return( ::IsReal( r ) && ::IsReal( i ) );
}

float complex::Length( void ) const
{
	return( sqrtf( ( r * r ) + ( i * i ) ) );
}

void complex::Pow( const float pow )
{
	float e = powf( ( ( r * r ) + ( i * i ) ), ( pow * 0.5f ) );
	float a = ( atan2f( i, r ) * pow );
	r = ( e * cosf( a ) );
	i = ( e * sinf( a ) );
}

void complex::Log( void )
{
	float n = ( ( r * r ) + ( i * i ) + consts::FLOAT_MIN_NORM );
	i = atan2f( i, r );
	r = ( logf( n ) * 0.5f );
}

void complex::Exp( void )
{
	float e = expf( r );
	r = ( e * cosf( i ) );
	i = ( e * sinf( i ) );
}

void complex::Sqrt( void )
{
	float n = sqrtf( ( r * r ) + ( i * i ) );
	float j = ( sqrtf( fabsf( n - r ) * 0.5f ) );
	float s = ( sqrtf( fabsf( n + r ) * 0.5f ) );
	i = ( ( i < 0.0f ) ? - j : j );
	r = s;
}

void complex::Invert( void )
{
	float n = ( 1.0f / ( ( r * r ) + ( i * i ) + consts::FLOAT_MIN_NORM ) );
	r *= n;
	i *= -n;
}

void complex::Normalize( void )
{
	float n = ( 1.0f / ( sqrtf( ( r * r ) + ( i * i ) ) + consts::FLOAT_MIN_NORM ) );
	r *= n;
	i *= n;
}

void complex::SetDiv( const complex& src, const complex& div )
{
	complex c;
	float n = ( 1.0f / ( ( div.r * div.r ) + ( div.i * div.i ) + consts::FLOAT_MIN_NORM ) );
	c.r = ( ( ( src.r * div.r ) + ( src.i * div.i ) ) * n );
	c.i = ( ( ( src.i * div.r ) - ( src.r * div.i ) ) * n );
	r = c.r;
	i = c.i;
}

void complex::SetDiv( const complex& src, const float div )
{
	ASSERT( fabsf( div ) >= consts::FLOAT_MIN_NORM );
	if( fabsf( div ) >= consts::FLOAT_MIN_NORM )
	{
		SetMul( src, ( 1.0f / div ) );
	}
	else
	{
		r = 1.0f;
		i = 0.0f;
	}
}

void complex::SetPow( const complex& src, const float pow )
{
	float e = powf( ( ( src.r * src.r ) + ( src.i * src.i ) ), ( pow * 0.5f ) );
	float a = ( atan2f( src.i, src.r ) * pow );
	r = ( e * cosf( a ) );
	i = ( e * sinf( a ) );
}

void complex::SetLog( const complex& src )
{
	float n = ( ( src.r * src.r ) + ( src.i * src.i ) + consts::FLOAT_MIN_NORM );
	i = atan2f( src.i, src.r );
	r = ( logf( n ) * 0.5f );
}

void complex::SetExp( const complex& src )
{
	float e = expf( src.r );
	r = ( e * cosf( src.i ) );
	i = ( e * sinf( src.i ) );
}

void complex::SetSqrt( const complex& src )
{
	float n = sqrtf( ( src.r * src.r ) + ( src.i * src.i ) );
	float j = ( sqrtf( fabsf( n - src.r ) * 0.5f ) );
	float s = ( sqrtf( fabsf( n + src.r ) * 0.5f ) );
	i = ( ( src.i < 0.0f ) ? - j : j );
	r = s;
}

void complex::SetInverse( const complex& src )
{
	float n = ( 1.0f / ( ( src.r * src.r ) + ( src.i * src.i ) + consts::FLOAT_MIN_NORM ) );
	r = ( src.r * n );
	i = -( src.i * n );
}

void complex::SetNormalized( const complex& src )
{
	float n = ( 1.0f / ( sqrtf( ( src.r * src.r ) + ( src.i * src.i ) ) + consts::FLOAT_MIN_NORM ) );
	r = ( src.r * n );
	i = ( src.i * n );
}

void complex::SetLerp( const complex& src, const complex& trg, const float t )
{
	float s = ( 1.0f - t );
	if( ( ( src.r * trg.r ) + ( src.i * trg.i ) ) < 0.0f )
	{
		r = ( ( src.r * s ) - ( trg.r * t ) );
		i = ( ( src.i * s ) - ( trg.i * t ) );
	}
	else
	{
		r = ( ( src.r * s ) + ( trg.r * t ) );
		i = ( ( src.i * s ) + ( trg.i * t ) );
	}
}

void complex::SetSlerp( const complex& src, const complex& trg, const float t )
{
	complex c;
	c.SetLog( trg );
	SetLog( src );
	Blend( c, t );
	Exp();
}

void complex::SetBlend( const complex& src, const complex& trg, const float t )
{
	float s = ( 1.0f - t );
	r = ( ( src.r * s ) + ( trg.r * t ) );
	i = ( ( src.i * s ) + ( trg.i * t ) );
}

void complex::SetDelta( const complex& src, const complex& trg )
{
	complex c;
	float n = ( 1.0f / ( ( src.r * src.r ) + ( src.i * src.i ) + consts::FLOAT_MIN_NORM ) );
	c.r = ( ( ( src.r * trg.r ) + ( src.i * trg.i ) ) * n );
	c.i = ( ( ( src.r * trg.i ) - ( src.i * trg.r ) ) * n );
	r = c.r;
	i = c.i;
}

void complex::SetNormalized( const float nr, const float ni )
{
	float n = ( 1.0f / ( sqrtf( ( nr * nr ) + ( ni * ni ) ) + consts::FLOAT_MIN_NORM ) );
	r = ( nr * n );
	i = ( ni * n );
}

void complex::Set( const vec2& v )
{
	r = v.x;
	i = v.y;
}

void complex::GetNormalized( float& nr, float& ni ) const
{
	float n = ( 1.0f / ( sqrtf( ( r * r ) + ( i * i ) ) + consts::FLOAT_MIN_NORM ) );
	nr = ( r * n );
	ni = ( i * n );
}

void complex::Get( vec2& v ) const
{
	v.x = r;
	v.y = i;
}

