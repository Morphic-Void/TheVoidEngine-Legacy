bool dual::IsNan( void ) const
{
	return( ::IsNan( r ) || ::IsNan( e ) );
}

bool dual::IsReal( void ) const
{
	return( ::IsReal( r ) && ::IsReal( e ) );
}

float dual::Length( void ) const
{
	return( sqrtf( ( r * r ) + ( e * e ) ) );
}

void dual::Pow( const float pow )
{
	ASSERT( r >= 0.0f );
	if( r >= 0.0f )
	{
		float t = powf( r, ( pow - 1.0f ) );
		e *= ( t * pow );
		r *= t;
	}
	else
	{
		e = 0.0f;
		r = 0.0f;
	}
}

void dual::Log( void )
{
	ASSERT( r >= maths::consts::FLOAT_MIN_NORM );
	if( r >= maths::consts::FLOAT_MIN_NORM )
	{
		e /= r;
		r = logf( r );
	}
	else
	{
		e = 0.0f;
		r = 1.0f;
	}
}

void dual::Exp( void )
{
	r = expf( r );
	e *= r;
}

void dual::Sqrt( void )
{
	ASSERT( r >= maths::consts::FLOAT_MIN_NORM );
	if( r >= maths::consts::FLOAT_MIN_NORM )
	{
		r = sqrtf( r );
		e /= ( r + r );
	}
	else
	{
		r = 1.0f;
		e = 0.0f;
	}
}

void dual::Invert( void )
{
	ASSERT( fabsf( r ) >= consts::FLOAT_MIN_NORM );
	if( fabsf( r ) >= consts::FLOAT_MIN_NORM )
	{
		r = ( 1.0f / r );
		e *= -( r * r );
	}
	else
	{
		r = 1.0f;
		e = 0.0f;
	}
}

void dual::Normalize( void )
{
	float n = ( 1.0f / ( sqrtf( ( r * r ) + ( e * e ) ) + consts::FLOAT_MIN_NORM ) );
	r *= n;
	e *= n;
}

void dual::SetDiv( const dual& src, const dual& div )
{
	float n = ( div.r * div.r );
	ASSERT( n >= consts::FLOAT_MIN_NORM );
	if( n >= consts::FLOAT_MIN_NORM )
	{
		e = ( ( ( src.e * div.r ) - ( src.r * div.e ) ) / n );
		r = ( src.r / div.r );
	}
	else
	{
		e = 0.0f;
		r = 1.0f;
	}
}

void dual::SetDiv( const dual& src, const float div )
{
	ASSERT( fabsf( div ) >= consts::FLOAT_MIN_NORM );
	if( fabsf( div ) >= consts::FLOAT_MIN_NORM )
	{
		SetMul( src, ( 1.0f / div ) );
	}
	else
	{
		r = 1.0f;
		e = 0.0f;
	}
}

void dual::SetPow( const dual& src, const float pow )
{
	ASSERT( src.r >= 0.0f );
	if( src.r >= 0.0f )
	{
		float t = powf( src.r, ( pow - 1.0f ) );
		e = ( src.e * pow * t );
		r = ( src.r * t );
	}
	else
	{
		e = 0.0f;
		r = 0.0f;
	}
}

void dual::SetLog( const dual& src )
{
	ASSERT( src.r >= maths::consts::FLOAT_MIN_NORM );
	if( src.r >= maths::consts::FLOAT_MIN_NORM )
	{
		e = ( src.e / src.r );
		r = logf( src.r );
	}
	else
	{
		e = 0.0f;
		r = 1.0f;
	}
}

void dual::SetExp( const dual& src )
{
	r = expf( src.r );
	e = ( src.e * r );
}

void dual::SetSqrt( const dual& src )
{
	ASSERT( src.r >= maths::consts::FLOAT_MIN_NORM );
	if( src.r >= maths::consts::FLOAT_MIN_NORM )
	{
		r = sqrtf( src.r );
		e = ( src.e / ( r + r ) );
	}
	else
	{
		r = 1.0f;
		e = 0.0f;
	}
}

void dual::SetInverse( const dual& src )
{
	ASSERT( fabsf( src.r ) >= consts::FLOAT_MIN_NORM );
	if( fabsf( src.r ) >= consts::FLOAT_MIN_NORM )
	{
		r = ( 1.0f / src.r );
		e = -( src.e * r * r );
	}
	else
	{
		r = 1.0f;
		e = 0.0f;
	}
}

void dual::SetNormalized( const dual& src )
{
	float n = ( 1.0f / ( sqrtf( ( src.r * src.r ) + ( src.e * src.e ) ) + consts::FLOAT_MIN_NORM ) );
	r = ( src.r * n );
	e = ( src.e * n );
}

void dual::SetLerp( const dual& src, const dual& trg, const float t )
{
	float s = ( 1.0f - t );
	if( ( ( src.r * trg.r ) + ( src.e * trg.e ) ) < 0.0f )
	{
		r = ( ( src.r * s ) - ( trg.r * t ) );
		e = ( ( src.e * s ) - ( trg.e * t ) );
	}
	else
	{
		r = ( ( src.r * s ) + ( trg.r * t ) );
		e = ( ( src.e * s ) + ( trg.e * t ) );
	}
}

void dual::SetSlerp( const dual& src, const dual& trg, const float t )
{
	dual c;
	c.SetLog( trg );
	SetLog( src );
	Blend( c, t );
	Exp();
}

void dual::SetBlend( const dual& src, const dual& trg, const float t )
{
	float s = ( 1.0f - t );
	r = ( ( src.r * s ) + ( trg.r * t ) );
	e = ( ( src.e * s ) + ( trg.e * t ) );
}

void dual::SetDelta( const dual& src, const dual& trg )
{
	float n = ( src.r * src.r );
	ASSERT( n >= consts::FLOAT_MIN_NORM );
	if( n >= consts::FLOAT_MIN_NORM )
	{
		e = ( ( ( trg.e * src.r ) - ( trg.r * src.e ) ) / n );
		r = ( trg.r / src.r );
	}
	else
	{
		r = 1.0f;
		e = 0.0f;
	}
}

void dual::SetNormalized( const float nr, const float ne )
{
	float n = ( 1.0f / ( sqrtf( ( nr * nr ) + ( ne * ne ) ) + consts::FLOAT_MIN_NORM ) );
	r = ( nr * n );
	e = ( ne * n );
}

void dual::Set( const vec2& v )
{
	r = v.x;
	e = v.y;
}

void dual::GetNormalized( float& nr, float& ne ) const
{
	float n = ( 1.0f / ( sqrtf( ( r * r ) + ( e * e ) ) + consts::FLOAT_MIN_NORM ) );
	nr = ( r * n );
	ne = ( e * n );
}

void dual::Get( vec2& v ) const
{
	v.x = r;
	v.y = e;
}

