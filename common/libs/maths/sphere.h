struct vec2;
struct vec3;
struct vec4;
struct aabb;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    sphere
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct sphere
{
	float					x, y, z, r;
	inline vec2&			t_vec2( void ) { return( *reinterpret_cast< vec2* >( this ) ); };
	inline const vec2&		t_vec2( void ) const { return( *reinterpret_cast< const vec2* >( this ) ); };
	inline vec3&			t_vec3( void ) { return( *reinterpret_cast< vec3* >( this ) ); };
	inline const vec3&		t_vec3( void ) const { return( *reinterpret_cast< const vec3* >( this ) ); };
	inline vec4&			t_vec4( void ) { return( *reinterpret_cast< vec4* >( this ) ); };
	inline const vec4&		t_vec4( void ) const { return( *reinterpret_cast< const vec4* >( this ) ); };
	bool					IsNan( void ) const;
	bool					IsReal( void ) const;
	void					Set( const sphere& asphere );
	void					Set( const aabb& abox );
	void					Set( const float sx, const float sy, const float sz, const float radius );
	void					Set( const vec3& position, const float radius );
	void					Get( float& sx, float& sy, float& sz, float& radius ) const;
	void					Get( vec3& position, float& radius ) const;
	bool					Contains( const vec3& apoint ) const;
	bool					Contains( const sphere& asphere ) const;
	bool					Contains( const aabb& abox ) const;
	bool					Intersects( const sphere& asphere ) const;
	bool					Intersects( const aabb& abox ) const;
	bool					Intersects( const vec3& start, const vec3& end ) const;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
