////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    joint
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct joint
{
	quat					q;
	vec3					v;
	bool					IsNan( void ) const;
	bool					IsReal( void ) const;
	void					Transform( const vec3& src, vec3& trg ) const;
	void					Transform( const vec4& src, vec4& trg ) const;
	void					UnscaledTransform( const vec3& src, vec3& trg ) const;
	void					UnscaledTransform( const vec4& src, vec4& trg ) const;
	void					InverseTransform( const vec3& src, vec3& trg ) const;
	void					InverseTransform( const vec4& src, vec4& trg ) const;
	void					PointAt( const vec3& from, const vec3& at, const vec3& up );
	void					PointTo( const vec3& from, const vec3& to, const vec3& up );
	void					PointAt( const vec3& at, const vec3& up );
	void					PointTo( const vec3& to, const vec3& up );
	void					LookAt( const vec3& from, const vec3& at, const vec3& up );
	void					LookTo( const vec3& from, const vec3& to, const vec3& up );
	void					LookAt( const vec3& at, const vec3& up );
	void					LookTo( const vec3& to, const vec3& up );
	void					Scale( const float scale );
	void					Twist( const float radians );
	void					Tilt( const float radians );
	void					Turn( const float radians );
	void					Roll( const float radians );
	void					Pitch( const float radians );
	void					Yaw( const float radians );
	void					EndScale( const float scale );
	void					EndTwist( const float radians );
	void					EndTilt( const float radians );
	void					EndTurn( const float radians );
	void					EndRoll( const float radians );
	void					EndPitch( const float radians );
	void					EndYaw( const float radians );
	void					Move( const float move_x, const float move_y, const float move_z );
	void					Move( const vec3& move );
	void					Pan( const float pan_x, const float pan_y, const float pan_z );
	void					Pan( const vec3& pan );
	void					UnscaledPan( const float pan_x, const float pan_y, const float pan_z );
	void					UnscaledPan( const vec3& pan );
	void					PreMul( const joint& mul );
	void					Mul( const joint& mul );
	void					Mul( const quat& mul );
	void					Mul( const float mul );
	void					Div( const joint& div );
	void					Div( const quat& div );
	void					Div( const float div );
	void					Invert( void );
	void					Lerp( const joint& trg, const float t );
	void					Slerp( const joint& trg, const float t );
	void					SetMul( const joint& src, const joint& mul );
	void					SetMul( const joint& src, const quat& mul );
	void					SetMul( const quat& src, const joint& mul );
	void					SetMul( const joint& src, const float mul );
	void					SetDiv( const joint& src, const joint& div );
	void					SetDiv( const joint& src, const quat& div );
	void					SetDiv( const joint& src, const float div );
	void					SetDelta( const joint& src, const joint& trg );
	void					SetInverse( const joint& src );
	void					SetLerp( const joint& src, const joint& trg, const float t );
	void					SetSlerp( const joint& src, const joint& trg, const float t );
	inline void				SetIdentity( void ) { Set( consts::JOINT::IDENTITY ); };
	void					Set( const joint& j );
	void					Set( const mat43& m );
	void					Set( const quat& rotate, const vec3& translate );
	void					Get( joint& j ) const;
	void					Get( mat43& m ) const;
	void					Get( quat& rotate, vec3& translate ) const;
	void					GetInverse( joint& j ) const;
	void					GetInverse( mat43& m ) const;
	void					GetInverse( quat& rotate, vec3& translate ) const;
	inline joint&			operator=( const joint& j ) { Set( j ); return( *this ); };
	inline joint&			operator=( const mat43& m ) { Set( m ); return( *this ); };
	inline joint&			operator*=( const joint& mul ) { Mul( mul ); return( *this ); };
	inline joint&			operator/=( const joint& div ) { Div( div ); return( *this ); };
};

inline joint operator*( const joint& src, const joint& mul ) { joint t; t.SetMul( src, mul ); return( t ); };

