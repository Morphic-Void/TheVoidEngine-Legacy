{
	namespace MAT33
	{
		extern const mat33		IDENTITY;
		extern const mat33		BEZIER;			//	A, B, C
		extern const mat33		B_SPLINE;		//	A, B, C
	};
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    mat33
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct mat33
{
	vec3					x, y, z;
	bool					IsNan( void ) const;
	bool					IsReal( void ) const;
	float					Det( void ) const;
	void					PointTo( const vec3& to, const vec3& up );
	void					LookTo( const vec3& to, const vec3& up );
	void					Scale( const float scale );
	void					Scale( const float scale_x, const float scale_y, const float scale_z );
	void					Twist( const float radians );
	void					Tilt( const float radians );
	void					Turn( const float radians );
	void					Roll( const float radians );
	void					Pitch( const float radians );
	void					Yaw( const float radians );
	void					Mul( const mat33& mul );
	bool					Invert( void );
	void					Transpose( void );
	bool					InvertTranspose( void );
	void					PartialInvert( void );
	void					PartialInvertTranspose( void );
	void					SetMul( const mat33& src, const mat33& mul );
	void					SetDelta( const mat33& src, const mat33& trg );
	bool					SetInverse( const mat33& src );
	void					SetTranspose( const mat33& src );
	bool					SetInverseTranspose( const mat33& src );
	void					SetPartialInverse( const mat33& src );
	void					SetPartialInverseTranspose( const mat33& src );
	inline void				SetTwistTiltTurn( const float twist, const float tilt, const float turn ) { SetYawPitchRoll( turn, tilt, twist ); };
	inline void				SetTurnTiltTwist( const float turn, const float tilt, const float twist ) { SetRollPitchYaw( twist, tilt, turn ); };
	void					SetRollPitchYaw( const float roll, const float pitch, const float yaw );
	void					SetYawPitchRoll( const float yaw, const float pitch, const float roll );
	inline void				SetTiltTurn( const float tilt, const float turn ) { SetYawPitch( turn, tilt ); };
	inline void				SetTurnTilt( const float turn, const float tilt ) { SetPitchYaw( tilt, turn ); };
	void					SetPitchYaw( const float pitch, const float yaw );
	void					SetYawPitch( const float yaw, const float pitch );
	void					SetAxisAngle( const float axis_x, const float axis_y, const float axis_z, const float radians );
	void					SetAxisAngle( const vec3& axis, const float radians );
	void					SetScaling( const float scale );
	void					SetScaling( const float scale_x, const float scale_y, const float scale_z );
	bool					SetTanSpace( const vec3& dc1, const vec2& dt1, const vec3& dc2, const vec2& dt2, float& weight );
	bool					SetTanSpace( const vec3& n, const vec3& dc1, const vec2& dt1, const vec3& dc2, const vec2& dt2, float& weight );
	void					SetTensor( const vec3& u, const vec3& v );
	inline void				SetIdentity( void ) { Set( consts::MAT33::IDENTITY ); };
	void					Set( const mat33& m );
	void					Set( const quat& q );
	void					Set( const qtangent& t );
	inline void				GetTwistTiltTurn( float& twist, float& tilt, float& turn ) const { GetYawPitchRoll( turn, tilt, twist ); };
	inline void				GetTurnTiltTwist( float& turn, float& tilt, float& twist ) const { GetRollPitchYaw( twist, tilt, turn ); };
	void					GetRollPitchYaw( float& roll, float& pitch, float& yaw ) const;
	void					GetYawPitchRoll( float& yaw, float& pitch, float& roll ) const;
	void					Get( mat33& m ) const;
	void					Get( quat& q ) const;
	void					Get( qtangent& t ) const;
	bool					SetBSpline( const float ( &knots )[ 4 ] );
    void					SetControl( const float a, const float b, const float c );
    void					SetControl( const vec2& a, const vec2& b, const vec2& c );
    void					SetControl( const vec3& a, const vec3& b, const vec3& c );
	void					EvaluatePolynomial( const float t, vec3& value ) const;
	void					EvaluatePolynomial( const float t, vec3& value, vec3& first ) const;
	void					EvaluateDerivative( const float t, vec3& first ) const;
	float					IntegratePolynomialLength( const float lower_t, const float upper_t ) const;
	float					IntegratePolynomialLength( const unsigned int steps, const float lower_t, const float upper_t ) const;
	void					EvaluateRationalPolynomial( const float t, vec3& value ) const;
	void					EvaluateRationalPolynomial( const float t, vec3& value, vec3& first ) const;
	void					EvaluateRationalDerivative( const float t, vec3& first ) const;
	float					IntegrateRationalPolynomialLength( const float lower_t, const float upper_t ) const;
	float					IntegrateRationalPolynomialLength( const unsigned int steps, const float lower_t, const float upper_t ) const;
	inline mat33&			operator=( const mat33& m ) { Set( m ); return( *this ); };
	inline mat33&			operator=( const quat& q ) { Set( q ); return( *this ); };
	inline mat33&			operator*=( const mat33& mul ) { Mul( mul ); return( *this ); };
};

inline mat33 operator*( const mat33& src, const mat33& mul ) { mat33 t; t.SetMul( src, mul ); return( t ); };

inline mat33 invert( const mat33& src ) { mat33 t; t.SetInverse( src ); return( t ); };
inline mat33 transpose( const mat33& src ) { mat33 t; t.SetTranspose( src ); return( t ); };

