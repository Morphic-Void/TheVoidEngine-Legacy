//! adaptive tolerance comparison
//! the tolerance is selected to be that larger of the absolute tolerance and the tolerance relative to the average of the given values
//! return true if the difference between the values is less than or equal to the higher tolerance
bool fcmpf( const float a, const float b, const float ta, const float tr ) { return( fabsf( a - b ) <= fmaxf( fabsf( ta ), fabsf( ( a + b ) * tr * 0.5f ) ) ); };

//! absolute tolerance comparison
//! return true if the difference between the values is less than or equal to the absolute tolerance
bool fcmpf( const float a, const float b, const float ta ) { return( fabsf( a - b ) <= fabsf( ta ) ); };

//! relative tolerance comparison
//!	the tolerance is relative to the average of the given values
//! return true if the difference between the values is less than or equal to the relative tolerance
bool fcmprf( const float a, const float b, const float tr ) { return( fabsf( a - b ) <= fabsf( ( a + b ) * tr * 0.5f ) ); };

