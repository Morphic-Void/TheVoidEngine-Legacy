//! cos( a ) to cos( 2a ), double angle cosine calculation
float CosAtoCos2A( const float cosA )
{
	return( ( cosA * cosA * 2.0f ) - 1.0f );
}

//! cos( a ) to cos( a/2 ), half angle cosine calculation
float Cos2AtoCosA( const float cos2A )
{
	return( sqrtf( ( 1.0f + cos2A ) * 0.5f ) );
}

//! cos( a ) to tan( a/8 ), useful for spotlight arc calculation
float Cos8AtoTanA( const float cos8A )
{
	float t = sqrtf( 0.125f + sqrtf( 0.0078125f + ( cos8A * 0.0078125f ) ) );
	return( sqrtf( ( 0.5f - t ) / ( 0.5f + t ) ) );
}

