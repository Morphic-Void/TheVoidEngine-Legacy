//! standard zero based exponential function
float ExpZFS( const float v, const float p )
{	//	apply zero floored exponential scaling
	ASSERT( v >= 0.0f );
	return( expf( logf( v + 1.0f ) * p ) - 1.0f );
}

//! standard zero based exponential function with bias
float ExpZFSB( const float v, const float p, const float b )
{	//	apply zero floored exponential scale and bias
	ASSERT( v >= 0.0f );
	return( expf( ( logf( v + 1.0f ) * p ) + b ) - 1.0f );
}

//! standard zero based exponential function (input scale is exponential)
float ExpZFE( const float v, const float e )
{	//	apply log2 zero floored exponential scale
	return( ExpZFS( v, exp2f( e ) ) );
}

//! standard zero based exponential function with bias (input scale is exponential)
float ExpZFEB( const float v, const float e, const float b )
{	//	apply log2 zero floored exponential scale and bias
	return( ExpZFSB( v, exp2f( e ), b ) );
}

//! calculate value remapping scale factor for ExpZFS()
float GetValueRemapExpZF( const float s, const float t )
{
	ASSERT( ( s >= 0.0f ) && ( t >= 0.0f ) );
	return( logf( t + 1.0f ) / logf( s + 1.0f ) );
}

//! calculate range remapping scale and bias factors for ExpZFSB()
void GetRangeRemapExpZF( const float sl, const float sh, const float tl, const float th, float& p, float& b )
{
	ASSERT( ( sl >= 0.0f ) && ( sh >= 0.0f ) && ( tl >= 0.0f ) && ( th >= 0.0f ) );
	float lsl = logf( sl + 1.0f );
	float lsh = logf( sh + 1.0f );
	float ltl = logf( tl + 1.0f );
	float lth = logf( th + 1.0f );
	ASSERT( fabsf( lsh - lsl ) >= consts::FLOAT_MIN_RCP );
	float d = ( 1.0f / ( lsh - lsl ) );
	p = ( ( lth - ltl ) * d );
	b = ( ( ( ltl * lsh ) - ( lth * lsl ) ) * d );
}

