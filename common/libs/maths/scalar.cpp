//! extract fractional part
float fracf( const float a ) { return( a - floorf( a ) ); };

//! select the minimum of the 2 values
float fminf( const float a, const float b ) { return( ( a + b - fabsf( a - b ) ) * 0.5f ); };

//! select the maximum of the 2 values
float fmaxf( const float a, const float b ) { return( ( a + b + fabsf( a - b ) ) * 0.5f ); };

//! clamp to negative values
float fnegf( const float a ) { return( ( a - fabsf( a ) ) * 0.5f ); };

//! clamp to positive values
float fposf( const float a ) { return( ( a + fabsf( a ) ) * 0.5f ); };

//! clamp the value to the 0 to 1 range
float fsatf( const float a ) { float s = ( a - fabsf( a - 1 ) + 1 ); return( ( s + fabsf( s ) ) * 0.25f ); };

//! clamp the value to the given range
float fclampf( const float a, const float l, const float h ) { return( fminf( h, fmaxf( l, a ) ) ); };

