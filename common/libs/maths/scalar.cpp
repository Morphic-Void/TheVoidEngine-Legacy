//! linear : log deviation
void SumLogDeviation( const float nSums, const float nLogs, float& Middle, float& Extent )
{
	ASSERT( ( ( nSums * nSums ) - expf( nLogs + nLogs ) ) >= 0.0f );
	Middle = nSums;
	Extent = sqrtf( ( nSums * nSums ) - expf( nLogs + nLogs ) );
}

//! linear : square deviation
void SumSqrDeviation( const float nSums, const float nSqrs, float& Middle, float& Extent )
{
	ASSERT( ( nSqrs - ( nSums * nSums ) ) >= 0.0f );
	Middle = nSums;
	Extent = sqrtf( nSqrs - ( nSums * nSums ) );
}

//! linear : root deviation
void SumRootDeviation( const float nSums, const float nRoots, float& Middle, float& Extent )
{
	float t = ( ( nRoots * nRoots * 4.0f ) - nSums - nSums );
	ASSERT( nSums >= t );
	Middle = nSums;
	Extent = sqrtf( ( nSums * nSums ) - ( t * t ) );
}

//! log : square deviation
void LogSqrDeviation( const float nLogs, const float nSqrs, float& Middle, float& Extent )
{
	float t = expf( nLogs + nLogs );
	ASSERT( nSqrs >= t );
	Middle = sqrtf( ( nSqrs + t ) * 0.5f );
	Extent = sqrtf( ( nSqrs - t ) * 0.5f );
}

//! log : root deviation
void LogRootDeviation( const float nLogs, const float nRoots, float& Middle, float& Extent )
{
	SumLogDeviation( ( ( nRoots * nRoots * 2.0f ) - ( expf( nLogs ) * 0.5f ) ), nLogs, Middle, Extent );
}

//! standard deviation ( linear : square )
void StandardDeviation( const float nSums, const float nSqrs, float& Middle, float& Extent )
{
	SumSqrDeviation( nSums, nSqrs, Middle, Extent );
}

//! cantelli probability
float CantelliProbability( const float nSums, const float nSqrs, const float value )
{
	if( value > nSums )
	{
		return( 0.0f );
	}
	float v = ( nSqrs - ( nSums * nSums ) );
	if( v < 0.0f )
	{
		return( 1.0f );
	}
	float d = ( nSums - value );
	d *= d;
	return( d / ( d + v ) );
}

//! cantelli probability for variance shadows
float CantelliShadow( const float nSums, const float nSqrs, const float value )
{
	if( value > nSums )
	{
		return( 1.0f );
	}
	float v = ( nSqrs - ( nSums * nSums ) );
	if( v < 0.0f )
	{
		return( 0.0f );
	}
	float d = ( nSums - value );
	return( v / ( v + ( d * d ) ) );
}

