float SumPowerSeries( const float ratio, const float powers )
{
	float r = fabsf( ratio );
	float d = ( 1.0f - r );
	return( ( fabsf( d ) < consts::FLOAT_MIN_RCP ) ? ( 1.0f + powers ) : ( ( 1.0f - powf( r, powers ) ) / d ) );
}

float SumPowerSeriesRange( const float ratio, const float power_lo, const float power_hi )
{
	float r = fabsf( ratio );
	float d = ( 1.0f - r );
	return( ( fabsf( d ) < consts::FLOAT_MIN_RCP ) ? ( power_hi - power_lo ) : ( ( powf( r, power_lo ) - powf( r, power_hi ) ) / d ) );
}

