bool sphere::IsNan( void ) const
{
	return( ::IsNan( x ) || ::IsNan( y ) || ::IsNan( z ) || ::IsNan( r ) );
}

bool sphere::IsReal( void ) const
{
	return( ::IsReal( x ) && ::IsReal( y ) && ::IsReal( z ) && ::IsReal( r ) );
}

void sphere::Set( const sphere& asphere )
{
    x = asphere.x;
    y = asphere.y;
    z = asphere.z;
    r = asphere.r;
}

void sphere::Set( const aabb& abox )
{
	x = ( ( abox.max.x + abox.min.x ) * 0.5f );
	y = ( ( abox.max.y + abox.min.y ) * 0.5f );
	z = ( ( abox.max.z + abox.min.z ) * 0.5f );
	float dx = ( abox.max.x - x );
	float dy = ( abox.max.y - y );
	float dz = ( abox.max.z - z );
	r = sqrtf( ( dx * dx ) + ( dy * dy ) + ( dz * dz ) );
}

void sphere::Set( const float sx, const float sy, const float sz, const float radius )
{
	x = sx;
	y = sy;
	z = sz;
	r = radius;
}

void sphere::Set( const vec3& position, const float radius )
{
	x = position.x;
	y = position.y;
	z = position.z;
	r = radius;
}

void sphere::Get( float& sx, float& sy, float& sz, float& radius ) const
{
	sx = x;
	sy = y;
	sz = z;
	radius = r;
}

void sphere::Get( vec3& position, float& radius ) const
{
	position.x = x;
	position.y = y;
	position.z = z;
	radius = r;
}

bool sphere::Contains( const vec3& apoint ) const
{
	float dx = ( x - apoint.x );
	float dy = ( y - apoint.y );
	float dz = ( z - apoint.z );
	return( ( ( ( dx * dx ) + ( dy * dy ) + ( dz * dz ) ) <= ( r * r ) ) ? true : false );
}

bool sphere::Contains( const sphere& asphere ) const
{
	if( r >= asphere.r )
	{
		float dx = ( x - asphere.x );
		float dy = ( y - asphere.y );
		float dz = ( z - asphere.z );
		float dr = ( r - asphere.r );
		return( ( ( ( dx * dx ) + ( dy * dy ) + ( dz * dz ) ) <= ( dr * dr ) ) ? true : false );
	}
	return( false );
}

bool sphere::Contains( const aabb& abox ) const
{
	float d = 0;
	float a, b;
	a = ( abox.min.x - x );
	b = ( x - abox.max.x );
	a *= a;
	b *= b;
	d += ( ( a > b ) ? a : b );
	a = ( abox.min.y - y );
	b = ( y - abox.max.y );
	a *= a;
	b *= b;
	d += ( ( a > b ) ? a : b );
	a = ( abox.min.z - z );
	b = ( z - abox.max.z );
	a *= a;
	b *= b;
	d += ( ( a > b ) ? a : b );
	return( ( d <= ( r * r ) ) ? true : false );
}

bool sphere::Intersects( const sphere& asphere ) const
{
	float dx = ( x - asphere.x );
	float dy = ( y - asphere.y );
	float dz = ( z - asphere.z );
	float tr = ( r + asphere.r );
	return( ( ( ( dx * dx ) + ( dy * dy ) + ( dz * dz ) ) <= ( tr * tr ) ) ? true : false );
}

bool sphere::Intersects( const aabb& abox ) const
{
	float d = 0;
	if( abox.min.x > x )
	{
		const float c = ( abox.min.x - x );
		d += ( c * c );
	}
	else if( x > abox.max.x )
	{
		const float c = ( x - abox.max.x );
		d += ( c * c );
	}
	if( abox.min.y > y )
	{
		const float c = ( abox.min.y - y );
		d += ( c * c );
	}
	else if( y > abox.max.y )
	{
		const float c = ( y - abox.max.y );
		d += ( c * c );
	}
	if( abox.min.z > z )
	{
		const float c = ( abox.min.z - z );
		d += ( c * c );
	}
	else if( z > abox.max.z )
	{
		const float c = ( z - abox.max.z );
		d += ( c * c );
	}
	return( ( d <= ( r * r ) ) ? true : false );
}

bool sphere::Intersects( const vec3& start, const vec3& end ) const
{
	vec3 axis, delta;
	axis.SetSub( end, start );
	delta.SetSub( t_vec3(), start );
	float rr = ( r * r );
	float asl = axis.SqrLen();
	if( asl >= consts::FLOAT_MIN_RCP )
	{	//	line segment has reasonable length
		float t = ( axis.Dot( delta ) / asl );
		if( t > 0 )
		{
			if( t < 1 ) axis.Mul( t );
			delta.Sub( axis );
		}
		return( ( delta.SqrLen() <= rr ) ? true : false );
	}
	else
	{	//	line segment treated as 2 points
		if( delta.SqrLen() <= rr ) return( true );;
		delta.Sub( axis );
		if( delta.SqrLen() <= rr ) return( true );;
	}
	return( false );
}

