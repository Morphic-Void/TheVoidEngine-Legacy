inline uint Randomise( const uint u )
{
	return( ( u * ( ( u * u * 15731u ) + 789221u ) ) + 1376312589u );
}

inline uint Noise( const uint x )
{
	return( Randomise( x ^ ( x << 13 ) ) );
}

inline uint Noise( const uint x, const uint y )
{
	return( Noise( x + ( ( y ^ ( y << 23 ) ) * 8191u ) ) );
}

inline uint Noise( const uint x, const uint y, const uint z )
{
	return( Noise( x + ( ( y ^ ( y << 23 ) ) * 8191u ) + ( ( z ^ ( z >> 17 ) ) * 524287u ) ) );
}

inline uint Noise( const uint x, const uint y, const uint z, const uint w )
{
	return( Noise( x + ( ( y ^ ( y << 23 ) ) * 8191 ) + ( ( z ^ ( z >> 17 ) ) * 524287u ) + ( ( w ^ ( w >> 7 ) ) * 521288629u ) ) );
}

inline float FNoise( const uint x )
{
	return( ( ( Noise( x ) >> 8 ) / 8388607.5f ) - 1.0f );
}

inline float FNoise( const uint x, const uint y )
{
	return( ( ( Noise( x, y ) >> 8 ) / 8388607.5f ) - 1.0f );
}

inline float FNoise( const uint x, const uint y, const uint z )
{
	return( ( ( Noise( x, y, z ) >> 8 ) / 8388607.5f ) - 1.0f );
}

inline float FNoise( const uint x, const uint y, const uint z, const uint w )
{
	return( ( ( Noise( x, y, z, w ) >> 8 ) / 8388607.5f ) - 1.0f );
}

