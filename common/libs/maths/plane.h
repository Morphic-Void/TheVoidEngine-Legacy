////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    plane
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct plane
{
	float					x, y, z, w;
	inline vec3&			t_vec3( void ) { return( *reinterpret_cast< vec3* >( this ) ); };
	inline const vec3&		t_vec3( void ) const { return( *reinterpret_cast< const vec3* >( this ) ); };
	inline vec4&			t_vec4( void ) { return( *reinterpret_cast< vec4* >( this ) ); };
	inline const vec4&		t_vec4( void ) const { return( *reinterpret_cast< const vec4* >( this ) ); };
	inline quat&			t_quat( void ) { return( *reinterpret_cast< quat* >( this ) ); };
	inline const quat&		t_quat( void ) const { return( *reinterpret_cast< const quat* >( this ) ); };
	bool					IsNan( void ) const;
	bool					IsReal( void ) const;
	void					Mul( const quat& mul );
	void					Mul( const dquat& mul );
	void					Mul( const joint& mul );
	void					Mul( const mat33& mul );
	void					Mul( const mat43& mul );
	void					Negate( void );
	void					Normalize( void );
	void					SetMul( const plane& src, const quat& mul );
	void					SetMul( const plane& src, const dquat& mul );
	void					SetMul( const plane& src, const joint& mul );
	void					SetMul( const plane& src, const mat33& mul );
	void					SetMul( const plane& src, const mat43& mul );
	void					SetNegate( const plane& src );
	void					SetNormalized( const plane& src );
    inline void             SetUnit( const plane& src ) { SetNormalized( src ); };
	void					Set( const vec3& va, const vec3& vb, const vec3& vc );
	void					Set( const float px, const float py, const float pz, const float pw );
	void					Set( const plane& p );
	void					Set( const vec3& pos, const vec3& dir );
	void					Get( float& px, float& py, float& pz, float& pw ) const;
	void					Get( plane& p ) const;
	void					Get( vec3& pos, vec3& dir ) const;
	inline plane&			operator=( const plane& p ) { Set( p ); return( *this ); };
	inline plane&			operator*=( const quat& q ) { Mul( q ); return( *this ); };
	inline plane&			operator*=( const joint& j ) { Mul( j ); return( *this ); };
	inline plane&			operator*=( const mat33& m ) { Mul( m ); return( *this ); };
	inline plane&			operator*=( const mat43& m ) { Mul( m ); return( *this ); };
};

inline plane operator*( const plane& src, const quat& mul ) { plane t; t.SetMul( src, mul ); return( t ); };
inline plane operator*( const plane& src, const dquat& mul ) { plane t; t.SetMul( src, mul ); return( t ); };
inline plane operator*( const plane& src, const joint& mul ) { plane t; t.SetMul( src, mul ); return( t ); };
inline plane operator*( const plane& src, const mat33& mul ) { plane t; t.SetMul( src, mul ); return( t ); };
inline plane operator*( const plane& src, const mat43& mul ) { plane t; t.SetMul( src, mul ); return( t ); };
inline plane operator-( const plane& src ) { plane t; t.SetNegate( src ); return( t ); };

void IntersectPlanes( const plane& a, const plane& b, const plane& c, vec3& intersection );

