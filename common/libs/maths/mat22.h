////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    mat22
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct mat22
{
	vec2					x, y;
	bool					IsNan( void ) const;
	bool					IsReal( void ) const;
	float					Det( void ) const;
	void					Scale( const float scale );
	void					Scale( const float scale_x, const float scale_y );
	void					Rotate( const float radians );
	void					Mul( const mat22& mul );
	bool					Invert( void );
	void					Transpose( void );
	bool					InvertTranspose( void );
	void					SetMul( const mat22& src, const mat22& mul );
	void					SetDelta( const mat22& src, const mat22& trg );
	bool					SetInverse( const mat22& src );
	void					SetTranspose( const mat22& src );
	bool					SetInverseTranspose( const mat22& src );
	void					SetRotate( const float radians );
	void					SetScaling( const float scale );
	void					SetScaling( const float scale_x, const float scale_y );
	void					SetTensor( const vec2& u, const vec2& v );
	inline void				SetIdentity( void ) { Set( consts::MAT22::IDENTITY ); };
	void					Set( const mat22& m );
	float					GetRotation( void ) const;
	void					Get( mat22& m ) const;
	void					GetSplineWeights( const float t, vec2& weights ) const;
	void					GetSplineWeightsD( vec2& weights ) const;
	inline mat22&			operator=( const mat22& m ) { Set( m ); return( *this ); };
	inline mat22&			operator*=( const mat22& mul ) { Mul( mul ); return( *this ); };
};

inline mat22 operator*( const mat22& src, const mat22& mul ) { mat22 t; t.SetMul( src, mul ); return( t ); };

inline mat22 invert( const mat22& src ) { mat22 t; t.SetInverse( src ); return( t ); };
inline mat22 transpose( const mat22& src ) { mat22 t; t.SetTranspose( src ); return( t ); };

