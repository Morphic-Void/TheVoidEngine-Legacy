////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    frect
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct frect
{
	float			l, t, r, b;
	bool			IsNan( void ) const;
	bool			IsReal( void ) const;
	inline void		SetFull( void ) { *this = consts::FRECT::FULL; };
	inline void		Set( const frect& other ) { l = other.l; t = other.t; r = other.r; b = other.b; };
	inline void		Set( const float _l, const float _t, const float _r, const float _b ) { l = _l; t = _t; r = _r; b = _b; };
	bool			Union( const frect& other );
	void			Merge( const frect& other );
	bool			SetUnion( const frect& rect1, const frect& rect2 );
	void			SetMerge( const frect& rect1, const frect& rect2 );
	bool			Contains( const vec2& point ) const;
	bool			Contains( const frect& other ) const;
	bool			Intersects( const frect& other ) const;
	inline frect&	operator=( const frect& other ) { Set( other ); return( *this ); };
};

class cfrect : public frect
{
public:
	inline			cfrect() {};
	inline			cfrect( const frect& other ) { Set( other ); };
	inline			cfrect( const float _l, const float _t, const float _r, const float _b ) { Set( _l, _t, _r, _b ); };
	inline			~cfrect() {};
	inline			operator frect() { return( *reinterpret_cast< frect* >( this ) ); };
	inline			operator frect() const { return( *reinterpret_cast< const frect* >( this ) ); };
	inline frect&	operator=( const frect& other ) { Set( other ); return( *this ); };
};

