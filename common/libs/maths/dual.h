////
////    dual
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct dual
{
	float					r, e;
	inline vec2&			t_vec2( void ) { return( *reinterpret_cast< vec2* >( this ) ); };
	inline const vec2&		t_vec2( void ) const { return( *reinterpret_cast< const vec2* >( this ) ); };
	bool					IsNan( void ) const;
	bool					IsReal( void ) const;
	float					Length( void ) const;
	inline float			SqrLen( void ) const { return( ( r * r ) + ( e * e ) ); };
	inline float			Dot( const dual& dot ) const { return( r * dot.r ); };
	inline void				Add( const dual& add ) { r += add.r; e += add.e; };
	inline void				Sub( const dual& sub ) { r -= sub.r; e -= sub.e; };
	inline void				Mul( const dual& mul ) { SetMul( *this, mul ); };
	inline void				Mul( const float mul ) { r *= mul; e *= mul; };
	inline void				Div( const dual& div ) { SetDiv( *this, div ); };
	inline void				Div( const float div ) { SetDiv( *this, div ); };
	inline void				Pow( const dual& pow ) { Log(); Mul( pow ); Exp(); };
	void					Pow( const float pow );
	void					Log( void );
	void					Exp( void );
	void					Sqrt( void );
	inline void				Negate( void ) { r = -r; e = -e; };
	void					Invert( void );
	inline void				Conjugate( void ) { e = -e; };
	void					Normalize( void );
	inline void				Lerp( const dual& trg, const float t ) { SetLerp( *this, trg, t ); };
	inline void				Slerp( const dual& trg, const float t ) { SetSlerp( *this, trg, t ); };
	inline void				Blend( const dual& trg, const float t ) { SetBlend( *this, trg, t ); };
	inline void				SetAdd( const dual& src, const dual& add ) { r = ( src.r + add.r ); e = ( src.e + add.e ); };
	inline void				SetSub( const dual& src, const dual& sub ) { r = ( src.r - sub.r ); e = ( src.e - sub.e ); };
	inline void				SetMul( const dual& src, const dual& mul ) { e = ( ( src.r * mul.e ) + ( src.e * mul.r ) ); r = ( src.r * mul.r ); };
	inline void				SetMul( const dual& src, const float mul )  { r = ( src.r * mul ); e = ( src.e * mul ); };
	void					SetDiv( const dual& src, const dual& div );
	void					SetDiv( const dual& src, const float div );
	inline void				SetPow( const dual& src, const dual& pow ) { SetLog( src ); Mul( pow ); Exp(); };
	void					SetPow( const dual& src, const float pow );
	void					SetLog( const dual& src );
	void					SetExp( const dual& src );
	void					SetSqrt( const dual& src );
	inline void				SetNegate( const dual& src ) { r = -src.r; e = -src.e; };
	void					SetInverse( const dual& src );
	inline void				SetConjugate( const dual& src ) { r = src.r; e = -src.e; };
	void					SetNormalized( const dual& src );
    inline void             SetUnit( const dual& src ) { SetNormalized( src ); };
	void					SetLerp( const dual& src, const dual& trg, const float t );
	void					SetSlerp( const dual& src, const dual& trg, const float t );
	void					SetBlend( const dual& src, const dual& trg, const float t );
	void					SetDelta( const dual& src, const dual& trg );
	inline void				SetIdentity( void ) { Set( consts::DUAL::IDENTITY ); };
	void					SetNormalized( const float nr, const float ne );
    inline void             SetUnit( const float nr, const float ne ) { SetNormalized( nr, ne ); };
	inline void				Set( const dual& c ) { r = c.r; e = c.e; };
	void					Set( const vec2& v );
	inline void				Set( const float _r, const float _e ) { r = _r; e = _e; };
	void					GetNormalized( float& nr, float& ne ) const;
	inline void				Get( dual& c ) const { c.r = r; c.e = e; };
	void					Get( vec2& v ) const;
	inline void				Get( float& _r, float& _e ) const { _r = r; _e = e; };
	inline dual&			operator=( const dual& c ) { Set( c ); return( *this ); };
	inline dual&			operator=( const vec2& v ) { Set( v ); return( *this ); };
	inline dual&			operator+=( const dual& add ) { Add( add ); return( *this ); };
	inline dual&			operator-=( const dual& sub ) { Sub( sub ); return( *this ); };
	inline dual&			operator*=( const dual& mul ) { Mul( mul ); return( *this ); };
	inline dual&			operator*=( const float mul ) { Mul( mul ); return( *this ); };
	inline dual&			operator/=( const dual& div ) { Div( div ); return( *this ); };
	inline dual&			operator/=( const float div ) { Div( div ); return( *this ); };
};

inline dual operator+( const dual& src, const dual& add ) { dual c; c.SetAdd( src, add ); return( c ); };
inline dual operator-( const dual& src, const dual& sub ) { dual c; c.SetSub( src, sub ); return( c ); };
inline dual operator*( const dual& src, const dual& mul ) { dual c; c.SetMul( src, mul ); return( c ); };
inline dual operator*( const dual& src, const float mul ) { dual c; c.SetMul( src, mul ); return( c ); };
inline dual operator/( const dual& src, const dual& div ) { dual c; c.SetDiv( src, div ); return( c ); };
inline dual operator/( const dual& src, const float div ) { dual c; c.SetDiv( src, div ); return( c ); };
inline dual operator-( const dual& src ) { dual c; c.SetNegate( src ); return( c ); };

inline dual normalize( const dual& src ) { dual c; c.SetNormalized( src ); return( c ); };
inline dual invert( const dual& src ) { dual c; c.SetInverse( src ); return( c ); };
inline dual conjugate( const dual& src ) { dual c; c.SetConjugate( src ); return( c ); };
inline dual pow( const dual& src, const dual& pow ) { dual c; c.SetPow( src, pow ); return( c ); };
inline dual pow( const dual& src, const float pow ) { dual c; c.SetPow( src, pow ); return( c ); };
inline dual log( const dual& src ) { dual c; c.SetLog( src ); return( c ); };
inline dual exp( const dual& src ) { dual c; c.SetExp( src ); return( c ); };
inline dual lerp( const dual& a, const dual& b, const float t ) { dual c; c.SetLerp( a, b, t ); return( c ); };
inline dual slerp( const dual& a, const dual& b, const float t ) { dual c; c.SetSlerp( a, b, t ); return( c ); };

