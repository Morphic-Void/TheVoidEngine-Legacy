enum CRC_TYPE
{
//	32-bit CRCs:
	CRC_DEFAULT = 0,
	CRC_CCITT_CRC_32 = CRC_DEFAULT,
	CRC_ETHERNET_32,
	CRC_AUTODIN_II,
	CRC_JAM_CRC_32,
	CRC_ADCCP_32,
	CRC_CRC_32,
	CRC_PKZIP,
	CRC_FDDI,
//	24-bit CRCs:
	CRC_MIL188,
//	16-bit CRCs:
	CRC_CCITT_CRC_16,
	CRC_BISYNC_CRC_16,
	CRC_ADCCP_16,
	CRC_CRC_16,
	CRC_XMODEM,
	CRC_ZMODEM,
	CRC_KERMIT,
	CRC_SDLC,
	CRC_HDLC,
	CRC_ARC,
	CRC_LHA,
	CRC_X25,
	CRC_X25REV,
//	8-bit CRCs:
	CRC_HEARD_CRC_08,
	CRC_TYPE_COUNT,
	CRC_TYPE_FORCE32 = 0x7fffffff
};

class UniCRC
{
public:
	inline					UniCRC() { SetType(); };
	inline					UniCRC( const CRC_TYPE type ) { SetType( type ); };
	inline					~UniCRC() {};
	void					SetType( const CRC_TYPE type = CRC_DEFAULT );
	inline uint32_t			GetCRC( const uint8_t* const data, const uint length ) const { return( ExtCRC( data, length, mk_InitialValues[ m_type ] ) ); };
	uint32_t				ExtCRC( const uint8_t* const data, const uint length, const uint32_t prev ) const;
protected:
	CRC_TYPE				m_type;
	uint32_t				m_crc[ 256 ];
	static const uint32_t	CRC_08BITS;
	static const uint32_t	CRC_16BITS;
	static const uint32_t	CRC_24BITS;
	static const uint32_t	CRC_32BITS;
	static const uint32_t	CRC_ALTPLY;
	static const uint32_t	CRC_REVPLY;
	static const uint32_t	CRC_RFLPLY;
	static const uint32_t	CRC_REVDAT;
	static const uint32_t	CRC_REVCRC;
	static const uint32_t	CRC_INVCRC;
	static const uint32_t	CRC_BITMSK;
	static const uint32_t	CRC_PLYSEL;
	static const uint32_t	CRC_OPTMSK;
	static const uint32_t	mk_Polynomials[ 64 ];
	static const uint32_t	mk_ControlFlags[ CRC_TYPE_COUNT ];
	static const uint32_t	mk_InitialValues[ CRC_TYPE_COUNT ];
};

