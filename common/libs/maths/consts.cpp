namespace consts
{
	const unsigned int	UINT_FLOAT_MIN = 0x00000001;
	const unsigned int	UINT_FLOAT_MAX = 0x7f7fffff;
	const unsigned int	UINT_FLOAT_MIN_RCP = 0x00200001;
	const unsigned int	UINT_FLOAT_MAX_RCP = 0x7f7ffff8;
	const unsigned int	UINT_FLOAT_MIN_NORM = 0x00800000;
	const unsigned int	UINT_FLOAT_MAX_DENORM = 0x007fffff;
	const unsigned int	UINT_FLOAT_ROOT_MIN_DENORM = 0x1a3504f3;
	const unsigned int	UINT_FLOAT_ROOT_MIN_NORM = 0x20000000;
	const unsigned int	UINT_FLOAT_RCP_MIN_NORM = 0x7e800000;
	const unsigned int	UINT_FLOAT_EPSILON = 0x34000000;
	const float			FLOAT_MIN = ( reinterpret_cast< const float* >( &UINT_FLOAT_MIN )[ 0 ] );
	const float			FLOAT_MAX = ( reinterpret_cast< const float* >( &UINT_FLOAT_MAX )[ 0 ] );
	const float			FLOAT_MIN_RCP = ( reinterpret_cast< const float* >( &UINT_FLOAT_MIN_RCP )[ 0 ] );
	const float			FLOAT_MAX_RCP = ( reinterpret_cast< const float* >( &UINT_FLOAT_MAX_RCP )[ 0 ] );
	const float			FLOAT_MIN_NORM = ( reinterpret_cast< const float* >( &UINT_FLOAT_MIN_NORM )[ 0 ] );
	const float			FLOAT_MAX_DENORM = ( reinterpret_cast< const float* >( &UINT_FLOAT_MAX_DENORM )[ 0 ] );
	const float			FLOAT_ROOT_MIN_DENORM = ( reinterpret_cast< const float* >( &UINT_FLOAT_ROOT_MIN_DENORM )[ 0 ] );
	const float			FLOAT_ROOT_MIN_NORM = ( reinterpret_cast< const float* >( &UINT_FLOAT_ROOT_MIN_NORM )[ 0 ] );
	const float			FLOAT_RCP_MIN_NORM = ( reinterpret_cast< const float* >( &UINT_FLOAT_RCP_MIN_NORM )[ 0 ] );
	const float			FLOAT_EPSILON = ( reinterpret_cast< const float* >( &UINT_FLOAT_EPSILON )[ 0 ] );
	const float			EPSILON = ( reinterpret_cast< const float* >( &UINT_FLOAT_MAX_DENORM )[ 0 ] );
	const float			ZERO = 0.0f;
	const float			ONE = 1.0f;
	const float			E = expf( 1.0f );				//	2.7182818284590452353602874713527f;
	const float			PI = acosf( -1.0f );			//	3.1415926535897932384626433832795f;
	const float			PI2 = ( PI * 2 );				//	6.283185307179586476925286766559f;
	const float			TAU = PI2;				        //	6.283185307179586476925286766559f;
	const float			ROOT2 = sqrtf( 2.0f );			//	1.4142135623730950488016887242097f;
	const float			ROOT3 = sqrtf( 3.0f );			//	1.7320508075688772935274463415059f;
	const float			RCPROOT2 = ( 1.0f / ROOT2 );	//	0.70710678118654752440084436210485f;
	const float			RCPROOT3 = ( 1.0f / ROOT3 );	//	0.57735026918962576450914878050196f;
	const float			RCP127 = ( 1.0f / 127.0f );
	const float			RCP255 = ( 1.0f / 255.0f );
	const float			RCP32767 = ( 1.0f / 32767.0f );
	const float			RCP65535 = ( 1.0f / 65535.0f );
	const float			DEGTORAD = ( PI / 180.0f );
	const float			RADTODEG = ( 180.0f / PI );
	const float			LOG2 = ( logf( 2.0f ) );
	const float			RCPLOG2 = ( 1.0f / logf( 2.0f ) );
	const float			GOLDEN = ( ( 1.0f + sqrtf( 5.0f ) ) * 0.5f );
	const float			RCPGOLDEN = ( 2.0f / ( 1.0f + sqrtf( 5.0f ) ) );
};

