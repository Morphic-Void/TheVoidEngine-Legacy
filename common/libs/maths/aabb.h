////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    aabb
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct aabb
{
	vec3					min, max;
	bool					IsNan( void ) const;
	bool					IsReal( void ) const;
    void                    Recenter( void );
	inline void				SetUnit( void ) { *this = consts::AABB::UNIT; };
	void					Set( const vec3& apoint );
	void					Set( const sphere& asphere );
	void					Merge( const vec3& apoint );
	void					Merge( const sphere& asphere );
	void					Merge( const aabb& abox );
	bool					Union( const aabb& abox );
	void					SetMerge( const vec3& point1, const vec3& point2 );
	void					SetMerge( const sphere& sphere1, const sphere& sphere2 );
	void					SetMerge( const sphere& asphere, const vec3& apoint );
	void					SetMerge( const aabb& abox, const vec3& apoint );
	void					SetMerge( const aabb& abox, const sphere& asphere );
	void					SetMerge( const aabb& box1, const aabb& box2 );
	bool					SetUnion( const aabb& box1, const aabb& box2 );
	bool					Contains( const vec3& apoint ) const;
	bool					Contains( const sphere& asphere ) const;
	bool					Contains( const aabb& abox ) const;
	bool					Intersects( const sphere& asphere ) const;
	bool					Intersects( const aabb& abox ) const;
	void					GetVerts( box_verts& verts ) const;
	void					GetFaces( box_faces& faces ) const;
};

