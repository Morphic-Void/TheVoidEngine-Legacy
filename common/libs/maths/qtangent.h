////
////    qtangent
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct qtangent : public quat
{
	inline quat&			t_quat( void ) { return( *reinterpret_cast< quat* >( this ) ); };
	inline const quat&		t_quat( void ) const { return( *reinterpret_cast< const quat* >( this ) ); };
	bool					IsNan( void ) const;
	bool					IsReal( void ) const;
	bool					SetTanSpace( const vec3& dc1, const vec2& dt1, const vec3& dc2, const vec2& dt2, float& weight );
	bool					SetTanSpace( const vec3& n, const vec3& dc1, const vec2& dt1, const vec3& dc2, const vec2& dt2, float& weight );
	bool					SetTanSpace( const mat33& mt );
	void					Set( const qtangent& t );
	void					Set( const mat33& m );
	void					Get( qtangent& t ) const;
	void					Get( mat33& m ) const;
};

