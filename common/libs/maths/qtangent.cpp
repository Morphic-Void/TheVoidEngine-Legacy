bool qtangent::IsNan( void ) const
{
	return( ::IsNan( x ) || ::IsNan( y ) || ::IsNan( z ) || ::IsNan( w ) );
}

bool qtangent::IsReal( void ) const
{
	return( ::IsReal( x ) && ::IsReal( y ) && ::IsReal( z ) && ::IsReal( w ) );
}

bool qtangent::SetTanSpace( const vec3& dc1, const vec2& dt1, const vec3& dc2, const vec2& dt2, float& weight )
{
	mat33 mt;
	if( !mt.SetTanSpace( dc1, dt1, dc2, dt2, weight ) )
	{
		return( false );
	}
	return( SetTanSpace( mt ) );
}

bool qtangent::SetTanSpace( const vec3& n, const vec3& dc1, const vec2& dt1, const vec3& dc2, const vec2& dt2, float& weight )
{
	mat33 mt;
	if( !mt.SetTanSpace( n, dc1, dt1, dc2, dt2, weight ) )
	{
		return( false );
	}
	return( SetTanSpace( mt ) );
}

bool qtangent::SetTanSpace( const mat33& mt )
{
	float h = mt.z.Triple( mt.x, mt.y );
	if( fabsf( h ) > consts::FLOAT_MAX_DENORM )
	{
		mat33 mq;
		mq.z.SetNormalized( mt.z );
		float u = mq.z.Dot( mt.x );
		float v = mq.z.Dot( mt.y );
		vec3 s, t;
		s.x = ( mt.x.x - ( mq.z.x * u ) ); 
		s.y = ( mt.x.y - ( mq.z.y * u ) ); 
		s.z = ( mt.x.z - ( mq.z.z * u ) ); 
		t.x = ( mt.y.x - ( mq.z.x * v ) ); 
		t.y = ( mt.y.y - ( mq.z.y * v ) ); 
		t.z = ( mt.y.z - ( mq.z.z * v ) ); 
		u = s.Length();
		v = t.Length();
		s.x = ( ( s.x * v ) + ( t.x * u ) );
		s.y = ( ( s.y * v ) + ( t.y * u ) );
		s.z = ( ( s.z * v ) + ( t.z * u ) );
		s.Mul( consts::RCPROOT2 / s.Length() );
		if( h < 0.0f )
		{
			t.SetCross( mq.z, s );
		}
		else
		{
			t.SetCross( s, mq.z );
		}
		mq.x.x = ( s.x + t.x );
		mq.x.y = ( s.y + t.y );
		mq.x.z = ( s.z + t.z );
		mq.y.x = ( s.x - t.x );
		mq.y.y = ( s.y - t.y );
		mq.y.z = ( s.z - t.z );
		Set( mq );
		return( true );
	}
	SetIdentity();
	return( false );
}

void qtangent::Set( const qtangent& t )
{
	x = t.x;
	y = t.y;
	z = t.z;
	w = t.w;
}

void qtangent::Set( const mat33& m )
{
	float d = m.z.Triple( m.x, m.y );
	if( d < 0.0f )
	{
		d = -d;
		ASSERT( d > consts::FLOAT_MAX_DENORM );
		d = ( ( d > consts::FLOAT_MAX_DENORM ) ? expf( logf( d ) / 3.0f ) : 1.0f );
		if( ( m.x.x + m.y.y - m.z.z ) > 0.0f )
		{
			float t = ( d + m.x.x + m.y.y - m.z.z );
			float s = ( -0.5f / sqrtf( t ) );
			w = ( t * s );
			z = ( ( m.y.x - m.x.y ) * s );
			y = ( ( m.x.z + m.z.x ) * s );
			x = -( ( m.z.y + m.y.z ) * s );
		}
		else if( ( m.x.x > m.y.y ) && ( m.x.x > -m.z.z ) )
		{
			float t = ( d + m.x.x - m.y.y + m.z.z );
			float s = ( -0.5f / sqrtf( t ) );
			x = ( t * s );
			y = ( ( m.y.x + m.x.y ) * s );
			z = ( ( m.x.z - m.z.x ) * s );
			w = -( ( m.z.y + m.y.z ) * s );
		}
		else if( m.y.y > -m.z.z )
		{
			float t = ( d - m.x.x + m.y.y + m.z.z );
			float s = ( -0.5f / sqrtf( t ) );
			y = ( t * s );
			x = ( ( m.y.x + m.x.y ) * s );
			w = ( ( m.x.z + m.z.x ) * s );
			z = ( ( m.y.z - m.z.y ) * s );
		}
		else
		{
			float t = ( d - m.x.x - m.y.y - m.z.z );
			float s = ( -0.5f / sqrtf( t ) );
			z = ( t * s );
			w = ( ( m.y.x - m.x.y ) * s );
			x = ( ( m.x.z - m.z.x ) * s );
			y = ( ( m.y.z - m.z.y ) * s );
		}
		if( w > 0.0f )
		{
			x = -x;
			y = -y;
			z = -z;
			w = -w;
		}
		w -= consts::FLOAT_MIN;
	}
	else
	{
		ASSERT( d > consts::FLOAT_MAX_DENORM );
		d = ( ( d > consts::FLOAT_MAX_DENORM ) ? expf( logf( d ) / 3.0f ) : 1.0f );
		if( ( m.x.x + m.y.y + m.z.z ) > 0.0f )
		{
			float t = ( d + m.x.x + m.y.y + m.z.z );
			float s = ( 0.5f / sqrtf( t ) );
			w = ( t * s );
			z = ( ( m.y.x - m.x.y ) * s );
			y = ( ( m.x.z - m.z.x ) * s );
			x = ( ( m.z.y - m.y.z ) * s );
		}
		else if( ( m.x.x > m.y.y ) && ( m.x.x > m.z.z ) )
		{
			float t = ( d + m.x.x - m.y.y - m.z.z );
			float s = ( 0.5f / sqrtf( t ) );
			x = ( t * s );
			y = ( ( m.y.x + m.x.y ) * s );
			z = ( ( m.x.z + m.z.x ) * s );
			w = ( ( m.z.y - m.y.z ) * s );
		}
		else if( m.y.y > m.z.z )
		{
			float t = ( d - m.x.x + m.y.y - m.z.z );
			float s = ( 0.5f / sqrtf( t ) );
			y = ( t * s );
			x = ( ( m.y.x + m.x.y ) * s );
			w = ( ( m.x.z - m.z.x ) * s );
			z = ( ( m.z.y + m.y.z ) * s );
		}
		else
		{
			float t = ( d - m.x.x - m.y.y + m.z.z );
			float s = ( 0.5f / sqrtf( t ) );
			z = ( t * s );
			w = ( ( m.y.x - m.x.y ) * s );
			x = ( ( m.x.z + m.z.x ) * s );
			y = ( ( m.z.y + m.y.z ) * s );
		}
		if( w < 0.0f )
		{
			x = -x;
			y = -y;
			z = -z;
			w = -w;
		}
		w += consts::FLOAT_MIN;
	}
}

void qtangent::Get( qtangent& t ) const
{
	t.x = x;
	t.y = y;
	t.z = z;
	t.w = w;
}

void qtangent::Get( mat33& m ) const
{
	quat::Get( m );
	if( w < 0.0f )
	{
		m.z.x = -m.z.x;
		m.z.y = -m.z.y;
		m.z.z = -m.z.z;
	}
}

