////    mat43
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct mat43
{
	vec4					x, y, z;
	inline mat33&			t_mat33( void ) { return( *reinterpret_cast< mat33* >( this ) ); };
	inline const mat33&		t_mat33( void ) const { return( *reinterpret_cast< const mat33* >( this ) ); };
	bool					IsNan( void ) const;
	bool					IsReal( void ) const;
	void					PointAt( const vec3& from, const vec3& at, const vec3& up );
	void					PointTo( const vec3& from, const vec3& to, const vec3& up );
	void					PointAt( const vec3& at, const vec3& up );
	void					PointTo( const vec3& to, const vec3& up );
	void					LookAt( const vec3& from, const vec3& at, const vec3& up );
	void					LookTo( const vec3& from, const vec3& to, const vec3& up );
	void					LookAt( const vec3& at, const vec3& up );
	void					LookTo( const vec3& to, const vec3& up );
	void					Scale( const float scale );
	void					Twist( const float radians );
	void					Tilt( const float radians );
	void					Turn( const float radians );
	void					Roll( const float radians );
	void					Pitch( const float radians );
	void					Yaw( const float radians );
	void					EndScale( const float scale );
	void					EndScale( const float scale_x, const float scale_y, const float scale_z );
	void					EndTwist( const float radians );
	void					EndTilt( const float radians );
	void					EndTurn( const float radians );
	void					EndRoll( const float radians );
	void					EndPitch( const float radians );
	void					EndYaw( const float radians );
	void					Move( const float move_x, const float move_y, const float move_z );
	void					Move( const vec3& move );
	void					Pan( const float pan_x, const float pan_y, const float pan_z );
	void					Pan( const vec3& pan );
	void					UnscaledPan( const float pan_x, const float pan_y, const float pan_z );
	void					UnscaledPan( const vec3& pan );
	void					Mul( const mat43& mul );
	void					Mul( const mat33& mul );
	bool					Invert( void );
	void					OrthoInvert( void );
	void					Reflect( const plane& about );
	void					SetMul( const mat43& src, const mat43& mul );
	void					SetMul( const mat43& src, const mat33& mul );
	void					SetMul( const mat33& src, const mat43& mul );
	void					SetRemap( const aabb& src, const aabb& trg );
	void					SetDelta( const mat43& src, const mat43& trg );
	bool					SetInverse( const mat43& src );
	void					SetOrthoInverse( const mat43& src );
	void					SetReflect( const plane& about );
	void					SetReflected( const mat43& src, const plane& about );
	void					SetBarycentricTetrahedron( const vec3& v1, const vec3& v2, const vec3& v3, const vec3& v4 );
	inline void				SetIdentity( void ) { Set( consts::MAT43::IDENTITY ); };
	void					Set( const mat43& m );
	void					Set( const dquat& d );
	void					Set( const joint& j );
	void					Set( const quat& q, const vec3& v );
	void					Set( const quat& q );
	void					Set( const sphere& asphere );
	void					Set( const aabb& abox );
	void					Get( mat43& m ) const;
	void					Get( dquat& d ) const;
	void					Get( joint& j ) const;
	void					Get( quat& q, vec3& v ) const;
	void					GetDescOBB( box_verts& verts, box_faces& faces ) const;
	void					GetDescOBB( box_verts& verts, box_faces& faces, const aabb& bounds ) const;
	void					GetDescOBB( box_verts& verts ) const;
	void					GetDescOBB( box_verts& verts, const aabb& bounds ) const;
	void					GetDescOBB( box_faces& faces ) const;
	void					GetDescOBB( box_faces& faces, const aabb& bounds ) const;
	void					GetDescFrustum( box_verts& verts, box_faces& faces ) const;
	void					GetDescFrustum( box_verts& verts, box_faces& faces, const aabb& bounds ) const;
	void					GetDescFrustum( box_verts& verts ) const;
	void					GetDescFrustum( box_verts& verts, const aabb& bounds ) const;
	void					GetDescFrustum( box_faces& faces ) const;
	void					GetDescFrustum( box_faces& faces, const aabb& bounds ) const;
	void					GetRawDescFrustum( box_faces& faces ) const;
	void					GetRawDescFrustum( box_faces& faces, const aabb& bounds ) const;
	inline mat43&			operator=( const mat43& m ) { Set( m ); return( *this ); };
	inline mat43&			operator=( const dquat& d ) { Set( d ); return( *this ); };
	inline mat43&			operator=( const joint& j ) { Set( j ); return( *this ); };
	inline mat43&			operator=( const quat& q ) { Set( q ); return( *this ); };
	inline mat43&			operator*=( const mat43& mul ) { Mul( mul ); return( *this ); };
	inline mat43&			operator*=( const mat33& mul ) { Mul( mul ); return( *this ); };
};

inline mat43 operator*( const mat43& src, const mat43& mul ) { mat43 t; t.SetMul( src, mul ); return( t ); };
inline mat43 operator*( const mat43& src, const mat33& mul ) { mat43 t; t.SetMul( src, mul ); return( t ); };
inline mat43 operator*( const mat33& src, const mat43& mul ) { mat43 t; t.SetMul( src, mul ); return( t ); };

inline mat43 invert( const mat43& src ) { mat43 t; t.SetInverse( src ); return( t ); };
inline mat43 conjugate( const mat43& src ) { mat43 t; t.SetOrthoInverse( src ); return( t ); };

