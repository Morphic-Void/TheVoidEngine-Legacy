void LEAK_INFO::addRef()
{
	if( this )
	{
		if( !count )
		{
			prev = 0;
			next = list;
			if( next ) next->prev = this;
			list = this;
		}
		++count;
	}
}

void LEAK_INFO::delRef()
{
	if( this )
	{
		--count;
		if( !count )
		{
			if( next ) next->prev = prev;
			if( prev )
			{
				prev->next = next;
			}
			else
			{
				list = next;
			}
			prev = next = 0;
		}
	}
}

LEAK_INFO* LEAK_INFO::list(0);

template <typename T, const char* type_name>
LEAK_INFO LEAK_TRACKER<T,type_name>::info(type_name);

