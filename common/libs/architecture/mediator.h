class Mediator : public IMediator, protected sorting::avl_name::Node
{
public:
	inline					Mediator() { Null(); };
	virtual					~Mediator() {};
	static Mediator*		Create( Mediator*& pool, Mediator*& root, const uint8_t* const name, const unsigned int type, const bool constant, const unsigned int count, uint8_t* const absolute, const uint8_t* const* const redirect, const bool indirect, const unsigned int size );
	Mediator*				Obtain( const uint8_t* const name, const unsigned int type, const bool constant ) const;
	void					Delete( void );
	bool					Rename( const uint8_t* const name );
	void					Resort( void );
	Mediator*				InitPool( const unsigned int count );
protected:
	void					Null( void );
	virtual	bool			AddRef( void );
	virtual	void			Abandon( void );
	virtual	void			Release( void );
	virtual bool			IsValid( void ) const { return( ( m_flags & FLAG_VALID ) ? true : false ); };
	virtual bool			IsConst( void ) const { return( ( m_flags & FLAG_CONST ) ? true : false ); };
	virtual const uint8_t*	GetName( void ) const { return( m_name ); };
	virtual unsigned int	GetType( void ) const { return( m_type ); };
	virtual unsigned int	GetCount( void ) const { return( m_count ); };
	virtual uint8_t*		Get( const unsigned int type, const bool constant, const unsigned int index ) const;
	virtual bool			Set( const unsigned int type, const bool constant, const unsigned int count, uint8_t* const absolute, const uint8_t* const* const redirect, const bool indirect );
	unsigned int			m_flags;
	unsigned int			m_count;
	unsigned int			m_type;
	unsigned int			m_size;
	unsigned int			m_refs;
	Mediator**				m_pool;
	Mediator**				m_root;
	uint8_t*				m_absolute;
	const uint8_t*			m_relative;
	const uint8_t* const*	m_redirect;
	enum					FLAGS { FLAG_VALID = 1, FLAG_CONST = 2, FLAG_TABLE = 4 };
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Mediator pool template class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< const unsigned int count >
class MediatorPool
{
public:
	inline					MediatorPool() { m_head = m_pool[ 0 ].InitPool( count ); };
	inline					~MediatorPool() {};
	inline Mediator*&		GetPool( void ) { return( m_head ); };
protected:
	Mediator*				m_head;
	Mediator				m_pool[ count ];
};

