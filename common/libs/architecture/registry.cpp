bool Registry::SetPool( Registry& registry )
{
	if( m_root ) return( false );
	m_pool = registry.m_pool;
	return( true );
}

bool Registry::SetPool( Mediator*& pool )
{
	if( m_root ) return( false );
	m_pool = &pool;
	return( true );
}

void Registry::Optimize( void )
{
	m_root->Resort();
}

Mediator* Registry::Create( const uint8_t* const name, const unsigned int type, const bool constant, const unsigned int count, uint8_t* const absolute, const uint8_t* const* const redirect, const bool indirect, const unsigned int size )
{
	return( Mediator::Create( *m_pool, m_root, name, type, constant, count, absolute, redirect, indirect, size ) );
}

Mediator* Registry::Obtain( const uint8_t* const name, const unsigned int type, const bool constant ) const
{
	return( m_root->Obtain( name, type, constant ) );
}

void Registry::Delete( void )
{
	while( m_root ) m_root->Delete();
}

