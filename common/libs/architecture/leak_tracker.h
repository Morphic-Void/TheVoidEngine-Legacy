class LEAK_INFO
{
public:
    inline                  LEAK_INFO( const char* const type_name ) : count( 0 ), name( type_name ), prev( 0 ), next( 0 ) {};
    inline                  ~LEAK_INFO() {};
    inline unsigned int     getCount() const { return( this ? count : 0 ); };
    inline const char*      getName() const { return( this ? name : 0 ); };
    inline LEAK_INFO*       getPrev() const { return( this ? prev : 0 ); };
    inline LEAK_INFO*       getNext() const { return( this ? next : 0 ); };
    static LEAK_INFO*       getLeaks() { return( list ); };
private:
    void                    addRef();
    void                    delRef();
    unsigned int            count;
    const char*             name;
    LEAK_INFO*              prev;
    LEAK_INFO*              next;
    static LEAK_INFO*       list;
};

template <typename T, const char* type_name>
class LEAK_TRACKER
{
private:
    static LEAK_INFO        info;
    friend void             LEAK_INFO::addRef();
    friend void             LEAK_INFO::delRef();
protected:
    inline                  LEAK_TRACKER() { info.addRef(); };
    inline                  ~LEAK_TRACKER() { info.delRef(); };
public:
    static inline const     LEAK_INFO* getInfo() { return( &info ); };
};

