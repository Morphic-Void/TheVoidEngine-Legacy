void Encode( uint8_t* const buffer, const uint32_t length )
{
	if( length )
	{
		for( uint32_t i = ( length - 1 ); i; --i )
		{
			buffer[ i ] -= buffer[ i - 1 ];
		}
	}
}

void Decode( uint8_t* const buffer, const uint32_t length )
{
	if( length )
	{
		for( uint32_t i = 1; i < length; ++i )
		{
			buffer[ i ] += buffer[ i - 1 ];
		}
	}
}

