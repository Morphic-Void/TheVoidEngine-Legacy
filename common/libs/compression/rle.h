enum RLE_ERROR
{
	RLE_SUCCESS = 0,							//	RLE encode or decode successfull
	RLE_ERROR_Encoded_Size_Incorrect = -1,		//	encoded data size was not as specified (encode or decode) - specification of output size is optional
	RLE_ERROR_Decoded_Size_Incorrect = -2,		//	decoded data size was not as specified (decode only) - specification of output size is optional
	RLE_ERROR_Encoded_Overflow = -3,			//	encoded buffer access out of range (encode or decode)
	RLE_ERROR_Decoded_Overflow = -4,			//	decoded buffer access out of range (decode only)
	RLE_ERROR_Force32 = 0x7fffffff
};

struct rle_chunk
{
	int8_t		Magic[ 4 ];					//	'r', 'l', 'e', 0
	uint32_t	MaxCopySize;				//	maximum copy size used for compression (1-255)
	uint32_t	EncodedSize;				//	compressed data size in bytes excluding this header
	uint32_t	DecodedSize;				//	decompressed data size in bytes
};

uint32_t	MaxEncodedSize( const uint32_t MaxCopySize, const uint32_t DecodedSize );
RLE_ERROR	Encode( const uint32_t MaxCopySize, const uint8_t* const DecodedBuffer, uint8_t* const EncodedBuffer, const uint32_t DecodedSize, uint32_t& EncodedSize, const uint32_t EncodedBufferSize = 0 );
RLE_ERROR	Decode( const uint32_t MaxCopySize, const uint8_t* const EncodedBuffer, uint8_t* const DecodedBuffer, uint32_t& EncodedSize, uint32_t& DecodedSize, const uint32_t EncodedBufferSize = 0, const uint32_t DecodedBufferSize = 0 );
RLE_ERROR	Encode( const uint32_t MaxCopySize, const uint8_t* const DecodedBuffer, const uint32_t DecodedSize, uint32_t& EncodedSize, const uint32_t EncodedBufferSize = 0 );
RLE_ERROR	Decode( const uint32_t MaxCopySize, const uint8_t* const EncodedBuffer, uint32_t& EncodedSize, uint32_t& DecodedSize, const uint32_t EncodedBufferSize = 0, const uint32_t DecodedBufferSize = 0 );
void		QuickEncode( const uint32_t MaxCopySize, const uint8_t* const DecodedBuffer, uint8_t* const EncodedBuffer, const uint32_t DecodedSize, uint32_t& EncodedSize );
void		QuickDecode( const uint32_t MaxCopySize, const uint8_t* const EncodedBuffer, uint8_t* const DecodedBuffer, uint32_t& EncodedSize, uint32_t& DecodedSize );
void		QuickEncode( const uint32_t MaxCopySize, const uint8_t* const DecodedBuffer, const uint32_t DecodedSize, uint32_t& EncodedSize );
void		QuickDecode( const uint32_t MaxCopySize, const uint8_t* const EncodedBuffer, uint32_t& EncodedSize, uint32_t& DecodedSize );

