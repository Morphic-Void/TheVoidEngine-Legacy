////
////    move to front encoding and decoding functions
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Encode( uint8_t* const buffer, const uint32_t length )
{
	uint8_t encode[ 256 ];
	for( uint32_t n = 0; n < 256; ++n ) encode[ n ] = static_cast< uint8_t >( n );
	for( uint32_t i = 0; i < length; ++i )
	{
		uint8_t d = buffer[ i ];
		uint8_t e = encode[ d ];
		for( uint32_t a = 0; a < 256; ++a ) if( encode[ a ] < e ) ++encode[ a ];
		encode[ d ] = 0;
		buffer[ i ] = e;
	}
}

void Decode( uint8_t* const buffer, const uint32_t length )
{
	uint8_t decode[ 256 ];
	for( uint32_t n = 0; n < 256; ++n ) decode[ n ] = static_cast< uint8_t >( n );
	for( uint32_t i = 0; i < length; ++i )
	{
		uint8_t e = buffer[ i ];
		uint8_t d = decode[ e ];
		while( e )
		{
			decode[ e ] = decode[ e - 1 ];
			--e;
		}
		decode[ 0 ] = d;
		buffer[ i ] = d;
	}
}

