#include "libs/maths/aabb.h"
#include "libs/maths/vec4.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    begin maths namespace
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

namespace maths
{

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    forward declarations
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct frect;
struct mat43;
struct mat44;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    clipper
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct clipper
{
	vec3					o;
	vec4					r;
	bool					IsNan( void ) const;
	bool					IsReal( void ) const;
	void					ClipDX( void );
	void					Zoom( const frect& zoom );
	void					Lerp( const clipper& trg, const float t );
	void					Chain( const clipper& chain );
	void					Remap( const aabb& src, const aabb& trg );
	void					SetDX( const clipper& src );
	void					SetZoom( const frect& zoom );
	void					SetLerp( const clipper& src, const clipper& trg, const float t );
	void					SetChain( const clipper& first, const clipper& chain );
	void					SetRemap( const aabb& src, const aabb& trg );
	void					SetDefaults( void );
	inline void				SetLeftHanded( void ) { r.w = 1.0f; };
	inline void				SetRightHanded( void ) { r.w = -1.0f; };
	inline void				HandednessFlip( void ) { r.w = -r.w; };
	inline void				HorizontalFlip( void ) { r.x = -r.x; };
	inline void				VerticalFlip( void ) { r.y = -r.y; };
	inline void				DepthFlip( void ) { r.z = -r.z; };
	void					SetClip( const aabb& clip );
	void					SetClipGL( void );
	void					SetClipDX( void );
	void					GetRemapAsSource( aabb& remap ) const;
	void					GetRemapAsTarget( aabb& remap ) const;
	void					Apply( mat43& trg ) const;
	void					Apply( mat44& trg ) const;
	void					Apply( const mat43& src, mat43& trg ) const;
	void					Apply( const mat44& src, mat44& trg ) const;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    end maths namespace
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

};	//	namespace maths

