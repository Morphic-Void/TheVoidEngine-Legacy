struct camera
{
    quat                    rotate;
    vec3                    translate;
    mat44                   project;
    mat44                   unproject;
    void                    WorldToRelative( const vec3& world, vec3& relative );
    void                    WorldToViewspace( const vec3& world, vec3& viewspace );
    void                    WorldToClipspace( const vec3& world, vec4& clipspace );
    bool                    WorldToProjected( const vec3& world, vec3& projected );
    void                    RelativeToWorld( const vec3& relative, vec3& world );
    void                    RelativeToViewspace( const vec3& relative, vec3& viewspace );
    void                    RelativeToClipspace( const vec3& relative, vec4& clipspace );
    bool                    RelativeToProjected( const vec3& relative, vec3& projected );
    void                    ViewspaceToWorld( const vec3& viewspace, vec3& world );
    void                    ViewspaceToRelative( const vec3& viewspace, vec3& relative );
    void                    ViewspaceToClipspace( const vec3& viewspace, vec4& clipspace );
    bool                    ViewspaceToProjected( const vec3& viewspace, vec3& projected );
    bool                    ClipspaceToWorld( const vec4& clipspace, vec3& world );
    bool                    ClipspaceToRelative( const vec4& clipspace, vec3& relative );
    bool                    ClipspaceToViewspace( const vec4& clipspace, vec3& viewspace );
    bool                    ClipspaceToProjected( const vec4& clipspace, vec3& projected );
    bool                    ProjectedToWorld( const vec3& projected, vec3& world );
    bool                    ProjectedToRelative( const vec3& projected, vec3& relative );
    bool                    ProjectedToViewspace( const vec3& projected, vec3& viewspace );
    void                    ProjectedToClipspace( const vec3& projected, vec4& clipspace );
	bool					IsNan( void ) const;
	bool					IsReal( void ) const;
    void                    SetDefaults();
	void					Set( const joint& object, const clipper& clipping, const projector& projection );
	void					SetLeft( const joint& object, const clipper& clipping, const projector& projection );
	void					SetRight( const joint& object, const clipper& clipping, const projector& projection );
	void					Set( const viewer& setup );
	void					SetLeft( const viewer& setup );
	void					SetRight( const viewer& setup );
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    stereo_camera
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct stereo_camera
{
    camera                  left;
    camera                  right;
	bool					IsNan( void ) const;
	bool					IsReal( void ) const;
    void                    SetDefaults();
	void					Set( const joint& object, const clipper& clipping, const projector& projection );
	void					Set( const viewer& setup );
};

