bool viewer::IsNan( void ) const
{
	return( object.IsNan() || clipping.IsNan() || projection.IsNan() );
}

bool viewer::IsReal( void ) const
{
	return( object.IsReal() && clipping.IsReal() && projection.IsReal() );
}

void viewer::SetDefaults( void )
{
	object.SetIdentity();
	clipping.SetDefaults();
	projection.SetDefaults();
}

