bool viewport::IsNan( void ) const
{
	return( ::IsNan( znear ) || ::IsNan( zfar ) );
}

bool viewport::IsReal( void ) const
{
	return( ::IsReal( znear ) && ::IsReal( zfar ) );
}

//! Gets the clipped window and required frustum clipping remap.
//! Returns false if the window is entirely clipped.
bool viewport::GetClipped( irect& port, clipper& clip ) const
{
	int il = window.l;
	int ir = window.r;
	int it = window.t;
	int ib = window.b;
	if( static_cast< unsigned int >( il ) > width ) il = ( ( il < 0 ) ? 0 : static_cast< int >( width ) );
	if( static_cast< unsigned int >( ir ) > width ) ir = ( ( ir < 0 ) ? 0 : static_cast< int >( width ) );
	if( static_cast< unsigned int >( it ) > height ) it = ( ( it < 0 ) ? 0 : static_cast< int >( height ) );
	if( static_cast< unsigned int >( ib ) > height ) ib = ( ( ib < 0 ) ? 0 : static_cast< int >( height ) );
	if( ( il < ir ) && ( it < ib ) )
	{
		port.l = il;
		port.r = ir;
		port.t = it;
		port.b = ib;
		float dx = ( 1.0f / ( ir - il ) );
		float dy = ( 1.0f / ( ib - it ) );
		clip.o.x = ( ( window.r + window.l - ir - il ) * dx );
		clip.r.x = ( ( window.r - window.l ) * dx );
		clip.o.y = ( ( ib + it - window.b - window.t ) * dy );
		clip.r.y = ( ( window.b - window.t ) * dy );
		clip.o.z = 0;
		clip.r.z = clip.r.w = 1;
		return( true );
	}
	return( false );
}

//! Emulates setting a fractional view-port for the full frustum.
//! Sets the window from the supplied relative rectangle and the surface dimensions.
//! The returned clipper takes account of both compensating for the quantization of
//! the window dimensions and clipping to the surface dimensions. 
//! Returns false if the window is entirely clipped.
bool viewport::SetPartial( const frect& port, clipper& clip )
{
	ASSERT( ( port.l < port.r ) && ( port.b < port.t ) );
	float fl = ( width * port.l );
	float fr = ( width * port.r );
	float fb = ( height * port.b );
	float ft = ( height * port.t );
	int il = static_cast< int >( floorf( fl ) );
	int ir = static_cast< int >( ceilf( fr ) );
	int ib = static_cast< int >( floorf( fb ) );
	int it = static_cast< int >( ceilf( ft ) );
	if( static_cast< unsigned int >( il ) > width ) il = ( ( il < 0 ) ? 0 : static_cast< int >( width ) );
	if( static_cast< unsigned int >( ir ) > width ) ir = ( ( ir < 0 ) ? 0 : static_cast< int >( width ) );
	if( static_cast< unsigned int >( it ) > height ) it = ( ( it < 0 ) ? 0 : static_cast< int >( height ) );
	if( static_cast< unsigned int >( ib ) > height ) ib = ( ( ib < 0 ) ? 0 : static_cast< int >( height ) );
	if( ( il < ir ) && ( it < ib ) )
	{
		window.l = il;
		window.r = ir;
		window.b = ib;
		window.t = it;
		float dl = ( fl - il );
		float dr = ( fr - ir );
		float db = ( ib - fb );
		float dt = ( it - ft );
		float dx = ( 1.0f / ( width + dl - dr ) );
		float dy = ( 1.0f / ( height + dt - db ) );
		clip.o.x = ( ( dl + dr ) * dx );
		clip.r.x = ( width * dx );
		clip.o.y = ( ( db + dt ) * dy );
		clip.r.y = ( height * dy );
		clip.o.z = 0.0f;
		clip.r.z = clip.r.w = 1.0f;
		return( true );
	}
	return( false );
}

