////
////    typed slot allocator class template
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
class TSlot
{
public:
    inline                  TSlot() : m_used( 0 ), m_free( NULL ) {};
    inline                  TSlot( const uint size ) : m_used( 0 ), m_free( NULL ) { init( size ); };
    inline                  ~TSlot() { kill(); };
    void                    init( const uint size = 32 );
    void                    kill();
    void                    reset();
    T&                      fetch();
    bool                    recycle( T& item );
    bool                    contains( const T& item ) const;
    inline uint             getUsed() const { return( m_used ); };
protected:
    struct Slot
    {
        Slot*               next;
        T*                  item;
    };
    struct Buffer
    {
        uint                count;
        Slot*               slots;
        T*                  items;
    };
    void                    reset( Buffer& buffer );
    void                    extend( const uint count );
    uint                    m_used;
    Slot*                   m_free;
    TBlob< Buffer >         m_buffers;
private:
    inline                  TSlot( const TSlot& ) {};
    inline void             operator=( const TSlot& ) {};
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    typed slot allocator class template function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
void TSlot< T >::init( const uint size )
{
    kill();
    if( size )
    {
        extend( ( size < 32 ) ? 32 : size );
    }
}

template< typename T >
void TSlot< T >::kill()
{
    m_used = 0;
    m_free = NULL;
    uint count = m_buffers.getUsed();
    for( uint index = 0; index < count; ++index )
    {
        Buffer& buffer = m_buffers.getItem( index );
        delete[] buffer.items;
        delete[] buffer.slots;
    }
    m_buffers.kill();
}

template< typename T >
void TSlot< T >::reset()
{
    m_free = NULL;
    uint count = m_buffers.getUsed();
    while( count )
    {
        --count;
        reset( m_buffers.getItem( count ) );
    }
}

template< typename T >
void TSlot< T >::reset( Buffer& buffer )
{
    Slot* next = m_free;
    Slot* slot = ( buffer.slots + buffer.count );
    T* item = ( buffer.items + buffer.count );
    for( uint count = buffer.count; count; --count )
    {
        --slot;
        --item;
        slot->next = next;
        slot->item = item;
        next = slot;
    }
    m_free = next;
}

template< typename T >
T& TSlot< T >::fetch()
{
    if( m_free == NULL )
    {   //  there are no free items, so we need to allocate a new buffer
        extend( m_used ? ( ( m_used >> 1 ) + 1 ) : 32 );
    }
    ++m_used;
    Slot& slot = *m_free;
    m_free = slot.next;
    T* item = slot.item;
    slot.item = NULL;
    slot.next = NULL;
    return( *item );
}

template< typename T >
bool TSlot< T >::recycle( T& item )
{
    uintptr_t address = reinterpret_cast< uintptr_t >( &item );
    uint count = m_buffers.getUsed();
    while( count )
    {
        --count;
        Buffer& buffer = m_buffers.getItem( count );
        uintptr_t offset = ( address - reinterpret_cast< uintptr_t >( buffer.items ) );
        uint index = static_cast< uint >( offset / sizeof( T ) );
        uint align = static_cast< uint >( offset % sizeof( T ) );
        if( ( index < buffer.count ) && ( align == 0 ) )
        {   //  item is within this buffer and correctly aligned
            Slot& slot = buffer.slots[ index ];
            if( slot.item == NULL )
            {   //  item is currently allocated
                slot.item = &item;
                slot.next = m_free;
                m_free = &slot;
                return( true );
            }
        }
    }
    return( false );
}

template< typename T >
bool TSlot< T >::contains( const T& item ) const
{
    uintptr_t address = reinterpret_cast< uintptr_t >( &item );
    uint count = m_buffers.getUsed();
    while( count )
    {
        --count;
        const Buffer& buffer = m_buffers.getItem( count );
        uintptr_t offset = ( address - reinterpret_cast< uintptr_t >( buffer.items ) );
        uint index = static_cast< uint >( offset / sizeof( T ) );
        uint align = static_cast< uint >( offset % sizeof( T ) );
        if( ( index < buffer.count ) && ( align == 0 ) )
        {   //  item is within this buffer and correctly aligned
            const Slot& slot = buffer.slots[ index ];
            if( slot.item == NULL )
            {   //  item is currently allocated
                return( true );
            }
        }
    }
    return( false );
}

template< typename T >
void TSlot< T >::extend( const uint count )
{ 
    Buffer& buffer = m_buffers.fetch();
    buffer = { count, new Slot[ count ], new T[ count ] };
    reset( buffer );
}

