#include "tblob.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    byte data append buffer object class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class NBuffer
{
public:
    inline                  NBuffer() : m_buffers(), m_count( 0 ), m_bytes( 0 ) {};
    inline                  NBuffer( const uint size ) : m_buffers(), m_count( 0 ), m_bytes( 0 ) { init( size ); };
    inline                  ~NBuffer() { kill(); };
    void                    init( const uint size = 4096 );
    void                    kill();
    void                    reset();
    uint8_t*                append( const uint bytes );
    uint8_t*                append( const void* const data, const uint bytes );
    template< typename T >
    inline uint8_t*         append( const T& data ) { return( append( reinterpret_cast< const void* const >( &data ), static_cast< uint >( sizeof( T ) ) ) ); };
    uint8_t*                appendString( const uint8_t* const string );
    uint8_t*                appendString( const uint8_t* const string, const uint length );
    inline uint8_t*         appendString( const char* const string ) { return( appendString( reinterpret_cast< const uint8_t* const >( string ) ) ); };
    inline uint8_t*         appendString( const char* const string, const uint length ) { return( appendString( reinterpret_cast< const uint8_t* const >( string ), length ) ); };
    inline uint             getCount() const { return( m_count ); };
    inline uint             getBytes() const { return( m_bytes ); };
protected:
    uint8_t*                allocate( const uint bytes );
    TBlob< Buffer >         m_buffers;
    uint                    m_count;
    uint                    m_bytes;
private:
    inline                  NBuffer( const NBuffer& ) {};
    inline void             operator=( const NBuffer& ) {};
};

