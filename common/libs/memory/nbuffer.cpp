void NBuffer::init( const uint size )
{
    kill();
    Buffer& buffer = m_buffers.fetch();
    buffer.init( ( size < 1024 ) ? 1024 : size );
}

void NBuffer::kill()
{
    m_count = 0;
    m_bytes = 0;
    uint count = m_buffers.getUsed();
    for( uint index = 0; index < count; ++index )
    {
        m_buffers.getItem( index ).kill();
    }
    m_buffers.kill();
}

void NBuffer::reset()
{
    m_count = 0;
    m_bytes = 0;
    uint count = m_buffers.getUsed();
    for( uint index = 0; index < count; ++index )
    {
        m_buffers.getItem( index ).reset();
    }
}

uint8_t* NBuffer::append( const uint bytes )
{
    ASSERT( bytes != 0 );
    uint8_t* memory = allocate( bytes );
    if( bytes )
    {
        memset( reinterpret_cast< void* >( memory ), 0, bytes );
    }
    return( memory );
}

uint8_t* NBuffer::append( const void* const data, const uint bytes )
{
    ASSERT( bytes != 0 );
    ASSERT( data != NULL );
    uint8_t* memory = allocate( bytes );
    if( bytes )
    {
        if( data == NULL )
        {
            memset( reinterpret_cast< void* >( memory ), 0, bytes );
        }
        else
        {
            memcpy_s( reinterpret_cast< void* >( memory ), bytes, data, bytes );
        }
    }
    return( memory );
}

uint8_t* NBuffer::appendString( const uint8_t* const string )
{
    uint length = 0;
    if( string != NULL ) while( string[ length ] ) ++length;
    return( appendString( string, length ) );
}

uint8_t* NBuffer::appendString( const uint8_t* const string, const uint length )
{
    uint8_t* memory = allocate( length + 1 );
    if( length )
    {
        if( string == NULL )
        {
            memset( reinterpret_cast< void* >( memory ), 0, length );
        }
        else
        {
            memcpy_s( reinterpret_cast< void* >( memory ), length, string, length );
        }
    }
    memory[ length ] = 0;
    return( memory );
}

uint8_t* NBuffer::allocate( const uint bytes )
{
    ++m_count;
    m_bytes += bytes;
    uint index = m_buffers.getUsed();
    while( index )
    {
        --index;
        Buffer& buffer = m_buffers.getItem( index );
        if( buffer.getFree() >= bytes )
        {   //  found space
            return( buffer.append( bytes ) );
        }
    }
    Buffer& buffer = m_buffers.fetch();
    buffer.init( ( ( ( m_bytes + m_count - 1 ) / m_count ) * ( ( m_count >> 1 ) + 1 ) ) + bytes );
    return( buffer.append( bytes ) );
}

