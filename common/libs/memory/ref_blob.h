#include "libs/system/base/types.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    reference counted byte data blob object class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class RefBlob
{
public:
    static inline RefBlob*  create( const uint size ) { return( new RefBlob( size ) ); };
    static inline RefBlob*  create( void* const data, const uint size ) { return( new RefBlob( data, size ) ); };
    inline void             addRef() { ++m_refs; };
    inline void             release() { if( m_refs > 0 ) { --m_refs; if( m_refs == 0 ) delete this; } };
    inline uint             getSize() const { return( m_size ); };
    inline uint8_t*         getBuffer() { return( m_data ); };
    inline const uint8_t*   getBuffer() const { return( m_data ); };
protected:
    inline                  RefBlob( const uint size ) : m_refs( 1 ), m_size( size ), m_data( NULL ) { m_data = ( size ? new uint8_t[ size ] : NULL ); };
    inline                  RefBlob( void* const data, const uint size ) : m_refs( 1 ), m_size( size ), m_data( reinterpret_cast< uint8_t* const >( data ) ) {};
    inline                  ~RefBlob() { m_refs = 0; m_size = 0; if( m_data != NULL ) { delete[] m_data; m_data = NULL; } };
    uint                    m_refs;
    uint                    m_size;
    uint8_t*                m_data;
};

