////
////    typed data append buffer object class template
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
class TBuffer
{
public:
    inline                  TBuffer() : m_size( 0 ), m_used( 0 ), m_data( NULL ) {};
    inline                  TBuffer( const uint size ) : m_size( 0 ), m_used( 0 ), m_data( NULL ) { init( size ); };
    inline                  ~Buffer() { kill(); };
    void                    init( const uint size = 32 );
    void                    kill();
    inline void             reset() { m_used = 0; };
    void                    clone( const TBlob< T >& source );
    T*                      fetch( const uint count );
    T&                      getItem( const uint index );
    const T&                getItem( const uint index ) const;
    inline uint             getSize() const { return( m_size ); };
    inline uint             getUsed() const { return( m_used ); };
    inline uint             getFree() const { return( m_size - m_used ); };
    inline T*               getBuffer() { return( m_data ); };
    inline const T*         getBuffer() const { return( m_data ); };
protected:
    uint                    m_size;
    uint                    m_used;
    T*                      m_data;
private:
    inline                  TBuffer( const TBuffer& ) {};
    inline void             operator=( const TBuffer& ) {};
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    typed data append buffer object class template functions
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
void TBuffer::init( const uint size )
{
    m_size = size;
    m_used = 0;
    m_data = ( size ? new T[ size ] : NULL );
}

template< typename T >
void TBuffer::kill()
{
    m_size = 0;
    m_used = 0;
    if( m_data != NULL )
    {
        delete[] m_data;
        m_data = NULL;
    }
}

template< typename T >
void TBuffer< T >::clone( const TBuffer< T >& source )
{
    kill();
    m_size = source.m_size;
    m_used = source.m_used;
    if( m_size != 0 )
    {
        m_data = new T[ m_size ];
        memcpy( m_data, source.m_data, ( m_size * sizeof( T ) ) );
    }
}

template< typename T >
T* TBuffer::fetch( const uint count )
{
    ASSERT( count <= space() );
    if( count <= space() )
    {
        T* data = m_data[ m_used ];
        m_used += count;
        return( data );
    }
    return( NULL );
}

template< typename T >
T& TBuffer< T >::getItem( const uint index )
{
    ASSERT( index < m_used );
    return( m_data[ index ] );
}

template< typename T >
const T& TBuffer< T >::getItem( const uint index ) const
{
    ASSERT( index  < m_used );
    return( m_data[ index ] );
}

