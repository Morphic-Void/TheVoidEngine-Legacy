////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    typed managed slot allocator class template
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
class TManaged
{
public:
    inline                  TManaged() : m_list(), m_pool() {};
    inline                  TManaged( const uint size ) : m_list(), m_pool() { init( size ); };
    inline                  ~TManaged() { kill(); };
    inline void             init( const uint size = 32 ) { m_list.init( size ); m_pool.init( size ); };
    inline void             kill() { m_list.kill(); m_pool.kill(); };
    void                    reset();
    T&                      fetch();
    void                    prefetch( const uint count );
    inline bool             contains( T& item ) const { return( m_pool.contains( item ) ); };
    inline T&               getItem( const uint index ) { return( *m_list.getItem( index ) ); };
    inline const T&         getItem( const uint index ) const { return( *m_list.getItem( index ) ); };
    inline uint             getItemSize() const { return( static_cast< uint >( sizeof( T ) ) ); };
    inline uint             getUsed() const { return( m_list.getUsed() ); };
protected:
    TBlob< T* >             m_list;
    TSlot< T >              m_pool;
private:
    inline                  TManaged( const TManaged& ) {};
    inline void             operator=( const TManaged& ) {};
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    typed managed slot allocator class template function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
void TManaged< T >::reset()
{
    uint count = m_list.getUsed();
    for( uint index = 0; index < count; ++index )
    {
        m_pool.recycle( *m_list.getItem( index ) );
    }
    m_list.reset();
}

template< typename T >
T& TManaged< T >::fetch()
{
    T& item = m_pool.fetch();
    m_list.append( &item );
    return( item );
}

template< typename T >
void TManaged< T >::prefetch( const uint count )
{
    for( uint index = 0; index < count; ++index )
    {
        fetch();
    }
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    typed managed slot allocator class template with element reset
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
class TManagedReset : public TManaged< T >
{
public:
    inline                  TManagedReset() : TManaged() {};
    inline                  TManagedReset( const uint size ) : TManaged( size ) { init( size ); };
    inline                  ~TManagedReset() { kill(); };
    void                    reset();
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    typed managed slot allocator class template with element reset function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
void TManagedReset< T >::reset()
{
    uint count = m_list.getUsed();
    for( uint index = 0; index < count; ++index )
    {
        m_list.getItem( index )->reset();
    }
    TManaged::reset();
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    typed managed slot allocator class template with element destroy
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
class TManagedDestroy : public TManaged< T >
{
public:
    inline                  TManagedDestroy() : TManaged() {};
    inline                  TManagedDestroy( const uint size ) : TManaged( size ) { init( size ); };
    inline                  ~TManagedDestroy() { kill(); };
    void                    reset();
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    typed managed slot allocator class template with element destroy function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
void TManagedDestroy< T >::reset()
{
    uint count = m_list.getUsed();
    for( uint index = 0; index < count; ++index )
    {
        m_list.getItem( index )->destroy();
    }
    TManaged::reset();
}

