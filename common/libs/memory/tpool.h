////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    typed pool allocator class template
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
class TPool
{
public:
    inline                  TPool() : m_used( 0 ) {};
    inline                  TPool( const uint size ) : m_used( 0 ) { init( size ); };
    inline                  ~TPool() { kill(); };
    void                    init( const uint size = 32 );
    void                    kill();
    T&                      fetch();
    bool                    contains( T& item ) const;
    inline uint             getUsed() const { return( m_used ); };
protected:
    struct Buffer
    {
        uint                count;
        uint                index;
        T*                  items;
    };
    inline T&               fetch( Buffer& buffer );
    inline Buffer&          extend( const uint count ) { Buffer& buffer = m_buffers.fetch(); buffer = { count, 0, new T[ count ] }; return( buffer ); };
    uint                    m_used;
    TBlob< Buffer >         m_buffers;
private:
    inline                  TPool( const TPool& ) {};
    inline void             operator=( const TPool& ) {};
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    typed pool allocator class template function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
void TPool< T >::init( const uint size )
{
    kill();
    if( size )
    {
        extend( ( size < 32 ) ? 32 : size );
    }
}

template< typename T >
void TPool< T >::kill()
{
    m_used = 0;
    uint count = m_buffers.getUsed();
    for( uint index = 0; index < count; ++index )
    {
        delete[] m_buffers.getItem( index ).items;
    }
    m_buffers.kill();
}

template< typename T >
T& TPool< T >::fetch()
{
    uint count = m_buffers.getUsed();
    if( count )
    {   //  see if we have any space in the current buffers
        Buffer& buffer = m_buffers.getItem( count - 1 );
        if( buffer.index < buffer.count )
        {
            return( fetch( buffer ) );
        }
    }
    return( fetch( extend( m_used ? ( ( m_used >> 1 ) + 1 ) : 32 ) ) );
}

template< typename T >
T& TPool< T >::fetch( Buffer& buffer )
{
    T& item = buffer.items[ buffer.index ];
    ++buffer.index;
    ++m_used;
    return( item );
}

template< typename T >
bool TPool< T >::contains( T& item ) const
{
    uintptr_t address = reinterpret_cast< uintptr_t >( &item );
    uint count = m_buffers.getUsed();
    while( count )
    {
        --count;
        Buffer& buffer = m_buffers.getItem( count );
        uintptr_t offset = ( address - reinterpret_cast< uintptr_t >( buffer.items ) );
        uint index = ( offset / sizeof( T ) );
        uint align = ( offset % sizeof( T ) );
        if( ( index < buffer.index ) && ( align == 0 ) )
        {   //  item is within this buffer and correctly aligned
            return( true );
        }
    }
    return( false );
}

