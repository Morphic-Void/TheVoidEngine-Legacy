////
////    TArraySort sortable typed array template
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
class TArraySort : public ArraySort, protected IArrayQuery
{
public:
    inline                      TArraySort() : ArraySort(), m_items() {};
    inline                      TArraySort( const uint count ) : ArraySort(), m_items() { init( count ); };
    virtual                     ~TArraySort() { m_items.kill(); };
    bool                        init( const uint count = 32 );
    inline void                 kill() { m_items.kill(); ArraySort::kill(); };
    inline void                 reset();
    void                        clone( const TArraySort< T >& source );
    inline int                  find( const T& item ) const { return( ArraySort::find( &item ) ); };
	int                         insert( const T& item, const void* const key );
	inline int                  insert( const T& item ) { return( insert( item, &item ) ); };
	bool                        update( const int index, const T& item );
    const T*                    access( const int index ) const;
    inline TBlob< T >&          getBlob() { return( m_items ); };
    inline const TBlob< T >&    getBlob() const { return( m_items ); };
protected:
    virtual const void*         getKey( const int index ) const;
    virtual int                 cmpKey( const void* const key, const int index ) const;
    virtual void                moveItem( const int source, const int target );
    TBlob< T >                  m_items;      //  array of items
    T                           m_item;       //  temporary storage
private:
    inline                      TArraySort( const TArraySort& ) {};
    inline void                 operator=( const TArraySort& ) {};
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    TArraySort sortable typed array template public function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
bool TArraySort< T >::init( const uint count )
{
    kill();
    uint size = ( ( count < 4 ) ? 4 : count );
    if( ArraySort::init( *this, size ) )
    {
        m_items.init( size );
        return( true );
    }
    kill();
    return( false );
}

template< typename T >
void TArraySort< T >::reset()
{
    ArraySort::reset();
    m_query = this;
    m_items.reset();
}

template< typename T >
void TArraySort< T >::clone( const TArraySort< T >& source )
{
    kill();
    ArraySort::clone( source );
    m_query = this;
    m_items.clone( source.m_items );
}

template< typename T >
int TArraySort< T >::insert( const T& item, const void* const key )
{
    int index = ArraySort::insert( key );
    if( index >= 0 )
    {
        m_items.setItem( static_cast< uint >( index ), item );
    }
    return( index );
}

template< typename T >
bool TArraySort< T >::update( const int index, const T& item )
{
    if( contains( index ) )
    {
        m_items.setItem( static_cast< uint >( index ), item );
        ArraySort::update( index );
        return( true );
    }
    ASSERT_FAIL( "TArraySort< T >::update() failed\n" );
    return( false );
}

template< typename T >
const T* TArraySort< T >::access( const int index ) const
{
    if( contains( index ) )
    {
        return( &m_items.getItem( static_cast< uint >( index ) ) );
    }
    ASSERT_FAIL( "TArraySort< T >::access() failed\n" );
    return( NULL );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    TArraySort sortable typed array template protected virtual function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
const void* TArraySort< T >::getKey( const int index ) const
{
    return( reinterpret_cast< const void* >( m_items.getBuffer() + index ) );
}

template< typename T >
int TArraySort< T >::cmpKey( const void* const key, const int index ) const
{
    return( memcmp( key, reinterpret_cast< const void* >( m_items.getBuffer() + index ), sizeof( T ) ) );
}

template< typename T >
void TArraySort< T >::moveItem( const int source, const int target )
{
    T* items = m_items.getBuffer();
    if( target < 0 )
    {
        m_item = items[ source ];
    }
    else
    {
        items[ target ] = ( ( source < 0 ) ? m_item : items[ source ] );
    }
}

