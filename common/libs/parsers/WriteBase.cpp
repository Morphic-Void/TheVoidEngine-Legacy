WriteBase::~WriteBase()
{
}

bool WriteBase::Parse( const QueryTree& tree, const void*& buffer, unsigned int& length )
{
	buffer = 0;
	length = 0;
	if( tree.IsValid() )
	{
		uint8_t* text = new uint8_t[ 4096 ];
		ASSERT( text );
		if( text )
		{
			m_write.buffer = text;
			m_write.length = 4096;
			m_write.offset = 3;
			m_write.buffer[ 0 ] = 0xef;
			m_write.buffer[ 1 ] = 0xbb;
			m_write.buffer[ 2 ] = 0xbf;
			m_depth = 0;
			m_tree = &tree;
			if( Parse() )
			{
				text = new uint8_t[ m_write.offset ];
				ASSERT( text );
				if( text )
				{
					buffer = reinterpret_cast< const void* >( text );
					length = m_write.offset;
					memcpy( reinterpret_cast< void* const >( text ), reinterpret_cast< const void* const >( m_write.buffer ), m_write.offset );
					delete[] m_write.buffer;
					Clear();
					return( true );
				}
			}
			delete[] m_write.buffer;
			Clear();
		}
	}
	return( false );
}

//! write a single ascii character
bool WriteBase::Write( const int32_t unicode )
{
	++m_chars;
	unicode::UNICODE_ERROR error = unicode::SetUTF8( m_write, unicode );
	if( error == unicode::UNICODE_ERROR_BUFFER_OVERFLOW )
	{
		unsigned int length = ( m_write.length + 4096 );
		uint8_t* buffer = new uint8_t[ length ];
		ASSERT( buffer );
		if( !buffer ) return( false );
		memcpy( reinterpret_cast< void* const>( buffer ), reinterpret_cast< const void* const >( m_write.buffer ), m_write.offset );
		delete[] m_write.buffer;
		m_write.buffer = buffer;
		m_write.length = length;
		error = unicode::SetUTF8( m_write, unicode );
	}
	return( ( error == unicode::UNICODE_SUCCESS ) ? true : false );
}

//! write an nul terminated ascii string excluding the nul terminator
bool WriteBase::Write( const char* const text )
{
	ASSERT( text );
	if( text )
	{
		for( uint32_t index = 0; text[ index ]; ++index )
		{
			if( !Write( text[ index ] ) ) return( false );
			if( text[ index ] == '\n' )
			{
				m_chars = 0;
				for( uint32_t indent = ( m_depth << 1 ); indent; --indent )
				{
					if( !Write( ' ' ) ) return( false );
				}
			}
		}
	}
	return( true );
}

//! clone a zero terminated utf-8 string
bool WriteBase::Clone( const uint8_t* const text )
{
	if( text )
	{
		return( Clone( text, static_cast< uint32_t >( strlen( reinterpret_cast< const char* const >( text ) ) ) ) );
	}
	return( false );
}

//! clone a specified length utf-8 string
bool WriteBase::Clone( const uint8_t* const text, const uint32_t bytes )
{
	if( text )
	{
		if( ( m_write.length - m_write.offset ) <= bytes )
		{
			unsigned int length = ( ( m_write.offset + bytes + 4096 ) & -4096 );
			uint8_t* buffer = new uint8_t[ length ];
			ASSERT( buffer );
			if( !buffer ) return( false );
			memcpy( reinterpret_cast< void* const>( buffer ), reinterpret_cast< const void* const >( m_write.buffer ), m_write.offset );
			delete[] m_write.buffer;
			m_write.buffer = buffer;
			m_write.length = length;
		}
		memcpy( reinterpret_cast< void* const >( &m_write.buffer[ m_write.offset ] ), reinterpret_cast< const void* const >( text ), bytes );
		m_write.offset += bytes;
	}
	return( true );
}

//! default parsing function
bool WriteBase::Parse( void )
{
	return( false );
}

//!	clear all members
void WriteBase::Clear( void )
{
	m_write.length = 0;
	m_write.offset = 0;
	m_write.buffer = 0;
	m_depth = 0;
	m_chars = 0;
	m_tree = 0;
}

