struct QueryList
{
	uint32_t			attribs;			//	first attrib item index
	uint32_t			members;			//	first member item index
};

union QueryData
{
	uint32_t			reserved[ 2 ];		//	unused
	QueryList			list;				//	list data
	double				number;				//	number
};

struct QueryItem
{
	uint32_t			type;				//	item type
	uint32_t			parent;				//	parent item index
	uint32_t			prev;				//	previous item index
	uint32_t			next;				//	next item index
	uint32_t			key;				//	key string offset
	uint32_t			text;				//	text or data string offset
	QueryData			data;				//	list description or number
};

struct QueryHead
{
	uint32_t			m_items;			//	items in the tree
	uint32_t			m_bytes;			//	bytes in the text array
	uint32_t			m_count;			//	texts in the sort array
};

