struct QueryHead;
struct QueryItem;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    QueryTree value types enumeration
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

enum QUERY_VALUE_TYPE
{
	QUERY_VALUE_INVALID = 0,			//	invalid
	QUERY_VALUE_LIST,					//	value type is a list (can have attributes and members)
	QUERY_VALUE_ITEM,					//	value type is an item (can have attributes)
	QUERY_VALUE_TEXT,					//	value type is parsed text
	QUERY_VALUE_DATA,					//	value type is unparsed text
	QUERY_VALUE_NULL,					//	value type is null
	QUERY_VALUE_TRUE,					//	value type is true
	QUERY_VALUE_FALSE,					//	value type is false
	QUERY_VALUE_NUMBER,					//	value type is a double
	QUERY_VALUE_TYPE_FORCE32 = 0x7fffffff
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    QueryTree class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class QueryTree
{
public:
	inline				QueryTree() : m_items( 0 ), m_bytes( 0 ), m_count( 0 ), m_head( 0 ), m_tree( 0 ), m_text( 0 ), m_sort( 0 ) {};
	virtual				~QueryTree();
	void				Release( void );
	bool				WriteJSON( const void*& buffer, unsigned int& length ) const;
	bool				WriteXML( const void*& buffer, unsigned int& length ) const;
	bool				ParseJSON( const void* const buffer, const unsigned int length );
	bool				ParseXML( const void* const buffer, const unsigned int length );
	bool				ParseBVH( const void* const buffer, const unsigned int length );
	void				GetBIN( const void*& buffer, unsigned int& length ) const;
	bool				SetBIN( const void* const buffer, const unsigned int length );
	inline bool			IsValid( void ) const { return( m_items ? true : false ); };
	inline bool			IsValid( uint32_t item ) const { return( ( item < m_items ) ? true : false ); };
	QUERY_VALUE_TYPE	GetType( const uint32_t item ) const;
	const char*			GetKey( const uint32_t item ) const;
	const char*			GetText( const uint32_t item ) const;
	bool				GetBool( const uint32_t item, bool& boolean ) const;
	bool				GetNumber( const uint32_t item, double& number ) const;
	bool				GetNumberAsInt( const uint32_t item, int& number ) const;
	bool				GetNumberAsUint( const uint32_t item, unsigned int& number ) const;
	uint32_t			GetMembers( const uint32_t item ) const;
	uint32_t			GetMember( const uint32_t item, const uint32_t index = 0 ) const;
	uint32_t			GetMember( const uint32_t root, const char* const path ) const;
	uint32_t			GetAttribs( const uint32_t item ) const;
	uint32_t			GetAttrib( const uint32_t item, const uint32_t index = 0 ) const;
	uint32_t			GetAttrib( const uint32_t item, const char* const name ) const;
	uint32_t			GetParent( const uint32_t item ) const;
	uint32_t			GetPrev( const uint32_t item ) const;
	uint32_t			GetNext( const uint32_t item ) const;
	uint32_t			FindRef( const uint32_t item ) const;
	uint32_t			FindRef( const char* const key, const uint32_t item ) const;
	uint32_t			FindRef( const char* const key, const char* const text ) const;
	inline uint32_t		FindItem( const char* const path ) const { return( FindItem( 0, path ) ); };
	uint32_t			FindItem( const uint32_t root, const char* const path ) const;
	uint32_t			GetItem( const uint32_t root, const char* const path ) const;
	uint32_t			GetHash( const char* const text ) const;
protected:
	void				Transfer( QueryTree& other );
	uint32_t			m_items;	//	items in the tree
	uint32_t			m_bytes;	//	bytes in the text array
	uint32_t			m_count;	//	texts in the sort array
	QueryHead*			m_head;
	QueryItem*			m_tree;
	char*				m_text;
	uint32_t*			m_sort;
};

