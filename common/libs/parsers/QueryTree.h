////    Reference finding function notes:
////
////    	a reference is a text string beginning with '#'
////
////    	FindRef( const uint32_t item ) const;
////
////    		returns the first item which matches the reference in the supplied item
////    		returns 0 if the item is not a reference or the reference is not found
////
////    	FindRef( const char* const key, const uint32_t item ) const;
////
////    		returns the first item which matches supplied key and the reference in the supplied item
////    		returns 0 if the item is not a reference or the key and reference combination are not found
////
////    	FindRef( const char* const key, const char* const text ) const;
////
////    		returns the first item whose key and text match the supplied key and text
////    		returns 0 if the supplied key and text combination are not found
////    		the text is searched for 'as is'
////
////
////    Path item finding function notes:
////
////    	FindItem( const char* const path ) const;
////
////    		calls the following FindItem function with a root item of 0
////
////    	FindItem( const uint32_t root, const char* const path ) const;
////
////    		searches for the supplied key path anywhere below the supplied root item
////    		returns 0 if the path cannot be found
////
////    	GetItem( const uint32_t root, const char* const path ) const;
////
////    		returns the item at the supplied key path relative to the supplied root item
////    		returns 0 if the path does not exist
////
////
////    Text hashing function notes:
////
////    	this function is primarily for internal use, but can be used for validation
////
////    	GetHash( const char* const text ) const;
////
////    		returns the text hash if it exists within the QueryTree string table
////    		returns 0 if the text does not exist
////
