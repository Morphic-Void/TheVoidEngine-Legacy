enum UNICODE_ERROR
{
	UNICODE_WARNING_MODIFIED_ENCODING = 3,			//	utf-8: the encoding is modified 2-byte 0
	UNICODE_WARNING_OVERLONG_ENCODING = 2,			//	utf-8: the encoding is overlong
	UNICODE_WARNING_ILLEGAL_UNICODE = 1,			//	parsable illegal unicode character
	UNICODE_SUCCESS = 0,							//	the operation was successful
	UNICODE_ERROR_ILLEGAL_UNICODE = -1,				//	illegal unicode character
	UNICODE_ERROR_INVALID_ENCODING = -2,			//	utf-8: an invalid leading byte was found
	UNICODE_ERROR_MISSING_CONTINUATION = -3,		//	utf-8: an unexpected leading byte was found
	UNICODE_ERROR_UNEXPECTED_CONTINUATION = -4,		//	utf-8: an unexpected continuation byte was found
	UNICODE_ERROR_UNEXPECTED_LOW_SURROGATE = -5,	//	utf-16: an unexpected low surrogate code was found
	UNICODE_ERROR_INVALID_SURROGATE = -6,			//	utf-16: an invalid low surrogate code was found
	UNICODE_ERROR_BUFFER_UNDERFLOW = -7,			//	the read operation would underflow the buffer
	UNICODE_ERROR_BUFFER_OVERFLOW = -8,				//	the write operation would overflow the buffer
	UNICODE_ERROR_FORCE32 = 0x7fffffff				//	force the enumeration to be 32-bit (or greater)
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    utf type enumeration
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

enum UTF_TYPE
{
	UTF_TYPE_UNKNOWN = 0,							//	unidentified type
	UTF_TYPE_UTF8 = 1,								//	utf-8
	UTF_TYPE_UTF16le = 2,							//	little endian utf-16
	UTF_TYPE_UTF16be = 3,							//	big endian utf-16
	UTF_TYPE_UTF32le = 4,							//	little endian utf-32
	UTF_TYPE_UTF32be = 5,							//	big endian utf-32
	UTF_TYPE_FORCE32 = 0x7fffffff					//	force the enumeration to be 32-bit (or greater)
};

