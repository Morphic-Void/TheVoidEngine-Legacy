bool IsNameFirstXML( const char ascii );
bool IsNameXML( const char ascii );
bool IsNameEndXML( const char ascii );
char AsciiToHex( const char ascii );
char HexToAscii( const char hex );

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
