////
////        enum        EPLUGIN_NOTIFY          :   enumeration of notification messages sent to a plug-in
////        INTERFACE   INTERFACE_QUERY         :   interface querying for interface exchange between the app and plug-in
////        class       NULL_INTERFACE_QUERY    :   interface querying class with default behaviours
////        INTERFACE   PLUGIN_INTERFACE        :   interface to the plug-in
////        class       NULL_PLUGIN_INTERFACE   :   plug-in class with default behaviours
////        #define     PLUGIN_FUNC(name)       :   macro defining the main plug-in function signature
////        #define     PLUGIN_FUNC_NAME        :   the name of the main plug-in function that will be exported
////        #define     PLUGIN_FUNC_DECL        :   a declaration to use within the plug-in as the main function signature
////        #define     PLUGIN_STRING(name)     :   a helper macro to convert names to strings
////        typedef     PLUGIN_FUNC_PTR         :   a typedef of the function pointer exported from the plug-in
////        class       CPlugin                 :   host class for the dll plug-in
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Plug-in processing:
////
////        Plug-in loaded for the first time:
////            notify( EPLUGIN_LOAD )
////            ( query )
////            getDllQuery()
////            notify( EPLUGIN_VERIFY )
////
////        Plug-in being unloaded to load an updated version:
////            notify( EPLUGIN_UNLOAD )
////
////        Updated version of the plug-in loaded:
////            notify( EPLUGIN_RELOAD )
////            setAppQuery( query )
////            getDllQuery()
////            notify( EPLUGIN_VERIFY )
////
////        Plug-in being permanently unloaded:
////            notify( EPLUGIN_EXIT )
////
////        update() will be called once a frame.
////
////        If notify(), update() or setAppQuery() return false the plug-in will be unloaded.
////
////        If the plug-in does not support additional interfaces, it may return NULL in response
////        to calls to getInterfaceQuery().
////
