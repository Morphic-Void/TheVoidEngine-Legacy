class CTiming
{
public:
    inline                      CTiming() { if( !s_enabled ) reset(); };
    inline                      ~CTiming() {};
    static void                 reset();
    static void                 update();
    static inline void          split( const ETimingID ID ) { if( s_counts[ WRITE_INDEX ][ ID ] == 0 ) { s_cycles[ WRITE_INDEX ][ ID ] = ( __rdtsc() - s_cycles[ WRITE_INDEX ][ ETimingUpdate ] ); ++s_counts[ WRITE_INDEX ][ ID ]; } };
    static inline void          begin( const ETimingID ID ) { if( s_counts[ WRITE_INDEX ][ ID ] == 0 ) s_cycles[ WRITE_INDEX ][ ID ] = __rdtsc(); };
    static inline void          end( const ETimingID ID ) { if( s_counts[ WRITE_INDEX ][ ID ] == 0 ) { s_cycles[ WRITE_INDEX ][ ID ] = ( __rdtsc() - s_cycles[ WRITE_INDEX ][ ID ] ); ++s_counts[ WRITE_INDEX ][ ID ]; } };
    static inline bool          isValid() { return( s_valid > 3 ? true : false ); };
    static inline const char*   getName( const ETimingID ID ) { return( s_names[ ID ] ); };
    static inline uint64_t      getCycles( const ETimingID ID ) { return( s_cycles[ READ_INDEX ][ ID ] ); };
    static inline uint32_t      getCount( const ETimingID ID ) { return( s_counts[ READ_INDEX ][ ID ] ); };
    static inline float         getMS( const uint64_t cycles ) { return( isValid() ? ( static_cast< float >( cycles ) * s_cyclesToMS ) : 0.0f ); };
    static inline float         getHZ( const uint64_t cycles ) { if( isValid() ) { float ms = getMS( cycles ); return( ( ms > ( 1.0f / 1000.0f ) ) ? ( 1000.0f / ms ) : 1000000.0f ); } return( 0.0f ); };
    static inline float         getMS() { return( isValid() ? ( static_cast< float >( s_counter[ READ_INDEX ] ) * s_counterMS ) : 0.0f ); };
    static inline float         getHZ() { if( isValid() ) { float ms = getMS(); return( ( ms > ( 1.0f / 1000.0f ) ) ? ( 1000.0f / ms ) : 1000000.0f ); } return( 0.0f ); };
protected:
    static const int            READ_INDEX = 0;
    static const int            WRITE_INDEX = 1;
    static uint64_t             s_frequency;
    static uint64_t             s_counter[ 2 ];
    static uint64_t             s_cycles[ 2 ][ ETimingCount ];
    static uint32_t             s_counts[ 2 ][ ETimingCount ];
    static const char*          s_names[ ETimingCount ];
    static float                s_counterMS;
    static float                s_cyclesToMS;
    static uint                 s_enabled;
    static uint                 s_valid;
};

class CScopeTiming : protected CTiming
{
public:
    inline                      CScopeTiming( const ETimingID ID ) : m_ID( ID ) { m_cycles = __rdtsc(); };
    inline                      ~CScopeTiming() { s_cycles[ WRITE_INDEX ][ m_ID ] += ( __rdtsc() - m_cycles ); ++s_counts[ WRITE_INDEX ][ m_ID ]; };
protected:
    uint64_t                    m_cycles;
    ETimingID                   m_ID;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Timing macros
////
////    notes:
////
////        These macros should only be used once in the project.
////
////        TIMING_BEGIN:
////
////            Captures the update rate and sets the base time for all split times.
////
////        TIMING_END:
////
////            This should appear after all code is run and captures the time that the application is actually active.
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#define TIMING_BEGIN            CTiming TIMING_OBJECT; CTiming::update();
#define TIMING_END              CTiming::split( ETimingActive )

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Profiling macros
////
////    notes:
////
////        PROFILE_SCOPE:
////
////            Accumulates total time within a scope and the number of times the scope is entered.
////            Times for multiple scopes can be aggregated by sharing the same ID.
////            Nested scopes with the same ID will produce invalid timings.
////
////        PROFILE_SPLIT:
////
////            Captures the split time since TIMING_BEGIN.
////            Only the first instance of PROFILE_SPLIT encountered for any particular ID is captured.
////
////        PROFILE_BEGIN:
////
////            Starts a profile timing section.
////            IDs for each PROFILE_BEGIN must be unique.
////
////        PROFILE_END:
////
////            Ends a profile timing section.
////            If used without a PROFILE_BEGIN with the same ID this captures the processor timestamp.
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#define PROFILE_SCOPE( ID )     CScopeTiming SCOPE_PROFILER( ID )
#define PROFILE_SPLIT( ID )     CTiming::split( ID )
#define PROFILE_BEGIN( ID )     CTiming::begin( ID )
#define PROFILE_END( ID )       CTiming::end( ID )

