//! initialise camera, objects etc.
bool CAppX::Create( ID3D11Device* device, ID3D11DeviceContext* context, const UINT Width, const UINT Height )
{
    m_device = device;
    m_context = context;
    if( !EnableDepthStencil() )
    {
        return( false );
    }
    return( CWndX::Create( device, context, Width, Height ) );
}

//! update cameras and buffers (other than the main swap-chain) to reflect window resizing and possible aspect ratio changes
bool CAppX::Resize( const UINT Width, const UINT Height )
{
    return( CWndX::Resize( Width, Height ) );
}

//! update the scene for each frame
bool CAppX::Update()
{
    return( CWndX::Update() );
}

//! render the scene
bool CAppX::Render()
{
    return( CWndX::Render() );
}

//! tidy up any assets ready to exit the application
void CAppX::Finish()
{
    m_device = NULL;
    m_context = NULL;
    DisableDepthStencil();
    return( CWndX::Finish() );
}

//! process activation and deactivation
void CAppX::Activate( const bool state )
{
    CWndX::Activate( state );
}

//! process the keyboard input stream
void CAppX::KeyInput( const UINT uiMsg, const WPARAM wParam, const LPARAM lParam )
{
    CWndX::KeyInput( uiMsg, wParam, lParam );
}

//! process the mouse input stream
void CAppX::MouseInput( const UINT uiMsg, const WPARAM wParam, const LPARAM lParam )
{
    CWndX::MouseInput( uiMsg, wParam, lParam );
}

