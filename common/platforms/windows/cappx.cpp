void CAppX::WndRunWithConfig( LPCTSTR pszName, const CWndConfig& config, const bool fullscreen, const bool altEnter )
{
	if( WndInit( pszName, config ) )
    {
        WndRunApp( fullscreen, altEnter );
        WndKill();
    }
}

void CAppX::WndRunAsToolWindow( LPCTSTR pszName, const bool fullscreen, const bool altEnter )
{
	CWndConfig config;
	config.SetToolWindowConfig( ( 16 * 60 ), ( 9 * 60 ) );
    WndRunWithConfig( pszName, config, fullscreen, altEnter );
}

void CAppX::WndRunAsStandardApp( LPCTSTR pszName, const bool fullscreen, const bool altEnter )
{
	CWndConfig config;
	config.SetStandardAppConfig( ( 16 * 60 ), ( 9 * 60 ) );
    WndRunWithConfig( pszName, config, fullscreen, altEnter );
}

void CAppX::WndRunAsFramelessApp( LPCTSTR pszName, const bool fullscreen, const bool altEnter )
{
	CWndConfig config;
	config.SetFramelessAppConfig( ( 16 * 60 ), ( 9 * 60 ) );
    WndRunWithConfig( pszName, config, fullscreen, altEnter );
}

