class CAppX : public CWndX
{
public:
	inline				    CAppX( const LPCTSTR pszName, const bool fullscreen = false, const bool altEnter = true ) : CWndX(), m_device( NULL ), m_context( NULL ) { WndRunAsStandardApp( pszName, fullscreen, altEnter ); };
protected:
    void                    WndRunWithConfig( LPCTSTR pszName, const CWndConfig& config, const bool fullscreen = false, const bool altEnter = true );
    void                    WndRunAsToolWindow( const LPCTSTR pszName, const bool fullscreen = false, const bool altEnter = true );
    void                    WndRunAsStandardApp( const LPCTSTR pszName, const bool fullscreen = false, const bool altEnter = true );
    void                    WndRunAsFramelessApp( const LPCTSTR pszName, const bool fullscreen = false, const bool altEnter = true );
protected:
	inline				    CAppX() : CWndX(), m_device( NULL ), m_context( NULL ) {};
	virtual bool		    Create( ID3D11Device* device, ID3D11DeviceContext* context, const UINT Width, const UINT Height );
	virtual bool		    Resize( const UINT Width, const UINT Height );
	virtual bool		    Update();
	virtual bool		    Render();
	virtual void		    Finish();
    virtual void            Activate( const bool state );
    virtual void            KeyInput( const UINT uiMsg, const WPARAM wParam, const LPARAM lParam );
    virtual void            MouseInput( const UINT uiMsg, const WPARAM wParam, const LPARAM lParam );
    ID3D11Device*           m_device;
    ID3D11DeviceContext*    m_context;
};

