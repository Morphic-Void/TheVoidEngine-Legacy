class CWndX : public CWnd
{
public:
    inline                              CWndX() : CWnd(), m_enableAltEnter( false ), m_enableDepthStencil( false ), m_minimised( false ), m_posChanged( false ), m_toggleCount( 0 ), m_resizeCount( 0 ) {};
    inline                              CWndX( const LPCTSTR pszName, const LONG left, const LONG top, const LONG width, const LONG height, const DWORD dwFlags = WF_DEFAULT, const DWORD dwStyle = WS_DEFAULT, const DWORD dwExStyle = WS_EX_DEFAULT, const HMENU hMenu = NULL, CWnd* const pWndParent = NULL ) : CWnd(), m_enableAltEnter( false ), m_enableDepthStencil( false ), m_toggleCount( 0 ), m_resizeCount( 0 ) { WndInit( pszName, left, top, width, height, dwFlags, dwStyle, dwExStyle, hMenu, pWndParent ); };
    inline                              CWndX( const LPCTSTR pszName, const CWndConfig& Config, CWnd* const pWndParent = NULL ) : CWnd(), m_enableAltEnter( false ), m_enableDepthStencil( false ), m_toggleCount( 0 ), m_resizeCount( 0 ) { WndInit( pszName, Config, pWndParent ); };
    inline                              CWndX( const LPCTSTR pszName, CWnd* const pWndParent = NULL ) : CWnd(), m_enableAltEnter( false ), m_enableDepthStencil( false ), m_toggleCount( 0 ), m_resizeCount( 0 ) { WndInit( pszName, CWndConfig::GetDefaults(), pWndParent ); };
    virtual                             ~CWndX() { WndKill(); };
    bool                                WndInit( const LPCTSTR pszName, const LONG left, const LONG top, const LONG width, const LONG height, const DWORD dwFlags = WF_DEFAULT, const DWORD dwStyle = WS_DEFAULT, const DWORD dwExStyle = WS_EX_DEFAULT, const HMENU hMenu = NULL, CWnd* const pWndParent = NULL );
    bool                                WndInit( const LPCTSTR pszName, const CWndConfig& Config, CWnd* const pWndParent = NULL );
    inline bool                         WndInit( const LPCTSTR pszName, CWnd* const pWndParent = NULL ) { return( WndInit( pszName, CWndConfig::GetDefaults(), pWndParent ) ); };
    inline void                         WndEnableAltEnter( const bool enable = true ) { m_enableAltEnter = enable; };
    inline void                         WndDisableAltEnter() { WndEnableAltEnter( false ); };
    void                                WndRunApp( const bool fullscreen = false, const bool altEnter = true );
protected:
    virtual bool                        WndUpdate();
    virtual bool                        WndDraw();
    virtual LRESULT                     WndUser( HWND hWnd, UINT uiMsg, WPARAM wParam, LPARAM lParam );
    virtual bool                        Create( ID3D11Device* device, ID3D11DeviceContext* context, const UINT Width, const UINT Height );
    virtual bool                        Resize( const UINT Width, const UINT Height );
    virtual bool                        Update();
    virtual bool                        Render();
    virtual void                        Finish();
    virtual void                        Activate( const bool state );
    virtual void                        KeyInput( const UINT uiMsg, const WPARAM wParam, const LPARAM lParam );
    virtual void                        MouseInput( const UINT uiMsg, const WPARAM wParam, const LPARAM lParam );
    inline bool                         ToggleMode() { return( m_swapChain.isWindowed() ? SetFullscreen() : SetWindowed() ); };
    bool                                SetWindowed();
    bool                                SetFullscreen();
    bool                                EnableDepthStencil();
    void                                DisableDepthStencil();
    inline ID3D11RenderTargetView*      GetDisplayView() { return( m_swapChain.getTargetView() ); };
    inline UINT                         GetDisplayWidth() { return( m_swapChain.getWidth() ); };
    inline UINT                         GetDisplayHeight() { return( m_swapChain.getHeight() ); };
    inline ID3D11DepthStencilView*      GetDepthStencilView() { return( m_depthStencil.getTargetView() ); };
    inline ID3D11DepthStencilState*     GetDepthStencilState() { return( m_depthState.getState() ); };
protected:
    bool                                m_enableAltEnter;
    bool                                m_enableDepthStencil;
    bool                                m_minimised;
    bool                                m_posChanged;
    int                                 m_toggleCount;
    int                                 m_resizeCount;
    UINT                                m_resizeWidth;
    UINT                                m_resizeHeight;
    rendering::CSwapChain               m_swapChain;
    rendering::CDepthStencil            m_depthStencil;
    rendering::CDepthStateX             m_depthState;
    static CWndX*                       ms_fullscreenWnd;
    bool                                ResizeDepthStencil();
};

