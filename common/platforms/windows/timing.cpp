void CTiming::reset()
{
    s_enabled = 1;
    s_valid = 0;
    if( QueryPerformanceFrequency( reinterpret_cast< LARGE_INTEGER* >( &s_frequency ) ) )
    {
        s_counter[ 0 ] = s_counter[ 1 ] = 0;
        memset( s_cycles, 0, sizeof( s_cycles ) );
        memset( s_counts, 0, sizeof( s_counts ) );
        s_counterMS = ( 1000.0f / static_cast< float >( s_frequency ) );
        s_valid = 1;
    }
}

void CTiming::update()
{
    if( s_enabled )
    {
        if( s_valid )
        {
            s_valid |= ( s_valid << 1 );    //  1 when starting capture, 3 at beginning of first capture, 7 once we have valid captured data
            s_counter[ READ_INDEX ] = s_counter[ WRITE_INDEX ];
            memcpy( s_cycles[ READ_INDEX ], s_cycles[ WRITE_INDEX ], sizeof( s_cycles[ READ_INDEX ] ) );
            memcpy( s_counts[ READ_INDEX ], s_counts[ WRITE_INDEX ], sizeof( s_counts[ READ_INDEX ] ) );
            memset( s_cycles[ WRITE_INDEX ], 0, sizeof( s_cycles[ WRITE_INDEX ] ) );
            memset( s_counts[ WRITE_INDEX ], 0, sizeof( s_counts[ WRITE_INDEX ] ) );
            if( QueryPerformanceCounter( reinterpret_cast< LARGE_INTEGER* >( &s_counter[ WRITE_INDEX ] ) ) )
            {
                s_counter[ READ_INDEX ] = ( s_counter[ WRITE_INDEX ] - s_counter[ READ_INDEX ] );
                if( s_counter[ READ_INDEX ] == 0 ) s_counter[ READ_INDEX ] = 1;
                s_cycles[ WRITE_INDEX ][ ETimingUpdate ] = __rdtsc();
                s_cycles[ READ_INDEX ][ ETimingUpdate ] = ( s_cycles[ WRITE_INDEX ][ ETimingUpdate ] - s_cycles[ READ_INDEX ][ ETimingUpdate ] );
                if( s_cycles[ READ_INDEX ][ ETimingUpdate ] == 0 ) s_cycles[ READ_INDEX ][ ETimingUpdate ] = 1;
                s_cyclesToMS = ( ( static_cast< float >( s_counter[ READ_INDEX ] ) / static_cast< float >( s_cycles[ READ_INDEX ][ ETimingUpdate ] ) ) * s_counterMS );
            }
            else
            {   //  if QueryPerformanceCounter() fails then we need to mark the current data as invalid and start a new capture sequence
                s_valid = 1;
            }
        }
        else
        {   //  try to reset if enabled but not processing a capture sequence
            reset();
        }
    }
}

