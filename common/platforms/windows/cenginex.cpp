//! initialise camera, objects etc.
bool CEngineX::Create( ID3D11Device* device, ID3D11DeviceContext* context, const UINT Width, const UINT Height )
{
    return( CAppX::Create( device, context, Width, Height ) ? m_engine.create( m_swapChain, m_depthStencil, device, context ) : false );
}

//! update cameras and buffers (other than the main swap-chain) to reflect window resizing and possible aspect ratio changes
bool CEngineX::Resize( const UINT Width, const UINT Height )
{
    return( CAppX::Resize( Width, Height ) ? m_engine.resize() : false );
}

//! update the scene for each frame
bool CEngineX::Update()
{
    return( CAppX::Update() ? m_engine.update( CTiming::getMS( CTiming::getCycles( ETimingUpdate ) ), CCoreX::getFirstConnectedController() ) : false );
}

//! render the scene
bool CEngineX::Render()
{
    return( CAppX::Render() ? m_engine.render() : false );
}

//! tidy up any assets ready to exit the application
void CEngineX::Finish()
{
    m_engine.destroy();
    return( CAppX::Finish() );
}

//! process activation and deactivation
void CEngineX::Activate( const bool state )
{
    m_engine.activate( m_hWnd, state );
}

//! process the keyboard input stream
void CEngineX::KeyInput( const UINT uiMsg, const WPARAM wParam, const LPARAM lParam )
{
    m_engine.keyInput( m_hWnd, uiMsg, wParam, lParam );
}

//! process the mouse input stream
void CEngineX::MouseInput( const UINT uiMsg, const WPARAM wParam, const LPARAM lParam )
{
    m_engine.mouseInput( m_hWnd, uiMsg, wParam, lParam );
}

