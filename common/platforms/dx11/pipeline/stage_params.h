//  Shader stage parameter description structure
//
//      purpose:
//
//          Describes a stage input or output parameter.
//
//      important member notes:
//
//          semanticIndex layout:
//              bits 0:15  -    16 bits -   Semantic index
//              bits 16:31 -    16 bits -   Geometry shader stream index
//
//          validationData layout:
//              bits 0:3   -     4 bits -   Mask
//              bits 4:7   -     4 bits -   ReadWriteMask
//              bits 8:15  -     8 bits -   D3D_MIN_PRECISION
//              bits 16:23 -     8 bits -   D3D_REGISTER_COMPONENT_TYPE
//              bits 24:31 -     8 bits -   D3D_NAME
//
struct StageParam
{
    uint                        semanticHandle;     //  handle of the semantic name
    uint                        semanticIndex;      //  index of the semantic (and geometry shader stream index - see notes with structure)
    UINT                        registerIndex;      //  register index
    BYTE                        mask;
    BYTE                        readWriteMask;
    D3D_MIN_PRECISION           minPrecision;
    D3D_REGISTER_COMPONENT_TYPE componentType;
    D3D_NAME                    d3dName;
    inline uint                 getSemanticIndex() const { return( semanticIndex & 0x0000ffff ); };
    inline uint                 getStreamIndex() const { return( ( semanticIndex & 0xffff0000 ) >> 16 ); };
    inline void                 setSemanticIndex( const UINT index ) { semanticIndex = ( ( semanticIndex & ~0x0000ffff ) | ( static_cast< uint >( index ) & 0x0000ffff ) ); };
    inline void                 setStreamIndex( const UINT index ) { semanticIndex = ( ( semanticIndex & ~0xffff0000 ) | ( ( static_cast< uint >( index ) << 16 ) & 0xffff0000 ) ); };
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    StageParams base class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

using StageParamsBase = TArraySort< StageParam >;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Shader stage parameter container class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class StageParams : public StageParamsBase
{
public:
    inline                          StageParams() : StageParamsBase(), m_strings( NULL ) {};
    inline                          StageParams( StringTableBlob& strings, const uint count = 32 ) : StageParamsBase(), m_strings( NULL ) { init( strings, count ); };
    virtual                         ~StageParams() {  m_strings = NULL; };
    inline bool                     valid() const { return( ( m_strings != NULL ) ? true : false ); };
    bool                            init( StringTableBlob& strings, const uint count = 32 );
    inline void                     kill() { m_strings = NULL; StageParamsBase::kill(); };
    int                             find( const uint8_t* const semanticName, const uint semanticIndex ) const;
    int                             find( const uint8_t* const semanticName, const uint semanticLength, const uint semanticIndex ) const;
    inline int                      find( const char* const semanticName, const uint semanticIndex ) const { return( find( reinterpret_cast< const uint8_t* const >( semanticName ), semanticIndex ) ); };
    inline int                      find( const char* const semanticName, const uint semanticLength, const uint semanticIndex ) const { return( find( reinterpret_cast< const uint8_t* const >( semanticName ), semanticLength, semanticIndex ) ); };
	int                             insert( const StageParam& param );
	bool                            update( const int index, const StageParam& param );
protected:
    struct                          KeyDesc { const uint8_t* semanticName; uint semanticLength; uint semanticIndex; };
    virtual const void*             getKey( const int index ) const;
    virtual int                     cmpKey( const void* const key, const int index ) const;
    StringTableBlob*                m_strings;
    mutable KeyDesc                 m_key;
private:
    inline                          StageParams( const StageParams& ) {};
    inline void                     operator=( const StageParams& ) const {};
};

