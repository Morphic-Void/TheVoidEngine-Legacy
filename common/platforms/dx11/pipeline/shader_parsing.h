////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    ShaderLink : shader linking structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct ShaderLink
{
    uint                    nameHandle;                     //  handle of the shader name
    int                     chainIndex;                     //  index of the shader chain
    int                     assemblyIndex;                  //  index of the vertex assembly
    int                     streamOutIndex;                 //  index of the stream-out vertex assembly
    uint                    indicesNameHandle;              //  handle of the index buffer name
    uint                    indirectNameHandle;             //  handle of the indirect args buffer name
    uint                    renderTargetCount;              //  count of render targets
    uint                    renderTargetNameHandles[ 8 ];   //  handles of the render-target names
    uint                    depthStencilNameHandle;         //  handle of the depth-stencil name
    uint                    depthStateNameHandle;           //  handle of the depth state name
    uint                    rasterStateNameHandle;          //  handle of the raster state name
    uint                    blendStateNameHandle;           //  handle of the blend state name
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    ShaderParser base class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

using ShaderParserBase = TArrayNamed< ShaderLink >;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    ShaderParser : shader description parsing and collection class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class ShaderParser : public ShaderParserBase
{
public:
    inline                  ShaderParser() : ShaderParserBase(), m_stages( NULL ), m_streams( NULL ), m_linkages( NULL ), m_assemblies( NULL ) {};
    inline                  ShaderParser( StringTableBlob& strings, const StageParser& stages, const StreamParser& streams, const LinkageParser& linkages, const AssemblyParser& assemblies, const uint count = 32 ) : ShaderParserBase(), m_stages( NULL ), m_streams( NULL ), m_linkages( NULL ), m_assemblies( NULL ) { init( strings, stages, streams, linkages, assemblies, count ); };
    virtual                 ~ShaderParser() { m_stages = NULL; m_streams = NULL; m_linkages = NULL; m_assemblies = NULL; };
    inline bool             valid() const { return( ( ( m_stages != NULL ) && ( m_assemblies != NULL ) && ( m_linkages != NULL ) ) ? ShaderParserBase::valid() : false ); };
    bool                    init( StringTableBlob& strings, const StageParser& stages, const StreamParser& streams, const LinkageParser& linkages, const AssemblyParser& assemblies, const uint count = 32 );
    inline void             kill() { m_stages = NULL; m_streams = NULL; m_linkages = NULL; m_assemblies = NULL; ShaderParserBase::kill(); };
    bool                    parseArray( const QueryTree& queryTree, const uint32_t root );
    bool                    parse( const QueryTree& queryTree, const uint32_t root );
protected:
    bool                    validateInputAssembly( const char* shaderName, ShaderLink& shaderLink, const ChainLink& chainLink ) const;
    bool                    validateOutputAssembly( const char* shaderName, ShaderLink& shaderLink, const ChainLink& chainLink ) const;
    void                    validateComputeShader( const char* shaderName, ShaderLink& shaderLink ) const;
    bool                    validatePixelShader( const char* shaderName, ShaderLink& shaderLink, const ChainLink& chainLink ) const;
    const StageParser*      m_stages;
    const StreamParser*     m_streams;
    const LinkageParser*    m_linkages;
    const AssemblyParser*   m_assemblies;
private:
    inline                  ShaderParser( const ShaderParser& ) {};
    inline void             operator=( const ShaderParser& ) const {};
};

