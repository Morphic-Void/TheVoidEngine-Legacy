////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    StreamElement : vertex stream element description structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct StreamElement
{
    uint                            nameHandle;                 //  handle of the element name
    uint                            sourceHandle;               //  handle of the source string
    uint                            dataFormat;                 //  rendering::EElementFormat
    uint                            dataSize;                   //  size of the element in bytes
    uint                            dataOffset;                 //  offset of the item in the stream
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    StreamElementParserBase : StreamElementParser base class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

using StreamElementParserBase = TArrayNamed< StreamElement >;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    StreamElementParser : vertex stream element parsing and collection class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class StreamElementParser : public StreamElementParserBase
{
public:
    inline                          StreamElementParser() : StreamElementParserBase(),  m_stride( 0 ) {};
    inline                          StreamElementParser( StringTableBlob& strings, const uint count = 32 ) : StreamElementParserBase(), m_stride( 0 ) { init( strings, count ); };
    virtual                         ~StreamElementParser() { m_stride = 0; };
    bool                            init( StringTableBlob& strings, const uint count = 32 );
    inline void                     kill() { m_stride = 0; StreamElementParserBase::kill(); };
    bool                            parseArray( const QueryTree& queryTree, const uint32_t root );
    bool                            parse( const QueryTree& queryTree, const uint32_t root );
    inline uint                     getStride() const { return( ( m_stride + 3 ) & ~3 ); };
protected:
    uint                            m_stride;
private:
    inline                          StreamElementParser( const StreamElementParser& ) {};
    inline void                     operator=( const StreamElementParser& ) const {};
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    StreamLink : vertex stream name mapping structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct StreamLink
{
    uint                            nameHandle;                 //  handle of the stream name
    StreamElementParser*            elements;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    StreamParserBase : StreamParser base class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

using StreamParserBase = TArrayNamed< StreamLink >;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    StreamParser : vertex stream parsing and collection class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class StreamParser : public StreamParserBase
{
public:
    inline                          StreamParser() : StreamParserBase(), m_elements() {};
    inline                          StreamParser( StringTableBlob& strings, const uint count = 32 ) : StreamParserBase(), m_elements() { init( strings, count ); };
    virtual                         ~StreamParser() { m_elements.kill(); };
    bool                            init( StringTableBlob& strings, const uint count = 32 );
    inline void                     kill() { m_elements.kill(); StreamParserBase::kill(); };
    bool                            parseArray( const QueryTree& queryTree, const uint32_t root );
    bool                            parse( const QueryTree& queryTree, const uint32_t root );
protected:
    TPool< StreamElementParser >    m_elements;
private:
    inline                          StreamParser( const StreamParser& ) {};
    inline void                     operator=( const StreamParser& ) const {};
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    LinkageElement : vertex linkage element description structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct LinkageElement
{
    uint                            nameHandle;                 //  handle of the stream element name
    uint                            semanticHandle;             //  handle of the binding semantic name
    uint                            semanticIndex;              //  index of the binding semantic
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    LinkageElementParserBase : LinkageElementParser base class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

using LinkageElementParserBase = TArrayNamed< LinkageElement >;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    LinkageElementParser : vertex linkage element parsing and collection class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class LinkageElementParser : public LinkageElementParserBase
{
public:
    inline                          LinkageElementParser() : LinkageElementParserBase() {};
    inline                          LinkageElementParser( StringTableBlob& strings, const uint count = 32 ) : LinkageElementParserBase() { init( strings, count ); };
    virtual                         ~LinkageElementParser() {};
    bool                            parseArray( const QueryTree& queryTree, const uint32_t root );
    bool                            parse( const QueryTree& queryTree, const uint32_t root );
    int                             findSemantic( const uint semanticHandle, const uint semanticIndex ) const;
    int                             findSemantic( const uint8_t* const semanticName, const uint semanticIndex ) const;
    int                             findSemantic( const uint8_t* const semanticName, const uint length, const uint semanticIndex ) const;
    inline int                      findSemantic( const char* const semanticName, const uint semanticIndex ) const { return( findSemantic( reinterpret_cast< const uint8_t* const >( semanticName ), semanticIndex ) ); };
    inline int                      findSemantic( const char* const semanticName, const uint semanticLength, const uint semanticIndex ) const { return( findSemantic( reinterpret_cast< const uint8_t* const >( semanticName ), semanticLength, semanticIndex ) ); };
private:
    inline                          LinkageElementParser( const LinkageElementParser& ) {};
    inline void                     operator=( const LinkageElementParser& ) const {};
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    LinkageLink : vertex linkage name mapping structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct LinkageLink
{
    uint                            nameHandle;                 //  handle of the linkage name
    LinkageElementParser*           elements;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    LinkageParserBase : LinkageParser base class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

using LinkageParserBase = TArrayNamed< LinkageLink >;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    LinkageParser : vertex linkage parsing and collection class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class LinkageParser : public LinkageParserBase
{
public:
    inline                          LinkageParser() : LinkageParserBase(), m_elements() {};
    inline                          LinkageParser( StringTableBlob& strings, const uint count = 32 ) : LinkageParserBase(), m_elements() { init( strings, count ); };
    virtual                         ~LinkageParser() { m_elements.kill(); };
    bool                            init( StringTableBlob& strings, const uint count = 32 );
    inline void                     kill() { m_elements.kill(); LinkageParserBase::kill(); };
    bool                            parseArray( const QueryTree& queryTree, const uint32_t root );
    bool                            parse( const QueryTree& queryTree, const uint32_t root );
    void                            listConflicts( const int linkage1, const int linkage2 ) const;
    bool                            linkagesConflict( const int linkage1, const int linkage2 ) const;
protected:
    TPool< LinkageElementParser >   m_elements;
private:
    inline                          LinkageParser( const LinkageParser& ) {};
    inline void                     operator=( const LinkageParser& ) const {};
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    AssemblyElement : vertex assembly stream and linkage binding description structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct AssemblyElement
{
    uint                            nameHandle;                 //  handle of the binding resource name
    uint                            streamIndex;                //  stream binding index
    uint                            instanceRate;               //  instance rate
    int                             streamLinkIndex;            //  index of the StreamLink within the StreamParser
    int                             linkageLinkIndex;           //  index of the LinkageLink within the LinkageParser
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    AssemblyElementParserBase : AssemblyElementParser base class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

using AssemblyElementParserBase = TArrayNamed< AssemblyElement >;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    AssemblyElementParser : vertex assembly stream and linkage binding parsing and collection class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class AssemblyElementParser : public AssemblyElementParserBase
{
public:
    inline                          AssemblyElementParser() : AssemblyElementParserBase(), m_streams( NULL ), m_linkages( NULL ) {};
    inline                          AssemblyElementParser( StringTableBlob& strings, const StreamParser& streams, const LinkageParser& linkages, const uint count = 32 ) : AssemblyElementParserBase(), m_streams( NULL ), m_linkages( NULL ) { init( strings, streams, linkages, count ); };
    virtual                         ~AssemblyElementParser() { m_streams = NULL; m_linkages = NULL; };
    inline bool                     valid() const { return( ( ( m_streams!= NULL ) && ( m_linkages != NULL ) ) ? true : false ); };
    bool                            init( StringTableBlob& strings, const StreamParser& streams, const LinkageParser& linkages, const uint count = 32 );
    inline void                     kill() { m_streams = NULL; m_linkages = NULL; AssemblyElementParserBase::kill(); };
    bool                            parseArray( const QueryTree& queryTree, const uint32_t root );
    bool                            parse( const QueryTree& queryTree, const uint32_t root );
    void                            listConflicts() const;
    bool                            linkagesConflict() const;
    bool                            singleElementStreams() const;
    bool                            streamOutCompatibleTypes() const;
    bool                            containsSemantic( const uint semanticHandle, const uint semanticIndex ) const;
    bool                            containsSemantic( const uint8_t* const semanticName, const uint semanticIndex ) const;
    bool                            containsSemantic( const uint8_t* const semanticName, const uint semanticLength, const uint semanticIndex ) const;
    inline bool                     containsSemantic( const char* const semanticName, const uint semanticIndex ) const { return( containsSemantic( reinterpret_cast< const uint8_t* const >( semanticName ), semanticIndex ) ); };
    inline bool                     containsSemantic( const char* const semanticName, const uint semanticLength, const uint semanticIndex ) const { return( containsSemantic( reinterpret_cast< const uint8_t* const >( semanticName ), semanticLength, semanticIndex ) ); };
    bool                            matchingDataSize( const uint dataSize, const int elementIndex, const uint semanticHandle, const uint semanticIndex ) const;
    bool                            matchingDataSize( const uint dataSize, const int elementIndex, const uint8_t* const semanticName, const uint semanticIndex ) const;
    bool                            matchingDataSize( const uint dataSize, const int elementIndex, const uint8_t* const semanticName, const uint semanticLength, const uint semanticIndex ) const;
    inline bool                     matchingDataSize( const uint dataSize, const int elementIndex, const char* const semanticName, const uint semanticIndex ) const { return( matchingDataSize( dataSize, elementIndex, reinterpret_cast< const uint8_t* const >( semanticName ), semanticIndex ) ); };
    inline bool                     matchingDataSize( const uint dataSize, const int elementIndex, const char* const semanticName, const uint semanticLength, const uint semanticIndex ) const { return( matchingDataSize( dataSize, elementIndex, reinterpret_cast< const uint8_t* const >( semanticName ), semanticLength, semanticIndex ) ); };
    const AssemblyElement*          getStreamElement( const uint streamIndex ) const;
    inline const StreamParser*      getStreams() const { return( m_streams ); };
    inline const LinkageParser*     getLinkages() const { return( m_linkages ); };
protected:
    const StreamParser*             m_streams;
    const LinkageParser*            m_linkages;
private:
    inline                          AssemblyElementParser( const AssemblyElementParser& ) {};
    inline void                     operator=( const AssemblyElementParser& ) const {};
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    AssemblyLink : vertex assembly bind mapping structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct AssemblyLink
{
    uint                            nameHandle;                 //  handle of the assembly name
    bool                            linkagesConflict;
    bool                            singleElementStreams;
    bool                            streamOutCompatibleTypes;
    AssemblyElementParser*          elements;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    AssemblyParserBase : AssemblyParser base class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

using AssemblyParserBase = TArrayNamed< AssemblyLink >;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    AssemblyParser : vertex assembly parsing and collection class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class AssemblyParser : public AssemblyParserBase
{
public:
    inline                          AssemblyParser() : AssemblyParserBase(), m_elements(), m_streams( NULL ), m_linkages( NULL ) {};
    inline                          AssemblyParser( StringTableBlob& strings, const StreamParser& streams, const LinkageParser& linkages, const uint count = 32 ) : AssemblyParserBase(), m_elements(), m_streams( NULL ), m_linkages( NULL ) { init( strings, streams, linkages, count ); };
    virtual                         ~AssemblyParser() { m_streams = NULL; m_linkages = NULL; m_elements.kill(); };
    inline bool                     valid() const { return( ( ( m_streams!= NULL ) && ( m_linkages != NULL ) ) ? true : false ); };
    bool                            init( StringTableBlob& strings, const StreamParser& streams, const LinkageParser& linkages,const uint count = 32 );
    inline void                     kill() { m_streams = NULL; m_linkages = NULL; m_elements.kill(); AssemblyParserBase::kill(); };
    bool                            parseArray( const QueryTree& queryTree, const uint32_t root );
    bool                            parse( const QueryTree& queryTree, const uint32_t root );
    bool                            assembliesConflict( const int assembly1, const int assembly2 ) const;
    inline const StreamParser*      getStreams() const { return( m_streams ); };
    inline const LinkageParser*     getLinkages() const { return( m_linkages ); };
protected:
    TPool< AssemblyElementParser >  m_elements;
    const StreamParser*             m_streams;
    const LinkageParser*            m_linkages;
private:
    inline                          AssemblyParser( const AssemblyParser& ) {};
    inline void                     operator=( const AssemblyParser& ) const {};
};

