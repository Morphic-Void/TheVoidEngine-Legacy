bool StageBindings::init( BoundResources& resources, const uint count )
{
    kill();
    uint size = ( ( count < 4 ) ? 4 : count );
    if( StageBindingsBase::init( size ) )
    {
        m_resources = &resources;
        return( true );
    }
    kill();
    return( false );
}

int StageBindings::cmpKey( const void* const key, const int index ) const
{
    const StageBinding& source = *reinterpret_cast< const StageBinding* >( key );
    const StageBinding& target = m_items.getItem( index );
    int delta = static_cast< int >( target.resourceType - source.resourceType );
    if( delta == 0 )
    {
        delta = static_cast< int >( target.bindingPoint - source.bindingPoint );
    }
    return( delta );
}

