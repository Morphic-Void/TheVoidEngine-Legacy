bool ResourceMapping::init( StringTableBlob& strings, ResourceItems& resources, const uint count )
{
    kill();
    uint size = ( ( count < 4 ) ? 4 : count );
    if( TArraySort< uint32_t >::init( size ) )
    {
        m_resources = &resources;
        m_strings = &strings;
        return( true );
    }
    kill();
    return( false );
}

void ResourceMapping::clone( const ResourceMapping& source )
{
    kill();
    TArraySort< uint32_t >::clone( source );
    m_resources = source.m_resources;
    m_strings = source.m_strings;
}

int ResourceMapping::find( const uint8_t* const name, uint resourceType ) const
{
    if( name != NULL )
    {
        uint length = 0;
        while( name[ length ] ) ++length;
        return( find( name, length, resourceType ) );
    }
    return( -1 );
}

int ResourceMapping::find( const uint8_t* const name, const uint length, uint resourceType ) const
{
    if( ( m_resources != NULL ) && ( m_strings != NULL ) )
    {
        if( name != NULL )
        {
            m_key = { name, length, resourceType };
            return( ArraySort::find( &m_key ) );
        }
        return( -1 );
    }
    ASSERT_FAIL( "ResourceMapping uninitialised" );
    return( -1 );
}

int ResourceMapping::insert( const uint32_t resourceItemIndex )
{
    if( ( m_resources != NULL ) && ( m_strings != NULL ) )
    {
        const binary::ResourceItem* resource = m_resources->access( static_cast< int >( resourceItemIndex ) );
        if( resource != NULL )
        {
            m_key.name = m_strings->getString( resource->nameHandle, m_key.length );
            m_key.resourceType = static_cast< uint >( resource->resourceType );
            int index = ArraySort::insert( &m_key );
            if( index >= 0 )
            {
                m_items.setItem( static_cast< uint >( index ), resourceItemIndex );
            }
            return( index );
        }
        ASSERT_FAIL( "ResourceMapping resource not found" );
    }
    else
    {
        ASSERT_FAIL( "ResourceMapping uninitialised" );
    }
    return( -1 );
}

const void* ResourceMapping::getKey( const int index ) const
{
    if( ( m_resources != NULL ) && ( m_strings != NULL ) )
    {
        const binary::ResourceItem* resource = m_resources->access( m_items.getItem( static_cast< uint >( index ) ) );
        if( resource != NULL )
        {
            m_key.name = m_strings->getString( resource->nameHandle, m_key.length );
            m_key.resourceType = static_cast< uint >( resource->resourceType );
            return( &m_key );
        }
    }
    return( NULL );
}

int ResourceMapping::cmpKey( const void* const key, const int index ) const
{
    int delta = 0;
    if( ( m_resources != NULL ) && ( m_strings != NULL ) )
    {
        const binary::ResourceItem* resource = m_resources->access( m_items.getItem( static_cast< uint >( index ) ) );
        if( resource != NULL )
        {
            const KeyDesc& key1 = *reinterpret_cast< const KeyDesc* const >( key );
            KeyDesc key2;
            key2.name = m_strings->getString( resource->nameHandle, key2.length );
            key2.resourceType = static_cast< uint >( resource->resourceType );
            delta = static_cast< int >( key1.resourceType - key2.resourceType );
            if( delta == 0 )
            {
                delta = static_cast< int >( key1.length - key2.length );
                if( delta == 0 )
                {
                    delta = memcmp( key1.name, key2.name, key2.length );
                }
            }
        }
    }
    return( delta );
}

