////
////    BoundResource bindable resource description structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//  Resource binding structure
//
//      purpose:
//
//          Describes a bindable resource.
//
struct BoundResource
{
    uint                        nameHandle;         //  handle of the resource name
    uint                        resourceType;       //  EResourceType
    UINT                        bindCount;          //  array resources binding slot count
    D3D_SHADER_INPUT_TYPE       inputType;
    UINT                        inputFlags;
    D3D_CBUFFER_TYPE            cbufferType;
    UINT                        cbufferFlags;
    D3D_SRV_DIMENSION           srvDimension;
    D3D_RESOURCE_RETURN_TYPE    returnType;
    UINT                        sampleCount;
    UINT                        size;               //  stride for structured buffers and stream buffers or size for constant buffers
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    BoundResources BoundResource collection class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

using BoundResources = TArrayResource< BoundResource >;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    StageBinding BoundResource indirection structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct StageBinding
{
    int                 resourceIndex;      //  index of the BoundResource structure
    uint                bindingPoint;       //  hardware bind point index
    uint                resourceType;       //  EResourceType (same as the referenced BoundResource)
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    StageBindings base class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

using StageBindingsBase = TArraySort< StageBinding >;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    StageBindings shader resource binding container class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class StageBindings : public StageBindingsBase
{
public:
    inline                          StageBindings() : StageBindingsBase(), m_resources( NULL ) {};
    inline                          StageBindings( BoundResources& resources, const uint count = 32 ) : StageBindingsBase(), m_resources( NULL ) { init( resources, count ); };
    virtual                         ~StageBindings() { m_resources = NULL; };
    inline bool                     valid() const { return( ( m_resources != NULL ) ? true : false ); };
    bool                            init( BoundResources& resources, const uint count = 32 );
    inline void                     kill() { m_resources = NULL; StageBindingsBase::kill(); };
protected:
    BoundResources*                 m_resources;
    virtual int                     cmpKey( const void* const key, const int index ) const;
private:
    inline                          StageBindings( const StageBindings& ) {};
    inline void                     operator=( const StageBindings& ) const {};
};

