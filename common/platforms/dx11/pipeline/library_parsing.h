////
////    ResourceLibraryParser : resource library .json parsing and binary file builder
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class ResourceLibraryParser
{
public:
    inline                  ResourceLibraryParser() { init(); };
    inline                  ~ResourceLibraryParser() { kill(); };
    bool                    init();
    void                    kill();
    inline bool             valid() const { return( !m_error ); };
    inline void             reset() { init(); };
    bool                    parse( const char* const json_file );
    bool                    build( const char* const bin_file );
protected:
    bool                    m_error;
    StringTableBlob         m_strings;
    SamplerParser           m_samplers;
    RasterStateParser       m_rasterStates;
    DepthStateParser        m_depthStates;
    BlendStateParser        m_blendStates;
private:
    inline                  ResourceLibraryParser( const ResourceLibraryParser& ) {};
    inline void             operator=( const ResourceLibraryParser& ) const {};
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    ShaderLibraryParser : shader library .json parsing and binary file builder
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class ShaderLibraryParser
{
public:
    inline                  ShaderLibraryParser() { init(); };
    inline                  ~ShaderLibraryParser() { kill(); };
    bool                    init();
    void                    kill();
    inline bool             valid() const { return( !m_error ); };
    inline void             reset() { init(); };
    bool                    parse( const char* const json_file );
    bool                    build( const char* const bin_file );
protected:
    void                    prepareParse();
    void                    cleanupParse();
    bool                    m_error;
    ShaderParsing           m_parse;
private:
    inline                  ShaderLibraryParser( const ShaderLibraryParser& ) {};
    inline void             operator=( const ShaderLibraryParser& ) const {};
};

