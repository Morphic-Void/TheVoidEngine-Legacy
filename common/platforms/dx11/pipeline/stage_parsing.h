////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    D3D11_BINDING_LINK compound structure of related D3D11 input binding structures and their associated shader stage
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct D3D11_BINDING_LINK
{
    D3D11_SHADER_INPUT_BIND_DESC    bindingDesc;
    D3D11_SHADER_BUFFER_DESC        bufferDesc;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    StageDesc reflected shader description structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct StageDesc
{
    StageParams                     inputs;
    StageParams                     outputs;
    StageBindings                   bindings;
    binary::BlobDesc                binaryBlob;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    StageLink reflected shader linking structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct StageLink
{
    uint                            nameHandle;                             //  handle of the file name
    StageDesc*                      stageDesc;                              //  pointer to the reflected shader data
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    ChainLink stage chain linkage structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct ChainLink
{
    uint                            nameHandle;                             //  index of the shader chain name
    bool                            streamOutOnly;                          //  this shader can only be stream-out
    int                             streamOutStage;                         //  stream-out stage index
    uint                            stageFlags;                             //  flags for the used stages
    int                             stages[ binary::EShaderStageCount ];    //  indices of the used shaders
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Templated type definitions
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

using StageDescs = TPool< StageDesc >;
using StageLinks = TArrayNamed< StageLink >;
using ChainLinks = TArrayNamed< ChainLink >;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    StageParser : stage chain parsing and collection class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class StageParser : public ChainLinks
{
public:
    inline                          StageParser() : ChainLinks(), m_binaries( NULL ), m_resources( NULL ) {};
    inline                          StageParser( StringTableBlob& strings, Blob& binaries, BoundResources& resources, const uint count = 32 ) : ChainLinks(), m_binaries( NULL ), m_resources( NULL ) { init( strings, binaries, resources, count ); };
    virtual                         ~StageParser() { cleanUp(); };
    inline bool                     valid() const { return( ( ( m_binaries != NULL ) && ( m_resources != NULL ) ) ? ChainLinks::valid() : false ); };
    bool                            init( StringTableBlob& strings, Blob& binaries, BoundResources& resources, const uint count = 32 );
    inline void                     kill() { cleanUp(); ChainLinks::kill(); };
    inline const uint               getStageCount() const { return( m_stageDescs.getUsed() ); };
    const StageDesc*                accessStage( const int index ) const;
    bool                            parseArray( const QueryTree& queryTree, const uint32_t root );
    bool                            parse( const QueryTree& queryTree, const uint32_t root );
protected:
    static const int                MAX_CB = 16;
    static const int                MAX_SRV = 128;
    static const int                MAX_UAV = 64;
    static const int                MAX_SS = 16;
    void                            cleanUp();
    int                             addStage( const char* const file_name );
    int                             findBinary( const void* const binaryData, const size_t binarySize ) const;
    bool                            reflect( ID3D11ShaderReflection* const reflection, StageDesc& stageDesc );
    void                            translate( const D3D11_SIGNATURE_PARAMETER_DESC& d3dParamDesc, StageParam& param );
    bool                            translate( const D3D11_BINDING_LINK& d3dBindingLink, StageBinding& binding );
    bool                            validate( const int sourceStage, const int targetStage ) const;
    Blob*                           m_binaries;
    BoundResources*                 m_resources;
    StringTableBlob                 m_files;
    StageDescs                      m_stageDescs;
    StageLinks                      m_stageLinks;
    bool                            m_streamOutOnly;
private:
    inline                          StageParser( const StageParser& ) {};
    inline void                     operator=( const StageParser& ) const {};
};

