using ResourceItems   = TArrayResource< binary::ResourceItem >;
using ResourceIndices = TBlob< uint32_t >;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    ResourceMapping : shader resource mapping
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class ResourceMapping : public TArraySort< uint32_t >
{
public:
    inline                      ResourceMapping() : TArraySort< uint32_t >(), m_resources( NULL ), m_strings( NULL ) {};
    inline                      ResourceMapping( StringTableBlob& strings, ResourceItems& resources, const uint count = 32 ) : TArraySort< uint32_t >(), m_resources( NULL ), m_strings( NULL ) { init( strings, resources, count ); };
    virtual                     ~ResourceMapping() { m_resources = NULL; m_strings = NULL; };
    inline bool                 valid() const { return( ( ( m_resources!= NULL ) && ( m_strings != NULL ) ) ? TArraySort< uint32_t >::valid() : false ); };
    void                        clone( const ResourceMapping& source );
    bool                        init( StringTableBlob& strings, ResourceItems& resources, const uint count = 32 );
    inline void                 kill() { m_resources = NULL; m_strings = NULL; TArraySort< uint32_t >::kill(); };
    int                         find( const uint8_t* const name, const uint resourceType ) const;
    int                         find( const uint8_t* const name, const uint length, const uint resourceType ) const;
    inline int                  find( const char* const name, const uint resourceType ) const { return( find( reinterpret_cast< const uint8_t* const >( name ), resourceType ) ); };
    inline int                  find( const char* const name, const uint length, const uint resourceType ) const { return( find( reinterpret_cast< const uint8_t* const >( name ), length, resourceType ) ); };
	int                         insert( const uint32_t resourceItemIndex );
protected:
    struct                      KeyDesc { const uint8_t* name; uint length; uint resourceType; };
    virtual const void*         getKey( const int index ) const;
    virtual int                 cmpKey( const void* const key, const int index ) const;
    ResourceItems*              m_resources;
    StringTableBlob*            m_strings;
    mutable KeyDesc             m_key;
private:
    inline                      ResourceMapping( const ResourceMapping& ) {};
    inline void                 operator=( const ResourceMapping& ) const {};
};

