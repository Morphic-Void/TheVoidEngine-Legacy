bool StageParams::init( StringTableBlob& strings, const uint count )
{
    kill();
    uint size = ( ( count < 4 ) ? 4 : count );
    if( StageParamsBase::init( size ) )
    {
        m_strings = &strings;
        return( true );
    }
    kill();
    return( false );
}

int StageParams::find( const uint8_t* const semanticName, const uint semanticIndex ) const
{
    if( semanticName != NULL )
    {
        uint semanticLength = 0;
        while( semanticName[ semanticLength ] ) ++semanticLength;
        return( find( semanticName, semanticLength, semanticIndex ) );
    }
    return( -1 );
}

int StageParams::find( const uint8_t* const semanticName, const uint semanticLength, const uint semanticIndex ) const
{
    if( m_strings != NULL )
    {
        if( semanticName != NULL )
        {
            m_key = { semanticName, semanticLength, semanticIndex };
            return( ArraySort::find( &m_key ) );
        }
        return( -1 );
    }
    ASSERT_FAIL( "ParamDescs uninitialised\n" );
    return( -1 );
}

int StageParams::insert( const StageParam& param )
{
    if( m_strings != NULL )
    {
        m_key.semanticName = m_strings->getString( param.semanticHandle, m_key.semanticLength );
        m_key.semanticIndex = param.semanticIndex;
        int index = ArraySort::insert( &m_key );
        if( index >= 0 )
        {
            m_items.setItem( static_cast< uint >( index ), param );
        }
        return( index );
    }
    ASSERT_FAIL( "ParamDescs uninitialised\n" );
    return( -1 );
}

bool StageParams::update( const int index, const StageParam& param )
{
    if( m_strings != NULL )
    {
        if( contains( index ) )
        {
            m_items.setItem( static_cast< uint >( index ), param );
            ArraySort::update( index );
            return( true );
        }
        ASSERT_FAIL( "ParamDescs::update() failed\n" );
        return( false );
    }
    ASSERT_FAIL( "ParamDescs uninitialised\n" );
    return( false );
}

const void* StageParams::getKey( const int index ) const
{
    if( m_strings != NULL )
    {
        const StageParam& param = m_items.getItem( static_cast< uint >( index ) );
        m_key.semanticName = m_strings->getString( param.semanticHandle, m_key.semanticLength );
        m_key.semanticIndex = param.semanticIndex;
        return( &m_key );
    }
    return( NULL );
}

int StageParams::cmpKey( const void* const key, const int index ) const
{
    int delta = 0;
    if( m_strings != NULL )
    {
        const StageParam& param = m_items.getItem( static_cast< uint >( index ) );
        const KeyDesc& key1 = *reinterpret_cast< const KeyDesc* const >( key );
        KeyDesc key2;
        key2.semanticName = m_strings->getString( param.semanticHandle, key2.semanticLength );
        key2.semanticIndex = param.semanticIndex;
        delta = static_cast< int >( key1.semanticIndex - key2.semanticIndex );
        if( delta == 0 )
        {
            delta = static_cast< int >( key1.semanticLength - key2.semanticLength );
            if( delta == 0 )
            {
                delta = memcmp( key1.semanticName, key2.semanticName, key1.semanticLength );
            }
        }
    }
    return( delta );
}

