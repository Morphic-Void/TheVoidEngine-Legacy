////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    ChainInfo processed StageChain information structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct ChainInfo
{
    uint32_t            chainIndex;
    uint32_t            stagingCount;
    uint32_t            streamOutStage;
    ResourceMapping     resourceMap;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    StageChainLink ChainInfo linking structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct StageChainLink
{
    int                 shaderIndex;
    ChainInfo*          chainInfo;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Index container class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

using BuildIndices = TBlob< uint >;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    ShaderBuilder base class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

using ShaderBuilderBase = TArraySort< StageChainLink >;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    ShaderBuilder : shader building and collection class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class ShaderBuilder : public ShaderBuilderBase
{
public:
    inline                      ShaderBuilder() : ShaderBuilderBase(), m_outputDescs( m_binaryDescs[ binary::EShaderStageCount ] ) {};
    virtual                     ~ShaderBuilder() {};
    bool                        build( const ShaderParsing& parsed, const char* const bin_file );
protected:
    struct                      ResourceStaging { int resourceIndex, stagingIndex; };
    bool                        prepare( const ShaderParsing& parsed );
    void                        cleanup();
    void                        optimise();
    void                        finalise();
    bool                        writeBinary( const char* const bin_file );
    void                        addShader( const ShaderParsing& parsed, const int shaderIndex );
    const ChainInfo*            addChain( const ShaderParsing& parsed, const int chainIndex );
    int                         addResource( const ShaderParsing& parsed, const uint resourceNameHandle, const uint resourceType );
    int                         addStreamFormat( const ShaderParsing& parsed, const int streamLinkIndex );
    uint32_t                    addInputAssembly( const ShaderParsing& parsed, const int assemblyIndex, const int chainIndex, ResourceMapping& resourceMapping );
    uint32_t                    addOutputAssembly( const ShaderParsing& parsed, const int assemblyIndex, const int chainIndex, ResourceMapping& resourceMapping );
    void                        mangle( void* const data, const uint32_t size ) const;
    void                        unmangle( void* const data, const uint32_t size ) const;
    virtual int                 cmpKey( const void* const key, const int index ) const;
    static const int            MAX_CB = 16;
    static const int            MAX_SRV = 128;
    static const int            MAX_UAV = 64;
    static const int            MAX_SS = 16;
    TPool< ChainInfo >          m_chains;
    StringTableBlob             m_strings;
    Blob                        m_binaries;
    binary::BlobDesc*           m_blobDescs;
    ResourceItems               m_resourceItems;
    ResourceIndices             m_resourceIndices;
    StreamFormats               m_streamFormats;
    StreamElements              m_streamElements;
    AssemblyLayouts             m_assemblyLayouts;
    AssemblyStreams             m_assemblyStreams;
    AssemblyElements            m_assemblyElements;
    StagingScatters             m_stagingScatters;
    StagingGathers              m_stagingGathers;
    StagingIndices              m_stagingIndices;
    StageChains                m_stageChains;
    ShaderDescs                 m_shaderDescs;
    BinaryDescs                 m_binaryDescs[ binary::EShaderStageCount + 1 ];
    BinaryDescs&                m_outputDescs;
    BuildIndices                m_inputAssemblyIndices;     //  indices into the assembly layout array
    BuildIndices                m_outputAssemblyIndices;    //  indices into the assembly layout array
    uint32_t                    m_maxStagingCount;          //  maximum staging buffer array element count in any ShaderDesc in the library
    uint32_t                    m_maxStreamElements;        //  maximum number of elements required for input layout interface creation
    uint32_t                    m_maxStreamOutElements;     //  maximum number of elements required for stream-out layout interface creation
    ResourceStaging             m_cbMap[ binary::EShaderStageCount ][ MAX_CB ];     //  constant buffer bind mapping
    ResourceStaging             m_srvMap[ binary::EShaderStageCount ][ MAX_SRV ];   //  shader resource view bind mapping
    ResourceStaging             m_uavMap[ binary::EShaderStageCount ][ MAX_UAV ];   //  unordered access view bind mapping
    ResourceStaging             m_ssMap[ binary::EShaderStageCount ][ MAX_SS ];     //  sampler state bind mapping
private:
    inline                      ShaderBuilder( const ShaderBuilder& ) : ShaderBuilderBase(), m_outputDescs( m_binaryDescs[ binary::EShaderStageCount ] ) {};
    inline void                 operator=( const ShaderBuilder& ) const {};
};

