////
////    StateLink name to state linking structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct StateLink
{
    uint                nameHandle;         //  handle of the state name
    int                 descIndex;          //  index of the state desc
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    TStateParser templated state parsing base class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
class TStateParser : public TArrayNamed< StateLink >
{
public:
    inline                      TStateParser() : TArrayNamed< StateLink >(), m_descs() {};
    inline                      TStateParser( StringTableBlob& strings, const uint count = 32 ) : TArrayNamed< StateLink >(), m_descs() { init( strings, count ); };
    virtual                     ~TStateParser() { m_descs.kill(); };
    bool                        init( StringTableBlob& strings, const uint count = 32 );
    inline void                 kill() { m_descs.kill(); TArrayNamed< StateLink >::kill(); };
    void                        finalise();
    const TBlob< StateLink >&   getLinks() const { return( getBlob() ); };
    const TBlob< T >&           getDescs() const { return( m_descs.getBlob() ); };
protected:
    TArraySort< T >             m_descs;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    TStateParser templated state parsing base class function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
bool TStateParser< T >::init( StringTableBlob& strings, const uint count )
{
    kill();
    if( TArrayNamed< StateLink >::init( strings, count ) )
    {
        m_descs.init( count );
        return( true );
    }
    kill();
    return( false );
}

template< typename T >
void TStateParser< T >::finalise()
{
    if( valid() )
    {
        const int* descRanks = m_descs.rank();
        TBlob< StateLink >& stateLinks = getBlob();
        uint count = stateLinks.getUsed();
        for( uint index = 0; index < count; ++index )
        {
            StateLink& stateLink = stateLinks.getItem( index );
            stateLink.descIndex = descRanks[ stateLink.descIndex ];
        }
        delete[] descRanks;
        m_descs.sort();
        TArrayNamed< StateLink >::sort();
    }
}

