class CRenderTarget
{
public:
    inline                              CRenderTarget() : m_valid( false ), m_texture( NULL ), m_sourceView( NULL ), m_targetView( NULL ) {};
    inline                              CRenderTarget( const UINT Width, const UINT Height, const DXGI_FORMAT Format ) : m_valid( false ), m_texture( NULL ), m_sourceView( NULL ), m_targetView( NULL ) { create( Width, Height, Format ); };
    inline                              ~CRenderTarget() { destroy(); };
    inline bool                         isValid() const { return( m_valid ); };
    bool                                create( const UINT Width, const UINT Height, const DXGI_FORMAT Format );
    bool                                resize( const UINT Width, const UINT Height );
    void                                destroy();
    inline UINT                         getWidth() const { return( m_textureDesc.Width ); };
    inline UINT                         getHeight() const { return( m_textureDesc.Height ); };
    inline ID3D11ShaderResourceView*    getSourceView() const { return( m_sourceView ); };
    inline ID3D11RenderTargetView*      getTargetView() const { return( m_targetView ); };
protected:
    bool                                resize();
    void                                zero();
    bool                                m_valid;
    D3D11_TEXTURE2D_DESC                m_textureDesc;
    D3D11_SHADER_RESOURCE_VIEW_DESC     m_sourceViewDesc;
    D3D11_RENDER_TARGET_VIEW_DESC       m_targetViewDesc;
    ID3D11Texture2D*                    m_texture;
    ID3D11ShaderResourceView*           m_sourceView;
    ID3D11RenderTargetView*             m_targetView;
private:
    inline                              CRenderTarget( const CRenderTarget& ) {};
    inline void                         operator=( const CRenderTarget& ) const {};
};

