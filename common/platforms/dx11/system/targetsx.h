class CSwapChain
{
public:
    inline                              CSwapChain() : m_valid( false ), m_available( false ), m_giSwapChain( NULL ), m_sourceView( NULL ), m_targetView( NULL ) {};
    inline                              CSwapChain( HWND hWnd, const bool SRGB, const bool isShaderResource = false ) : m_valid( false ), m_available( false ), m_giSwapChain( NULL ), m_sourceView( NULL ), m_targetView( NULL ) { create( hWnd, SRGB, isShaderResource ); };
    inline                              ~CSwapChain() { destroy(); };
    inline bool                         isValid() const { return( m_valid ); };
    inline bool                         isAvailable() const { return( m_available ); };
    inline bool                         isWindowed() const { return( ( m_giSwapDesc.Windowed == TRUE ) ? true : false ); };
    inline bool                         isFullscreen() const { return( ( m_giSwapDesc.Windowed == FALSE ) ? true : false ); };
    bool                                create( HWND hWnd, const bool SRGB, const bool isShaderResource = false );
    bool                                resize( const UINT Width, const UINT Height );
    bool                                present( const UINT SyncInterval, const UINT Flags );
    void                                destroy();
    inline HWND                         getHwnd() const { return( m_giSwapDesc.OutputWindow ); };
    inline bool                         toggleMode() { return( ( m_giSwapDesc.Windowed == TRUE ) ? setFullscreen() : setWindowed() ); };
    bool                                setWindowed();
    bool                                setFullscreen();
    inline UINT                         getWidth() const { return( m_giSwapDesc.BufferDesc.Width ); };
    inline UINT                         getHeight() const { return( m_giSwapDesc.BufferDesc.Height ); };
    inline IDXGISwapChain*              getSwapChain() const { return( m_giSwapChain ); };
    inline ID3D11RenderTargetView*      getSourceView() const { return( m_targetView ); };
    inline ID3D11RenderTargetView*      getTargetView() const { return( m_targetView ); };
protected:
    void                                zero();
    bool                                resizeTarget();
    bool                                resizeBuffers();
    bool                                createSwapChain();
    bool                                createViews();
    bool                                setSwapChainDesc( HWND hWnd, const bool isShaderResource );
    bool                                setWindowedModeDesc( HWND hWnd );
    bool                                setFullscreenModeDesc();
    bool                                m_valid;
    bool                                m_available;
    DXGI_MODE_DESC                      m_giWindowed;
    DXGI_MODE_DESC                      m_giFullscreen;
    DXGI_SWAP_CHAIN_DESC                m_giSwapDesc;
    D3D11_SHADER_RESOURCE_VIEW_DESC     m_sourceViewDesc;
    D3D11_RENDER_TARGET_VIEW_DESC       m_targetViewDesc;
    IDXGISwapChain*                     m_giSwapChain;
    ID3D11ShaderResourceView*           m_sourceView;
    ID3D11RenderTargetView*             m_targetView;
private:
    inline                              CSwapChain( const CSwapChain& ) {};
    inline void                         operator=( const CSwapChain& ) const {};
};

