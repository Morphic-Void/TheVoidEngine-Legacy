template< typename TDesc, typename TStateX >
class TStateCacheX : protected IArrayQuery
{
public:
    inline                  TStateCacheX() : m_device( NULL ), m_sorting(), m_states(), m_descs() {};
    inline                  TStateCacheX( const uint count ) : m_device( NULL ), m_sorting(), m_states(), m_descs() { init( count ); };
    inline                  ~TStateCacheX() { kill(); };
    inline bool             isValid() const { return( m_device ? m_sorting.valid() : false ); };
    bool                    init( const uint count = 32 );
    void                    kill();
    TStateX                 create( const TDesc& desc, const bool cached );
protected:
    virtual const void*     getKey( const int index ) const;
    virtual int             cmpKey( const void* const key, const int index ) const;
    virtual void            moveItem( const int source, const int target );
    ID3D11Device*           m_device;
    ArraySort               m_sorting;
    TBlob< TStateX >        m_states;
    TBlob< TDesc >          m_descs;
private:
    inline                  TStateCacheX( const TStateCacheX& ) {};
    inline void             operator=( const TStateCacheX& ) const {};
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    TStateCache state cache class template public function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename TDesc, typename TStateX >
bool TStateCacheX< TDesc, TStateX >::init( const uint count )
{
    if( CCoreX::acquire() )
    {
        kill();
        m_device = CCoreX::getDevice();
        uint size = ( ( count < 4 ) ? 4 : count );
        if( m_sorting.init( *this, size ) )
        {
            m_states.init( size );
            m_descs.init( size );
            return( true );
        }
        kill();
    }
    return( false );
}

template< typename TDesc, typename TStateX >
void TStateCacheX< TDesc, TStateX >::kill()
{
    if( m_device )
    {
        m_sorting.kill();
        m_states.kill();
        m_descs.kill();
        m_device = NULL;
        CCoreX::release();
    }
}

template< typename TDesc, typename TStateX >
TStateX TStateCacheX< TDesc, TStateX >::create( const TDesc& desc, const bool cached )
{
    TStateX state;
    if( m_device )
    {
        if( cached )
        {
            int cacheIndex = m_sorting.find( &desc );
            if( cacheIndex >= 0 )
            {
                return( m_states.getItem( static_cast< uint >( cacheIndex ) ) );
            }
        }
        if( state.create( desc ) )
        {
            if( cached )
            {
                int cacheIndex = m_sorting.insert( &desc );
                m_descs.setItem( static_cast< uint >( cacheIndex ), desc );
                m_states.setItem( static_cast< uint >( cacheIndex ), state );
            }
            return( state );
        }
    }
    return( state );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    TStateCache state cache class template protected virtual function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename TDesc, typename TStateX >
const void* TStateCacheX< TDesc, TStateX >::getKey( const int index ) const
{
    return( reinterpret_cast< const void* >( m_descs.getBuffer() + index ) );
}

template< typename TDesc, typename TStateX >
int TStateCacheX< TDesc, TStateX >::cmpKey( const void* const key, const int index ) const
{
    return( memcmp( key, reinterpret_cast< const void* >( m_descs.getBuffer() + index ), sizeof( TDesc ) ) );
}

template< typename TDesc, typename TStateX >
void TStateCacheX< TDesc, TStateX >::moveItem( const int source, const int target )
{
    UNUSED( source );
    UNUSED( target );
}

