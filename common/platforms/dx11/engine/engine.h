class CEngine
{
public:
    inline                          CEngine() : m_valid( false ), m_gamePlugin(), m_gamePadInput(), m_keyMouseInput(), m_renderer() {};
    inline                          ~CEngine() { destroy(); };
    inline bool                     valid() const { return( m_valid ); };
	bool				            create( rendering::CSwapChain& swapChain, rendering::CDepthStencil& depthStencil, ID3D11Device* const device, ID3D11DeviceContext* const context );
	bool				            resize();
	bool				            update( const float time, const _XINPUT_GAMEPAD* game_pad );
	bool				            render();
	void				            destroy();
    void                            activate( const HWND hWnd, const bool state );
    void                            keyInput( const HWND hWnd, const UINT uiMsg, const WPARAM wParam, const LPARAM lParam );
    void                            mouseInput( const HWND hWnd, const UINT uiMsg, const WPARAM wParam, const LPARAM lParam );
protected:
    bool                            init( rendering::CSwapChain& swapChain, rendering::CDepthStencil& depthStencil, ID3D11Device* const device, ID3D11DeviceContext* const context );
    void                            kill();
    bool                            m_valid;
    platform_windows::CPlugin       m_gamePlugin;
    input::CGamePadInput            m_gamePadInput;
    input::CKeyMouseInput           m_keyMouseInput;
    rendering::CRenderer            m_renderer;
};

