bool CEngine::create( rendering::CSwapChain& swapChain, rendering::CDepthStencil& depthStencil, ID3D11Device* const device, ID3D11DeviceContext* const context )
{
    ASSERT( ( device != NULL ) && ( context != NULL ) );
    if( ( device != NULL ) && ( context != NULL ) )
    {
        kill();
        if( init( swapChain, depthStencil, device, context ) )
        {
            m_valid = true;
            return( true );
        }
        kill();
    }
    return( false );
}

bool CEngine::resize()
{
    return( valid() ? m_renderer.resize() : false );
}

bool CEngine::update( const float time, const _XINPUT_GAMEPAD* game_pad )
{
    if( valid() )
    {
        m_keyMouseInput.update();
        m_gamePadInput.update( game_pad );
        if( m_renderer.update( time, m_gamePadInput, m_keyMouseInput ) )
        {
            m_keyMouseInput.setLocked( !m_renderer.cmdActive() );
            return( m_gamePlugin.refresh() );
        }
    }
    return( false );
}

bool CEngine::render()
{
    return( valid() ? m_renderer.render() : false );
}

void CEngine::destroy()
{
    if( valid() )
    {
        kill();
    }
}

void CEngine::activate( const HWND hWnd, const bool state)
{
    if( valid() )
    {
        m_keyMouseInput.activate( hWnd, state );
    }
}

void CEngine::keyInput( const HWND hWnd, const UINT uiMsg, const WPARAM wParam, const LPARAM lParam )
{
    if( valid() )
    {
        input::CKeyEvent keyEvent;
        if( m_keyMouseInput.processKey( hWnd, uiMsg, wParam, lParam, keyEvent ) )
        {
            m_renderer.keyInput( keyEvent );
        }
    }
}

void CEngine::mouseInput( const HWND hWnd, const UINT uiMsg, const WPARAM wParam, const LPARAM lParam )
{
    if( valid() )
    {
        input::CMouseEvent mouseEvent;
        input::CMouseParam mouseParam;
        if( m_keyMouseInput.processMouse( hWnd, uiMsg, wParam, lParam, mouseEvent, mouseParam ) )
        {
        }
    }
}

bool CEngine::init( rendering::CSwapChain& swapChain, rendering::CDepthStencil& depthStencil, ID3D11Device* const device, ID3D11DeviceContext* const context )
{
    m_gamePadInput.reset();
    m_keyMouseInput.reset();
    m_keyMouseInput.setWindow( swapChain.getHwnd(), true );
    return( m_renderer.create( swapChain, depthStencil, device, context ) ? m_gamePlugin.load( "libs\\game.dll", true ) : false );
}

void CEngine::kill()
{
    m_valid = false;
    m_gamePlugin.unload();
    m_gamePadInput.reset();
    m_keyMouseInput.reset();
    m_renderer.destroy();
}

