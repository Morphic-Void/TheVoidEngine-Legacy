const float CGamePadState::c_deadZone = 8192.0f;
const float CGamePadState::c_maxRange = 32767.0f;
const float CGamePadState::c_constant = ( 1.0f / ( c_maxRange - c_deadZone ) );
const float CGamePadState::c_numerator = ( c_deadZone * c_constant );

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    CGamePadState game pad controller state class function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void CGamePadState::zero()
{
    m_lTrigger = m_rTrigger = m_lStick.x = m_lStick.y = m_rStick.x = m_rStick.y = m_dPad.x = m_dPad.y = 0.0f;
    m_buttonState = 0;
    m_buttonPressed = 0;
    m_buttonReleased = 0;
}

