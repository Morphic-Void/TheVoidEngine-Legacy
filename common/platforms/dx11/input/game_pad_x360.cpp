void CGamePadInput::reset()
{
    zero();
}

void CGamePadInput::update( const _XINPUT_GAMEPAD* const game_pad )
{
    m_lTrigger = m_rTrigger = m_lStick.x = m_lStick.y = m_rStick.x = m_rStick.y = m_dPad.x = m_dPad.y = 0.0f;
    uint buttonState = 0;
    if( game_pad )
    {
        m_lStick.x = static_cast< float >( game_pad->sThumbLX );
        m_lStick.x = static_cast< float >( game_pad->sThumbLX );
        m_lStick.y = static_cast< float >( game_pad->sThumbLY );
        m_lStick.y = static_cast< float >( game_pad->sThumbLY );
        m_rStick.x = static_cast< float >( game_pad->sThumbRX );
        m_rStick.x = static_cast< float >( game_pad->sThumbRX );
        m_rStick.y = static_cast< float >( game_pad->sThumbRY );
        m_rStick.y = static_cast< float >( game_pad->sThumbRY );
        float lLength = m_lStick.Length();
        m_lStick.Mul( ( lLength < c_deadZone ) ? 0.0f : ( c_constant - ( c_numerator / lLength ) ) );
        float rLength = m_rStick.Length();
        m_rStick.Mul( ( rLength < c_deadZone ) ? 0.0f : ( c_constant - ( c_numerator / rLength ) ) );
        m_lTrigger = ( game_pad->bLeftTrigger * ( 1.0f / 255.0f ) );
        m_rTrigger = ( game_pad->bRightTrigger * ( 1.0f / 255.0f ) );
        WORD buttons = game_pad->wButtons;
        if( buttons & XINPUT_GAMEPAD_START ) buttonState |= EGamePadBitStart;
        if( buttons & XINPUT_GAMEPAD_BACK ) buttonState |= EGamePadBitBack;
        if( buttons & XINPUT_GAMEPAD_LEFT_THUMB ) buttonState |= EGamePadBitStickL;
        if( buttons & XINPUT_GAMEPAD_RIGHT_THUMB ) buttonState |= EGamePadBitStickR;
        if( buttons & XINPUT_GAMEPAD_LEFT_SHOULDER ) buttonState |= EGamePadBitShoulderL;
        if( buttons & XINPUT_GAMEPAD_RIGHT_SHOULDER ) buttonState |= EGamePadBitShoulderR;
        if( buttons & XINPUT_GAMEPAD_DPAD_UP ) buttonState |= EGamePadBitUp;
        if( buttons & XINPUT_GAMEPAD_DPAD_DOWN ) buttonState |= EGamePadBitDown;
        if( buttons & XINPUT_GAMEPAD_DPAD_LEFT ) buttonState |= EGamePadBitLeft;
        if( buttons & XINPUT_GAMEPAD_DPAD_RIGHT ) buttonState |= EGamePadBitRight;
        if( buttons & XINPUT_GAMEPAD_A ) buttonState |= EGamePadBitA;
        if( buttons & XINPUT_GAMEPAD_B ) buttonState |= EGamePadBitB;
        if( buttons & XINPUT_GAMEPAD_X ) buttonState |= EGamePadBitX;
        if( buttons & XINPUT_GAMEPAD_Y ) buttonState |= EGamePadBitY;
    }
    m_buttonPressed = ( buttonState & ( m_buttonState ^ buttonState ) );
    m_buttonReleased = ( m_buttonState & ( m_buttonState ^ buttonState ) );
    m_buttonState = buttonState;
    int dx = 0;
    int dy = 0;
    if( buttonState & EGamePadBitUp ) ++dy;
    if( buttonState & EGamePadBitDown ) --dy;
    if( buttonState & EGamePadBitRight ) ++dx;
    if( buttonState & EGamePadBitLeft ) --dx;
    m_dPad.x = m_dPad.y = 0.0f;
    if( dx && dy )
    {
        m_dPad.x = ( ( dx > 0 ) ? maths::consts::ROOT2 : -maths::consts::ROOT2 );
        m_dPad.y = ( ( dy > 0 ) ? maths::consts::ROOT2 : -maths::consts::ROOT2 );
    }
    else if( dx )
    {
        m_dPad.x = ( ( dx > 0 ) ? 1.0f : -1.0f );
    }
    else if( dy )
    {
        m_dPad.y = ( ( dy > 0 ) ? 1.0f : -1.0f );
    }
}

