////
////    CKeyMouse keyboard and mouse input class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class CKeyMouseInput : public CKeyMouseState
{
public:
    inline                      CKeyMouseInput() : m_hWnd( NULL ) { reset(); };
    inline                      ~CKeyMouseInput() { reset(); };
    void                        setWindow( const HWND hWnd );
    inline void                 setWindow( const HWND hWnd, const bool automatic ) { m_automatic = automatic; setWindow( hWnd ); };
    inline void                 setAutomatic( const bool automatic = true ) { m_automatic = automatic; };
    inline void                 setManual( const bool manual = true ) { setAutomatic( !manual ); };
    void                        setActive( const bool active = true );
    inline void                 setInactive( const bool inactive = true ) { setActive( !inactive ); };
    void                        setLocked( const bool locked = true );
    inline void                 setUnlocked( const bool unlocked = true ) { setLocked( !unlocked ); };
    void                        reset();
    void                        update();
    void                        activate( const HWND hWnd, const bool state );
    bool                        processKey( const HWND hWnd, const UINT uiMsg, const WPARAM wParam, const LPARAM lParam, CKeyEvent& keyEvent );
    bool                        processMouse( const HWND hWnd, const UINT uiMsg, const WPARAM wParam, const LPARAM lParam, CMouseEvent& mouseEvent, CMouseParam& mouseParam );
protected:
    void                        refreshCoords();
    uint32_t                    getMouseState( const WPARAM wParam ) const;
    bool                        m_automatic;
    LPARAM                      m_lParam;
    RECT                        m_screenRect;
    POINT                       m_screenCenter;
    POINT                       m_clientCenter;
    POINT                       m_screenCursor;
    POINT                       m_clientCursor;
    int                         m_sumDeltaX;
    int                         m_sumDeltaY;
    int                         m_sumWheelV;
    int                         m_sumWheelH;
    HWND                        m_hWnd;
};

