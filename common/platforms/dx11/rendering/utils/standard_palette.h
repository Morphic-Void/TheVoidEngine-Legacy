////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Standard palette colour enumeration
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

enum EColour
{
    EColourGreen = 0,
    EColourCyan,
    EColourLime,
    EColourPaleGreen,
    EColourEmerald,
    EColourTeal,
    EColourDarkBrown,
    EColourGoldenBrown,
    EColourMustard,
    EColourOlive,
    EColourLemon,
    EColourPaleStraw,
    EColourPaleYellow,
    EColourDarkRed,
    EColourRichPink,
    EColourFuscia,
    EColourCerise,
    EColourPastelPink,
    EColourViolet,
    EColourImperialPurple,
    EColourLightPurple,
    EColourPurple,
    EColourPastelPurple,
    EColourDarkBlue,
    EColourAzure,
    EColourRichBlue,
    EColourSteelBlue,
    EColourAqua,
    EColourPaleBlue,
    EColourPastelBlue,
    EColourGrey,
    EColourWhite,
    EColourBlack,
    EColourCount,
    EColourForce32 = 0x7fffffff,
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Standard palette colour array
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

extern const uint32_t g_standardPalette[ EColourCount ];

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Standard palette colour fetch function
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

inline uint32_t getStandardColour( const EColour eColour, const uint shift = 0 )
{
    uint32_t mask = ( ( 0x000000ff >> shift ) * 0x00010101 );
    return( ( ( g_standardPalette[ eColour ] >> shift ) & mask ) | 0xff000000 );
}

