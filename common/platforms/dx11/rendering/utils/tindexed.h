template< typename T >
class TIndexed : protected ArraySort, IArrayQuery
{
public:
    inline                  TIndexed() : ArraySort(), m_limit( 0 ), m_count( 0 ), m_stream( NULL ) {};
    inline                  TIndexed( const uint count ) : ArraySort(), m_limit( 0 ), m_count( 0 ), m_stream( NULL ) { init( count ); };
    virtual                 ~TIndexed() { kill(); };
    inline bool             valid() const { return( ArraySort::valid() ); };
    bool                    init( const uint count = 32, const bool resize = false );
    void                    kill();
    void                    reset( const uint count = 0 );
    void                    clone( const TIndexed< T >& source );
	int                     insert( const T& item );
    inline uint             getStride() const { return( static_cast< uint >( sizeof( T ) ) ); };
    inline uint             getCount() const { return( m_count ); };
    inline uint             getBytes() const { return( static_cast< uint >( m_count * sizeof( T ) ) ); };
    inline T*               getBuffer() { return( m_stream ); };
    inline const T*         getBuffer() const { return( m_stream ); };
protected:
    virtual const void*     getKey( const int index ) const;
    virtual int             cmpKey( const void* const key, const int index ) const;
    virtual void            moveItem( const int source, const int target );
    uint                    m_limit;
    uint                    m_count;
    T*                      m_stream;
private:
    inline                  TIndexed( const TIndexed& ) {};
    inline void             operator=( const TIndexed& ) {};
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    TIndexed indexed vertex data array template public function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
bool TIndexed< T >::init( const uint count, const bool resize )
{
    if( !resize || ( count > m_limit ) )
    {
        kill();
        if( ArraySort::init( *this, count ) )
        {
            m_limit = count;
            m_stream = ( count ? new T[ m_limit ] : NULL );
            return( true );
        }
        return( false );
    }
    m_count = 0;
    ArraySort::reset();
    return( true );
}

template< typename T >
void TIndexed< T >::kill()
{
    ArraySort::kill();
    m_limit = 0;
    m_count = 0;
    if( m_stream != NULL )
    {
        delete[] m_stream;
        m_stream = NULL;
    }
}

template< typename T >
void TIndexed< T >::reset( const uint count )
{
    if( count < m_count )
    {
        ArraySort::reset();
        m_count = count;
        for( uint index = 0; index < count; ++index )
        {
            ArraySort::insert( &m_stream[ index ] );
        }
    }
}

template< typename T >
void TIndexed< T >::clone( const TIndexed< T >& source )
{
    kill();
    ArraySort::clone( source );
    m_query = this;
    m_limit = source.m_limit;
    m_count = source.m_count;
    if( m_limit )
    {
        m_stream = new T[ m_limit ];
        if( source.m_stream != NULL )
        {
            memcpy( m_stream, source.m_stream, ( m_limit * sizeof( T ) ) );
        }
    }
}

template< typename T >
int TIndexed< T >::insert( const T& item )
{
    int index = ArraySort::find( &item );
    if( index < 0 )
    {
        index = ArraySort::insert( &item );
        if( m_count == m_limit )
        {   //  need to grow the buffer
            m_limit = ( m_limit ? ( ( m_limit + ( m_limit << 1 ) + 3 ) >> 1 ) : 4 );
            T* data = m_stream;
            m_stream = new T[ m_limit ];
            if( data )
            {
                memcpy_s( reinterpret_cast< void* >( m_stream ), ( m_limit * sizeof( T ) ), reinterpret_cast< const void* >( data ), ( m_count * sizeof( T ) ) );
                delete[] data;
            }
        }
        m_stream[ m_count ] = item;
        ++m_count;
    }
    return( index );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    TIndexed indexed vertex data array template protected virtual function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
const void* TIndexed< T >::getKey( const int index ) const
{
    return( reinterpret_cast< const void* >( m_stream + index ) );
}

template< typename T >
int TIndexed< T >::cmpKey( const void* const key, const int index ) const
{
    return( memcmp( key, reinterpret_cast< const void* >( m_stream + index ), sizeof( T ) ) );
}

template< typename T >
void TIndexed< T >::moveItem( const int source, const int target )
{
    UNUSED( source );
    UNUSED( target );
}

