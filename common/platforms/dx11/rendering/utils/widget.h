class Widget
{
public:
    inline                          Widget() : m_shader() {};
    inline                          Widget( const CoreSystem& system, const widgets::IWidget& widget, const bool use_normals ) : m_shader() { create( system, widget, use_normals ); };
    inline                          ~Widget() { destroy(); };
    inline bool                     valid() const { return( m_shader.valid() ); };
    bool                            create( const CoreSystem& system, const widgets::IWidget& widget, const bool use_normals );
    void                            destroy();
    void                            render();
    inline bool                     bind( const Binding& binding ) { return( valid() ? m_shader.bind( binding ) : false ); };
    inline bool                     unbind( const Binding& binding ) { return( valid() ? m_shader.unbind( binding ) : false ); };
    inline bool                     bind( const BindingGroup& bindingGroup ) { return( valid() ? m_shader.bind( bindingGroup ) : false ); };
    inline bool                     unbind( const BindingGroup& bindingGroup ) { return( valid() ? m_shader.unbind( bindingGroup ) : false ); };
protected:
    bool                            init( const CoreSystem& system, const widgets::IWidget& widget, const bool use_normals );
    void                            kill();
    Shader                          m_shader;
    BindingStream                   m_vertexBinding;
    ResourceStream                  m_vertexResource;
    CVertices                       m_vertexBuffer;
    ResourceIndices                 m_indexResource;
    CIndices                        m_indexBuffer;
    ResourceCommand                 m_command;
private:
    inline                          Widget( const Widget& ) {};
    inline void                     operator=( const Widget& ) {};
};

