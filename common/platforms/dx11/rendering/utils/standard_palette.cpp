const char* getEPaletteColourName( const EColour colourIndex )
{
    return( EColourNames[ ( colourIndex < EColourCount ) ? colourIndex : EColourCount ] );
}

const char* getEPaletteColourNameUpper( const EColour colourIndex )
{
    return( EColourNamesUpper[ ( colourIndex < EColourCount ) ? colourIndex : EColourCount ] );
}

const char* getEPaletteColourNameLower( const EColour colourIndex )
{
    return( EColourNamesLower[ ( colourIndex < EColourCount ) ? colourIndex : EColourCount ] );
}

const char* getEPaletteColourNameMixed( const EColour colourIndex )
{
    return( EColourNamesMixed[ ( colourIndex < EColourCount ) ? colourIndex : EColourCount ] );
}

