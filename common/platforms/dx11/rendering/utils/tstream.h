////
////    simple stream append buffer class template
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
class TStream
{
public:
    inline                  TStream() : m_limit( 0 ), m_count( 0 ), m_stream( NULL ) {};
    inline                  TStream( const uint count ) : m_limit( 0 ), m_count( 0 ), m_stream( NULL ) { init( count ); };
    inline                  ~TStream() { kill(); };
    inline bool             valid() const { return( ( m_stream != NULL ) ? true : false ); };
    void                    init( const uint count = 32, const bool resize = false );
    void                    kill();
    inline void             reset( const uint count = 0 ) { if( count < m_count ) m_count = count; };
    void                    clone( const TStream< T >& source );
    void                    append( const T& item );
    inline uint             getStride() const { return( static_cast< uint >( sizeof( T ) ) ); };
    inline uint             getCount() const { return( m_count ); };
    inline uint             getBytes() const { return( static_cast< uint >( m_count * sizeof( T ) ) ); };
    inline T*               getBuffer() { return( m_stream ); };
    inline const T*         getBuffer() const { return( m_stream ); };
protected:
    uint                    m_limit;
    uint                    m_count;
    T*                      m_stream;
private:
    inline                  TStream( const TStream& ) {};
    inline void             operator=( const TStream& ) {};
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    simple stream append buffer class template function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template< typename T >
void TStream< T >::init( const uint count, const bool resize )
{
    m_count = 0;
    if( !resize || ( count > m_limit ) )
    {
        kill();
        m_limit = count;
        m_stream = ( count ? new T[ m_limit ] : NULL );
    }
}

template< typename T >
void TStream< T >::kill()
{
    m_limit = 0;
    m_count = 0;
    if( m_stream != NULL )
    {
        delete[] m_stream;
        m_stream = NULL;
    }
}

template< typename T >
void TStream< T >::clone( const TStream< T >& source )
{
    kill();
    m_limit = source.m_limit;
    m_count = source.m_count;
    if( m_limit )
    {
        m_stream = new T[ m_limit ];
        if( source.m_stream != NULL )
        {
            memcpy( m_stream, source.m_stream, ( m_limit * sizeof( T ) ) );
        }
    }
}

template< typename T >
void TStream< T >::append( const T& item )
{
    if( m_count == m_limit )
    {   //  need to grow the buffer
        m_limit = ( m_limit ? ( ( m_limit + ( m_limit << 1 ) + 3 ) >> 1 ) : 4 );
        T* data = m_stream;
        m_stream = new T[ m_limit ];
        if( data )
        {
            memcpy_s( reinterpret_cast< void* >( m_stream ), ( m_limit * sizeof( T ) ), reinterpret_cast< const void* >( data ), ( m_count * sizeof( T ) ) );
            delete[] data;
        }
    }
    m_stream[ m_count ] = item;
    ++m_count;
}

