struct ConstantBuffer
{
    CompoundBuffer      consts;
    CConstants          buffer;
    inline bool         create( const CoreSystem& system, const StringItem& name ) { return( consts.create( system, name ) ); };
    inline void         destroy() { consts.release(); buffer.destroy(); };
    inline              ~ConstantBuffer() { destroy(); };
};

struct DrawingObject
{
    CompoundDrawing     object;
    CVertices           vertices;
    CIndices            indices;
    inline bool         create( const CoreSystem& system, const StringItem& name ) { return( object.create( system, name ) ); };
    inline void         destroy() { object.release(); vertices.destroy(); indices.destroy(); };
    inline              ~DrawingObject() { destroy(); };
};

struct IndexedObject
{
    CompoundIndexed     object;
    CVertices           vertices;
    CIndices            indices;
    inline bool         create( const CoreSystem& system, const StringItem& name ) { return( object.create( system, name ) ); };
    inline void         destroy() { object.release(); vertices.destroy(); indices.destroy(); };
    inline              ~IndexedObject() { destroy(); };
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    DirectX 11 renderer class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class CRenderer
{
public:
    inline                          CRenderer() : m_swapChain( NULL ), m_depthStencil( NULL ), m_showControls( true ) {};
    inline                          ~CRenderer() { destroy(); };
    inline bool                     valid() const { return( m_swapChain ? true : false ); };
    bool                            create( CSwapChain& swapChain, CDepthStencil& depthStencil, ID3D11Device* const device, ID3D11DeviceContext* const context );
    bool                            resize();
    bool                            update( const float time, const input::CGamePadState& gamePadState, const input::CKeyMouseState& keyMouseState );
    bool                            render();
    void                            destroy();
    inline bool                     cmdActive() const { return( ( valid() && m_console.active() ) ? true : false ); };
    void                            keyInput( const input::CKeyEvent keyEvent );
protected:
    bool                            init( CSwapChain& swapChain, CDepthStencil& depthStencil, ID3D11Device* const device, ID3D11DeviceContext* const context );
    void                            kill();
    void                            drawTimingAndLocation();
    void                            drawTimingAndLocation( const float ms, const float hz, const maths::vec3& location );
    void                            drawTabToggleAndTitle( const bool show );
    void                            drawControlMappings();
    void                            setupScene();
    bool                            createSystem( ID3D11Device* const device, ID3D11DeviceContext* const context );
    void                            removeSystem();
    void                            updatePrimary();
    bool                            loadResources();
    CSwapChain*                     m_swapChain;
    CDepthStencil*                  m_depthStencil;
    CoreSystem                      m_system;
    PrimaryViews                    m_primary;
    ConstantBuffer                  m_scene;
    hlsl::Scene                     m_sceneData;
    Widget                          m_gridWidget;
    ConstantBuffer                  m_gridEntity;
    hlsl::Entity                    m_gridEntityData;
    Widget                          m_wireWidget;
    ConstantBuffer                  m_wireEntity;
    hlsl::Entity                    m_wireEntityData;
    Widget                          m_solidWidget;
    ConstantBuffer                  m_solidEntity;
    hlsl::Entity                    m_solidEntityData;
    CVectorConsole                  m_console;
    CVectorOverlay                  m_overlay;
    maths::viewer                   m_viewer;
    locality::GameWorld             m_gameWorld;
    bool                            m_showControls;
};

