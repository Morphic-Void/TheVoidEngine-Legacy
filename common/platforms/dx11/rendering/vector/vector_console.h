class CVectorConsole : public CVectorDisplay
{
public:
    enum EHPos { ELeft = 0, ECenter, ERight, EHPosForce32 = 0x7fffffff };
    enum EVPos { ETop = 0, EMiddle, EBottom, EVPosForce32 = 0x7fffffff };
public:
    inline                          CVectorConsole() : CVectorDisplay() {};
    inline                          CVectorConsole( const CoreSystem& system, const DIMS2D& display, const CONFIG& config ) : CVectorDisplay() { create( system, display, config ); };
    inline                          ~CVectorConsole() { destroy(); };
    bool                            create( const CoreSystem& system, const DIMS2D& display );
    bool                            create( const CoreSystem& system, const DIMS2D& display, const CONFIG& config );
    void                            destroy();
    void                            resize( const DIMS2D& display );
    void                            update( const float time );
    void                            render();
    inline void                     setCursor( const int x, const int y ) { m_cursor.x = ( ( x < 0 ) ? ( m_vcmap.width - 1 - ( ( -1 - x ) % m_vcmap.width ) ) : ( x % m_vcmap.width ) ); m_cursor.y = ( ( y < 0 ) ? ( m_vcmap.height - 1 - ( ( -1 - y ) % m_vcmap.height ) ) : ( y % m_vcmap.height ) ); };
    inline void                     position( const EHPos hPos, const EVPos vPos ) { m_hPos = hPos; m_vPos = vPos; };
    inline bool                     active() const { return( ( m_state & 1 ) ? m_lerp : !m_lerp ); };
    inline bool                     hidden() const { return( ( m_state & 1 ) ? !m_lerp : m_lerp ); };
    inline void                     toggle() { m_show = true; if( m_lerp ) { m_state ^= 5; m_glide = ( 1.0f - m_glide ); } else { m_lerp = true; m_glide = 0.0f; } };
    inline void                     hide( const bool hide = true ) { if( hidden() != hide ) toggle(); };
    inline void                     show( const bool show = true ) { if( hidden() == show ) toggle(); };
    void                            keyInput( const input::CKeyEvent keyEvent );
protected:
    enum ENav { ENavNone = 0, ENavPgUp, ENavPgDown, ENavEnd, ENavHome, ENavLeft, ENavUp, ENavRight, ENavDown, ENavBack, ENavTab, ENavEnter, ENavIns, ENavDel, ENavPause, ENavEscape, ENavCount, ENavForce32 = 0x7fffffff };
    uint                            getNavigation( const uint vkey, const uint state ) const;
    bool                            init( const CoreSystem& system, const DIMS2D& display, const CONFIG& config );
    void                            kill();
    void                            setup( const DIMS2D& display );
    uint                            m_blink;
    SPOINT                          m_cursor;
    DIMS2D                          m_display;
    BindingConsts                   m_sceneBinding;
    ResourceConsts                  m_sceneResource;
    CConstants                      m_sceneBuffer;
    hlsl::Scene                     m_sceneData;
    BindingConsts                   m_entityBinding;
    ResourceConsts                  m_entityResource;
    CConstants                      m_entityBuffer;
    hlsl::Entity                    m_entityData;
    maths::viewer                   m_viewer;
    int                             m_state;
    float                           m_glide;
    bool                            m_lerp;
    bool                            m_show;
    EHPos                           m_hPos;
    EVPos                           m_vPos;
};

