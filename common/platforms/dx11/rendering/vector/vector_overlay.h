class CVectorOverlay
{
public:
    inline                          CVectorOverlay() : m_valid( false ), m_enable( 0 ) {};
    inline                          CVectorOverlay( const CoreSystem& system, const DIMS2D& display ) : m_valid( false ), m_enable( 0 ) { create( system, display ); };
    inline                          ~CVectorOverlay() { destroy(); };
    inline bool                     valid() const { return( m_valid ); };
    bool                            create( const CoreSystem& system, const DIMS2D& display );
    void                            destroy();
    void                            resize( const DIMS2D& display );
    void                            update( const float time );
    void                            render();
    inline void                     toggle() { m_show = true; if( m_lerp ) { m_state ^= 5; m_glide = ( 1.0f - m_glide ); } else { m_lerp = true; m_glide = 0.0f; } };
    inline bool                     hidden() const { return( ( m_state & 1 ) ? !m_lerp : m_lerp ); };
    inline void                     hide( const bool hide = true ) { if( hidden() != hide ) toggle(); };
    inline void                     show( const bool show = true ) { if( hidden() == show ) toggle(); };
    bool                            bind( const Binding& binding );
    bool                            unbind( const Binding& binding );
    bool                            bind( const BindingGroup& bindingGroup );
    bool                            unbind( const BindingGroup& bindingGroup );
    inline bool                     isLayerEnabled( const int index ) const { return( ( m_valid && ( index == ( index & 15 ) ) ) ? ( ( ( m_enable >> index ) & 1 ) ? true : false ) : false ); };
    inline void                     enableLayer( const int index ) { if( m_valid && ( index == ( index & 15 ) ) ) m_enable |= ( uint( 1 ) << index ); };
    inline void                     disableLayer( const int index ) { if( m_valid && ( index == ( index & 15 ) ) ) m_enable &= ~( uint( 1 ) << index ); };
    inline CVectorDrawing&          getLayer( const int index ) { return( m_layers[ index & 15 ] ); };
    inline const CVectorDrawing&    getLayer( const int index ) const { return( m_layers[ index & 15 ] ); };
    inline uint                     getWidth() const { return( m_display.width ); };
    inline uint                     getHeight() const { return( m_display.height ); };
protected:
    bool                            init( const CoreSystem& system, const DIMS2D& display );
    void                            kill();
    void                            setup( const DIMS2D& display );
    bool                            m_valid;
    uint                            m_enable;
    CVectorDrawing                  m_layers[ 16 ];
    DIMS2D                          m_display;
    BindingConsts                   m_sceneBinding;
    ResourceConsts                  m_sceneResource;
    CConstants                      m_sceneBuffer;
    hlsl::Scene                     m_sceneData;
    BindingConsts                   m_entityBinding;
    ResourceConsts                  m_entityResource;
    CConstants                      m_entityBuffer;
    hlsl::Entity                    m_entityData;
    maths::viewer                   m_viewer;
    int                             m_state;
    float                           m_glide;
    bool                            m_lerp;
    bool                            m_show;
};

