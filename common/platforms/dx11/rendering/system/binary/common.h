//  File structure section description
struct SectionDesc
{
    uint32_t            size;               //  size of the section in bytes
    uint32_t            offset;             //  offset of the section in multiples of uint32_t from the start of the file
};

//  File structure array description
struct ArrayDesc
{
    uint32_t            count;              //  count of array elements
    uint32_t            offset;             //  offset of the array in multiples of uint32_t from the start of the file
};

//  File structure binary blob description
struct BlobDesc
{
    uint32_t            size;               //  size of the binary blob
    uint32_t            offset;             //  offset of the binary blob in bytes from the start of the section
};

//  File structure list description
struct ListDesc
{
    uint32_t            count;              //  item count
    uint32_t            index;              //  item index (index of the first list item within the specific array)
};

//  File structure name linking description
struct LinkDesc
{
    uint32_t            nameHandle;         //  index of the item name
    uint32_t            itemIndex;          //  index of the item
};

//  File structure indexing
struct Indexing
{
    uint32_t            offset;             //  offset to apply to the indices
    ListDesc            indices;            //  list of indices
};

//  File structure library header description
struct FileHeader
{
    uint32_t            magic;              //  magic file identifier 'is??', last 2 digits are specific to the file contents
    uint32_t            platform;           //  platform format 'DX11'
    uint32_t            version;            //  file format version (high 16-bits major/compatibility version, low 16-bits minor version)
    uint32_t            fileSize;           //  file size for loading validation
};

