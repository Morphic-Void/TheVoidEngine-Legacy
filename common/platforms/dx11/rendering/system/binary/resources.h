struct ResourceLibraryIndex
{
    SectionDesc             stringTable;                            //  description of the FrozenStringTable section
    ArrayDesc               samplerDescs;                           //  description of the samplerDescs array (CSamplerDesc sorted by memcmp)
    ArrayDesc               rasterStateDescs;                       //  description of the rasterDescs array (CRasterStateDesc sorted by memcmp)
    ArrayDesc               depthStateDescs;                        //  description of the depthDescs array (CDepthStateDesc sorted by memcmp)
    ArrayDesc               blendStateDescs;                        //  description of the blendDescs array (CMultiBlendStateDesc sorted by memcmp)
    ArrayDesc               samplerLinks;                           //  description of the samplerLinks array (LinkDesc sorted by name index)
    ArrayDesc               rasterStateLinks;                       //  description of the rasterLinks array (LinkDesc sorted by name index)
    ArrayDesc               depthStateLinks;                        //  description of the depthLinks array (LinkDesc sorted by name index)
    ArrayDesc               blendStateLinks;                        //  description of the blendLinks array (LinkDesc sorted by name index)
};

struct ResourceLibrary
{
    FileHeader              header;                                 //  magic file identifier 'isRL'
    ResourceLibraryIndex    index;                                  //  file contents locations
};

union ResourceBinaryPtr
{
    ResourceLibrary*        library;                                //  resource library
    uint32_t*               data;                                   //  SectionDesc and ArrayDesc are relative to this
};

