////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Command function enumeration
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

enum ECmdFunc
{
    ECmdFuncNULL = 0,
    ECmdFuncAuto,
    ECmdFuncDrawing,
    ECmdFuncIndexed,
    ECmdFuncInstancedDrawing,
    ECmdFuncInstancedIndexed,
    ECmdFuncInstancedDrawingIndirect,
    ECmdFuncInstancedIndexedIndirect,
    ECmdFuncDispatch,
    ECmdFuncDispatchIndirect,
    ECmdFuncCount,
    ECmdFuncForce32 = 0x7fffffff
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Resource state description structures
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct ResourceDescMemory
{
    void*               unknown;
    UINT                flags;
    inline void         reset() { unknown = NULL; flags = 0; };
};

struct ResourceDescLinear
{
    void*               unknown;
    UINT                offset;
    UINT                stride;
    inline void         reset() { unknown = NULL; offset = 0; stride = 0; };
};

struct ResourceDescSRV
{
    void*               unknown;
    inline void         reset() { unknown = NULL; };
};

struct ResourceDescUAV
{
    void*               unknown;
    UINT                initialCount;
    inline void         reset() { unknown = NULL; initialCount = 0; };
};

struct ResourceDescRTV
{
    void*               unknown;
    FBOX                viewport;
    SRECT               scissoring;
    inline void         reset() { memset( this, 0, sizeof( *this ) ); };
};

struct ResourceDescDSV
{
    void*               unknown;
    FBOX                viewport;
    SRECT               scissoring;
    inline void         reset() { memset( this, 0, sizeof( *this ) ); };
};

struct ResourceDescSampler
{
    void*               unknown;
    inline void         reset() { unknown = NULL; };
};

struct ResourceDescRasterState
{
    void*               unknown;
    inline void         reset() { unknown = NULL; };
};

struct ResourceDescDepthState
{
    void*               unknown;
    UINT                stencilRef;
    inline void         reset() { unknown = NULL; stencilRef = 0; };
};

struct ResourceDescBlendState
{
    void*               unknown;
    UINT                sampleMask;
    FVEC4               blendFactor;
    inline void         reset() { unknown = NULL; sampleMask = 0xffffffff; blendFactor.x = blendFactor.y = blendFactor.z = blendFactor.w = 1.0f; };
};

struct ResourceDescCommand
{
    UINT                func;
    PrimDesc            primDesc;
    DispatchParams      dispatch;
    DrawingParams       drawing;
    IndexedParams       indexed;
    InstancingParams    instancing;
    ResourceIndices     indices;
    ResourceParams      indirect;
    inline void         reset() { memset( this, 0, sizeof( *this ) ); func = ECmdFuncNULL; };
};

