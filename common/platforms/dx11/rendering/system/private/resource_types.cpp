static const char* EResourceTypeNames[] =      { "Unknown", "Consts", "SRV", "UAV", "Sampler", "Raster state", "Depth state", "Blend state", "DSV", "RTV", "Stream", "Output", "Params", "Indices", "Command", "Linear" };
static const char* EResourceTypeNamesUpper[] = { "UNKNOWN", "CONSTS", "SRV", "UAV", "SAMPLER", "RASTER_STATE", "DEPTH_STATE", "BLEND_STATE", "DSV", "RTV", "STREAM", "OUTPUT", "PARAMS", "INDICES", "COMMAND", "LINEAR" };
static const char* EResourceTypeNamesLower[] = { "unknown", "consts", "srv", "uav", "sampler", "raster_state", "depth_state", "blend_state", "dsv", "rtv", "stream", "output", "params", "indices", "command", "linear" };
static const char* EResourceTypeNamesMixed[] = { "unknown", "consts", "SRV", "UAV", "sampler", "raster_state", "depth_state", "blend_state", "DSV", "RTV", "stream", "output", "params", "indices", "command", "linear" };

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    EResourceType name functions
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

const char* getEResourceTypeName( const EResourceType resourceType )
{
    return( EResourceTypeNames[ ( resourceType < EResourceTypeCount ) ? resourceType : EResourceTypeUnknown ] );
}

const char* getEResourceTypeNameUpper( const EResourceType resourceType )
{
    return( EResourceTypeNamesUpper[ ( resourceType < EResourceTypeCount ) ? resourceType : EResourceTypeUnknown ] );
}

const char* getEResourceTypeNameLower( const EResourceType resourceType )
{
    return( EResourceTypeNamesLower[ ( resourceType < EResourceTypeCount ) ? resourceType : EResourceTypeUnknown ] );
}

const char* getEResourceTypeNameMixed( const EResourceType resourceType )
{
    return( EResourceTypeNamesMixed[ ( resourceType < EResourceTypeCount ) ? resourceType : EResourceTypeUnknown ] );
}

