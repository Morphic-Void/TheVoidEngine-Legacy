////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Physical resource types enumeration
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

enum EPhysicalType
{
    EPhysicalTypeUnknown = 0,   //  undefined physical type
    EPhysicalTypeBuffer,        //  memory buffer
    EPhysicalTypeAccess,        //  memory access
    EPhysicalTypeSampler,       //  sampler state
    EPhysicalTypeRasterState,   //  raster state
    EPhysicalTypeDepthState,    //  depth state
    EPhysicalTypeBlendState,    //  blend state
    EPhysicalTypeCount,
    EPhysicalTypeForce32 = 0x7fffffff
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    EPhysicalType name functions
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

const char* getEPhysicalTypeName( const EPhysicalType physicalType );
const char* getEPhysicalTypeNameUpper( const EPhysicalType physicalType );
const char* getEPhysicalTypeNameLower( const EPhysicalType physicalType );
const char* getEPhysicalTypeNameMixed( const EPhysicalType physicalType );

