////
////    Proxy for D3D11_VIEWPORT
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct VIEWPORT
{
    FLOAT                   TopLeftX;
    FLOAT                   TopLeftY;
    FLOAT                   Width;
    FLOAT                   Height;
    FLOAT                   MinDepth;
    FLOAT                   MaxDepth;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Structures describing the shader staging
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct StateStaging
{
    void*                   rasterState;        //  raster state interface pointer
    void*                   depthState;         //  depth state interface pointer
    void*                   blendState;         //  blend state interface pointer
    UINT                    stencilRef;         //  depth state stencil reference value
    UINT                    sampleMask;         //  blend state sample masking value
    FLOAT                   blendFactor[ 4 ];   //  blend state factor
};

struct DepthStencilStaging
{
    void*                   buffer;             //  buffer interface for depth-stencil
    VIEWPORT                viewport;           //  viewport for depth-stencil
    RECT                    scissoring;         //  scissoring for depth-stencil
};

struct RenderTargetsStaging
{
    void*                   buffers[ 8 ];       //  buffer interfaces for multiple render target rendering
    VIEWPORT                viewports[ 8 ];     //  viewports for multiple render target rendering
    RECT                    scissoring[ 8 ];    //  scissoring for multiple render target rendering
};

struct RenderableStaging
{
    StateStaging            stateStaging;       //  staging for render states
    DepthStencilStaging     depthStencil;       //  depth-stencil staging
    RenderTargetsStaging    renderTargets;      //  render targets staging
};

struct ShaderStaging
{
    void**                  buffers;            //  staging array for buffer interface pointers
    UINT*                   offsets;            //  staging array for UINT parameters most commonly used for offsets into the buffer
    UINT*                   strides;            //  staging array for UINT parameters most commonly used for strides of elements in the buffer
    RenderableStaging*      renderable;         //  staging for renderable shaders
};

