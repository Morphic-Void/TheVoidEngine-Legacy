RefBlob* loadBlob( const char* const file_name )
{
    size_t size = 0;
    void* data = file::loadFile( file_name, size );
    return( data ? RefBlob::create( data, static_cast< uint >( size ) ) : NULL );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Library loader function bodies
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool loadLibrary( const char* const file_name, const SystemFactory& factory, ShaderLibrary& library )
{
    bool ret = false;
    if( factory.valid() )
    {
        RefBlob* blob = loadBlob( file_name );
        if( blob )
        {
            ret = factory.create( *blob, library );
            blob->release();
            blob = NULL;
        }
    }
    return( ret );
}

bool loadLibrary( const char* const file_name, const SystemFactory& factory, ResourceLibrary& library )
{
    bool ret = false;
    if( factory.valid() )
    {
        RefBlob* blob = loadBlob( file_name );
        if( blob )
        {
            ret = factory.create( *blob, library );
            blob->release();
            blob = NULL;
        }
    }
    return( ret );
}

bool createLibrary( RefBlob* const blob, const SystemFactory& factory, ShaderLibrary& library )
{
    return( ( factory.valid() && blob ) ? factory.create( *blob, library ) : false );
}

bool createLibrary( RefBlob* const blob, const SystemFactory& factory, ResourceLibrary& library )
{
    return( ( factory.valid() && blob ) ? factory.create( *blob, library ) : false );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    System object factory creation function body
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool createFactory( ID3D11Device* device, ID3D11DeviceContext* context, SystemFactory& factory )
{
    CSystemFactory* concrete = new CSystemFactory;
    if( concrete->create( device, context, factory ) )
    {
        return( true );
    }
    delete concrete;
    return( false );
}

