static const char* EPhysicalTypeNames[] =      { "Unknown", "Buffer", "Access", "Sampler", "Raster state", "Depth state", "Blend state" };
static const char* EPhysicalTypeNamesUpper[] = { "UNKNOWN", "BUFFER", "ACCESS", "SAMPLER", "RASTER_STATE", "DEPTH_STATE", "BLEND_STATE" };
static const char* EPhysicalTypeNamesLower[] = { "unknown", "buffer", "access", "sampler", "raster_state", "depth_state", "blend_state" };
static const char* EPhysicalTypeNamesMixed[] = { "unknown", "buffer", "access", "sampler", "raster_state", "depth_state", "blend_state" };

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    EPhysicalType name functions
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

const char* getEPhysicalTypeName( const EPhysicalType physicalType )
{
    return( EPhysicalTypeNames[ ( physicalType < EPhysicalTypeCount ) ? physicalType : EPhysicalTypeUnknown ] );
}

const char* getEPhysicalTypeNameUpper( const EPhysicalType physicalType )
{
    return( EPhysicalTypeNamesUpper[ ( physicalType < EPhysicalTypeCount ) ? physicalType : EPhysicalTypeUnknown ] );
}

const char* getEPhysicalTypeNameLower( const EPhysicalType physicalType )
{
    return( EPhysicalTypeNamesLower[ ( physicalType < EPhysicalTypeCount ) ? physicalType : EPhysicalTypeUnknown ] );
}

const char* getEPhysicalTypeNameMixed( const EPhysicalType physicalType )
{
    return( EPhysicalTypeNamesMixed[ ( physicalType < EPhysicalTypeCount ) ? physicalType : EPhysicalTypeUnknown ] );
}

