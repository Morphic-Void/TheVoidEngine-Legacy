class CSystemFactory : public ISystemFactory
{
public:
    inline                      CSystemFactory() : m_refs( 0 ), m_reserved( 0 ), m_device( NULL ), m_context( NULL ) {};
    inline                      ~CSystemFactory() { m_refs = 0; m_reserved = 0; m_device = NULL; m_context = NULL; };
    inline bool                 valid() const { return( ( m_refs && m_device && m_context ) ? true : false ); };
    bool                        create( ID3D11Device* device, ID3D11DeviceContext* context, SystemFactory& systemFactory );
    void                        destroy();
public:
    virtual void                addRef();
    virtual void                release();
    virtual bool                create( RefBlob& blob, ShaderLibrary& shaderLibrary );
    virtual bool                create( RefBlob& blob, ResourceLibrary& resourceLibrary );
    virtual bool                create( ResourceRegistry& resourceRegistry );
    virtual bool                create( ResourceStream& resourceStream );
    virtual bool                create( ResourceOutput& resourceOutput );
    virtual bool                create( ResourceConsts& resourceConsts );
    virtual bool                create( ResourceSRV& resourceSRV );
    virtual bool                create( ResourceUAV& resourceUAV );
    virtual bool                create( ResourceRTV& resourceRTV );
    virtual bool                create( ResourceDSV& resourceDSV );
    virtual bool                create( const CSamplerDesc& desc, ResourceSampler& resourceSampler );
    virtual bool                create( const CRasterStateDesc& desc, ResourceRasterState& resourceRasterState );
    virtual bool                create( const CDepthStateDesc& desc, ResourceDepthState& resourceDepthState );
    virtual bool                create( const CBlendStateDesc& desc, ResourceBlendState& resourceBlendState );
    virtual bool                create( const CMultiBlendStateDesc& desc, ResourceBlendState& resourceBlendState );
    virtual bool                create( const CBlendStateConfig& config, ResourceBlendState& resourceBlendState );
    virtual bool                create( ResourceParams& resourceParams );
    virtual bool                create( ResourceIndices& resourceIndices );
    virtual bool                create( ResourceCommand& resourceCommand );
public:
	inline ID3D11Device*        getDevice() const { return( m_device ); };
	inline ID3D11DeviceContext* getContext() const { return( m_context ); };
protected:
    uint32_t                    m_refs;
    uint32_t                    m_reserved;
	ID3D11Device*               m_device;
	ID3D11DeviceContext*        m_context;
    StringTableBlob             m_strings;
    CResourceLinears            m_resourceLinears;
    CResourceSRVs               m_resourceSRVs;
    CResourceUAVs               m_resourceUAVs;
    CResourceRTVs               m_resourceRTVs;
    CResourceDSVs               m_resourceDSVs;
    CResourceSamplers           m_resourceSamplers;
    CResourceRasterStates       m_resourceRasterStates;
    CResourceDepthStates        m_resourceDepthStates;
    CResourceBlendStates        m_resourceBlendStates;
    CResourceCommands           m_resourceCommands;
    CPhysicalSamplers           m_physicalSamplers;
    CPhysicalRasterStates       m_physicalRasterStates;
    CPhysicalDepthStates        m_physicalDepthStates;
    CPhysicalBlendStates        m_physicalBlendStates;
private:
    inline                      CSystemFactory( const CSystemFactory& ) {};
    inline void                 operator=( const CSystemFactory& ) const {};
};

