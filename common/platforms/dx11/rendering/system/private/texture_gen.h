enum EStdTexID
{
    EStdTexInvalid = 0,     //  256 entry 8-bit permute lookup
    EStdTexPermute1D,       //  256 entry 8-bit permute lookup
    EStdTexPermute1Dx4,     //  256 entry 4x8-bit permute lookup
    EStdTexPermute2D,       //  256x256 entry 8-bit permute lookup
    EStdTexPermute2Dx4,     //  256x256 entry 4x8-bit permute lookup
    EStdTexPermuteQuad2D,   //  256x256 entry 8-bit permute lookup with neighbours
    EStdTexRandom1D,        //  256 entry 8-bit random lookup
    EStdTexRandom1Dx4,      //  256 entry 4x8-bit or 1x32-bit random lookup
    EStdTexRandom2D,        //  256x256 entry 8-bit random lookup
    EStdTexRandom2Dx4,      //  256x256 entry 4x8-bit or 1x32-bit random lookup
    EStdTexRandomQuad2D,    //  256x256 entry 8-bit random lookup with neighbours
    EStdTexMedian2D,        //  256x256 entry 8-bit median texture lookup
    EStdTexMedianQuad2D,    //  256x256 entry 8-bit median texture lookup with neighbours
    EStdTexPerlin2Dx4a,     //  256x256 entry 4x8-bit Perlin noise texture with steppings 1, 2, 4 and 8
    EStdTexPerlin2Dx4b,     //  256x256 entry 4x8-bit Perlin noise texture with steppings 16, 32, 64 and 128
    EStdTexCount,
    EStdTexForce32 = 0x7fffffff
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Standard texture generation class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class CStdTexGen
{
public:
    inline                      CStdTexGen() { zero(); };
    inline                      ~CStdTexGen() { kill(); };
    inline bool                 valid() const { return( ( this != NULL ) && ( m_buffer != NULL ) ); };
    void                        init( const EStdTexID id );
    void                        kill();
    void                        writeTGA();
    inline uint32_t             getWidth() const { return( m_width ); };
    inline uint32_t             getHeight() const { return( m_height ); };
    inline uint32_t             getStride() const { return( m_stride ); };
    inline const uint8_t*       getBuffer() const { return( m_buffer ); };
    inline const char*          getName() const { return( s_names[ m_id ] ); };
protected:
    void                        zero();
    EStdTexID                   m_id;
    uint32_t                    m_width;
    uint32_t                    m_height;
    uint32_t                    m_stride;
    uint8_t*                    m_buffer;
    static const char*          s_names[ EStdTexCount ];
};

