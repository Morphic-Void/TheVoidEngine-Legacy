enum EElementFormat
{
    EElement_UNKNOWN = 0,    //  unknown size and type (used for stream-out gaps)
    EElement_8x4_SINT,       //  4 bytes (32-bits):   4 * 8-bit signed integers
    EElement_8x4_UINT,       //  4 bytes (32-bits):   4 * 8-bit unsigned integers
    EElement_8x4_SNORM,      //  4 bytes (32-bits):   4 * 8-bit signed integers to be scaled into the range -1.0:+1.0
    EElement_8x4_UNORM,      //  4 bytes (32-bits):   4 * 8-bit unsigned integers to be scaled into the range 0.0:+1.0
    EElement_8x4_SRGB,       //  4 bytes (32-bits):   4 * 8-bit unsigned integers to be scaled into the range 0.0:+1.0 with SRGB decode to linear
    EElement_16x2_SINT,      //  4 bytes (32-bits):   2 * 16-bit signed integers
    EElement_16x2_UINT,      //  4 bytes (32-bits):   2 * 16-bit unsigned integers
    EElement_16x2_SNORM,     //  4 bytes (32-bits):   2 * 16-bit signed integers to be scaled into the range -1.0:+1.0
    EElement_16x2_UNORM,     //  4 bytes (32-bits):   2 * 16-bit unsigned integers to be scaled into the range 0.0:+1.0
    EElement_16x2_FLOAT,     //  4 bytes (32-bits):   2 * 16-bit half-precision floating point values
    EElement_16x4_SINT,      //  8 bytes (64-bits):   4 * 16-bit signed integers
    EElement_16x4_UINT,      //  8 bytes (64-bits):   4 * 16-bit unsigned integers
    EElement_16x4_SNORM,     //  8 bytes (64-bits):   4 * 16-bit signed integers to be scaled into the range -1.0:+1.0
    EElement_16x4_UNORM,     //  8 bytes (64-bits):   4 * 16-bit unsigned integers to be scaled into the range 0.0:+1.0
    EElement_16x4_FLOAT,     //  8 bytes (64-bits):   4 * 16-bit half-precision floating point values
    EElement_32x1_TYPELESS,  //  4 bytes (32-bits):   1 * 32-bit typeless value
    EElement_32x1_SINT,      //  4 bytes (32-bits):   1 * 32-bit signed integer
    EElement_32x1_UINT,      //  4 bytes (32-bits):   1 * 32-bit unsigned integer
    EElement_32x1_FLOAT,     //  4 bytes (32-bits):   1 * 32-bit floating point value
    EElement_32x2_TYPELESS,  //  8 bytes (64-bits):   2 * 32-bit typeless values
    EElement_32x2_SINT,      //  8 bytes (64-bits):   2 * 32-bit signed integers
    EElement_32x2_UINT,      //  8 bytes (64-bits):   2 * 32-bit unsigned integers
    EElement_32x2_FLOAT,     //  8 bytes (64-bits):   2 * 32-bit floating point values
    EElement_32x3_TYPELESS,  //  12 bytes (96-bits):  3 * 32-bit typeless values
    EElement_32x3_SINT,      //  12 bytes (96-bits):  3 * 32-bit signed integers
    EElement_32x3_UINT,      //  12 bytes (96-bits):  3 * 32-bit unsigned integers
    EElement_32x3_FLOAT,     //  12 bytes (96-bits):  3 * 32-bit floating point values
    EElement_32x4_TYPELESS,  //  16 bytes (128-bits): 4 * 32-bit typeless values
    EElement_32x4_SINT,      //  16 bytes (128-bits): 4 * 32-bit signed integers
    EElement_32x4_UINT,      //  16 bytes (128-bits): 4 * 32-bit unsigned integers
    EElement_32x4_FLOAT,     //  16 bytes (128-bits): 4 * 32-bit floating point values
    EElementFormatCount,
    EElementFormatForce32 = 0x7fffffff
};

