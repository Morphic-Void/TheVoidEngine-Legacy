////
////    Core system components structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct CoreSystem
{
    SystemFactory       factory;
    ShaderLibrary       shaders;
    ResourceRegistry    registry;
    BindingGroup        defaults;
    inline void         release() { factory.release(); shaders.release(); registry.release(); defaults.release(); };
    inline              ~CoreSystem() { release(); };
};

