////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    Primary views structure
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct PrimaryViews
{
    FBOX                viewport;
    SRECT               scissoring;
    DIMS2D              dimensions;
    CompoundRTV         primaryRTV;
    CompoundDSV         primaryDSV;
    inline void         release() { primaryRTV.release(); primaryDSV.release(); viewport.min.x = viewport.min.y = viewport.min.z = viewport.max.x = viewport.max.y = viewport.max.z = 0.0f; scissoring.left = scissoring.top = scissoring.right = scissoring.bottom = 0; dimensions.width = dimensions.height = 0; };
    inline              ~PrimaryViews() { release(); };
};

