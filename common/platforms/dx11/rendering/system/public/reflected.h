class LintelEffect
{
public:
    inline                      LintelEffect() { zero(); };
    inline                      ~LintelEffect() { zero(); };
    void                        zero();
    inline const hlsl::TrimAB&  getConsts() const { return( m_consts ); };
    void                        setColour( const uint32_t colour );
    void                        setParams( const float side, const float face, const float back, const float shift );
    void                        setParamA( const float side, const float face, const float back, const float shift );
    void                        setParamB( const float side, const float face, const float back, const float shift );
    inline void                 setShifts( const float shift ) { m_consts.param1.w = m_consts.param2.w = shift; };
    inline void                 setShifts( const float shift1, const float shift2 ) { m_consts.param1.w = shift1; m_consts.param2.w = shift2; };
    inline void                 setShiftA( const float shift ) { m_consts.param1.w = shift; };
    inline void                 setShiftB( const float shift ) { m_consts.param2.w = shift; };
    void                        setMixer( const ELintelFunc func1, const ELintelFunc func2 );
protected:
    void                        setMixer( const ELintelFunc func, hlsl::fvec4& mixer );
    hlsl::TrimAB                m_consts;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////
////    lintel consts helper class
////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class LintelConsts
{
public:
    inline                      LintelConsts() { zero(); };
    inline                      LintelConsts( const ELintelEffect effect, const float t ) { zero(); setEffect( effect, t ); };
    inline                      ~LintelConsts() { zero(); };
    void                        zero();
    inline const hlsl::Lintel&  getConsts() const { return( m_consts ); };
    void                        setEffect( const ELintelEffect effect, const float t );
    inline LintelEffect&        getEffect1() { return( *reinterpret_cast< LintelEffect* >( &m_consts.effects[ 0 ] ) ); };
    inline LintelEffect&        getEffect2() { return( *reinterpret_cast< LintelEffect* >( &m_consts.effects[ 1 ] ) ); };
    inline void                 enableEffects( const bool enable = true ) { m_consts.masking.x = m_consts.masking.y = m_consts.masking.z = m_consts.masking.w = ( enable ? 1.0f : 0.0f ); };
    inline void                 enableEffect1( const bool enable = true ) { m_consts.masking.x = m_consts.masking.y = ( enable ? 1.0f : 0.0f ); };
    inline void                 enableEffect2( const bool enable = true ) { m_consts.masking.z = m_consts.masking.w = ( enable ? 1.0f : 0.0f ); };
    inline void                 enableFaceEffects( const bool enable = true ) { m_consts.masking.y = m_consts.masking.w = ( enable ? 1.0f : 0.0f ); };
    inline void                 enableBackEffects( const bool enable = true ) { m_consts.masking.x = m_consts.masking.z = ( enable ? 1.0f : 0.0f ); };
    inline void                 enableFaceEffect1( const bool enable = true ) { m_consts.masking.y = ( enable ? 1.0f : 0.0f ); };
    inline void                 enableBackEffect1( const bool enable = true ) { m_consts.masking.x = ( enable ? 1.0f : 0.0f ); };
    inline void                 enableFaceEffect2( const bool enable = true ) { m_consts.masking.w = ( enable ? 1.0f : 0.0f ); };
    inline void                 enableBackEffect2( const bool enable = true ) { m_consts.masking.z = ( enable ? 1.0f : 0.0f ); };
    inline void                 disableEffects( const bool disable = true ) { enableEffects( !disable ); };
    inline void                 disableEffect1( const bool disable = true ) { enableEffect1( !disable ); };
    inline void                 disableEffect2( const bool disable = true ) { enableEffect2( !disable ); };
    inline void                 disableFaceEffects( const bool disable = true ) { enableFaceEffects( !disable ); };
    inline void                 disableBackEffects( const bool disable = true ) { enableBackEffects( !disable ); };
    inline void                 disableFaceEffect1( const bool disable = true ) { enableFaceEffect1( !disable ); };
    inline void                 disableBackEffect1( const bool disable = true ) { enableBackEffect1( !disable ); };
    inline void                 disableFaceEffect2( const bool disable = true ) { enableFaceEffect2( !disable ); };
    inline void                 disableBackEffect2( const bool disable = true ) { enableBackEffect2( !disable ); };
protected:
    hlsl::Lintel                m_consts;
};

