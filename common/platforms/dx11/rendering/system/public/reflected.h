enum ELintelEffect
{
    ELintelEffectDefault = 0,
    ELintelEffectPlasma,
    ELintelEffectError,
    ELintelEffectCount,
    ELintelEffectForce32 = 0x7fffffff
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
