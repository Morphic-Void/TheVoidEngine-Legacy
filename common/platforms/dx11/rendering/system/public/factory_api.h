    inline bool         create( const RASTER_STATE_DEF& def, ResourceRasterState& resourceRasterState ) const { return( create( CRasterStateDesc( def ), resourceRasterState ) ); };
    inline bool         create( const DEPTH_STATE_DEF& def, ResourceDepthState& resourceDepthState ) const { return( create( CDepthStateDesc( def ), resourceDepthState ) ); };
