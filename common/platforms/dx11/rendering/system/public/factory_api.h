    inline bool         create( RefBlob& blob, ResourceLibrary& resourceLibrary ) const { return( usable() ? iptr()->create( blob, resourceLibrary ) : false ); };
    inline bool         create( ResourceRegistry& resourceRegistry ) const { return( usable() ? iptr()->create( resourceRegistry ) : false ); };
    inline bool         create( ResourceStream& resourceStream ) const { return( usable() ? iptr()->create( resourceStream ) : false ); };
    inline bool         create( ResourceOutput& resourceOutput ) const { return( usable() ? iptr()->create( resourceOutput ) : false ); };
    inline bool         create( ResourceConsts& resourceConsts ) const { return( usable() ? iptr()->create( resourceConsts ) : false ); };
    inline bool         create( ResourceSRV& resourceSRV ) const { return( usable() ? iptr()->create( resourceSRV ) : false ); };
    inline bool         create( ResourceUAV& resourceUAV ) const { return( usable() ? iptr()->create( resourceUAV ) : false ); };
    inline bool         create( ResourceRTV& resourceRTV ) const { return( usable() ? iptr()->create( resourceRTV ) : false ); };
    inline bool         create( ResourceDSV& resourceDSV ) const { return( usable() ? iptr()->create( resourceDSV ) : false ); };
    inline bool         create( const CSamplerDesc& desc, ResourceSampler& resourceSampler ) const { return( usable() ? iptr()->create( desc, resourceSampler ) : false ); };
