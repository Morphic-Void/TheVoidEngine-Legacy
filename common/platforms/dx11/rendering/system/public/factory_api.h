    inline bool         create( const MULTI_BLEND_STATE_DEF& def, ResourceBlendState& resourceBlendState ) const { return( create( CMultiBlendStateDesc( def ), resourceBlendState ) ); };
    inline bool         create( const BLEND_CONFIG_STATE_DEF& def, ResourceBlendState& resourceBlendState ) const { return( create( CBlendStateConfig( def ), resourceBlendState ) ); };
    inline bool         create( const BLEND_TARGET_STATE_DEF& def, ResourceBlendState& resourceBlendState ) const { return( create( CBlendStateDesc( def ), resourceBlendState ) ); };
    inline bool         create( ResourceParams& resourceParams ) const { return( usable() ? iptr()->create( resourceParams ) : false ); };
    inline bool         create( ResourceIndices& resourceIndices ) const { return( usable() ? iptr()->create( resourceIndices ) : false ); };
    inline bool         create( ResourceCommand& resourceCommand ) const { return( usable() ? iptr()->create( resourceCommand ) : false ); };
